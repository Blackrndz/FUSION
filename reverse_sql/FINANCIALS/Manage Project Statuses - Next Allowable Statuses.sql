SELECT pjfPROJECTstatEO.PROJECT_STATUS_NAME RES_PROJECT_STATUS
 ,(SELECT MEANING FROM FND_LOOKUPS
			WHERE LOOKUP_CODE = pjfPROJECTstatEO.STATUS_TYPE
			AND LOOKUP_TYPE = 'PJF_STATUS_TYPE')RES_STATUS_TYPE
 ,(SELECT MEANING FROM FND_LOOKUPS
			WHERE LOOKUP_CODE = pjfPROJECTstatEO.NEXT_ALLOWABLE_STATUS_FLAG
			AND LOOKUP_TYPE = 'PJF_NEXT_STATUS_FLAG') RES_FLAG_NEXT_ALLOWABLE_STATUS
 ,(CASE 
	WHEN pjfPROJECTstatEO.NEXT_ALLOWABLE_STATUS_FLAG = 'S' THEN
		(SELECT MEANING FROM FND_LOOKUPS
			WHERE LOOKUP_CODE = pjfNALLOWstatEO.NEXT_ALLOWABLE_STATUS_CODE
			AND LOOKUP_TYPE = 'PJF_PROJECT_SYSTEM_STATUS')
    WHEN pjfPROJECTstatEO.NEXT_ALLOWABLE_STATUS_FLAG = 'U' THEN
		(SELECT PROJECT_STATUS_NAME FROM PJF_PROJECT_STATUSES_VL
			WHERE PROJECT_STATUS_CODE = pjfNALLOWstatEO.NEXT_ALLOWABLE_STATUS_CODE)
	WHEN pjfPROJECTstatEO.NEXT_ALLOWABLE_STATUS_FLAG = 'N' OR pjfPROJECTstatEO.NEXT_ALLOWABLE_STATUS_FLAG = 'A' 
		THEN
			NULL
	END) RES_NEXT_ALLOWABLE_STATUS
 ,pjfNALLOWstatEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
 ,pjfNALLOWstatEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
 ,pjfNALLOWstatEO.CREATED_BY RSC_CREATED_BY
 ,pjfNALLOWstatEO.CREATION_DATE RSC_CREATION_DATE
 ,NULL RSC_LEDGER_ID
 ,NULL RSC_CHART_OF_ACCOUNTS_ID
 ,NULL RSC_BUSINESS_UNIT_ID
 ,NULL RSC_LEGAL_ENTITY_ID
 ,NULL RSC_ORGANIZATION_ID
 ,NULL RSC_BUSINESS_GROUP_ID
 ,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJF_PROJECT_STATUSES_VL pjfPROJECTstatEO 
,PJF_NEXT_ALLOW_STATUSES pjfNALLOWstatEO
WHERE pjfPROJECTstatEO.PROJECT_STATUS_CODE  = pjfNALLOWstatEO.STATUS_CODE (+)


 	