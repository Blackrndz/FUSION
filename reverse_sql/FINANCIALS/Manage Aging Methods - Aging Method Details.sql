select AGINGBUCKETSEO.BUCKET_NAME RES_AGING_METHOD_NAME
,SETIDSETPEO.SET_NAME RES_AGING_METHOD_SET
,AGINGBUCKETLINESEO.BUCKET_SEQUENCE_NUM RES_SEQUENCE
,ARLOOKUPPEO.MEANING RES_BUCKET_TYPE
,AGINGBUCKETLINESEO.DAYS_START RES_DAYS_RANGE_AGING_DAYS_FROM
,AGINGBUCKETLINESEO.DAYS_TO RES_DAYS_RANGE_AGING_DAYS_TO
,AGINGBUCKETLINESEO.REPORT_HEADING1 RES_DISPLAY_INFORMATION_AGING_
,AGINGBUCKETLINESEO.REPORT_HEADING2 RES_DISPLAY_INFORMATION_AGIN_0
,AGINGBUCKETLINESEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,AGINGBUCKETLINESEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,AGINGBUCKETLINESEO.CREATED_BY  RSC_CREATED_BY
,AGINGBUCKETLINESEO.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM AR_AGING_BUCKETS AgingBucketsEO
,AR_AGING_BUCKET_LINES_VL AGINGBUCKETLINESEO
,FND_SETID_SETS_VL SETIDSETPEO
,AR_LOOKUPS ARLOOKUPPEO
WHERE AGINGBUCKETSEO.AGING_TYPE != 'INTTIER'
AND AGINGBUCKETLINESEO.TYPE IN('PAST','CURRENT')
AND AgingBucketLinesEO.AGING_BUCKET_ID = AgingBucketsEO.AGING_BUCKET_ID
AND AGINGBUCKETLINESEO.SET_ID          = AGINGBUCKETSEO.SET_ID
AND SETIDSETPEO.SET_ID                 = AGINGBUCKETSEO.SET_ID
AND ARLOOKUPPEO.LOOKUP_CODE            = AGINGBUCKETLINESEO.TYPE
and ARLOOKUPPEO.LOOKUP_TYPE            = 'AGING_BUCKET_LINE_TYPE'
ORDER BY AGINGBUCKETSEO.BUCKET_NAME
,AgingBucketLinesEO.BUCKET_SEQUENCE_NUM