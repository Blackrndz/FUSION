SELECT DISTINCT elementsE0.ELEMENT_NAME RES_ELEMENT_NAME
,legislativeDataGrpsE0.NAME RES_LEGISLATIVE_DATA_GROUP
,(SELECT CLASSIFICATION_NAME
    FROM PAY_ELE_CLASSIFICATIONS_VL
	WHERE BASE_CLASSIFICATION_ID = elementsE0.CLASSIFICATION_ID
	AND LEGISLATION_CODE = legislativeDataGrpsE0.LEGISLATION_CODE
	AND SYSDATE BETWEEN DATE_FROM AND NVL(DATE_TO,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_PRIMARY_CLASSIFICATION
,(SELECT CLASSIFICATION_NAME
    FROM PAY_ELE_CLASSIFICATIONS_VL
    WHERE BASE_CLASSIFICATION_ID = elementsE0.SECONDARY_CLASSIFICATION_ID
	AND LEGISLATION_CODE = legislativeDataGrpsE0.LEGISLATION_CODE
	AND SYSDATE BETWEEN DATE_FROM AND NVL(DATE_TO,TO_DATE('4712-12-31','YYYY-MM-DD'))
    ) RES_SECONDARY_CLASSIFICATION
,TO_CHAR(elefreqruleE0.START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,(SELECT PAYROLL_NAME FROM PAY_ALL_PAYROLLS_F WHERE PAYROLL_ID = elefreqruleE0.PAYROLL_ID
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_PAYROLL_NAME
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'PAY_PROC_PERIOD_TYPE'
    AND LOOKUP_CODE = elefreqruleE0.RESET_PERIOD_TYPE
    )  RES_PAYROLL_FREQUENCY
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'PAY_FRULE_DATES'
    AND LOOKUP_CODE = elefreqruleE0.RULE_DATE_CODE
    ) RES_DATE
,(CASE WHEN EXISTS (SELECT 1 FROM PAY_FREQ_RULE_PERIODS WHERE ELE_PAYROLL_FREQ_RULE_ID = elefreqruleE0.ELE_PAYROLL_FREQ_RULE_ID AND PERIOD_NO_IN_RESET_PERIOD = 1 AND elefreqruleE0.RESET_NO_OF_PERIODS >= 1)
	THEN
		'Yes'
	ELSE 'No' END) RES_FREQUENCY_1
,(CASE WHEN EXISTS (SELECT 1 FROM PAY_FREQ_RULE_PERIODS WHERE ELE_PAYROLL_FREQ_RULE_ID = elefreqruleE0.ELE_PAYROLL_FREQ_RULE_ID AND PERIOD_NO_IN_RESET_PERIOD = 2 AND elefreqruleE0.RESET_NO_OF_PERIODS >= 2)
	THEN
		'Yes'
	ELSE 'No' END)  RES_FREQUENCY_2
,(CASE WHEN EXISTS (SELECT 1 FROM PAY_FREQ_RULE_PERIODS WHERE ELE_PAYROLL_FREQ_RULE_ID = elefreqruleE0.ELE_PAYROLL_FREQ_RULE_ID AND PERIOD_NO_IN_RESET_PERIOD = 3 AND elefreqruleE0.RESET_NO_OF_PERIODS >= 3)
	THEN
		'Yes'
	ELSE 'No' END)  RES_FREQUENCY_3
,(CASE WHEN EXISTS (SELECT 1 FROM PAY_FREQ_RULE_PERIODS WHERE ELE_PAYROLL_FREQ_RULE_ID = elefreqruleE0.ELE_PAYROLL_FREQ_RULE_ID AND PERIOD_NO_IN_RESET_PERIOD = 4 AND elefreqruleE0.RESET_NO_OF_PERIODS >= 4)
	THEN
		'Yes'
	ELSE 'No' END)  RES_FREQUENCY_4
,(CASE WHEN EXISTS (SELECT 1 FROM PAY_FREQ_RULE_PERIODS WHERE ELE_PAYROLL_FREQ_RULE_ID = elefreqruleE0.ELE_PAYROLL_FREQ_RULE_ID AND PERIOD_NO_IN_RESET_PERIOD = 5 AND elefreqruleE0.RESET_NO_OF_PERIODS >= 5)
	THEN
		'Yes'
	ELSE 'No' END)  RES_FREQUENCY_5
,(CASE WHEN EXISTS (SELECT 1 FROM PAY_FREQ_RULE_PERIODS WHERE ELE_PAYROLL_FREQ_RULE_ID = elefreqruleE0.ELE_PAYROLL_FREQ_RULE_ID AND PERIOD_NO_IN_RESET_PERIOD = 6 AND elefreqruleE0.RESET_NO_OF_PERIODS >= 6)
	THEN
		'Yes'
	ELSE 'No' END)  RES_FREQUENCY_6 


,elefreqruleE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,elefreqruleE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,elefreqruleE0.CREATED_BY RSC_CREATED_BY
,elefreqruleE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,legislativeDataGrpsE0.LEGISLATION_CODE RSC_COUNTRY_ID


FROM PAY_ELEMENT_TYPES_VL elementsE0
,PAY_ELE_PAY_FREQ_RULES elefreqruleE0


,(SELECT NAME
    ,LEGISLATIVE_DATA_GROUP_ID
	,LEGISLATION_CODE
    FROM PER_LEGISLATIVE_DATA_GROUPS_VL
    )legislativeDataGrpsE0
	
WHERE elementsE0.LEGISLATIVE_DATA_GROUP_ID = legislativeDataGrpsE0.LEGISLATIVE_DATA_GROUP_ID
AND elementsE0.ELEMENT_TYPE_ID = elefreqruleE0.ELEMENT_TYPE_ID
--AND freqruleE0.EFFECTIVE_START_DATE = elementsE0.EFFECTIVE_START_DATE
--AND SYSDATE BETWEEN ruleE0.EFFECTIVE_START_DATE AND NVL(ruleE0.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
--AND SYSDATE BETWEEN elementsE0.EFFECTIVE_START_DATE AND NVL(elementsE0.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY elementsE0.ELEMENT_NAME


