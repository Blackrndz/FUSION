SELECT pjfPROTypeE0.PROJECT_TYPE RES_NAME
 ,pjfINDRateE0.IND_SCH_NAME RES_DEFAULT_COST_BURDEN_SCHEDU
 ,DECODE(pjfPROTypeE0.ENABLE_BILLING_FLAG,'Y','Yes','No') RES_ALLOW_COST_BURDEN_SCHEDULE
 ,(SELECT MEANING FROM FND_LOOKUPS
	WHERE LOOKUP_CODE = pjfPROTypeE0.BURDEN_AMT_DISPLAY_METHOD
	AND (LOOKUP_TYPE = 'PJF_BURDEN_METHOD_DIFFERENT' OR LOOKUP_TYPE = 'PJF_BURDEN_METHOD_SAME')) RES_BURDEN_COST_ACCOUNTING_OPT
 ,DECODE(pjfPROTypeE0.BURDEN_ACCOUNT_FLAG,'Y','Yes','No') RES_CREATE_EXPENDITURE_ITEMS_F
 ,(SELECT DESCRIPTION FROM PJF_PROJECTS_ALL_VL
	WHERE PROJECT_ID = pjfPROTypeE0.BURDEN_SUM_DEST_PROJECT_ID) RES_PROJECT_NAME
 ,(SELECT NAME FROM PJF_PROJ_ELEMENTS_VL
	WHERE PROJ_ELEMENT_ID = pjfPROTypeE0.BURDEN_SUM_DEST_TASK_ID) RES_TASK_NAME	
 ,DECODE(pjfPROTypeE0.BURDEN_COST_JOURNAL_FLAG,'Y','Yes','No') RES_CREATE_BURDEN_COST_ACCOUNT
 ,DECODE(pjfPROTypeE0.TOTAL_BURDENED_JOURNAL_FLAG,'Y','Yes','No') RES_CREATE_BURDENED_COST_ACCOU
 ,pjfPROTypeE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
 ,pjfPROTypeE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
 ,pjfPROTypeE0.CREATED_BY RSC_CREATED_BY
 ,pjfPROTypeE0.CREATION_DATE RSC_CREATION_DATE
 ,NULL RSC_LEDGER_ID
 ,NULL RSC_CHART_OF_ACCOUNTS_ID
 ,NULL RSC_BUSINESS_UNIT_ID
 ,NULL RSC_LEGAL_ENTITY_ID
 ,NULL RSC_ORGANIZATION_ID
 ,NULL RSC_BUSINESS_GROUP_ID
 ,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJF_PROJECT_TYPES_VL pjfPROTypeE0
,PJF_IND_RATE_SCH_VL pjfINDRateE0
WHERE pjfINDRateE0.IND_RATE_SCH_ID = pjfPROTypeE0.COST_IND_RATE_SCH_ID
ORDER BY 1
