-- RSC_PREREQUISITE_OBJECTS=HWM_GRPS_VL
 
SELECT hcmGroupsE0.GROUP_NAME RES_NAME
,hcmGroupsE0.DESCRIPTION RES_DESCRIPTION
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'HWM_GRP_APPL_USG_CD'
	AND LOOKUP_CODE = hcmGroupsE0.APPL_USG_CD
	) RES_PRODUCT_USAGE
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'HWM_GRP_APPLIES_TO_CD'
	AND LOOKUP_CODE = hcmGroupsE0.APPLIES_TO_CD
	) RES_MEMBER_USAGE
,DECODE(hcmGroupsE0.FREEZE_FLAG,'Y','Yes','No') RES_LOCKED
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'ORA_HWM_GRP_ASSIGNMENT_TO_USE'
	AND LOOKUP_CODE = hcmGroupsE0.ASSIGNMENT_TO_USE_CD
	) RES_ASSIGNMENT
,DECODE(hcmGroupsE0.MASS_EVAL_FLAG,'Y','Yes','No') RES_INCLUDE_IN_REFRESH_ALL_GRO
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'HWM_GRP_RUN_TYPE'
	AND LOOKUP_CODE = hcmGroupsE0.RUN_TYPE
	) RES_EVALUATION_PERIOD
,(CASE
	WHEN hcmGroupsE0.RUN_TYPE = 'DT_RANGE' THEN
		hcmGroupsE0.NUM_DAYS_PREV
	END) RES_NUMBER_OF_DAYS_BEFORE_EVAL
,(CASE
	WHEN hcmGroupsE0.RUN_TYPE = 'DT_RANGE' THEN
		hcmGroupsE0.NUM_DAYS_NEXT
	END) RES_NUMBER_OF_DAYS_AFTER_EVALU
,TO_CHAR(hcmGroupsE0.LAST_REFRESH_DT,'DD-Mon-YYYY') RES_LAST_REFRESH_DATE
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'HWM_GRP_REFRESH_STATUS'
	AND LOOKUP_CODE = hcmGroupsE0.EVAL_STATUS_CD
	) RES_REFRESH_STATUS
,hcmGroupsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,hcmGroupsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,hcmGroupsE0.CREATED_BY  RSC_CREATED_BY
,hcmGroupsE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM HWM_GRPS_VL hcmGroupsE0
WHERE hcmGroupsE0.ONLINE_EVAL_FLAG = 'N'
ORDER BY hcmGroupsE0.GROUP_NAME