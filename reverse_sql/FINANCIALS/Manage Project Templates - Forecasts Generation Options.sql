SELECT projTemplatesE0.NAME RES_TEMPLATE_NAME
,projTemplatesE0.SEGMENT1 RES_TEMPLATE_NUMBER
,forecastsE0.VERSION_NAME RES_NAME
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJO_PLAN_OVER_ETC_METHOD'
	AND LOOKUP_CODE = planningOptionsE0.ETC_METHOD_CODE 
	) RES_FORECAST_ETC_METHOD
,(CASE 
	WHEN planningOptionsE0.ETC_METHOD_CODE IS NOT NULL THEN
		DECODE(planningOptionsE0.ALLOW_FCST_AMT_CARRY_FLAG,'Y','Yes','No') 
	END) RES_CARRY_FORWARD_UNUSED_AMOUN
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJO_ACT_AMT_THRU'
	AND LOOKUP_CODE = planningOptionsE0.GEN_ACT_AMTS_THRU_CODE
	) RES_ACTUAL_AMOUNTS_THROUGH_PER
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJO_PLAN_TYPE_CODE'
	AND LOOKUP_CODE = planningOptionsE0.GEN_SRC_CODE
	) RES_ETC_GENERATION_SOURCE
,'' RES_SOURCE_PLAN_TYPE
,'' RES_SOURCE_PLAN_VERSION
,'' RES_STATUS
,(CASE
	WHEN planningOptionsE0.PLANNED_FOR_CODE != 'COST' THEN
		(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'PJO_REV_CLASSIFICATION_METHOD'
			AND LOOKUP_CODE = planningOptionsE0.REV_CLASSIFICATION_METHOD)
	END) RES_REVENUE_GENERATION_METHOD
,DECODE(planningOptionsE0.ALLOW_NEG_ETC_FLAG,'Y','Yes','No') RES_ALLOW_NEGATIVE_ETC_CALCULA
,(CASE
	WHEN forecastsE0.PLANNED_FOR_CODE != 'REVENUE' THEN
		DECODE(planningOptionsE0.GEN_INCL_OPENCOMM_FLAG,'Y','Yes','No') 
	END) RES_OPEN_COMMITMENTS
,(CASE
	WHEN planningOptionsE0.BUDGET_CREATION_METHOD = 'GENERATE' AND forecastsE0.PLANNED_FOR_CODE != 'REVENUE' THEN
		DECODE(planningOptionsE0.GEN_RET_MAN_LINE_FLAG,'Y','Yes','No')
	END) RES_RETAIN_MANUALLY_ADDED_FORE
,(CASE
	WHEN planningOptionsE0.BUDGET_CREATION_METHOD != 'MANUAL' AND forecastsE0.PLANNED_FOR_CODE != 'REVENUE' THEN
		DECODE(planningOptionsE0.RET_OVER_RATES_FRM_SRC_FLAG,'Y','Yes','No')
	END) RES_RETAIN_OVERRIDE_RATES_FROM
,forecastsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,forecastsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,forecastsE0.CREATED_BY  RSC_CREATED_BY
,forecastsE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,projTemplatesE0.ORG_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJF_PROJECTS_ALL_VL projTemplatesE0
,FUN_ALL_BUSINESS_UNITS_V buE0
,PJO_PLAN_VERSIONS_VL forecastsE0
,PJO_PLANNING_OPTIONS planningOptionsE0
WHERE projTemplatesE0.TEMPLATE_FLAG = 'Y'
AND projTemplatesE0.ORG_ID = buE0.BU_ID
AND projTemplatesE0.PROJECT_ID = forecastsE0.PROJECT_ID
AND forecastsE0.PLAN_CLASS_CODE = 'FORECAST'
AND forecastsE0.STRUCTURE_VERSION_ID IS NULL
AND forecastsE0.PLAN_VERSION_ID = planningOptionsE0.PLAN_VERSION_ID
--AND forecastsE0.PLANNED_FOR_CODE IN ('COST_AND_REV_TOGETHER','Revenue')
ORDER BY projTemplatesE0.NAME
,forecastsE0.VERSION_NAME