SELECT (SELECT NAME FROM PER_LEGISLATIVE_DATA_GROUPS_VL WHERE LEGISLATIVE_DATA_GROUP_ID = payBALNTYPEO.LEGISLATIVE_DATA_GROUP_ID) RES_LEGISLATIVE_DATA_GROUP
,payBALNTYPEO.BALANCE_NAME RES_NAME
,payBALNTYPEO.REPORTING_NAME RES_REPORTING_NAME
,(SELECT BASE_CATEGORY_NAME FROM PAY_BALANCE_CATEGORIES_VL WHERE BALANCE_CATEGORY_ID = payBALNTYPEO.BALANCE_CATEGORY_ID) RES_BALANCE_CATEGORY
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = payBALNTYPEO.BALANCE_UOM
		AND LOOKUP_TYPE = 'PAY_UNITS' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_UNIT_OF_MEASURE
,(SELECT NAME FROM FND_CURRENCIES_VL WHERE CURRENCY_CODE = payBALNTYPEO.CURRENCY_CODE) RES_CURRENCY
,(SELECT BALANCE_NAME FROM PAY_BALANCE_TYPES_VL WHERE  BALANCE_TYPE_ID = payBALNTYPEO.BASE_BALANCE_TYPE_ID) RES_BASE_BALANCE_ID
,(SELECT ELEMENT_NAME FROM PAY_ELEMENT_TYPES_VL WHERE ELEMENT_TYPE_ID = payINPUTEO.ELEMENT_TYPE_ID
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_ELEMENT_NAME
,payINPUTEO.NAME RES_INPUT_VALUE
,TO_CHAR(SYSDATE,'DD-Mon-YYYY') RES_EFFECTIVE_AS_OF_DATE

,payBALNTYPEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,payBALNTYPEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,payBALNTYPEO.CREATED_BY RSC_CREATED_BY
,payBALNTYPEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PAY_BALANCE_TYPES_VL  payBALNTYPEO
,PAY_INPUT_VALUES_VL payINPUTEO
WHERE payBALNTYPEO.INPUT_VALUE_ID = payINPUTEO.INPUT_VALUE_ID(+)
ORDER BY RES_LEGISLATIVE_DATA_GROUP,payBALNTYPEO.LEGISLATION_CODE,RES_NAME