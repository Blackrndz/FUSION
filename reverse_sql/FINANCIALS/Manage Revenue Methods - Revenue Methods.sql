 --RSC_PREREQUISITE_OBJECTS=PJB_BILLING_METHODS_VL
 
SELECT pjbBMethodE0.BILL_METHOD_NAME RES_REVENUE_METHOD_NAME
 ,pjbBTypeE0.BILL_TYPE_DESC RES_REVENUE_METHOD_CLASSIFICAT
 ,(SELECT MEANING FROM FND_LOOKUPS
	WHERE LOOKUP_CODE = pjbBMethodE0.METHOD_CFG_TYPE
	AND LOOKUP_TYPE = 'ORA_PJB_CFG_BILL_METHOD_TYPE') RES_METHOD_ASSIGNMENT_TYPE
 ,(SELECT MEANING FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJB_STATUS'
	AND LOOKUP_CODE = pjbBMethodE0.ACTIVE_FLAG) RES_STATUS
 ,DECODE(pjbBMethodE0.INTERCOMPANY_METHOD,'Y','Yes','No') RES_USE_REVENUE_METHOD_FOR_INT
 ,(SELECT MEANING FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJB_LABOR_SCHEDULE'
	AND LOOKUP_CODE = pjbBMethodE0.LABOR_RATE_TYPE_CODE) RES_LABOR_SCHEDULE_TYPE
 ,(SELECT MEANING FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJB_LABOR_SCHEDULE'
	AND LOOKUP_CODE = pjbBMethodE0.NL_RATE_TYPE_CODE) RES_NONLABOR_SCHEDULE_TYPE
 ,pjbBMethodE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
 ,pjbBMethodE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
 ,pjbBMethodE0.CREATED_BY RSC_CREATED_BY
 ,pjbBMethodE0.CREATION_DATE RSC_CREATION_DATE
 ,NULL RSC_LEDGER_ID
 ,NULL RSC_CHART_OF_ACCOUNTS_ID
 ,NULL RSC_BUSINESS_UNIT_ID
 ,NULL RSC_LEGAL_ENTITY_ID
 ,NULL RSC_ORGANIZATION_ID
 ,NULL RSC_BUSINESS_GROUP_ID
 ,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJB_BILLING_METHODS_VL pjbBMethodE0
 ,PJB_BILL_TYPE_CLASSES_VL pjbBTypeE0
WHERE pjbBMethodE0.BILL_TYPE_CLASS_CODE = pjbBTypeE0.BILL_TYPE_CLASSIFICATION_CODE
AND pjbBMethodE0.BILL_METHOD_FLAG = 'R'
ORDER BY pjbBTypeE0.BILL_TYPE_DESC,pjbBMethodE0.BILL_METHOD_NAME



