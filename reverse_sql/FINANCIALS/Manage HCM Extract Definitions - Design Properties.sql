-- RSC_PREREQUISITE_OBJECTS=PER_EXT_DEFINITIONS_VL

SELECT extDefsE0.BASE_DEFINITION_NAME RES_NAME
/*,(SELECT BLOCK_NAME
    FROM PAY_REPORT_BLOCKS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND REPORT_BLOCK_ID = objRelatesE0.PARENT_BLOCK_ID
    ) PARENT_BLOCK*/
,objsE0.BLOCK_NAME RES_PROPERTIES_NAME
,objsE0.XML_TAG_NAME RES_TAG_NAME
,(SELECT BASE_USER_ENTITY_NAME
    FROM FF_USER_ENTITIES_B
    WHERE USER_ENTITY_ID = objsE0.USER_ENTITY_ID
    ) RES_USER_ENTITY
,(SELECT groupsE0.GROUP_NAME
    FROM FF_DBI_USAGES usagesE0
    ,FF_DBI_GROUPS_VL groupsE0
    WHERE groupsE0.DBI_GROUP_ID = usagesE0.DBI_GROUP_ID
    AND usagesE0.DBI_ID = objsE0.MULTI_DBI_ID
    ) RES_THREADING_DATABASE_ITEM
,DECODE(extDefsE0.ATTRIBUTE22,'Y','Yes','No') RES_ENABLE_OBJECT_SNAPSHOT
,(CASE WHEN extDefsE0.ATTRIBUTE22 = 'Y'
	THEN (SELECT groupsE0.GROUP_NAME FROM FF_DBI_USAGES usagesE0,FF_DBI_GROUPS_VL groupsE0
			WHERE groupsE0.DBI_GROUP_ID = usagesE0.DBI_GROUP_ID
			AND usagesE0.DBI_ID = objsE0.MULTI_DBI_ID)
	ELSE NULL END)RES_OBJECT_SNAPSHOT_KEY
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = objsE0.MULTI_THREAD_ACT_TYPE 
	AND LOOKUP_TYPE = 'PER_EXT_THREAD_ACTIONS' AND LANGUAGE = USERENV('LANG'))  RES_THREADING_ACTION_TYPE
,(SELECT groupsE0.GROUP_NAME
    FROM FF_DBI_USAGES usagesE0
    ,FF_DBI_GROUPS_VL groupsE0
    WHERE groupsE0.DBI_GROUP_ID = usagesE0.DBI_GROUP_ID
    AND usagesE0.DBI_ID = objsE0.INTERLOCK_DBI_ID 
    ) RES_INTERLOCKING_DATABASE_ITEM
,DECODE(objsE0.MAIN_BLOCK_FLAG,'Y','Yes','No') RES_ROOT_DATA_GROUP
,objsE0.DESCRIPTION RES_DESCRIPTION
--,formularsE0.DISP_CRITERIA_EXP --> might not be able to translate
,objsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,objsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,objsE0.CREATED_BY RSC_CREATED_BY
,objsE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,(SELECT LEGISLATION_CODE
	FROM PER_LEGISLATIVE_DATA_GROUPS_VL
	WHERE LEGISLATIVE_DATA_GROUP_ID = extDefsE0.LEGISLATIVE_DATA_GROUP_ID
	) RSC_COUNTRY_ID

FROM PER_EXT_DEFINITIONS_VL extDefsE0
,PAY_REPORT_BLOCKS_VL objsE0
/*,(SELECT DISTINCT REPORT_BLOCK_ID
    ,PARENT_BLOCK_ID
    FROM PER_EXT_BLOCK_LINKS
    ) objRelatesE0
,PAY_REP_CRITERIA_F formularsE0*/
WHERE extDefsE0.LEGISLATION_CODE IS NULL
AND extDefsE0.CREATOR_ID IS NULL
AND extDefsE0.EXT_DEFINITION_ID = objsE0.EXT_DEFINITION_ID
/*AND objsE0.REPORT_BLOCK_ID = objRelatesE0.REPORT_BLOCK_ID(+)
AND objsE0.REPORT_BLOCK_ID = formularsE0.REPORT_BLOCK_ID(+)*/
ORDER BY extDefsE0.BASE_DEFINITION_NAME