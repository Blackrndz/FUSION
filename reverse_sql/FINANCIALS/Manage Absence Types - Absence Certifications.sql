-- RSC_PREREQUISITE_OBJECTS=ANC_ABSENCE_TYPES_VL 

SELECT DISTINCT ancABSENTYPEEO.NAME RES_NAME
,(SELECT TERRITORY_SHORT_NAME 
	FROM FND_TERRITORIES_TL 
	WHERE LANGUAGE = USERENV('LANG')
	AND TERRITORY_CODE = ancABSENTYPEEO.LEGISLATION_CODE
	) RES_LEGISLATION
,(SELECT NAME 
	FROM PER_LEGISLATIVE_DATA_GROUPS_TL 
	WHERE LANGUAGE = USERENV('LANG')
	AND LEGISLATIVE_DATA_GROUP_ID = ancABSENTYPEEO.LEGISLATIVE_DATA_GROUP_ID
	) RES_LEGISLATIVE_DATA_GROUP
,TO_CHAR(ancABSENTYPECEREO.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,ancABSCERVLEO.NAME RES_CERTIFICATION_NAME
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = ancABSCERVLEO.CERT_USE_CD
	AND LOOKUP_TYPE = 'ANC_ABS_CERT_USE' 
	AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_TYPE
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = ancABSCERVLEO.CERT_STATUS_CD
	AND LOOKUP_TYPE = 'ANC_ABSENCE_CERT_STATUS' 
	AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_STATUS
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = ancABSCERVLEO.CERT_TRIGGER_CD
	AND LOOKUP_TYPE = 'ANC_ABS_CERT_TRIGGER' 
	AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_TRIGGER
,DECODE(TO_CHAR(ancABSENTYPECEREO.EFFECTIVE_END_DATE ,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(ancABSENTYPECEREO.EFFECTIVE_END_DATE ,'DD-Mon-YYYY')) RES_EFFECTIVE_END_DATE
,ancABSENTYPECEREO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ancABSENTYPECEREO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ancABSENTYPECEREO.CREATED_BY RSC_CREATED_BY
,ancABSENTYPECEREO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,ancABSENTYPEEO.LEGISLATION_CODE RSC_COUNTRY_ID

FROM ANC_ABSENCE_TYPES_VL ancABSENTYPEEO
,ANC_ABS_TYPE_CERTS_F ancABSENTYPECEREO
,ANC_ABS_CERTIFICATIONS_VL ancABSCERVLEO
WHERE ancABSENTYPEEO.ABSENCE_TYPE_ID = ancABSENTYPECEREO.ABSENCE_TYPE_ID
AND ancABSENTYPECEREO.ABSENCE_CERTIFICATION_ID =  ancABSCERVLEO.ABSENCE_CERTIFICATION_ID
AND ancABSCERVLEO.LANGUAGE = USERENV('LANG')
AND NVL(ancABSENTYPEEO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) BETWEEN ancABSENTYPECEREO.EFFECTIVE_START_DATE AND NVL(ancABSENTYPECEREO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND NVL(ancABSENTYPECEREO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) BETWEEN ancABSCERVLEO.EFFECTIVE_START_DATE AND NVL(ancABSCERVLEO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY ancABSENTYPEEO.NAME
,4
,ancABSCERVLEO.NAME