-- RSC_PREREQUISITE_OBJECTS=CMP_RANGE_DIFFS_F_VL
  
SELECT cmpRANGEDIFFEO.CODE RES_CODE
,(SELECT NAME FROM HR_ALL_ORGANIZATION_UNITS_F_VL WHERE  ORGANIZATION_ID = cmpRANGEVALEO.BUSINESS_UNIT_ID
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_BUSINESS_UNIT
,(SELECT LOCATION_NAME FROM PER_LOCATION_DETAILS_F_VL WHERE LOCATION_ID = cmpRANGEVALEO.LOCATION_ID
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE  AND NVL(EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_LOCATION
,cmpRANGEVALEO.DIFFERENTIAL RES_DIFFERENTIAL
,(SELECT GEOGRAPHY_NAME
	FROM HZ_GEOGRAPHIES 
	WHERE GEOGRAPHY_ID = cmpRANGEVALEO.GEOGRAPHY_ID) RES_COMPENSATION_ZONE
,cmpRANGEVALEO.GRADE_RATE_MINIMUM_LIMIT RES_LOWER_LIMIT
,cmpRANGEVALEO.FREQUENCY RES_FREQUENCY_FOR_MINIMUM_AMOU
,cmpRANGEVALEO.ANNUALIZATION_FACTOR RES_ANNUALIZATION_FACTOR_FOR_M
,(SELECT NAME FROM PER_RATES_F_VL WHERE RATE_ID = cmpRANGEVALEO.GRADE_RATE_ID 
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_GRADE_RATE

,cmpRANGEVALEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,cmpRANGEVALEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,cmpRANGEVALEO.CREATED_BY RSC_CREATED_BY
,cmpRANGEVALEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM CMP_RANGE_DIFFS_F_VL cmpRANGEDIFFEO
,CMP_RANGE_DIFF_VALUES_F cmpRANGEVALEO
WHERE  cmpRANGEDIFFEO.RANGE_DIFF_ID = cmpRANGEVALEO.RANGE_DIFF_ID
AND cmpRANGEDIFFEO.LANGUAGE = USERENV('LANG')
AND SYSDATE BETWEEN cmpRANGEDIFFEO.EFFECTIVE_START_DATE  AND NVL(cmpRANGEDIFFEO.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))
AND SYSDATE BETWEEN cmpRANGEVALEO.EFFECTIVE_START_DATE  AND NVL(cmpRANGEVALEO.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))