SELECT projTemplatesE0.NAME RES_TEMPLATE_NAME
,projTemplatesE0.SEGMENT1 RES_TEMPLATE_NUMBER
,(CASE
	WHEN EXISTS (SELECT 1
		FROM PJF_PROJECT_OPTIONS
		WHERE OPTION_CODE = 'COSTING_CURRENCY'
		AND ENABLED = 'Y'
		AND PROJECT_ID = projTemplatesE0.PROJECT_ID) THEN
			projTemplatesE0.PROJECT_CURRENCY_CODE 
	END) RES_PROJECT_CURRENCY
,(CASE
	WHEN EXISTS (SELECT 1
		FROM PJF_PROJECT_OPTIONS
		WHERE OPTION_CODE = 'COSTING_CURRENCY'
		AND ENABLED = 'Y'
		AND PROJECT_ID = projTemplatesE0.PROJECT_ID) THEN
			(SELECT USER_CONVERSION_TYPE 
			FROM GL_DAILY_CONVERSION_TYPES 
			WHERE CONVERSION_TYPE = projTemplatesE0.CURRENCY_CONV_RATE_TYPE)
	END) RES_RATE_TYPE
,(CASE
	WHEN EXISTS (SELECT 1
		FROM PJF_PROJECT_OPTIONS
		WHERE OPTION_CODE = 'COSTING_CURRENCY'
		AND ENABLED = 'Y'
		AND PROJECT_ID = projTemplatesE0.PROJECT_ID) THEN
			(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'PJF_DEF_RATE_DATE_CODE'
			AND LOOKUP_CODE = projTemplatesE0.CURRENCY_CONV_DATE_TYPE_CODE)
    END) RES_DATE_TYPE
,(CASE
	WHEN EXISTS (SELECT 1
		FROM PJF_PROJECT_OPTIONS
		WHERE OPTION_CODE = 'COSTING_CURRENCY'
		AND ENABLED = 'Y'
		AND PROJECT_ID = projTemplatesE0.PROJECT_ID) THEN
			TO_CHAR(projTemplatesE0.CURRENCY_CONV_DATE,'DD-Mon-YYYY')
	END) RES_DATE
,(CASE
	WHEN EXISTS (SELECT 1
		FROM PJF_PROJECT_OPTIONS
		WHERE OPTION_CODE = 'CAPITALIZATION_OPTIONS'
		AND ENABLED = 'Y'
		AND PROJECT_ID = projTemplatesE0.PROJECT_ID) THEN
			DECODE(projTemplatesE0.CINT_ELIGIBLE_FLAG,'Y','Yes','No') 
	END) RES_ALLOW_CAPITALIZED_INTEREST
,(CASE
	WHEN EXISTS (SELECT 1
		FROM PJF_PROJECT_OPTIONS
		WHERE OPTION_CODE = 'CAPITALIZATION_OPTIONS'
		AND ENABLED = 'Y'
		AND PROJECT_ID = projTemplatesE0.PROJECT_ID) THEN
			(SELECT IND_SCH_NAME
			FROM PJF_IND_RATE_SCH_TL
			WHERE LANGUAGE = USERENV('LANG')
			AND IND_RATE_SCH_ID = projTemplatesE0.CINT_RATE_SCH_ID)
    END) RES_CAPITALIZED_INTEREST_RATE_
,(CASE
	WHEN EXISTS (SELECT 1
		FROM PJF_PROJECT_OPTIONS
		WHERE OPTION_CODE = 'CAPITALIZATION_OPTIONS'
		AND ENABLED = 'Y'
		AND PROJECT_ID = projTemplatesE0.PROJECT_ID) THEN
			TO_CHAR(projTemplatesE0.CINT_STOP_DATE,'DD-Mon-YYYY') 
	END) RES_CAPITALIZED_INTEREST_STOP_
,(CASE
	WHEN EXISTS (SELECT 1
		FROM PJF_PROJECT_OPTIONS
		WHERE OPTION_CODE = 'CAPITALIZATION_OPTIONS'
		AND ENABLED = 'Y'
		AND PROJECT_ID = projTemplatesE0.PROJECT_ID) THEN
			(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'PJC_ASSET_ALLOCATION_METHOD'
			AND LOOKUP_CODE = projTemplatesE0.ASSET_ALLOCATION_METHOD)
    END) RES_ASSET_COST_ALLOCATION_METH
,(CASE
	WHEN EXISTS (SELECT 1
		FROM PJF_PROJECT_OPTIONS
		WHERE OPTION_CODE = 'CAPITALIZATION_OPTIONS'
		AND ENABLED = 'Y'
		AND PROJECT_ID = projTemplatesE0.PROJECT_ID) THEN
			(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'PJC_CAPITAL_EVENT_PROCESSING'
			AND LOOKUP_CODE = projTemplatesE0.CAPITAL_EVENT_PROCESSING)
    END) RES_EVENT_PROCESSING_METHOD
,(CASE
	WHEN EXISTS (SELECT 1
		FROM PJF_PROJECT_OPTIONS
		WHERE OPTION_CODE = 'BURDEN_SCHEDULE_ASSIGNMENTS'
		AND ENABLED = 'Y'
		AND PROJECT_ID = projTemplatesE0.PROJECT_ID) THEN
			(SELECT IND_SCH_NAME
				FROM PJF_IND_RATE_SCH_TL
				WHERE LANGUAGE = USERENV('LANG')
				AND IND_RATE_SCH_ID = projTemplatesE0.COST_IND_RATE_SCH_ID)
    END) RES_BURDEN_SCHEDULE
,(CASE
	WHEN EXISTS (SELECT 1
		FROM PJF_PROJECT_OPTIONS
		WHERE OPTION_CODE = 'BURDEN_SCHEDULE_ASSIGNMENTS'
		AND ENABLED = 'Y'
		AND PROJECT_ID = projTemplatesE0.PROJECT_ID) THEN
			TO_CHAR(projTemplatesE0.COST_IND_SCH_FIXED_DATE,'DD-Mon-YYYY') 
	END) RES_FIXED_DATE
,(CASE
	WHEN EXISTS (SELECT 1
		FROM PJF_PROJECT_OPTIONS
		WHERE OPTION_CODE = 'BURDEN_SCHEDULE_ASSIGNMENTS'
		AND ENABLED = 'Y'
		AND PROJECT_ID = projTemplatesE0.PROJECT_ID) THEN
			''
	END) RES_ASSIGNMENT_OPTION
,projTemplatesE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,projTemplatesE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,projTemplatesE0.CREATED_BY  RSC_CREATED_BY
,projTemplatesE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,projTemplatesE0.ORG_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJF_PROJECTS_ALL_VL projTemplatesE0
,FUN_ALL_BUSINESS_UNITS_V buE0
WHERE projTemplatesE0.TEMPLATE_FLAG = 'Y'
AND projTemplatesE0.ORG_ID = buE0.BU_ID
ORDER BY projTemplatesE0.NAME