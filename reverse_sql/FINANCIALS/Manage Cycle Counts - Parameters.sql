SELECT (SELECT ORGANIZATION_CODE 
	FROM INV_ORG_PARAMETERS
	WHERE ORGANIZATION_ID = CycleCountHE0.ORGANIZATION_ID
	) RES_ORGANIZATION
,CycleCountHE0.CYCLE_COUNT_HEADER_NAME RES_COUNT_NAME
,CycleCountHE0.NEXT_USER_COUNT_SEQUENCE RES_STARTING_COUNT_SEQUENCE
,CycleCountHE0.DAYS_UNTIL_LATE RES_MAXIMUM_DAYS_BEFORE_LATE
,CycleCountHE0.MAXIMUM_AUTO_RECOUNTS RES_MAXIMUM_RECOUNTS
,TO_CHAR(CycleCountHE0.START_DATE,'DD-Mon-YYYY') RES_START_DATE
,TO_CHAR(CycleCountHE0.END_DATE,'DD-Mon-YYYY') RES_END_DATE
,DECODE(CycleCountHE0.UNSCHEDULED_COUNT_ENTRY,1,'Yes','No') RES_MANUAL_COUNTS_ALLOWED
,(CASE
	WHEN CycleCountHE0.UNSCHEDULED_COUNT_ENTRY = 1 THEN
		CycleCountHE0.START_MANUAL_COUNTS_PREFIX 
	END) RES_MANUAL_COUNT_SEQUENCE_PREF
,DECODE(CycleCountHE0.ZERO_COUNT_FLAG,1,'Yes','No') RES_COUNT_ZERO_QUANTITY
,DECODE(CycleCountHE0.ONHAND_VISIBLE_FLAG,1,'Yes','No') RES_DISPLAY_SUGGESTED_QUANTITY
,DECODE(CycleCountHE0.ALLOW_SERIAL_ITEMS,1,'Yes','No') RES_SERIALIZED_ITEMS_ALLOWED
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'INV_CC_SERIAL_COUNT_OPTIONS'
	AND LOOKUP_CODe = CycleCountHE0.SERIAL_COUNT_OPTION
	) RES_SERIAL_NUMBERS_PER_COUNT
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'INV_CC_SERIAL_ADJ_OPTIONS'
	AND LOOKUP_CODe = CycleCountHE0.SERIAL_ADJUSTMENT_OPTION
	) RES_SERIAL_NUMBER_ADJUSTMENT
,DECODE(CycleCountHE0.SERIAL_DETAIL_OPTION,1,'Yes','No') RES_RECORD_COUNT_SERIALS
,DECODE(CycleCountHE0.SERIAL_DISCREPANCY_OPTION,1,'Yes','No') RES_SERIAL_DISCREPANCY_ALLOWED
,CycleCountHE0.HIT_MISS_TOLERANCE_POSITIVE RES_POSITIVE_HIT_AND_MISS_PERC
,CycleCountHE0.HIT_MISS_TOLERANCE_NEGATIVE RES_NEGATIVE_HIT_AND_MISS_PERC
,CycleCountHE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,CycleCountHE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,CycleCountHE0.CREATED_BY  RSC_CREATED_BY
,CycleCountHE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,CycleCountHE0.ORGANIZATION_ID RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID
   
FROM INV_CYCLE_COUNT_HEADERS CycleCountHE0
ORDER BY 1,2