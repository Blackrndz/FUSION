-- RSC_PREREQUISITE_OBJECTS=PER_CHECKLIST_ACTIONS

SELECT chklistsE0.NAME RES_CHECKLIST_NAME
,DECODE(configActionsE0.EVENT_CODE,'ORA_CHK_PER_MAR_STATUS','Person Legislative Data - Marital Status'
    ,'ORA_CHK_PER_ETHNICITY','Person Ethnicity - Ethnicity'
    ,'ORA_CHK_PER_NID_TYPE','Person National Identifier - National Identifier Type'
    ,'ORA_CHK_PER_NID_NUMBER','Person National Identifier - National Identifier Number'
    ,'ORA_CHK_PER_RELIGION','Person Religion - Religion'
    ,'ORA_CHK_PER_CONTACT_REL_TYPE','Person Contact Relationship - Contact Type'
    ,'ORA_CHK_PER_DISABILITY_CATEGORY','Person Disability - Disability Category'
    ,'ORA_CHK_PER_ADDR_USAGE_TYPE','Person Address - Address Type'
    ,'ORA_CHK_PER_DOR_TYPE','Person Document Record - Document Type'
    ,'ORA_CHK_PER_LAST_NAME','Person Name - Last Name'
    ,'ORA_CHK_PER_FIRST_NAME','Person Name - First Name'
    ,'ORA_CHK_PER_MIDDLE_NAMES','Person Name - Middle Names'
    ) RES_EVENT_NAME
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ORA_CHK_EVENT_VALUE'
    AND LOOKUP_CODE = configActionsE0.OLD_VALUE
     ) RES_OLD_VALUE_TYPE
,(CASE
    WHEN configActionsE0.OLD_VALUE = 'ORA_SPECIFIC_VALUE' THEN
        (CASE
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_ETHNICITY' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'ORA_PER_ETHNICITY'
					AND LOOKUP_CODE = configActionsE0.OLD_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_MAR_STATUS' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'ORA_PER_MAR_STATUS'
					AND LOOKUP_CODE = configActionsE0.OLD_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_NID_TYPE' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'ORA_PER_NATIONAL_IDENTIFIER_TY'
					AND LOOKUP_CODE = configActionsE0.OLD_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_RELIGION' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'PER_RELIGION'
					AND LOOKUP_CODE = configActionsE0.OLD_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_CONTACT_REL_TYPE' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'ORA_PER_CONTACT'
					AND LOOKUP_CODE = configActionsE0.OLD_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_DISABILITY_CATEGORY' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'DISABILITY_CATEGORY'
					AND LOOKUP_CODE = configActionsE0.OLD_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_ADDR_USAGE_TYPE' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'ADDRESS_TYPE'
					AND LOOKUP_CODE = configActionsE0.OLD_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_DOR_TYPE' THEN 
				(SELECT DOCUMENT_TYPE
					FROM HR_DOCUMENT_TYPES_TL
					WHERE LANGUAGE = USERENV('LANG')
					AND DOCUMENT_TYPE_ID = configActionsE0.OLD_VALUE_SPECIFIC)
			ELSE
				configActionsE0.OLD_VALUE_SPECIFIC
			END)
    END) RES_OLD_VALUE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ORA_CHK_EVENT_VALUE'
    AND LOOKUP_CODE = configActionsE0.NEW_VALUE
     ) RES_NEW_VALUE_TYPE
,(CASE
    WHEN configActionsE0.OLD_VALUE = 'ORA_SPECIFIC_VALUE' THEN
        (CASE
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_ETHNICITY' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'ORA_PER_ETHNICITY'
					AND LOOKUP_CODE = configActionsE0.NEW_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_MAR_STATUS' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'ORA_PER_MAR_STATUS'
					AND LOOKUP_CODE = configActionsE0.NEW_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_NID_TYPE' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'ORA_PER_NATIONAL_IDENTIFIER_TY'
					AND LOOKUP_CODE = configActionsE0.NEW_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_RELIGION' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'PER_RELIGION'
					AND LOOKUP_CODE = configActionsE0.NEW_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_CONTACT_REL_TYPE' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'ORA_PER_CONTACT'
					AND LOOKUP_CODE = configActionsE0.NEW_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_DISABILITY_CATEGORY' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'DISABILITY_CATEGORY'
					AND LOOKUP_CODE = configActionsE0.NEW_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_ADDR_USAGE_TYPE' THEN 
				(SELECT MEANING
					FROM FND_LOOKUP_VALUES
					WHERE LANGUAGE = USERENV('LANG')
					AND LOOKUP_TYPE = 'ADDRESS_TYPE'
					AND LOOKUP_CODE = configActionsE0.NEW_VALUE_SPECIFIC)
			WHEN configActionsE0.EVENT_CODE = 'ORA_CHK_PER_DOR_TYPE' THEN 
				(SELECT DOCUMENT_TYPE
					FROM HR_DOCUMENT_TYPES_TL
					WHERE LANGUAGE = USERENV('LANG')
					AND DOCUMENT_TYPE_ID = configActionsE0.NEW_VALUE_SPECIFIC)
			ELSE
				configActionsE0.NEW_VALUE_SPECIFIC
			END)
    END) RES_NEW_VALUE
,DECODE(configActionsE0.ENABLED_FLAG,'Y','Yes','No') RES_ACTIVE
,configActionsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,configActionsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,configActionsE0.CREATED_BY RSC_CREATED_BY
,configActionsE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PER_CHECKLISTS_VL chklistsE0
,PER_CHECKLIST_ACTIONS configActionsE0
WHERE chklistsE0.CHECKLIST_ID = configActionsE0.CHECKLIST_ID
AND configActionsE0.ACTION_TYPE = 'ORA_PERSON_DATA_CHANGE'
ORDER BY chklistsE0.NAME