SELECT benELIGYEO.NAME RES_NAME
,benELIGGRDEO.ORDR_NUM RES_SEQUENCE
,perGRADESEO.NAME RES_GRADE
,(SELECT SET_NAME FROM FND_SETID_SETS WHERE perGRADESEO.SET_ID = SET_ID 
	AND LANGUAGE = USERENV('LANG')) RES_GRADE_SET_NAME
,DECODE(benELIGGRDEO.EXCLD_FLAG,'Y','Yes','No') RES_EXCLUDE
,TO_CHAR(benELIGGRDEO.START_DATE,'DD-Mon-YYYY') RES_START_DATE
,TO_CHAR(benELIGGRDEO.END_DATE,'DD-Mon-YYYY') RES_END_DATE


,benELIGGRDEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,benELIGGRDEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,benELIGGRDEO.CREATED_BY  RSC_CREATED_BY
,benELIGGRDEO.CREATION_DATE  RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_ELIGY_PRFL benELIGYEO
,BEN_ELIG_GRD_PRTE benELIGGRDEO
,PER_GRADES_F_VL perGRADESEO
WHERE benELIGYEO.ELIGY_PRFL_ID = benELIGGRDEO.ELIGY_PRFL_ID 
AND benELIGGRDEO.GRADE_ID =  perGRADESEO.GRADE_ID
AND SYSDATE BETWEEN perGRADESEO.EFFECTIVE_START_DATE AND NVL(perGRADESEO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND benELIGYEO.END_DATE BETWEEN benELIGGRDEO.START_DATE AND NVL(benELIGGRDEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND SYSDATE BETWEEN benELIGYEO.START_DATE AND NVL(benELIGYEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY benELIGYEO.NAME,benELIGGRDEO.ORDR_NUM