-- RSC_PREREQUISITE_OBJECTS=QP_PRICING_STRATEGIES_TL
-- RSC_PREREQUISITE_OBJECTS=QP_PRICING_STRATEGIES_ALL_B
 
SELECT QpStrategiesTLE0.NAME RES_NAME
,QpStrategiesTLE0.DESCRIPTION RES_DESCRIPTION
,(SELECT BU_NAME
	FROM FUN_ALL_BUSINESS_UNITS_V
	WHERE BU_ID = QpStrategiesBE0.ORG_ID
	AND SYSDATE BETWEEN DATE_FROM AND DATE_TO
	) RES_BUSINESS_UNIT
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_TYPE = 'ORA_QP_APPROVAL_STATUSES'
	AND LOOKUP_CODE = QpStrategiesBE0.STATUS_CODE 
	AND LANGUAGE = USERENV('LANG')
	) RES_STATUS
,QpStrategiesBE0.DEFAULT_CURRENCY_CODE||' - '||(SELECT NAME FROM FND_CURRENCIES_VL WHERE CURRENCY_CODE = QpStrategiesBE0.DEFAULT_CURRENCY_CODE) RES_DEFAULT_CURRENCY
,(SELECT USER_CONVERSION_TYPE
	FROM GL_DAILY_CONVERSION_TYPES
	WHERE CONVERSION_TYPE = QpStrategiesBE0.DEFAULT_GL_CONV_TYPE_CODE
	) RES_DEFAULT_GL_CONVERSION_TYPE
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_TYPE = 'ORA_QP_STRATEGY_OBJECTIVES'
	AND LOOKUP_CODE = QpStrategiesBE0.OBJECTIVE_CODE
	AND LANGUAGE = USERENV('LANG')
	) RES_OBJECTIVE
,TO_CHAR(QpStrategiesBE0.START_DATE,'DD-Mon-YYYY HH24:MI:SS') RES_START_DATE
,TO_CHAR(QpStrategiesBE0.END_DATE,'DD-Mon-YYYY HH24:MI:SS') RES_END_DATE
,DECODE(QpStrategiesBE0.ALLOW_PRICE_LIST_OVERRIDE_FLAG,'Y','Yes','No') RES_ALLOW_PRICE_LIST_OVERRIDE
,DECODE(QpStrategiesBE0.ALLOW_CURRENCY_OVERRIDE_FLAG,'Y','Yes','No') RES_ALLOW_CURRENCY_OVERRIDE
,QpStrategiesBE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,QpStrategiesBE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,QpStrategiesBE0.CREATED_BY  RSC_CREATED_BY
,QpStrategiesBE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,QpStrategiesBE0.ORG_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM QP_PRICING_STRATEGIES_TL QpStrategiesTLE0
,QP_PRICING_STRATEGIES_ALL_B QpStrategiesBE0
WHERE QpStrategiesTLE0.PRICING_STRATEGY_ID = QpStrategiesBE0.PRICING_STRATEGY_ID
AND QpStrategiesTLE0.LANGUAGE = USERENV('LANG')
ORDER BY QpStrategiesTLE0.NAME