 --RSC_PREREQUISITE_OBJECTS=CMP_BUDGET_POOLS_VL
 
SELECT cmpPLANEO.PLAN_NAME RES_PLAN
,cmpBUDGETPOOLEO.POOL_NAME RES_POOL_NAME
,cmpBUDGETPOOLEO.ORDER_NUM RES_DISPLAY_ORDER
,DECODE(cmpBUDGETPOOLEO.BUDGETING_STYLE,'EMP','Worker level budgeting','Manager level budgeting') RES_BUDGETING_METHOD
,(CASE WHEN cmpBUDGETPOOLEO.BUDGETING_STYLE = 'EMP' 
	THEN
		NULL
	ELSE
		(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = cmpBUDGETPOOLEO.BUDGETING_STYLE
			AND LOOKUP_TYPE = 'CMP_SETUP_STORE_BDGT_AS' AND LANGUAGE = USERENV('LANG')
			AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD')))
		END)  RES_BUDGET_STORAGE_METHOD
,(CASE WHEN cmpBUDGETPOOLEO.BUDGETING_STYLE = 'EMP' 
	THEN
		'No'
	ELSE
		DECODE(cmpBUDGETPOOLEO.AUTO_ISSUE_FLAG,'Y','Yes','No')
		END) RES_AUTOMATICALLY_PUBLISH_BUDG
,(CASE WHEN cmpBUDGETPOOLEO.NON_MONETARY_UOM IS NOT NULL
	THEN
		'Yes'
	ELSE
		'No'
		END) RES_BUDGET_IS_IN_NONMONETARY_U
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = cmpBUDGETPOOLEO.NON_MONETARY_UOM 
			AND LOOKUP_TYPE = 'CMP_NONMONETARY_UOM' AND LANGUAGE = USERENV('LANG')
			AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_NONMONETARY_UNITS
,DECODE(cmpBUDGETPOOLEO.PREVENT_OVER_BUDGET_FLAG,'Y','No','Yes') RES_ALLOW_OVER_BUDGET_WHEN_BUD
,cmpBUDGETPOOLEO.OVER_BUDGET_TOLERANCE RES_BUDGETING_MAXIMUM_PERCENTA
,DECODE(cmpBUDGETPOOLEO.PREVENT_OVER_ALLOCATE_FLAG,'Y','No','Yes') RES_ALLOW_OVER_BUDGET_WHEN_ALL
,cmpBUDGETPOOLEO.OVER_ALLOCATE_TOLERANCE RES_ALLOCATING_COMPENSATION_MA
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = cmpBUDGETPOOLEO.OVER_BUDGET_SEVERITY_CODE
			AND LOOKUP_TYPE = 'ORA_CMP_OVER_BUDGET_SEVERITY' AND LANGUAGE = USERENV('LANG')
			AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_SEVERITY_WHEN_OVER_BUDGET
,cmpBUDGETPOOLEO.GROUPING_COLUMN_CODE RES_GROUPING_COLUMN
,DECODE(cmpBUDGETPOOLEO.ENFORCE_BDGT_BY_GRP_FLAG,'Y','Yes','No') RES_GROUPING_COLUMN_ENFORCE_BU
,cmpBUDGETPOOLEO.MAX_PCT_OVER_ALLOCATE RES_GROUPING_COLUMN_MAXIMUM_PE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = cmpBUDGETPOOLEO.OVER_BUDGET_GROUP_SEV_CODE
			AND LOOKUP_TYPE = 'ORA_CMP_OVER_BUDGET_SEVERITY' AND LANGUAGE = USERENV('LANG')
			AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_GROUPING_COLUMN_SEVERITY_W
,DECODE(cmpBUDGETPOOLEO.HIDE_FROM_MANAGERS_FLAG,'Y','Yes','No') RES_HIDE_FROM_MANAGERS

,cmpBUDGETPOOLEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,cmpBUDGETPOOLEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,cmpBUDGETPOOLEO.CREATED_BY RSC_CREATED_BY
,cmpBUDGETPOOLEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM CMP_PLANS_VL cmpPLANEO
,CMP_BUDGET_POOLS_VL cmpBUDGETPOOLEO
WHERE cmpPLANEO.PLAN_ID = cmpBUDGETPOOLEO.PLAN_ID
AND cmpPLANEO.COMP_TYPE = 'CWB'
ORDER BY cmpPLANEO.PLAN_NAME,cmpBUDGETPOOLEO.POOL_NAME,cmpBUDGETPOOLEO.ORDER_NUM