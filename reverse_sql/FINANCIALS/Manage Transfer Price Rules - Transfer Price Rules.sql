SELECT tpRulesE0.TP_RULE_NAME RES_NAME
,tpRulesE0.DESCRIPTION RES_DESCRIPTION
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJF_TP_RULE_TYPE_CODE'
	AND LOOKUP_CODE = tpRulesE0.RULE_TYPE_CODE
	) RES_TYPE
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJF_TP_RULE_BASE_CODE'
	AND LOOKUP_CODE = tpRulesE0.MARKUP_CALC_BASE_CODE
	) RES_TRANSFER_PRICE_BASIS
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJF_TP_RULE_CALC_METHOD_CODE'
	AND LOOKUP_CODE = tpRulesE0.CALC_METHOD_CODE
	) RES_CALCULATION_METHOD
,(SELECT IND_SCH_NAME
	FROM PJF_IND_RATE_SCH_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND IND_RATE_SCH_ID = tpRulesE0.IND_RATE_SCH_ID
	) RES_BURDEN_SCHEDULE
,(SELECT RATE_SCHEDULE_NAME
	FROM PJF_RATE_SCHEDULES_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND RATE_SCHEDULE_ID = tpRulesE0.RATE_SCHEDULE_ID
	) RES_RATE_SCHEDULE
,tpRulesE0.PERCENTAGE_APPLIED RES_MARKUP_OR_DISCOUNT_PERCENT
,TO_CHAR(tpRulesE0.START_DATE_ACTIVE,'DD-Mon-YYYY') RES_FROM_DATE
,TO_CHAR(tpRulesE0.END_DATE_ACTIVE,'DD-Mon-YYYY') RES_TO_DATE
,tpRulesE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,tpRulesE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,tpRulesE0.CREATED_BY RSC_CREATED_BY
,tpRulesE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJF_TP_RULES tpRulesE0
ORDER BY tpRulesE0.TP_RULE_NAME