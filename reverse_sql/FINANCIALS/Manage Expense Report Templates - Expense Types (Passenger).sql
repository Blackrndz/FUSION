-- RSC_PREREQUISITE_OBJECTS=EXM_EXP_TYPE_PASS_NAME_MAP

SELECT ExpenseTemplateEO.NAME RES_EXPENSE_REPORT_TEMPLATES_N
,(SELECT BU_NAME
	FROM FUN_ALL_BUSINESS_UNITS_V
	WHERE BU_ID = ExpenseTemplateEO.ORG_ID
	) RES_BUSINESS_UNIT
,ExpenseTypeEO.NAME RES_EXPENSE_TYPE_NAME
,'Yes' RES_ENFORCE_PASSENGER_NAME_TO_
,TO_CHAR(ExpensePassengerE0.START_DATE,'DD-Mon-YYYY') RES_START_DATE
,TO_CHAR(ExpensePassengerE0.END_DATE,'DD-Mon-YYYY') RES_END_DATE
,(SELECT DISPLAYED_FIELD
	FROM EXM_LOOKUP_VALUES
	WHERE LOOKUP_TYPE = 'EXM_PREFERRED_ENFORCEMENT'
	AND ENABLED_FLAG  = 'Y'
	AND LOOKUP_CODE   = ExpensePassengerE0.PREFERRED_ENFORCEMENT_CODE
	) RES_POLICY_ENFORCEMENT
,DECODE(ExpensePassengerE0.DISP_WARNING_TO_USER_FLAG,'Y','Yes','No') RES_DISPLAY_WARNING_TO_USER
,ExpensePassengerE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ExpensePassengerE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ExpensePassengerE0.CREATED_BY RSC_CREATED_BY
,ExpensePassengerE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,ExpenseTemplateEO.ORG_ID RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM EXM_EXPENSE_TEMPLATES ExpenseTemplateEO
,EXM_EXPENSE_TYPES ExpenseTypeEO
,(SELECT EXPENSE_TYPE_ID
	,START_DATE
	,END_DATE
	,PREFERRED_ENFORCEMENT_CODE
	,DISP_WARNING_TO_USER_FLAG
	,LAST_UPDATED_BY
	,LAST_UPDATE_DATE
	,CREATED_BY
	,CREATION_DATE
	,ROW_NUMBER() OVER (PARTITION BY EXPENSE_TYPE_ID ORDER BY LAST_UPDATE_DATE DESC) ROWX
	FROM EXM_EXP_TYPE_PASS_NAME_MAP
	) ExpensePassengerE0
WHERE ExpenseTemplateEO.EXPENSE_TEMPLATE_ID = ExpenseTypeEO.EXPENSE_TEMPLATE_ID
AND ExpenseTypeEO.EXPENSE_TYPE_ID = ExpensePassengerE0.EXPENSE_TYPE_ID
AND ExpensePassengerE0.ROWX = 1
ORDER BY ExpenseTemplateEO.NAME
,RES_BUSINESS_UNIT
,ExpenseTypeEO.NAME