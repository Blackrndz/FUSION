SELECT pjnRATESCHEO.RATE_SCH_NAME RES_NAME
,FndTREEEO.TREE_NAME RES_DEFAULT_ORGANIZATION_HIERA
,FndTREEVerEO.TREE_VERSION_NAME	RES_HIERARCHY_VERSION
,HROrgUNITEO.NAME RES_HIERARCHY_START_ORGANIZATI
,TO_CHAR(pjnRATESCHEO.START_DATE_ACTIVE,'DD-Mon-YYYY') RES_FROM_DATE
,TO_CHAR(pjnRATESCHEO.END_DATE_ACTIVE,'DD-Mon-YYYY') RES_TO_DATE
,pjnRATESCHEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,pjnRATESCHEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,pjnRATESCHEO.CREATED_BY RSC_CREATED_BY
,pjnRATESCHEO.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJC_CINT_RATE_SCH_VL pjnRATESCHEO
,FND_TREE_VL FndTREEEO
,FND_TREE_VERSION_VL FndTREEVerEO
,HR_ORGANIZATION_UNITS HROrgUNITEO
WHERE pjnRATESCHEO.ORG_TREE_STRUCTURE_CODE = FndTREEEO.TREE_STRUCTURE_CODE
AND pjnRATESCHEO.ORG_TREE_CODE = FndTREEEO.TREE_CODE
AND pjnRATESCHEO.ORG_TREE_STRUCTURE_CODE = FndTREEVerEO.TREE_STRUCTURE_CODE
AND pjnRATESCHEO.ORG_TREE_CODE = FndTREEVerEO.TREE_CODE
AND pjnRATESCHEO.ORG_TREE_VERSION_ID = FndTREEVerEO.TREE_VERSION_ID
AND pjnRATESCHEO.START_ORGANIZATION_ID = HROrgUNITEO.ORGANIZATION_ID
ORDER BY pjnRATESCHEO.RATE_SCH_NAME