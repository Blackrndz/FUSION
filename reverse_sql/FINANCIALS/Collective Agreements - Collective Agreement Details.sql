-- RSC_PREREQUISITE_OBJECTS=PER_COL_AGREEMENTS_F_VL

SELECT TO_CHAR(colAgreementsE0.EFFECTIVE_START_DATE,'DD-MM-YYYY') RES_EFFECTIVE_START_DATE
,DECODE(TO_CHAR(colAgreementsE0.EFFECTIVE_END_DATE,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(colAgreementsE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY')) RES_EFFECTIVE_END_DATE
,(SELECT TERRITORY_SHORT_NAME
    FROM FND_TERRITORIES_VL
    WHERE TERRITORY_CODE = colAgreementsE0.LEGISLATION_CODE
    ) RES_COUNTRY
,DECODE(colAgreementsE0.STATUS,'A','Active','I','Inactive') RES_STATUS

--Collective Agreement Details
,colAgreementsE0.IDENTIFICATION_CODE RES_IDENTIFICATION_CODE
,colAgreementsE0.COLLECTIVE_AGREEMENT_NAME RES_NAME
,colAgreementsE0.DESCRIPTION RES_DESCRIPTION
,colAgreementsE0.COMMENTS RES_COMMENTS
,(SELECT NAME
    FROM HR_ORGANIZATION_UNITS
    WHERE SYSDATE BETWEEN DATE_FROM AND DATE_TO
    AND ORGANIZATION_ID = colAgreementsE0.UNION_ID
    ) RES_UNION
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BARGAINING_UNIT_CODE'
    AND LOOKUP_CODE = colAgreementsE0.BARGAINING_UNIT_CODE
    ) RES_BARGAINING_UNIT
,(SELECT NAME
    FROM HR_ORGANIZATION_UNITS
    WHERE SYSDATE BETWEEN DATE_FROM AND DATE_TO
    AND ORGANIZATION_ID = colAgreementsE0.LEGAL_ENTITY_ID
    ) RES_LEGAL_EMPLOYER

--Employee Organization
,colAgreementsE0.EMPLOYEE_ORG_NAME RES_EMPLOYEE_ORGANIZATION
,colAgreementsE0.EMPLOYEE_ORG_CONTACT RES_EMPLOYEE_CONTACT

--Employer Organization
,colAgreementsE0.EMPLOYER_ORG_NAME RES_EMPLOYER_ORGANIZATION
,colAgreementsE0.EMPLOYER_ORG_CONTACT RES_EMPLOYER_CONTACT
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ORA_HRX_IT_COLL_AGREEMENT'
    AND LOOKUP_CODE = colAgreementsE0.CAG_INFORMATION1
    ) RES_COLLECTIVE_AGREEMENT_TYPE
,colAgreementsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,colAgreementsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,colAgreementsE0.CREATED_BY RSC_CREATED_BY
,colAgreementsE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,colAgreementsE0.LEGISLATION_CODE RSC_COUNTRY_ID

FROM PER_COL_AGREEMENTS_F_VL colAgreementsE0
ORDER BY colAgreementsE0.IDENTIFICATION_CODE
,colAgreementsE0.COLLECTIVE_AGREEMENT_NAME
,colAgreementsE0.EFFECTIVE_START_DATE