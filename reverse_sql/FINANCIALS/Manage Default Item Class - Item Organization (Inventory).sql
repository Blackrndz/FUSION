--RSC_PREREQUISITE_OBJECTS=EGP_SYSTEM_ITEMS_V
 
SELECT ItemClassPEO.ITEM_CLASS_CODE RES_INTERNAL_NAME
,ItemVersionEO.TEMPLATE_NAME RES_NAME
,(SELECT ORGANIZATION_CODE
	FROM INV_ORG_PARAMETERS
	WHERE ORGANIZATION_ID = ItemVersionEO.ORGANIZATION_ID
	) RES_ORGANIZATION
,DECODE(ItemVersionEO.INVENTORY_ITEM_FLAG,'Y','Yes','N','No') RES_INVENTORY_ITEM
,DECODE(ItemVersionEO.STOCK_ENABLED_FLAG,'Y','Yes','N','No') RES_STOCKED
,DECODE(ItemVersionEO.MTL_TRANSACTIONS_ENABLED_FLAG,'Y','Yes','N','No') RES_TRANSACTION_ENABLED
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_COMMON_YES_NO_NUM'
	AND LOOKUP_CODE = ItemVersionEO.RESERVABLE_TYPE
	) RES_RESERVABLE
,DECODE(ItemVersionEO.CHECK_SHORTAGES_FLAG,'Y','Yes','N','No') RES_CHECK_MATERIAL_SHORTAGE
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_COMMON_NO_YES_NUM'
	AND LOOKUP_CODE = ItemVersionEO.REVISION_QTY_CONTROL_CODE
	) RES_REVISION_CONTROL
,DECODE(ItemVersionEO.BULK_PICKED_FLAG,'Y','Yes','N','No') RES_BULK_PICKED
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'INV_LOT_CONTROL'
	AND LOOKUP_CODE = ItemVersionEO.LOT_CONTROL_CODE
	) RES_LOT_CONTROL
,ItemVersionEO.AUTO_LOT_ALPHA_PREFIX RES_LOT_STARTING_NUMBER
,ItemVersionEO.START_AUTO_LOT_NUMBER RES_LOT_STARTING_PREFIX
,ItemVersionEO.MATURITY_DAYS RES_LOT_MATURITY_DAYS
,ItemVersionEO.HOLD_DAYS RES_LOT_HOLD_DAYS
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'INV_SHELF_LIFE'
	AND LOOKUP_CODE = ItemVersionEO.SHELF_LIFE_CODE
	) RES_LOT_EXPIRATION_CONTROL
,ItemVersionEO.SHELF_LIFE_DAYS RES_LOT_EXPIRATION_SHELF_LIFE_
,ItemVersionEO.RETEST_INTERVAL RES_LOT_EXPIRATION_RETEST_INTE
,ItemVersionEO.EXPIRATION_ACTION_CODE RES_LOT_EXPIRATION_EXPIRATION_
,ItemVersionEO.EXPIRATION_ACTION_INTERVAL RES_LOT_EXPIRATION_EXPIRATIO_0
,DECODE(ItemVersionEO.CHILD_LOT_FLAG,'Y','Yes','N','No') RES_CHILD_LOT_CHILD_LOT_ENABLE
,DECODE(ItemVersionEO.CHILD_LOT_VALIDATION_FLAG,'Y','Yes','N','No') RES_CHILD_LOT_FORMAT_VALIDATIO
,DECODE(ItemVersionEO.COPY_LOT_ATTRIBUTE_FLAG,'Y','Yes','N','No') RES_CHILD_LOT_COPY_LOT_ATTRIBU
,ItemVersionEO.CHILD_LOT_PREFIX RES_CHILD_LOT_PREFIX
,ItemVersionEO.CHILD_LOT_STARTING_NUMBER RES_CHILD_LOT_STARTING_NUMBER
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_PARENT_CHILD_GEN_TYPE'
	AND LOOKUP_CODE = ItemVersionEO.PARENT_CHILD_GENERATION_FLAG
	) RES_CHILD_LOT_CHILD_LOT_GENERA
,DECODE(ItemVersionEO.LOT_TRANSLATE_ENABLED,'Y','Yes','N','No') RES_LOT_SPLIT_OR_MERGE_LOT_TRA
,DECODE(ItemVersionEO.LOT_SPLIT_ENABLED,'Y','Yes','N','No') RES_LOT_SPLIT_OR_MERGE_LOT_SPL
,DECODE(ItemVersionEO.LOT_DIVISIBLE_FLAG,'Y','Yes','N','No') RES_LOT_SPLIT_OR_MERGE_LOT_DIV
,DECODE(ItemVersionEO.LOT_SUBSTITUTION_ENABLED,'Y','Yes','N','No') RES_LOT_SPLIT_OR_MERGE_LOT_SUB
,DECODE(ItemVersionEO.LOT_MERGE_ENABLED,'Y','Yes','N','No') RES_LOT_SPLIT_OR_MERGE_LOT_MER
,DECODE(ItemVersionEO.GRADE_CONTROL_FLAG,'Y','Yes','N','No') RES_GRADE_CONTROLLED
,ItemVersionEO.DEFAULT_GRADE RES_DEFAULT_GRADE
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_SERIAL_NUMBER_CONTROL_TYPE'
	AND LOOKUP_CODE = ItemVersionEO.SERIAL_NUMBER_CONTROL_CODE
	) RES_SEIAL_GENERATION
,ItemVersionEO.AUTO_SERIAL_ALPHA_PREFIX RES_SEIAL_STARTING_PREFIX
,ItemVersionEO.START_AUTO_SERIAL_NUMBER RES_SEIAL_STARTING_NUMBER
,ItemVersionEO.NEGATIVE_MEASUREMENT_ERROR RES_NEGATIVE_MEASUREMENT_ERROR
,ItemVersionEO.POSITIVE_MEASUREMENT_ERROR RES_POSITIVE_MEASUREMENT_ERROR
,DECODE(ItemVersionEO.CYCLE_COUNT_ENABLED_FLAG,'Y','Yes','N','No') RES_CYCLE_COUNT_ENABLED
,DECODE(ItemVersionEO.LOT_STATUS_ENABLED,'Y','Yes','N','No') RES_LOT_STATUS_ENABLED
,DECODE(ItemVersionEO.SERIAL_STATUS_ENABLED,'Y','Yes','N','No') RES_SERIAL_STATUS_ENABLED
,(SELECT STATUS_CODE
	FROM INV_MATERIAL_STATUSES_VL
	WHERE STATUS_ID = ItemVersionEO.DEFAULT_LOT_STATUS_ID
	) RES_DEFAULT_LOT_STATUS
,(SELECT STATUS_CODE
	FROM INV_MATERIAL_STATUSES_VL
	WHERE STATUS_ID = ItemVersionEO.DEFAULT_SERIAL_STATUS_ID
	) RES_DEFAULT_SERIAL_STATUS
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_COMMON_YES_NO_NUM'
	AND LOOKUP_CODE = ItemVersionEO.RESTRICT_SUBINVENTORIES_CODE
	) RES_RESTRICT_SUBINVENTORY
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_COMMON_YES_NO_NUM'
	AND LOOKUP_CODE = ItemVersionEO.RESTRICT_LOCATORS_CODE
	) RES_RESTRICT_LOCATORS
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_LOCATOR_CONTROL_CODE_TYPE'
	AND LOOKUP_CODE = ItemVersionEO.LOCATION_CONTROL_CODE
	) RES_STOCK_LOCATOR_CONTROL
,(SELECT EXPENDITURE_TYPE_NAME
	FROM PJF_EXP_TYPES_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND EXPENDITURE_TYPE_ID = ItemVersionEO.EXPENDITURE_TYPE_ID 
	) RES_DEFAULT_EXPENDITURE_TYPE
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_EGP_HARD_PEGGING_LEVEL'
	AND LOOKUP_CODE = ItemVersionEO.HARD_PEGGING_LEVEL
	) RES_HARD_PEGGING_LEVEL
,DECODE(ItemVersionEO.COMN_SUPPLY_PRJ_DEMAND_FLAG,'Y','Yes','N','No') RES_ALLOW_USE_OF_COMMON_SUPPLI
,ItemVersionEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ItemVersionEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ItemVersionEO.CREATED_BY RSC_CREATED_BY
,ItemVersionEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,ItemVersionEO.ORGANIZATION_ID RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM EGP_ITEM_CLASSES_VL ItemClassPEO
,EGP_SYSTEM_ITEMS_V ItemVersionEO
WHERE ItemClassPEO.PARENT_ITEM_CLASS_ID NOT IN (SELECT ITEM_CLASS_ID FROM EGP_ITEM_CLASSES_VL)
AND ItemVersionEO.TEMPLATE_NAME IS NOT NULL
AND ItemClassPEO.ITEM_CLASS_ID = ItemVersionEO.ITEM_CATALOG_GROUP_ID
AND (ItemVersionEO.INVENTORY_ITEM_FLAG IS NOT NULL
OR ItemVersionEO.STOCK_ENABLED_FLAG IS NOT NULL
OR ItemVersionEO.MTL_TRANSACTIONS_ENABLED_FLAG IS NOT NULL
OR ItemVersionEO.RESERVABLE_TYPE IS NOT NULL
OR ItemVersionEO.CHECK_SHORTAGES_FLAG IS NOT NULL
OR ItemVersionEO.REVISION_QTY_CONTROL_CODE IS NOT NULL
OR ItemVersionEO.BULK_PICKED_FLAG IS NOT NULL
OR ItemVersionEO.LOT_CONTROL_CODE IS NOT NULL
OR ItemVersionEO.AUTO_LOT_ALPHA_PREFIX IS NOT NULL
OR ItemVersionEO.START_AUTO_LOT_NUMBER IS NOT NULL
OR ItemVersionEO.MATURITY_DAYS IS NOT NULL
OR ItemVersionEO.HOLD_DAYS IS NOT NULL
OR ItemVersionEO.SHELF_LIFE_CODE IS NOT NULL
OR ItemVersionEO.SHELF_LIFE_DAYS IS NOT NULL
OR ItemVersionEO.RETEST_INTERVAL IS NOT NULL
OR ItemVersionEO.EXPIRATION_ACTION_CODE IS NOT NULL
OR ItemVersionEO.EXPIRATION_ACTION_INTERVAL IS NOT NULL
OR ItemVersionEO.CHILD_LOT_FLAG IS NOT NULL
OR ItemVersionEO.CHILD_LOT_VALIDATION_FLAG IS NOT NULL
OR ItemVersionEO.COPY_LOT_ATTRIBUTE_FLAG IS NOT NULL
OR ItemVersionEO.CHILD_LOT_PREFIX IS NOT NULL
OR ItemVersionEO.CHILD_LOT_STARTING_NUMBER IS NOT NULL
OR ItemVersionEO.PARENT_CHILD_GENERATION_FLAG IS NOT NULL
OR ItemVersionEO.LOT_TRANSLATE_ENABLED IS NOT NULL
OR ItemVersionEO.LOT_SPLIT_ENABLED IS NOT NULL
OR ItemVersionEO.LOT_DIVISIBLE_FLAG IS NOT NULL
OR ItemVersionEO.LOT_SUBSTITUTION_ENABLED IS NOT NULL
OR ItemVersionEO.LOT_MERGE_ENABLED IS NOT NULL
OR ItemVersionEO.GRADE_CONTROL_FLAG IS NOT NULL
OR ItemVersionEO.DEFAULT_GRADE IS NOT NULL
OR ItemVersionEO.SERIAL_NUMBER_CONTROL_CODE  IS NOT NULL
OR ItemVersionEO.AUTO_SERIAL_ALPHA_PREFIX IS NOT NULL
OR ItemVersionEO.START_AUTO_SERIAL_NUMBER IS NOT NULL
OR ItemVersionEO.NEGATIVE_MEASUREMENT_ERROR IS NOT NULL
OR ItemVersionEO.POSITIVE_MEASUREMENT_ERROR IS NOT NULL
OR ItemVersionEO.CYCLE_COUNT_ENABLED_FLAG IS NOT NULL
OR ItemVersionEO.LOT_STATUS_ENABLED IS NOT NULL
OR ItemVersionEO.SERIAL_STATUS_ENABLED IS NOT NULL
OR ItemVersionEO.DEFAULT_LOT_STATUS_ID IS NOT NULL
OR ItemVersionEO.DEFAULT_SERIAL_STATUS_ID IS NOT NULL
OR ItemVersionEO.RESTRICT_SUBINVENTORIES_CODE IS NOT NULL
OR ItemVersionEO.RESTRICT_LOCATORS_CODE IS NOT NULL
OR ItemVersionEO.LOCATION_CONTROL_CODE IS NOT NULL
)
ORDER BY ItemClassPEO.ITEM_CLASS_CODE
,ItemVersionEO.TEMPLATE_NAME
