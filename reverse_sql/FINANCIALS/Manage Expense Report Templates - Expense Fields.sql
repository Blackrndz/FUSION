-- RSC_PREREQUISITE_OBJECTS=EXM_AIRFARE_POLICIES
-- RSC_PREREQUISITE_OBJECTS=EXM_CARRENTAL_POLICIES
-- RSC_PREREQUISITE_OBJECTS=EXM_PERDIEM_POLICIES
 

SELECT ExpenseTemplateEO.NAME RES_NAME
,(SELECT BU_NAME
	FROM FUN_ALL_BUSINESS_UNITS_V
	WHERE BU_ID = ExpenseTemplateEO.ORG_ID
	) RES_BUSINESS_UNIT
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = ExpenseTemplateEO.DESCRIPTION_BEHAVIOUR_CODE
		AND LOOKUP_TYPE = 'EXM_FIELD_BEHAVIOUR'
		AND LANGUAGE = USERENV('LANG')) RES_DESCRIPTION
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = ExpenseTemplateEO.LOCATION_BEHAVIOUR_CODE
		AND LOOKUP_TYPE = 'EXM_FIELD_BEHAVIOUR'
		AND LANGUAGE = USERENV('LANG')) RES_EXPENSE_LOCATION
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = ExpenseTemplateEO.NUM_OF_DAYS_BEHAVIOUR_CODE
		AND LOOKUP_TYPE = 'EXM_FIELD_BEHAVIOUR'
		AND LANGUAGE = USERENV('LANG')) RES_NUMBER_OF_DAYS
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = ExpenseTemplateEO.MERCHANT_BEHAVIOUR_CODE
		AND LOOKUP_TYPE = 'EXM_FIELD_BEHAVIOUR'
		AND LANGUAGE = USERENV('LANG')) RES_MERCHANT_NAME
,ExpenseTemplateEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ExpenseTemplateEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ExpenseTemplateEO.CREATED_BY RSC_CREATED_BY
,ExpenseTemplateEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,ExpenseTemplateEO.ORG_ID RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM EXM_EXPENSE_TEMPLATES ExpenseTemplateEO
ORDER BY ExpenseTemplateEO.NAME
,RES_BUSINESS_UNIT