--RSC_PREREQUISITE_OBJECTS=PJB_BILLING_METHODS_VL
 
SELECT pjbBillMethodEO.BILL_METHOD_NAME	RES_INVOICE_METHOD_NAME
,(SELECT BILL_TYPE_DESC FROM PJB_BILL_TYPE_CLASSES_VL
		WHERE BILL_TYPE_CLASSIFICATION_CODE = pjbBillMethodEO.BILL_TYPE_CLASS_CODE) RES_INVOICE_METHOD_CLASSIFICAT
,(SELECT MEANING FROM FND_LOOKUPS
	WHERE LOOKUP_CODE = pjbBillMethodEO.METHOD_CFG_TYPE
	AND LOOKUP_TYPE = 'ORA_PJB_CFG_BILL_METHOD_TYPE') RES_METHOD_ASSIGNMENT_TYPE
,DECODE(pjbBillMethodEO.ACTIVE_FLAG,'Y','Active','Inactive') RES_STATUS
,DECODE(pjbBillMethodEO.INTERCOMPANY_METHOD,'Y','Yes','No') RES_USE_INVOICE_METHOD_FOR_INT
,(SELECT MEANING FROM FND_LOOKUPS
	WHERE LOOKUP_CODE = pjbBillMethodEO.LABOR_RATE_TYPE_CODE
	AND LOOKUP_TYPE = 'PJB_LABOR_SCHEDULE') RES_LABOR_SCHEDULE_TYPE
,(SELECT MEANING FROM FND_LOOKUPS
	WHERE LOOKUP_CODE = pjbBillMethodEO.NL_RATE_TYPE_CODE
	AND LOOKUP_TYPE = 'PJB_NONLABOR_SCHEDULE')  RES_NONLABOR_SCHEDULE_TYPE
,pjbBillMethodEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,pjbBillMethodEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,pjbBillMethodEO.CREATED_BY RSC_CREATED_BY
,pjbBillMethodEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJB_BILLING_METHODS_VL pjbBillMethodEO
WHERE pjbBillMethodEO.BILL_METHOD_FLAG =  'I'
ORDER BY pjbBillMethodEO.BILL_METHOD_NAME
