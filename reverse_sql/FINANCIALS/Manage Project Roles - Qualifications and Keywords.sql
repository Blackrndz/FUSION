SELECT pjfPROCTypesE0.PROJECT_ROLE_NAME  RES_NAME
,hrtCONTENTE0.NAME RES_REQUIREMENTS
,hrtCONTYPEE0.CONTENT_TYPE_NAME RES_TYPE
,(SELECT RATING_DESCRIPTION 
	FROM HRT_RATING_LEVELS_VL
	WHERE RATING_LEVEL_ID = pjrQUALIFIE0.RATING_LEVEL_ID 
	) RES_PROFICIENCY_LEVEL	
,(SELECT RATING_DESCRIPTION 
	FROM HRT_RATING_LEVELS_VL
	WHERE RATING_LEVEL_ID = pjrQUALIFIE0.READING_RATING_LEVEL_ID
	) RES_READING
,(SELECT RATING_DESCRIPTION 
	FROM HRT_RATING_LEVELS_VL
	WHERE RATING_LEVEL_ID = pjrQUALIFIE0.SPEAKING_RATING_LEVEL_ID 
	) RES_SPEAKING
,(SELECT RATING_DESCRIPTION 
	FROM HRT_RATING_LEVELS_VL
	WHERE RATING_LEVEL_ID = pjrQUALIFIE0.WRITING_RATING_LEVEL_ID 
	) RES_WRITING
,pjfPROCTypesE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,pjfPROCTypesE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,pjfPROCTypesE0.CREATED_BY  RSC_CREATED_BY
,pjfPROCTypesE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID
 
FROM PJF_PROJ_ROLE_TYPES_VL pjfPROCTypesE0
,PJR_PROJ_ROLE_QUALIFICATIONS pjrQUALIFIE0
,HRT_CONTENT_ITEMS_VL hrtCONTENTE0
,HRT_CONTENT_TYPES_VL hrtCONTYPEE0
WHERE pjfPROCTypesE0.PROJECT_ROLE_ID = pjrQUALIFIE0.PROJECT_ROLE_ID
AND pjrQUALIFIE0.CONTENT_ITEM_ID = hrtCONTENTE0.CONTENT_ITEM_ID
AND hrtCONTYPEE0.CONTENT_TYPE_ID = hrtCONTENTE0.CONTENT_TYPE_ID(+)
AND pjfPROCTypesE0.PROJECT_ROLE_ID  NOT IN (100,101,102)