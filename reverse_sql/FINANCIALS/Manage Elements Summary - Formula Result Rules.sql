SELECT elementsE0.ELEMENT_NAME RES_ELEMENT_NAME
,legislativeDataGrpsE0.NAME RES_LEGISLATIVE_DATA_GROUP
,(SELECT CLASSIFICATION_NAME
    FROM PAY_ELE_CLASSIFICATIONS_VL
	WHERE BASE_CLASSIFICATION_ID = elementsE0.CLASSIFICATION_ID
	AND LEGISLATION_CODE = legislativeDataGrpsE0.LEGISLATION_CODE
	AND SYSDATE BETWEEN DATE_FROM AND NVL(DATE_TO,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_PRIMARY_CLASSIFICATION
,(SELECT CLASSIFICATION_NAME
    FROM PAY_ELE_CLASSIFICATIONS_VL
    WHERE BASE_CLASSIFICATION_ID = elementsE0.SECONDARY_CLASSIFICATION_ID
	AND LEGISLATION_CODE = legislativeDataGrpsE0.LEGISLATION_CODE
	AND SYSDATE BETWEEN DATE_FROM AND NVL(DATE_TO,TO_DATE('4712-12-31','YYYY-MM-DD'))
    ) RES_SECONDARY_CLASSIFICATION
,TO_CHAR(payformularesultE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = paystatuprocE0.FORMULA_ID
    ) RES_FORMULA_NAME
,payformularesultE0.RESULT_NAME RES_RESULT_RETURNED
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = payformularesultE0.RESULT_RULE_TYPE
	AND LOOKUP_TYPE = 'PAY_RESULT_RULE_TYPE' AND LANGUAGE = USERENV('LANG'))  RES_RESULT_RULE
,(SELECT ELEMENT_NAME FROM PAY_ELEMENT_TYPES_VL WHERE ELEMENT_TYPE_ID = payformularesultE0.ELEMENT_TYPE_ID
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_TARGET_ELEMENT_NAME
,payinputvalE0.NAME RES_TARGET_INPUT_VALUE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = payinputvalE0.UOM 
	AND LOOKUP_TYPE = 'PAY_UNITS' AND LANGUAGE = USERENV('LANG')) RES_UNIT_OF_MEASURE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = payformularesultE0.SEVERITY_LEVEL 
	AND LOOKUP_TYPE = 'FORMULA_RESULT_MESSAGE_LEVEL' AND LANGUAGE = USERENV('LANG'))  RES_SEVERITY_LEVEL
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE =  payformularesultE0.TARGET_LEVEL 
	AND LOOKUP_TYPE = 'PAY_TARGET_LEVEL' AND LANGUAGE = USERENV('LANG')) RES_EMPLOYMENT_LEVEL
,DECODE(TO_CHAR(payformularesultE0.EFFECTIVE_END_DATE ,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(payformularesultE0.EFFECTIVE_END_DATE ,'DD-Mon-YYYY')) RES_EFFECTIVE_END_DATE

,payformularesultE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,payformularesultE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,payformularesultE0.CREATED_BY RSC_CREATED_BY
,payformularesultE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,legislativeDataGrpsE0.LEGISLATION_CODE RSC_COUNTRY_ID


FROM PAY_ELEMENT_TYPES_VL elementsE0
,PAY_STATUS_PROC_RULES_F paystatuprocE0
,PAY_FORMULA_RESULT_RULES_F payformularesultE0
,PAY_INPUT_VALUES_VL payinputvalE0
,(SELECT NAME
    ,LEGISLATIVE_DATA_GROUP_ID
	,LEGISLATION_CODE
    FROM PER_LEGISLATIVE_DATA_GROUPS_VL
    )legislativeDataGrpsE0
	
WHERE elementsE0.LEGISLATIVE_DATA_GROUP_ID = legislativeDataGrpsE0.LEGISLATIVE_DATA_GROUP_ID
AND elementsE0.ELEMENT_TYPE_ID = paystatuprocE0.ELEMENT_TYPE_ID
AND paystatuprocE0.STATUS_PROCESSING_RULE_ID = payformularesultE0.STATUS_PROCESSING_RULE_ID
AND payformularesultE0.INPUT_VALUE_ID = payinputvalE0.INPUT_VALUE_ID(+)
AND SYSDATE BETWEEN payformularesultE0.EFFECTIVE_START_DATE AND NVL(payformularesultE0.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY elementsE0.ELEMENT_NAME


