-- RSC_PREREQUISITE_OBJECTS=BEN_PL_F
 
SELECT benPlansE0.NAME RES_PLAN_NAME
,TO_CHAR(benPlansE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ACTN_TYP'
    AND LOOKUP_CODE = dpntCertsE0.ACTN_TYP_CD
    ) RES_ACTION_ITEM
,(SELECT NAME
    FROM BEN_LER_F
    WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
    AND LER_ID = dpntLesE0.LER_ID
     ) RES_LIFE_EVENT
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_DPNT_CVG_CTFN_TYP'
    AND LOOKUP_CODE = dpntCertTypesE0.DPNT_CVG_CTFN_TYP_CD
    ) RES_CERTIFICATION_TYPE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = dpntCertTypesE0.CTFN_RQD_WHEN_RL
    ) RES_DETERMINATION_FORMULA
,DECODE(dpntCertTypesE0.RQD_FLAG,'Y','Yes','No') RES_REQUIRED
,TO_CHAR(dpntCertTypesE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_LIFE_EVENT_DETAILS_EFFECTI
,DECODE(TO_CHAR(dpntCertTypesE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY'),'31-12-4712',NULL,TO_CHAR(dpntCertTypesE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY')) RES_LIFE_EVENT_DETAILS_EFFEC_0
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'CONTACT'
    AND LOOKUP_CODE = dpntCertTypesE0.RLSHP_TYP_CD
    ) RES_RELATIONSHIP_TYPE
,dpntCertTypesE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,dpntCertTypesE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,dpntCertTypesE0.CREATED_BY  RSC_CREATED_BY
,dpntCertTypesE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPlansE0.LEGISLATION_CODE RSC_COUNTRY_ID

FROM BEN_PL_F benPlansE0
,BEN_LER_CHG_DPNT_CVG_F dpntLesE0
,(SELECT NAME
	,PL_TYP_ID
    FROM BEN_PL_TYP_F
    WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
	AND OPT_TYP_CD != 'FLXCR'
	) planTypesE0
,(SELECT *
    FROM BEN_LER_POPL_ACTN_TYP_F
    WHERE ACTN_TYP_LVL_CD = 'DPNTCTFN'
    ) dpntCertsE0
,BEN_LER_CHG_DPNT_CVG_CTFN_F dpntCertTypesE0
WHERE SYSDATE BETWEEN benPlansE0.EFFECTIVE_START_DATE AND benPlansE0.EFFECTIVE_END_DATE
AND benPlansE0.PL_TYP_ID = planTypesE0.PL_TYP_ID
AND benPlansE0.PL_ID = dpntLesE0.PL_ID
AND dpntLesE0.PL_ID = dpntCertsE0.PL_ID
AND dpntLesE0.LER_ID = dpntCertsE0.LER_ID
AND dpntCertsE0.LER_POPL_ACTN_TYP_ID = dpntCertTypesE0.LER_POPL_ACTN_TYP_ID
ORDER BY benPlansE0.NAME
,benPlansE0.EFFECTIVE_START_DATE