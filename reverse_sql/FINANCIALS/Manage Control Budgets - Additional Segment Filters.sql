-- RSC_PREREQUISITE_OBJECTS=XCC_CONTROL_BUDGETS

SELECT xccCONTROLBUDE0.NAME  RES_NAME
,xccRULE0.RULE_NAME RES_SUPPLEMENTAL_RULES_NAME
,(SELECT SEGMENT_CODE FROM FND_KF_SEGMENTS_VL seg
	,FND_KF_STR_INSTANCES_VL strInst
	WHERE seg.STRUCTURE_ID                 = strInst.STRUCTURE_ID
	AND seg.ENABLED_FLAG                   = 'Y'
	AND strInst.ENABLED_FLAG               = 'Y'
	AND strInst.APPLICATION_ID             = 101
	AND strInst.KEY_FLEXFIELD_CODE         = 'GL#'
	AND strInst.STRUCTURE_INSTANCE_NUMBER  = xccCONTROLBUDE0.CHART_OF_ACCOUNTS_ID
	AND seg.COLUMN_NAME = xccRULEFILTERE0.FILTER_CONDITION_CODE) RES_SEGMENT
,xccRULEFILTERE0.TREE_CODE RES_TREE
,(SELECT TREE_VERSION_NAME 
    FROM FND_TREE_VERSION_VL
    WHERE TREE_VERSION_ID = xccRULEFILTERE0.TREE_VERSION_ID
    )  RES_TREE_VERSION


,xccRULEFILTERE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,xccRULEFILTERE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,xccRULEFILTERE0.CREATED_BY RSC_CREATED_BY
,xccRULEFILTERE0.CREATION_DATE RSC_CREATION_DATE
,xccCONTROLBUDE0.LEDGER_ID RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM XCC_CONTROL_BUDGETS xccCONTROLBUDE0
,XCC_CB_RULES xccRULE0
,XCC_CB_RULE_FILTER_CONDITIONS xccRULEFILTERE0
WHERE xccCONTROLBUDE0.CONTROL_BUDGET_ID  = xccRULE0.CONTROL_BUDGET_ID 
AND xccRULE0.RULE_ID = xccRULEFILTERE0.RULE_ID
AND xccRULEFILTERE0.FILTER_CONDITION_TYPE = 'GL_COA_SEGMENT'
ORDER BY 1,2,3