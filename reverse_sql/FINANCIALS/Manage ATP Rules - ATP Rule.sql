SELECT AtpRule.RULE_NAME RES_NAME
,AtpRule.DESCRIPTION RES_DESCRIPTION
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		'Supply chain availability search'
	WHEN NVL(AtpRule.LEAD_TIME_CODE,2) = 1 THEN
		'Lead time based'
	WHEN NVL(AtpRule.INFINITE_AVAILABLE_CODE,2) = 1 THEN
		'Infinite availability based'	
	END) RES_PROMISING_MODE
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		DECODE(AtpRule.SEARCH_COMPONENTS_CODE,1,'Yes','No')
	END) RES_SEARCH_COMPONENTS_AND_RESO
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		DECODE(AtpRule.PROFITABLE_TO_PROMISE_CODE,1,'Yes','No')
	END) RES_ENABLE_PROFITABLE_TO_PROMI
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		DECODE(AtpRule.ALLOCATED_ATP_CODE,1,'Yes','No')
	END) RES_RESPECT_ALLOCATION_CONSTRA
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		DECODE(AtpRule.INCLUDE_ONHAND_AVAILABLE,1,'Yes','No')
	END) RES_ON_HAND
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		DECODE(AtpRule.INCLUDE_PURCHASE_ORDERS,1,'Yes','No')
	END) RES_PURCHASE_ORDERS
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		DECODE(AtpRule.INCLUDE_REQUISITION,1,'Yes','No')
	END) RES_SUPPLIER_REQUISITIONS
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		DECODE(AtpRule.INCLUDE_INTERNAL_REQS,1,'Yes','No')
	END) RES_INTERNAL_REQUISITIONS
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		DECODE(AtpRule.INCLUDE_INTERORG_TRANSFERS,1,'Yes','No')
	END) RES_IN_TRANSIT_SUPPLIES
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		DECODE(AtpRule.INCLUDE_DISCRETE_WIP_RECEIPTS,1,'Yes','No')
	END) RES_STANDARD_WORK_ORDERS
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		DECODE(AtpRule.INCLUDE_NONSTD_WIP_RECEIPTS,1,'Yes','No')
	END) RES_NONSTANDARD_WORK_ORDERS
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		DECODE(AtpRule.INCLUDE_PLANNED_SUPPLY,1,'Yes','No')
	END) RES_PLANNED_SUPPLY
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		DECODE(AtpRule.INCLUDE_SALES_ORDERS,1,'Yes','No')
	END) RES_FULFILLMENT_LINES
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		DECODE(AtpRule.INCLUDE_DEPENDENT_WIP_DEMAND,1,'Yes','No')
	END) RES_DEPENDENT_WORK_ORDER_DEMAN
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		(SELECT MEANING 
		FROM FND_LOOKUP_VALUES
		WHERE LOOKUP_TYPE = 'MSC_LEAD_TIME_TYPE'
		AND LOOKUP_CODE = AtpRule.INFINITE_SUPPLY_FENCE_CODE
		AND LANGUAGE = USERENV('LANG'))
	END) RES_INFINITE_AVAILABILITY_FENC
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		AtpRule.INFINITE_SUPPLY_TIME_FENCE_UD
	END) RES_INFINITE_AVAILABILITY_FE_0
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		AtpRule.PAST_DUE_DEMAND_CUTOFF_FENCE
	END) RES_PAST_DUE_DEMAND_CONSIDERED
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		AtpRule.PAST_DUE_SUPPLY_CUTOFF_FENCE
	END) RES_PAST_DUE_SUPPLY_CONSIDERED
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		(SELECT MEANING 
		FROM FND_LOOKUP_VALUES
		WHERE LOOKUP_TYPE = 'MSC_LEAD_TIME_TYPE'
		AND LOOKUP_CODE = AtpRule.ATP_TIME_FENCE
		AND LANGUAGE = USERENV('LANG'))
	END) RES_ATP_TIME_FENCE_SPECIFICATI
,(CASE
	WHEN NVL(AtpRule.SUPPLY_CHAIN_SEARCH_CODE,2) = 1 THEN
		AtpRule.ATP_TIME_FENCE_UD
	END) RES_ATP_TIME_FENCE_SPECIFICA_1
,(CASE
	WHEN NVL(AtpRule.LEAD_TIME_CODE,2) = 1 THEN
		(SELECT MEANING 
		FROM FND_LOOKUP_VALUES
		WHERE LOOKUP_TYPE = 'MSC_LEAD_TIME_TYPE'
		AND LOOKUP_CODE = AtpRule.LEAD_TIME_TYPE
		AND LANGUAGE = USERENV('LANG'))
	END) RES_LEAD_TIME_CONSIDERED
,(CASE
	WHEN NVL(AtpRule.LEAD_TIME_CODE,2) = 1 THEN
		AtpRule.LEAD_TIME_DAYS_UD
	END) RES_USER_DEFINED_LEAD_TIME_IN_
,AtpRule.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,AtpRule.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,AtpRule.CREATED_BY RSC_CREATED_BY
,AtpRule.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM MSC_ATP_RULES AtpRule
ORDER BY AtpRule.RULE_NAME