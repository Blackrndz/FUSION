SELECT RES_PROGRAM_NAME
,RES_EFFECTIVE_START_DATE
,RES_PROGRAM_HIERARCHY
,RES_PROGRAM_HIERARCHY_DESCRIPT
,RES_ELIGIBILITY_OVERRIDES_EFFE
,RES_ELIGIBILITY_OVERRIDES_EF_0
,RES_ELIGIBILITY_OVERRIDES_FAMI
,RES_ELIGIBILITY_OVERRIDES_FA_1
,RES_ELIGIBILITY_OVERRIDES_TRAC
,RES_ELIGIBILITY_OVERRIDES_ENAB
,RES_ELIGIBILITY_OVERRIDES_PART

,RSC_LAST_UPDATED_BY
,RSC_LAST_UPDATE_DATE
,RSC_CREATED_BY
,RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,RSC_COUNTRY_ID

FROM

(SELECT benPGMEO.NAME RES_PROGRAM_NAME
,benPGMEO.NAME RES_PROGRAM_HIERARCHY
,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_FMLY_MMBR'
         AND LOOKUP_CODE = benPGMEO.VRFY_FMLY_MMBR_CD
         ) RES_ELIGIBILITY_OVERRIDES_FAMI
,(SELECT FORMULA_NAME
         FROM FF_FORMULAS_TL
         WHERE LANGUAGE = USERENV('LANG')
         AND FORMULA_ID = benPGMEO.VRFY_FMLY_MMBR_RL
         ) RES_ELIGIBILITY_OVERRIDES_FA_1
,DECODE(benPGMEO.TRK_INELIG_PER_FLAG,'Y','Yes','No') RES_ELIGIBILITY_OVERRIDES_TRAC
,DECODE(benPGMEO.PRTN_ELIG_OVRID_ALWD_FLAG,'Y','Yes','No') RES_ELIGIBILITY_OVERRIDES_ENAB
,NULL RES_ELIGIBILITY_OVERRIDES_PART
,TO_CHAR(benPGMEO.EFFECTIVE_START_DATE,'DD-MM-YYYY') RES_EFFECTIVE_START_DATE
,TO_CHAR(benPGMEO.EFFECTIVE_START_DATE,'DD-MM-YYYY') RES_ELIGIBILITY_OVERRIDES_EFFE
,DECODE(TO_CHAR(benPGMEO.EFFECTIVE_END_DATE ,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(benPGMEO.EFFECTIVE_END_DATE ,'DD-Mon-YYYY')) RES_ELIGIBILITY_OVERRIDES_EF_0
,'Programs' RES_PROGRAM_HIERARCHY_DESCRIPT
,'1' SEQUENCE

,benPGMEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,benPGMEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,benPGMEO.CREATED_BY RSC_CREATED_BY
,benPGMEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPGMEO.LEGISLATION_CODE RSC_COUNTRY_ID
	
	
FROM BEN_PGM_F benPGMEO
WHERE SYSDATE BETWEEN benPGMEO.EFFECTIVE_START_DATE AND NVL(benPGMEO.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))

UNION

SELECT benPGMEO.NAME RES_PROGRAM_NAME
,benPLT.NAME RES_PROGRAM_HIERARCHY
,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_FMLY_MMBR'
         AND LOOKUP_CODE = benPT.VRFY_FMLY_MMBR_CD
         ) RES_ELIGIBILITY_OVERRIDES_FAMI
,(SELECT FORMULA_NAME
         FROM FF_FORMULAS_TL
         WHERE LANGUAGE = USERENV('LANG')
         AND FORMULA_ID = benPT.VRFY_FMLY_MMBR_RL
         ) RES_ELIGIBILITY_OVERRIDES_FA_1
,DECODE(benPT.TRK_INELIG_PER_FLAG,'Y','Yes','No') RES_ELIGIBILITY_OVERRIDES_TRAC
,DECODE(benPT.PRTN_ELIG_OVRID_ALWD_FLAG,'Y','Yes','No') RES_ELIGIBILITY_OVERRIDES_ENAB
,DECODE(benPT.WVBL_FLAG,'Y','Yes','No') RES_ELIGIBILITY_OVERRIDES_PART
,TO_CHAR(benPGMEO.EFFECTIVE_START_DATE,'DD-MM-YYYY') RES_EFFECTIVE_START_DATE
,TO_CHAR(benPT.EFFECTIVE_START_DATE,'DD-MM-YYYY') RES_ELIGIBILITY_OVERRIDES_EFFE
,DECODE(TO_CHAR(benPT.EFFECTIVE_END_DATE ,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(benPT.EFFECTIVE_END_DATE ,'DD-Mon-YYYY')) RES_ELIGIBILITY_OVERRIDES_EF_0
,'Plan Type' RES_PROGRAM_HIERARCHY_DESCRIPT
,'2' SEQUENCE


,benPT.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,benPT.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,benPT.CREATED_BY RSC_CREATED_BY
,benPT.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPGMEO.LEGISLATION_CODE RSC_COUNTRY_ID

FROM BEN_PGM_F benPGMEO
,BEN_PTIP_F benPT
,BEN_PL_TYP_F benPLT
WHERE benPGMEO.PGM_ID = benPT.PGM_ID
AND benPT.PL_TYP_ID = benPLT.PL_TYP_ID
AND SYSDATE BETWEEN benPGMEO.EFFECTIVE_START_DATE AND NVL(benPGMEO.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))
AND SYSDATE BETWEEN benPT.EFFECTIVE_START_DATE AND NVL(benPT.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))
AND SYSDATE BETWEEN benPLT.EFFECTIVE_START_DATE AND NVL(benPLT.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))

UNION

SELECT benPGMEO.NAME RES_PROGRAM_NAME
,benPL.NAME RES_PROGRAM_HIERARCHY
,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_FMLY_MMBR'
         AND LOOKUP_CODE = benPLIP.VRFY_FMLY_MMBR_CD
         ) RES_ELIGIBILITY_OVERRIDES_FAMI
,(SELECT FORMULA_NAME
         FROM FF_FORMULAS_TL
         WHERE LANGUAGE = USERENV('LANG')
         AND FORMULA_ID = benPLIP.VRFY_FMLY_MMBR_RL
         ) RES_ELIGIBILITY_OVERRIDES_FA_1
,DECODE(benPLIP.TRK_INELIG_PER_FLAG,'Y','Yes','No') RES_ELIGIBILITY_OVERRIDES_TRAC
,DECODE(benPLIP.PRTN_ELIG_OVRID_ALWD_FLAG,'Y','Yes','No') RES_ELIGIBILITY_OVERRIDES_ENAB
,NULL RES_ELIGIBILITY_OVERRIDES_PART
,TO_CHAR(benPGMEO.EFFECTIVE_START_DATE,'DD-MM-YYYY') RES_EFFECTIVE_START_DATE
,TO_CHAR(benPLIP.EFFECTIVE_START_DATE,'DD-MM-YYYY') RES_ELIGIBILITY_OVERRIDES_EFFE
,DECODE(TO_CHAR(benPLIP.EFFECTIVE_END_DATE ,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(benPLIP.EFFECTIVE_END_DATE ,'DD-Mon-YYYY')) RES_ELIGIBILITY_OVERRIDES_EF_0
,'Plan' RES_PROGRAM_HIERARCHY_DESCRIPT
,'3' SEQUENCE


,benPLIP.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,benPLIP.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,benPLIP.CREATED_BY RSC_CREATED_BY
,benPLIP.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPGMEO.LEGISLATION_CODE RSC_COUNTRY_ID
	
FROM BEN_PGM_F benPGMEO
,BEN_PLIP_F benPLIP
,BEN_PL_F benPL
WHERE benPLIP.PL_ID = benPL.PL_ID
AND benPGMEO.PGM_ID = benPLIP.PGM_ID
AND SYSDATE BETWEEN benPGMEO.EFFECTIVE_START_DATE AND NVL(benPGMEO.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))
AND SYSDATE BETWEEN benPLIP.EFFECTIVE_START_DATE AND NVL(benPLIP.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))
AND SYSDATE BETWEEN benPL.EFFECTIVE_START_DATE AND NVL(benPL.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY')) 

ORDER BY RES_PROGRAM_NAME,SEQUENCE)