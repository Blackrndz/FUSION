SELECT planTypesE0.NAME RES_PLAN_TYPE_NAME
,TO_CHAR(planTypesE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,DECODE(TO_CHAR(planTypesE0.EFFECTIVE_END_DATE ,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(planTypesE0.EFFECTIVE_END_DATE ,'DD-Mon-YYYY')) RES_EFFECTIVE_END_DATE
,(SELECT USER_NAME
    FROM BEN_BNFT_CATEGORY
    WHERE USAGE_CD = 'SS'
    AND CATEGORY_CD = planTypesE0.SS_CATEGORY_CD
    ) RES_SELF_SERVICE_GROUPING
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_OPT_TYP'
    AND LOOKUP_CODE = planTypesE0.OPT_TYP_CD
    ) RES_OPTION_TYPE
,(SELECT USER_NAME
    FROM BEN_BNFT_CATEGORY
    WHERE USAGE_CD = 'ADMIN'
    AND CATEGORY_CD = planTypesE0.ADMIN_CATEGORY_CD
    ) RES_ADMINISTRATIVE_GROUPING
,planTypesE0.SHORT_NAME RES_SHORT_NAME
,planTypesE0.SHORT_CODE RES_SHORT_CODE
,planTypesE0.IVR_IDENT RES_INTERACTIVE_VOICE_RESPONSE
,planTypesE0.MN_ENRL_RQD_NUM RES_MINIMUM_PLAN_ENROLLMENT
,planTypesE0.MX_ENRL_ALWD_NUM RES_MAXIMUM_PLAN_ENROLLMENT
,planTypesE0.CARRIER_PLAN_TYPE_NAME RES_BENEFITS_EXTRACT_PLAN_TYPE
,planTypesE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,planTypesE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,planTypesE0.CREATED_BY RSC_CREATED_BY
,planTypesE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,planTypesE0.LEGISLATION_CODE RSC_COUNTRY_ID
FROM BEN_PL_TYP_F planTypesE0
ORDER BY planTypesE0.NAME
,planTypesE0.EFFECTIVE_START_DATE