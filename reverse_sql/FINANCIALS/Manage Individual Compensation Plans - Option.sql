SELECT cmpPLANEO.PLAN_NAME RES_PLAN
,cmpCOMPOEO.COMPONENT_NAME RES_OPTION
,(CASE
	WHEN cmpCOMPOEO.POOL_ID_TO_CONSUME IS NOT NULL THEN
		(SELECT PLAN_NAME 
			FROM CMP_PLANS_VL 
			WHERE PLAN_ID = cmpBUDGETPOLLEO.PLAN_ID)
		||'- '||cmpBUDGETPOLLEO.POOL_NAME 
	END) RES_BUDGET_POOL
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = cmpCOMPOEO.NON_MONETARY_UOM
	AND LOOKUP_TYPE = 'CMP_NONMONETARY_UOM' 
	AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))
	) RES_NONMONETARY_UNITS
,(SELECT DISPLAY_NAME 
	FROM CMP_PLAN_PERIODS 
	WHERE PERIOD_ID = cmpCOMPOEO.PERIOD_ID
	) RES_PERIOD_NAME
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = cmpCOMPOEO.GRANT_TYPE
	AND LOOKUP_TYPE = 'CMP_STOCK_GRANT_TYPE' 
	AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))
	)  RES_GRANT_TYPE
,cmpCOMPOEO.TRADING_SYMBOL RES_TRADING_SYMBOL
,cmpCOMPOEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,cmpCOMPOEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,cmpCOMPOEO.CREATED_BY RSC_CREATED_BY
,cmpCOMPOEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM CMP_PLANS_VL cmpPLANEO
,CMP_COMPONENTS_VL cmpCOMPOEO
,CMP_PLAN_ATTRIBUTES cmpPLANATTEO
,CMP_BUDGET_POOLS_VL cmpBUDGETPOLLEO
WHERE cmpPLANEO.COMP_TYPE = 'ICD'
AND cmpPLANATTEO.COMP_TYPE = 'ICD'
AND cmpCOMPOEO.COMP_TYPE = 'ICD'
AND cmpPLANEO.PLAN_ID = cmpPLANATTEO.PLAN_ID
AND cmpPLANATTEO.COMPONENT_ID = cmpCOMPOEO.COMPONENT_ID
AND cmpCOMPOEO.POOL_ID_TO_CONSUME = cmpBUDGETPOLLEO.POOL_ID(+)
ORDER BY cmpPLANEO.PLAN_NAME