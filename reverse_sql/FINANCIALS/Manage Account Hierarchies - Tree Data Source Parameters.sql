SELECT FNDTREEEO.TREE_NAME RES_TREE_NAME
,FNDTREEEO.TREE_CODE RES_CODE
,(SELECT TREE_STRUCTURE_NAME
	FROM FND_TREE_STRUCTURE_VL
	WHERE TREE_STRUCTURE_CODE = FNDTREEEO.TREE_STRUCTURE_CODE
	) RES_TREE_STRUCTURE
,FndTsDataSourceEO.DATA_SOURCE_NAME RES_PARENT_NAME
,FNDTSDATASOURCEPARAMSEO.PARAMETER_NAME RES_NAME
,FNDTSDATASOURCEPARAMSEO.PARAMETER_VALUE RES_BASE_VALUE
,FNDTREEDATASOURCEPARAMSEO.PARAMETER_VALUE RES_VALUE
,FNDTREEDATASOURCEPARAMSEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,FNDTREEDATASOURCEPARAMSEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,FNDTREEDATASOURCEPARAMSEO.CREATED_BY RSC_CREATED_BY
,FNDTREEDATASOURCEPARAMSEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,FNDTREEDATASOURCEPARAMSEO.ENTERPRISE_ID RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM FND_TREE_VL FNDTREEEO
,FND_TREE_DATA_SOURCE_PARAMS FNDTREEDATASOURCEPARAMSEO
,FND_TS_DATA_SOURCE_VL FNDTSDATASOURCEEO
,FND_TS_DATA_SOURCE_PARAMS FNDTSDATASOURCEPARAMSEO
WHERE FNDTREEEO.TREE_STRUCTURE_CODE          = 'GL_ACCT_FLEX'
AND FNDTREEEO.TREE_CODE                      = FNDTREEDATASOURCEPARAMSEO.TREE_CODE
AND FNDTSDATASOURCEEO.DATA_SOURCE_ID         = FNDTREEDATASOURCEPARAMSEO.DATA_SOURCE_ID
AND FNDTREEDATASOURCEPARAMSEO.DATA_SOURCE_ID = FNDTSDATASOURCEPARAMSEO.DATA_SOURCE_ID
AND FNDTREEDATASOURCEPARAMSEO.PARAMETER_ID   = FNDTSDATASOURCEPARAMSEO.PARAMETER_ID
ORDER BY FNDTREEEO.TREE_NAME
,FndTsDataSourceEO.DATA_SOURCE_NAME