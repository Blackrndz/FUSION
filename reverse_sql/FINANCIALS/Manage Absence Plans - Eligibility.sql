SELECT (SELECT NAME 
	FROM ANC_ABSENCE_PLANS_F_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND ABSENCE_PLAN_ID = absencePlansE0.ABSENCE_PLAN_ID
	AND EFFECTIVE_START_DATE = absencePlansE0.EFFECTIVE_START_DATE
	) RES_PLAN
,(SELECT TERRITORY_SHORT_NAME
	FROM FND_TERRITORIES_VL
	WHERE TERRITORY_CODE = absencePlansE0.LEGISLATION_CODE 
	) RES_LEGISLATION
,(SELECT NAME
	FROM PER_LEGISLATIVE_DATA_GROUPS_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND LEGISLATIVE_DATA_GROUP_ID = absencePlansE0.LEGISLATIVE_DATA_GROUP_ID 
	) RES_LEGISLATIVE_DATA_GROUP
,TO_CHAR(elgProfilesE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,elgProfilesE0.ELIG_PROFILE_SEQUENCE RES_SEQUENCE
,(SELECT NAME
	FROM BEN_ELIGY_PRFL
	WHERE ELIGY_PRFL_ID = elgProfilesE0.BEN_ELIG_PROFILE_ID 
	AND SYSDATE BETWEEN START_DATE AND END_DATE
	) RES_ELIGIBILITY_PROFILE
,DECODE(elgProfilesE0.REQUIRED_FLAG,'Y','Yes','No') RES_REQUIRED
,DECODE(TO_CHAR(elgProfilesE0.EFFECTIVE_END_DATE ,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(elgProfilesE0.EFFECTIVE_END_DATE ,'DD-Mon-YYYY')) RES_EFFECTIVE_END_DATE
,elgProfilesE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,elgProfilesE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,elgProfilesE0.CREATED_BY  RSC_CREATED_BY
,elgProfilesE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,absencePlansE0.LEGISLATION_CODE RSC_COUNTRY_ID
 
FROM ANC_ABSENCE_PLANS_F absencePlansE0
,ANC_PLAN_ELIG_PROFILES_F elgProfilesE0
WHERE absencePlansE0.ENTL_METHOD_CD IN ('A','ORA_ANC_COMP','N')
AND absencePlansE0.ABSENCE_PLAN_ID = elgProfilesE0.ABSENCE_PLAN_ID
AND absencePlansE0.EFFECTIVE_END_DATE BETWEEN elgProfilesE0.EFFECTIVE_START_DATE AND elgProfilesE0.EFFECTIVE_END_DATE
ORDER BY 1
,absencePlansE0.EFFECTIVE_START_DATE