 -- RSC_PREREQUISITE_OBJECTS=BEN_HRS_WKD_IN_PERD_FCTR

SELECT benHRSEO.NAME RES_NAME
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benHRSEO.HRS_SRC_CD
	AND LOOKUP_TYPE = 'BEN_HRS_WKD_SRC' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_SOURCE
,(SELECT (SELECT BASE_BALANCE_NAME FROM PAY_BALANCE_TYPES WHERE BALANCE_TYPE_ID = TMP.BALANCE_TYPE_ID)
	||' '||(SELECT BASE_DIMENSION_NAME FROM PAY_BALANCE_DIMENSIONS WHERE BALANCE_DIMENSION_ID = TMP.BALANCE_DIMENSION_ID)
	FROM (SELECT BALANCE_TYPE_ID,BALANCE_DIMENSION_ID FROM PAY_DEFINED_BALANCES
	WHERE DEFINED_BALANCE_ID = benHRSEO.DEFINED_BALANCE_ID) TMP )RES_SOURCE_VALUE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benHRSEO.PYRL_FREQ_CD
	AND LOOKUP_TYPE = 'BEN_FREQ' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_PAYROLL_FREQUENCY
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benHRSEO.ONCE_R_CNTUG_CD
	AND LOOKUP_TYPE = 'BEN_ONCE_R_CNTNG' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_EVALUATION_FREQUENCY
,benHRSEO.MN_HRS_NUM RES_GREATER_THAN_OR_EQUAL_TO_H
,benHRSEO.MX_HRS_NUM RES_LESS_THAN_HOURS_WORKED
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benHRSEO.HRS_WKD_DET_CD
	AND LOOKUP_TYPE = 'BEN_AGE_DET' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_DETERMINATION_RULE
,(SELECT FORMULA_NAME FROM FF_FORMULAS_VL 
	WHERE FORMULA_ID = benHRSEO.HRS_WKD_DET_RL 
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_DETERMINATION_FORMULA
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benHRSEO.RNDG_CD
	AND LOOKUP_TYPE = 'BEN_RNDG' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_ROUNDING_RULE
,(SELECT FORMULA_NAME FROM FF_FORMULAS_VL 
	WHERE FORMULA_ID = benHRSEO.RNDG_RL 
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_ROUNDING_FORMULA


,benHRSEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,benHRSEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,benHRSEO.CREATED_BY RSC_CREATED_BY
,benHRSEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,benHRSEO.BUSINESS_GROUP_ID RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_HRS_WKD_IN_PERD_FCTR benHRSEO
ORDER BY benHRSEO.NAME































