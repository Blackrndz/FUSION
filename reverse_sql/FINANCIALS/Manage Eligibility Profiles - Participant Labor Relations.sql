SELECT benELIGYEO.NAME RES_NAME
,benELIGYEO.DESCRIPTION RES_DESCRIPTION
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LANGUAGE = USERENV('LANG') 
	AND LOOKUP_TYPE = 'BEN_ELIG_PRFL_PERS_TYPE' 
	AND LOOKUP_CODE = benELIGYEO.PROFILE_TYPE 
	) RES_PROFILE_TYPE
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LANGUAGE = USERENV('LANG') 
	AND LOOKUP_TYPE = 'BEN_BNFT_CAGR_PRTN' 
	AND LOOKUP_CODE = benELIGYEO.BNFT_CAGR_PRTN_CD
	) RES_PROFILE_USAGE
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LANGUAGE = USERENV('LANG') 
	AND LOOKUP_TYPE = 'BEN_STAT'
	AND LOOKUP_CODE = benELIGYEO.STAT_CD
	) RES_STATUS
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LANGUAGE = USERENV('LANG') 
	AND LOOKUP_TYPE = 'BEN_ASMT_TO_USE' 
	AND LOOKUP_CODE = benELIGYEO.ASMT_TO_USE_CD
	) RES_ASSIGNMENT_TO_USE
,benELIGYEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,benELIGYEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,benELIGYEO.CREATED_BY  RSC_CREATED_BY
,benELIGYEO.CREATION_DATE  RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_ELIGY_PRFL benELIGYEO
WHERE SYSDATE BETWEEN benELIGYEO.START_DATE AND NVL(benELIGYEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND benELIGYEO.ELIGY_PRFL_ID IN (SELECT ELIGY_PRFL_ID 
	FROM BEN_ELIG_GEN_CRIT_PRTE 
	WHERE ELIG_CRITERIA_CODE IN ('CAG','UNN')
	AND benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')))
ORDER BY benELIGYEO.NAME
,benELIGYEO.START_DATE