 -- RSC_PREREQUISITE_OBJECTS=ANC_ABSENCE_TYPES_VL

SELECT DISTINCT ancABSENTYPEEO.NAME RES_NAME
,(SELECT TERRITORY_SHORT_NAME 
	FROM FND_TERRITORIES_TL 
	WHERE LANGUAGE = USERENV('LANG')
	AND TERRITORY_CODE = ancABSENTYPEEO.LEGISLATION_CODE
	) RES_LEGISLATION
,(SELECT NAME 
	FROM PER_LEGISLATIVE_DATA_GROUPS_TL 
	WHERE LANGUAGE = USERENV('LANG')
	AND LEGISLATIVE_DATA_GROUP_ID = ancABSENTYPEEO.LEGISLATIVE_DATA_GROUP_ID
	) RES_LEGISLATIVE_DATA_GROUP
,TO_CHAR(ancABSENTYPEPLANEO.EFFECTIVE_START_DATE,'DD-Mon-YYYY')  RES_EFFECTIVE_START_DATE
,(SELECT NAME 
	FROM ANC_ABSENCE_PLANS_VL 
	WHERE ABSENCE_PLAN_ID = ancABSENTYPEPLANEO.ABSENCE_PLAN_ID 
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_PLAN
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = ancABSENTYPEPLANEO.PLAN_STATUS_CD
	AND LOOKUP_TYPE = 'ANC_SETUP_OBJ_STATUS' 
	AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_STATUS
,ancABSENTYPEPLANEO.PRIORITY RES_PRIORITY
,'No' RES_CONCURRENT
,DECODE(TO_CHAR(ancABSENTYPEPLANEO.EFFECTIVE_END_DATE ,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(ancABSENTYPEPLANEO.EFFECTIVE_END_DATE ,'DD-Mon-YYYY')) RES_EFFECTIVE_END_DATE
,ancABSENTYPEPLANEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ancABSENTYPEPLANEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ancABSENTYPEPLANEO.CREATED_BY RSC_CREATED_BY
,ancABSENTYPEPLANEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,ancABSENTYPEEO.LEGISLATION_CODE RSC_COUNTRY_ID

FROM ANC_ABSENCE_TYPES_VL ancABSENTYPEEO
,ANC_ABSENCE_TYPE_PLANS_F ancABSENTYPEPLANEO
WHERE ancABSENTYPEEO.ABSENCE_TYPE_ID = ancABSENTYPEPLANEO.ABSENCE_TYPE_ID
AND NVL(ancABSENTYPEEO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) BETWEEN ancABSENTYPEPLANEO.EFFECTIVE_START_DATE AND NVL(ancABSENTYPEPLANEO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY ancABSENTYPEEO.NAME
,4,5