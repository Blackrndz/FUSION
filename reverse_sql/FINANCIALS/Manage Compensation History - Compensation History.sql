SELECT payELEMENTEO.ELEMENT_NAME RES_NAME
,(SELECT TERRITORY_SHORT_NAME FROM FND_TERRITORIES_VL WHERE TERRITORY_CODE = cmpHISTORYEO.LEGISLATION_CODE) RES_COUNTRY
,(SELECT NAME FROM PER_LEGISLATIVE_DATA_GROUPS_VL 
	WHERE LEGISLATIVE_DATA_GROUP_ID = cmpHISTORYEO.LEGISLATIVE_DATA_GROUP_ID) RES_LEGISLATIVE_DATA_GROUP
,payELEMENTEO.REPORTING_NAME RES_REPORTING_NAME
,(SELECT NAME FROM PAY_INPUT_VALUES_VL WHERE INPUT_VALUE_ID = cmpHISTORYEO.INPUT_VALUE_ID
	AND ELEMENT_TYPE_ID = cmpHISTORYEO.ELEMENT_TYPE_ID
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_INPUT_VALUE
,NULL RES_HISTORY_CATEGORY

,cmpHISTORYEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,cmpHISTORYEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,cmpHISTORYEO.CREATED_BY RSC_CREATED_BY
,cmpHISTORYEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM CMP_HISTORY cmpHISTORYEO
,PAY_ELEMENT_TYPES_VL payELEMENTEO
WHERE cmpHISTORYEO.ELEMENT_TYPE_ID = payELEMENTEO.ELEMENT_TYPE_ID
AND SYSDATE BETWEEN payELEMENTEO.EFFECTIVE_START_DATE AND NVL(payELEMENTEO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
