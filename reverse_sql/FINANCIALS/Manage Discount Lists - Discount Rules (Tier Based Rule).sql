-- RSC_PREREQUISITE_OBJECTS=QP_DISCOUNT_LISTS_TL 
-- RSC_PREREQUISITE_OBJECTS=QP_DISCOUNT_LISTS_ALL_B 
-- RSC_PREREQUISITE_OBJECTS=QP_DISCOUNT_LIST_ITEMS
-- RSC_PREREQUISITE_OBJECTS=QP_PRICING_TERMS_VL
-- RSC_PREREQUISITE_OBJECTS=QP_TIER_HEADERS
-- RSC_PREREQUISITE_OBJECTS=QP_PRICING_BASIS_TL

SELECT QpDiscountListsTl.NAME RES_NAME
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_QP_ITEM_LEVELS'
	AND LOOKUP_CODE = QpDiscountListItems.ITEM_LEVEL_CODE
	) RES_ITEM_LEVEL
,(SELECT ITEM_NUMBER
	FROM EGP_SYSTEM_ITEMS_VL
	WHERE INVENTORY_ITEM_ID = QpDiscountListItems.ITEM_ID
	AND INVENTORY_ORGANIZATION_ID = QpDiscountListsAllB.ORG_ID
	) RES_DISCOUNT_LINES_NAME
,(SELECT UNIT_OF_MEASURE
	FROM INV_UNITS_OF_MEASURE_VL
	WHERE UOM_CODE = QpDiscountListItems.PRICING_UOM_CODE
	) RES_PRICING_UOM
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_QP_RETURN_LINE_TYPES'
	AND LOOKUP_CODE = QpDiscountListItems.LINE_TYPE_CODE
	) RES_LINE_TYPE
,QpPricingTermsVl.NAME RES_RULE_NAME
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_QP_PRICING_RULE_TYPES'
	AND LOOKUP_CODE = QpPricingTermsVl.PRICING_RULE_TYPE_CODE
	) RES_RULE_TYPE
,TO_CHAR(QpPricingTermsVl.START_DATE,'DD-Mon-YYYY HH24:MI:SS') RES_RULE_START_DATE
,TO_CHAR(QpPricingTermsVl.END_DATE,'DD-Mon-YYYY HH24:MI:SS') RES_RULE_END_DATE
,DECODE(QpPricingTermsVl.PRICE_TYPE_CODE,'ALL','All',(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_QP_PRICE_TYPES'
	AND LOOKUP_CODE = QpPricingTermsVl.PRICE_TYPE_CODE
	)) RES_PRICE_TYPE
,DECODE(QpPricingTermsVl.CHARGE_TYPE_CODE,'ALL','All',(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_QP_CHARGE_TYPES'
	AND LOOKUP_CODE = QpPricingTermsVl.CHARGE_TYPE_CODE
	)) RES_CHARGE_TYPE
,DECODE(QpPricingTermsVl.CHARGE_SUBTYPE_CODE,'ALL','All',(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_QP_CHARGE_SUBTYPES'
	AND LOOKUP_CODE = QpPricingTermsVl.CHARGE_SUBTYPE_CODE
	)) RES_CHARGE_SUBTYPE
,DECODE(QpPricingTermsVl.PRICE_PERIODICITY_CODE,'ALL','All',(SELECT DESCRIPTION
	FROM INV_UNITS_OF_MEASURE_VL
	WHERE UOM_CODE = QpPricingTermsVl.PRICE_PERIODICITY_CODE
	AND UOM_CLASS = (SELECT UOM_CLASS FROM INV_UNITS_OF_MEASURE_VL WHERE UOM_CODE = QpDiscountListItems.PRICING_UOM_CODE)
	)) RES_PRICE_PERIODICITY
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_QP_TIER_BASIS_TYPES'
	AND LOOKUP_CODE = QpTierHeaders.TIER_BASIS_TYPE_CODE
	) RES_TIER_BASIS_TYPE
,(SELECT NAME
	FROM QP_PRICING_BASIS_TL
	WHERE PRICING_BASIS_ID = QpTierHeaders.TIER_BASIS_ID
	AND LANGUAGE = USERENV('LANG')) RES_TIER_BASIS
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_QP_TIER_GRADUATED_TYPES'
	AND LOOKUP_CODE = QpTierHeaders.GRADUATED_CODE
	) RES_APPLY_TO	
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_QP_APPLICATION_METHODS'
	AND LOOKUP_CODE = QpTierHeaders.APPLICATION_METHOD_CODE 
	) RES_APPLICATION_METHOD
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_QP_PARTIAL_BLOCK_ACTIONS'
	AND LOOKUP_CODE = QpTierHeaders.PARTIAL_BLOCK_ACTION_CODE
	) RES_PARTIAL_BLOCK_ACTION
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_QP_AGGREGATION_METHODS'
	AND LOOKUP_CODE = QpTierHeaders.AGGREGATION_METHOD_CODE
	) RES_AGGREGATION_METHOD
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_QP_LINE_ADJ_TYPES'
	AND LOOKUP_CODE = QpTierHeaders.ADJUSTMENT_TYPE_CODE
	)RES_ADJUSTMENT_TYPE
,(SELECT NAME 
	FROM QP_PRICING_BASIS_TL
	WHERE PRICING_BASIS_ID = QpTierHeaders.ADJUSTMENT_BASIS_ID
	AND LANGUAGE = USERENV('LANG')
	) RES_ADJUSTMENT_BASIS
,DECODE(QpTierHeaders.ENFORCE_ADJ_CALCULATION_FLAG,'Y','Yes','No') RES_ENFORCE_ADJUSTMENT_CALCULA
,QpTierHeaders.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,QpTierHeaders.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,QpTierHeaders.CREATED_BY  RSC_CREATED_BY
,QpTierHeaders.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,QpDiscountListsAllB.ORG_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM QP_DISCOUNT_LISTS_TL QpDiscountListsTl
,QP_DISCOUNT_LISTS_ALL_B QpDiscountListsAllB
,QP_DISCOUNT_LIST_ITEMS QpDiscountListItems
,QP_PRICING_TERMS_VL QpPricingTermsVl
,QP_TIER_HEADERS QpTierHeaders
WHERE QpDiscountListsTl.DISCOUNT_LIST_ID = QpDiscountListsAllB.DISCOUNT_LIST_ID
AND QpDiscountListsTl.DISCOUNT_LIST_ID = QpDiscountListItems.DISCOUNT_LIST_ID
AND QpDiscountListItems.DISCOUNT_LIST_ITEM_ID = QpPricingTermsVl.PARENT_ENTITY_KEY_COLUMN1
AND QpPricingTermsVl.PRICING_RULE_TYPE_CODE = 'TIERED_PRICING'
AND QpPricingTermsVl.TIERED_PRICING_HEADER_ID = QpTierHeaders.TIERED_PRICING_HEADER_ID
AND QpDiscountListsTl.LANGUAGE = USERENV('LANG')
ORDER BY QpDiscountListsTl.NAME
,RES_ITEM_LEVEL
,RES_DISCOUNT_LINES_NAME
,QpPricingTermsVl.NAME