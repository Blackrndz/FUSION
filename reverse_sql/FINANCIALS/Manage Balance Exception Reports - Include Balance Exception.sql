SELECT (SELECT NAME FROM PER_LEGISLATIVE_DATA_GROUPS_VL WHERE LEGISLATIVE_DATA_GROUP_ID = payBALEXEREPORTO.LEGISLATIVE_DATA_GROUP_ID) RES_LEGISLATIVE_DATA_GROUP
,payBALEXEREPORTO.BAL_EXCEP_REPORT_NAME RES_EXCEPTION_REPORT_NAME
,payBALEXEO.BAL_EXCEPTION_NAME RES_BALANCE_EXCEPTION_NAME
,(SELECT BALANCE_NAME FROM PAY_BALANCE_TYPES_TL WHERE BALANCE_TYPE_ID = payDEFINEBALEO.BALANCE_TYPE_ID  
	AND LANGUAGE = USERENV('LANG'))  RES_BALANCE_NAME
,SUBSTR(payBALDIMENEO.BASE_DIMENSION_NAME,5,LENGTH(payBALDIMENEO.BASE_DIMENSION_NAME)-4) RES_DIMENSION_NAME
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = payBALEXEO.COMPARISON_TYPE
		AND LOOKUP_TYPE = 'PAY_BAL_EXCP_COMPARISON_TYPE' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_COMPARISON_TYPE
,payBALEXEO.COMPARISON_VALUE RES_COMPARISON_VALUE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = payBALEXEO.VARIANCE_TYPE
		AND LOOKUP_TYPE = 'PAY_BAL_EXCP_VARIANCE_TYPE' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_VARIANCE_TYPE
,payBALEXEO.VARIANCE_VALUE RES_VARIANCE_VALUE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = payBALEXEO.VARIANCE_OPERATOR
		AND LOOKUP_TYPE = 'PAY_BAL_EXCP_VAR_OPERATOR_TYPE' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_VARIANCE_OPERATOR
,(SELECT NAME FROM PER_LEGISLATIVE_DATA_GROUPS_VL WHERE LEGISLATIVE_DATA_GROUP_ID = payBALEXEO.LEGISLATIVE_DATA_GROUP_ID) RES_NAME

,payBALEXREPOENO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,payBALEXREPOENO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,payBALEXREPOENO.CREATED_BY RSC_CREATED_BY
,payBALEXREPOENO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PAY_BAL_EXCEPTION_REPORTS_VL  payBALEXEREPORTO
,PAY_BAL_EXCEP_REPORT_ENTRIES payBALEXREPOENO
,PAY_BAL_EXCEPTIONS_VL  payBALEXEO
,PAY_DEFINED_BALANCES payDEFINEBALEO
,PAY_BALANCE_DIMENSIONS payBALDIMENEO
WHERE payBALEXREPOENO.BAL_EXCEPTION_REPORT_ID = payBALEXEREPORTO.BAL_EXCEPTION_REPORT_ID
AND payBALEXREPOENO.BAL_EXCEPTION_ID = payBALEXEO.BAL_EXCEPTION_ID
AND payBALEXEO.DEFINED_BALANCE_ID = payDEFINEBALEO.DEFINED_BALANCE_ID
AND payDEFINEBALEO.BALANCE_DIMENSION_ID = payBALDIMENEO.BALANCE_DIMENSION_ID
ORDER BY RES_LEGISLATIVE_DATA_GROUP,payBALEXEO.LEGISLATION_CODE,RES_EXCEPTION_REPORT_NAME