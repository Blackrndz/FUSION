SELECT TO_CHAR(gradeRatesE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,legisDataGroupsE0.NAME RES_LEGISLATIVE_DATA_GROUP
,gradeRatesE0.NAME RES_NAME
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'GRADE_PAY_RATE_TYPE'
    AND LOOKUP_CODE = gradeRatesE0.RATE_TYPE
    ) RES_RATE_TYPE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ACTIVE_INACTIVE'
    AND LOOKUP_CODE = gradeRatesE0.ACTIVE_STATUS
    ) RES_STATUS
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'CMP_SALARY_BASIS'
    AND LOOKUP_CODE = gradeRatesE0.RATE_FREQUENCY
    ) RES_FREQUENCY
,gradeRatesE0.ANNUALIZATION_FACTOR RES_ANNUALIZATION_FACTOR
,gradeRatesE0.CURRENCY_CODE RES_CURRENCY
,(SELECT ACTION_REASON
    FROM PER_ACTION_OCCURRENCES actOccE0
    ,PER_ACTION_REASONS_TL ActReasonsE0
    WHERE ActReasonsE0.LANGUAGE = USERENV('LANG')
    AND actOccE0.ACTION_REASON_ID = ActReasonsE0.ACTION_REASON_ID
    AND actOccE0.ACTION_OCCURRENCE_ID = gradeRatesE0.ACTION_OCCURRENCE_ID
    ) RES_ACTION_REASON
,gradeRatesE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,gradeRatesE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,gradeRatesE0.CREATED_BY RSC_CREATED_BY
,gradeRatesE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,legisDataGroupsE0.LEGISLATION_CODE RSC_COUNTRY_ID

FROM PER_RATES_F_VL gradeRatesE0
,(SELECT LEGISLATIVE_DATA_GROUP_ID
    ,NAME
	,LEGISLATION_CODE
    FROM PER_LEGISLATIVE_DATA_GROUPS_VL
    ) legisDataGroupsE0
WHERE gradeRatesE0.RATE_OBJECT_TYPE= 'GRADE'
AND gradeRatesE0.LEGISLATIVE_DATA_GROUP_ID = legisDataGroupsE0.LEGISLATIVE_DATA_GROUP_ID
ORDER BY legisDataGroupsE0.NAME
,gradeRatesE0.NAME,gradeRatesE0.EFFECTIVE_START_DATE