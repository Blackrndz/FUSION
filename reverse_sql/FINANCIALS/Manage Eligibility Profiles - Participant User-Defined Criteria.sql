SELECT benELIGYEO.NAME RES_NAME
,benELIGCRITVALEO.ORDR_NUM RES_SEQUENCE
,benELIGCRITEO.NAME RES_USER_DEFINED_CRITERIA
,DECODE(benELIGCRITVALEO.EXCLD_FLAG,'Y','Yes','No') RES_EXCLUDE
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LANGUAGE = USERENV('LANG')
	AND VIEW_APPLICATION_ID = 3
	AND LOOKUP_TYPE = benELIGCRITEO.COL1_LOOKUP_TYPE 
	AND LOOKUP_CODE = benELIGCRITVALEO.CHAR_VALUE1
	) RES_SET_1_MEANING
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LANGUAGE = USERENV('LANG')
	AND VIEW_APPLICATION_ID = 3
	AND LOOKUP_TYPE = benELIGCRITEO.COL1_LOOKUP_TYPE 
	AND LOOKUP_CODE = benELIGCRITVALEO.CHAR_VALUE2
	) RES_SET_1_TO_MEANING
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LANGUAGE = USERENV('LANG')
	AND VIEW_APPLICATION_ID = 3
	AND LOOKUP_TYPE = benELIGCRITEO.COL2_LOOKUP_TYPE 
	AND LOOKUP_CODE = benELIGCRITVALEO.CHAR_VALUE3
	) RES_SET_2_MEANING
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LANGUAGE = USERENV('LANG')
	AND VIEW_APPLICATION_ID = 3
	AND LOOKUP_TYPE = benELIGCRITEO.COL2_LOOKUP_TYPE 
	AND LOOKUP_CODE = benELIGCRITVALEO.CHAR_VALUE4
	) RES_SET_2_TO_MEANING
,TO_CHAR(benELIGCRITVALEO.START_DATE,'DD-Mon-YYYY') RES_START_DATE
,TO_CHAR(benELIGCRITVALEO.END_DATE,'DD-Mon-YYYY') RES_END_DATE
,benELIGCRITVALEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,benELIGCRITVALEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,benELIGCRITVALEO.CREATED_BY  RSC_CREATED_BY
,benELIGCRITVALEO.CREATION_DATE  RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_ELIGY_PRFL benELIGYEO
,BEN_ELIGY_CRIT_VALUES benELIGCRITVALEO
,BEN_ELIGY_CRITERIA benELIGCRITEO
WHERE SYSDATE BETWEEN benELIGYEO.START_DATE AND NVL(benELIGYEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND benELIGYEO.ELIGY_PRFL_ID = benELIGCRITVALEO.ELIGY_PRFL_ID 
AND SYSDATE BETWEEN benELIGCRITVALEO.START_DATE AND NVL(benELIGCRITVALEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND benELIGCRITVALEO.ELIGY_CRITERIA_ID = benELIGCRITEO.ELIGY_CRITERIA_ID
ORDER BY benELIGYEO.NAME
,benELIGCRITVALEO.ORDR_NUM