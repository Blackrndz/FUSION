-- RSC_PREREQUISITE_OBJECTS=HWM_TCATS_VL

SELECT configPoolsE0.NAME RES_NAME
,configPoolsE0.CODE RES_CODE
,phasesE0.NAME RES_PHASES_NAME
,StatesE0.NAME RES_STATE_NAME
,(SELECT ACTION_NAME
	FROM IRC_LC_ACTIONS_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND ACTION_ID = actionsE0.ACTION_ID
	) RES_ACTION_NAME
,(SELECT NAME
	FROM IRC_PHASES_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND PHASE_ID = (SELECT STRING_VALUE
        FROM IRC_LC_SETTING_ITEMS
        WHERE ITEM_KEY_NAME = 'TargetPhaseId'
        AND SETTING_ID = actionsE0.SETTING_ID)
    ) RES_ACTION_PHASE
,(SELECT NAME
	FROM IRC_STATES_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND STATE_ID = (SELECT STRING_VALUE
        FROM IRC_LC_SETTING_ITEMS
        WHERE ITEM_KEY_NAME = 'TargetStateId'
        AND SETTING_ID = actionsE0.SETTING_ID)
    ) RES_ACTION_STATE
,DECODE(sendNotisE0.NOTIFY_RECRUITER_FLAG,'Y','Yes','N','No') RES_RECRUITER
,DECODE(sendNotisE0.NOTIFY_HIRING_MGR_FLAG,'Y','Yes','N','No') RES_HIRING_MANAGER
,(SELECT APPLY_FLOW_NAME
	FROM IRC_APPLY_FLOWS_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND APPLY_FLOW_ID = reqsE0.APPLY_FLOW_ID 
	) RES_REQUEST_INFORMATION_FLOW
,(SELECT NAME
    FROM IRC_DESCRIPTIONS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND DESCRIPTION_ID = sendNotisE0.INT_CAND_NOTIF_TMPL_ID
    ) RES_INTERNAL_CANDIDATE_NOTIFIC
,(SELECT NAME
    FROM IRC_DESCRIPTIONS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND DESCRIPTION_ID = NVL(sendNotisE0.EXT_CAND_NOTIF_TMPL_ID,reqsE0.CAND_NOTIF_TMPL_ID)
    ) RES_EXTERNAL_CANDIDATE_NOTIFIC
,sendNotisE0.CANDIDATE_NOTIF_DELAY RES_DELAY_DELIVERY
,DECODE(candidatesE0.NOTIFY_RECRUITER_FLAG,'Y','Yes','N','No') RES_NOTIFY_RECRUITER
,DECODE(candidatesE0.NOTIFY_HIRING_MGR_FLAG,'Y','Yes','N','No') RES_NOTIFY_HIRING_MANAGER
,(SELECT NAME
	FROM IRC_DESCRIPTIONS_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND DESCRIPTION_ID = candidatesE0.NOTIF_TEMPLATE_ID 
	) RES_EMAIL_NOTIFICATION_TEMPLAT
,candidatesE0.MATCH_SCORE_LEVEL RES_MATCH_SCORE
,actionsE0.ACTION_NAME RES_ACTION_NAME_DETAIL
,actionsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,actionsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,actionsE0.CREATED_BY RSC_CREATED_BY
,actionsE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM IRC_PROCESSES_VL configPoolsE0
,(SELECT *
    FROM IRC_ROUTING_STEPS_B
    WHERE TYPE_CODE = 'PHASE'
    ) rountingStepsE0
,(SELECT *
    FROM IRC_PHASES_TL
    WHERE LANGUAGE = USERENV('LANG')
    ) phasesE0
,(SELECT *
    FROM IRC_ROUTING_STEPS_B
    WHERE TYPE_CODE = 'STATE'
    ) rStepStatesE0
,(SELECT *
    FROM IRC_STATES_TL
    WHERE LANGUAGE = USERENV('LANG')
    ) StatesE0
/*,(SELECT *
    FROM IRC_FN_EXPRESSION_REFS
    WHERE OBJECT_TYPE = 'SETTING_ID'
    ) actionEventsE0*/
,(SELECT *
    FROM IRC_LC_ACTION_USAGES_VL
    ) actionsE0
,IRC_JA_NOTIF_CONFIG sendNotisE0
,IRC_SEC_FLOW_CONFIG reqsE0
,IRC_CAND_DUP_NOTIF_CONFIG candidatesE0
WHERE configPoolsE0.TYPE_CODE = 'CSW'
AND configPoolsE0.USAGE_CODE = 'TEMPLATE'
AND configPoolsE0.PROCESS_ID = rountingStepsE0.PROCESS_ID
AND rountingStepsE0.PHASE_ID = phasesE0.PHASE_ID
AND rountingStepsE0.SUB_PROCESS_ID = rStepStatesE0.PROCESS_ID
AND rStepStatesE0.STATE_ID = StatesE0.STATE_ID
AND rStepStatesE0.ROUTING_STEP_ID = actionsE0.ROUTING_STEP_ID
--AND actionsE0.SETTING_ID = actionEventsE0.OBJECT_ID
AND actionsE0.STEP_ACTION_USAGE_ID = sendNotisE0.STEP_ACTION_USAGE_ID(+)
AND actionsE0.STEP_ACTION_USAGE_ID = reqsE0.STEP_ACTION_USAGE_ID(+)
AND actionsE0.STEP_ACTION_USAGE_ID = candidatesE0.STEP_ACTION_USAGE_ID(+)
ORDER BY configPoolsE0.NAME
,rountingStepsE0.SEQUENCE_NUMBER
,rStepStatesE0.SEQUENCE_NUMBER