    ) RES_CHANGE_DEPENDENT_COVERAG_0
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_DPNT_CVG_END'
    AND LOOKUP_CODE = benLERCHGDPNTEO.CVG_EFF_END_CD
    ) RES_PREVIOUS_DEPENDENT_COVERAG
