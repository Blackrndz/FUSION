,BEN_PTIP_F benPT
,BEN_PL_TYP_F benPLT 
,BEN_LER_CHG_DPNT_CVG_F benLERCHGDPNTEO
,BEN_LER_POPL_ACTN_TYP_F benLERPOPLACTNTYPEEO
WHERE benPGMEO.PGM_ID = benPT.PGM_ID
AND benPT.PL_TYP_ID = benPLT.PL_TYP_ID
AND benPT.PTIP_ID = benLERCHGDPNTEO.PTIP_ID
AND benLERCHGDPNTEO.PTIP_ID = benLERPOPLACTNTYPEEO.PTIP_ID
