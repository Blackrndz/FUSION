,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_LER_CHG_DPNT_CVG'
    AND LOOKUP_CODE = benLERCHGDPNTEO.LER_CHG_DPNT_CVG_CD
    ) RES_CHANGE_DEPENDENT_COVERAGE_
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_DPNT_CVG_STRT'
    AND LOOKUP_CODE = benLERCHGDPNTEO.CVG_EFF_STRT_CD
    ) RES_DEPENDENT_COVERAGE_START_D
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benLERCHGDPNTEO.CVG_EFF_STRT_RL
    ) RES_DEPENDENT_COVERAGE_START_F
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
