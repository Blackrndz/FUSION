SELECT cxSitesE0.SITE_NAME RES_SITE_NAME
,(SELECT DECODE(GEOGRAPHY_ELEMENT10,NULL,NULL,GEOGRAPHY_ELEMENT10||', ')||
        DECODE(GEOGRAPHY_ELEMENT9,NULL,NULL,GEOGRAPHY_ELEMENT9||', ')||
        DECODE(GEOGRAPHY_ELEMENT8,NULL,NULL,GEOGRAPHY_ELEMENT8||', ')||
        DECODE(GEOGRAPHY_ELEMENT7,NULL,NULL,GEOGRAPHY_ELEMENT7||', ')||
        DECODE(GEOGRAPHY_ELEMENT6,NULL,NULL,GEOGRAPHY_ELEMENT6||', ')||
        DECODE(GEOGRAPHY_ELEMENT5,NULL,NULL,GEOGRAPHY_ELEMENT5||', ')||
        DECODE(GEOGRAPHY_ELEMENT4,NULL,NULL,GEOGRAPHY_ELEMENT4||', ')||
        DECODE(GEOGRAPHY_ELEMENT3,NULL,NULL,GEOGRAPHY_ELEMENT3||', ')||
        DECODE(GEOGRAPHY_ELEMENT2,NULL,NULL,GEOGRAPHY_ELEMENT2||', ')||
        DECODE(GEOGRAPHY_ELEMENT1,NULL,NULL,GEOGRAPHY_ELEMENT1)
    FROM HZ_GEOGRAPHIES
    WHERE GEOGRAPHY_ID = geoNodesE0.GEOGRAPHY_ID
    ) RES_LOCATIONS
,contextsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,contextsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,contextsE0.CREATED_BY RSC_CREATED_BY
,contextsE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM IRC_CX_SITES_VL cxSitesE0
,IRC_AVAILABILITY_CONTEXT contextsE0
,IRC_GEO_HIER_NODES geoNodesE0
WHERE cxSitesE0.SITE_ID = contextsE0.OBJECT_ID
AND contextsE0.OBJECT_TYPE_CODE = 'ORA_CE_SITES'
AND contextsE0.DIMENSION_TYPE_CODE = 'ORA_LOCATION'
AND contextsE0.GEOGRAPHY_NODE_ID = geoNodesE0.GEOGRAPHY_NODE_ID
ORDER BY cxSitesE0.SEQUENCE_NUMBER