 -- RSC_PREREQUISITE_OBJECTS=HRQ_QUESTIONNAIRES_VL
 -- RSC_PREREQUISITE_OBJECTS=HRQ_QUESTIONS_VL
 -- RSC_PREREQUISITE_OBJECTS=HRQ_QSTN_ANSWERS_VL
 
SELECT hrqQUESTIONAIREO.QUESTIONNAIRE_CODE RES_QUESTIONNAIRE_TEMPLATE_ID
,hrqQUESTIONAIREO.NAME RES_NAME
,(SELECT NAME 
	FROM HRQ_SUBSCRIBERS_TL	
	WHERE LANGUAGE = USERENV('LANG') 
	AND SUBSCRIBER_ID = hrqQUESTIONSEO.SUBSCRIBER_ID
	) RES_SUBSCRIBER
,hrqQSTNRSECTIONEO.NAME RES_TITLE
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = hrqQUESTIONSEO.PRIVACY_FLAG
	AND LOOKUP_TYPE = 'HRQ_PRIVACY' AND LANGUAGE = USERENV('LANG') 
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_PRIVACY
,(SELECT NAME 
	FROM HRQ_CATEGORIES_VL 
	WHERE CATEGORY_ID = hrqQUESTIONSEO.CATEGORY_ID
	) RES_FOLDER
,hrqQUESTIONSEO.QUESTION_CODE RES_QUESTION_CODE
,hrqANSWEREO.ANSWER_CODE RES_RESPONSE_CODE
,hrqANSWEREO.LONG_TEXT RES_SHORT_DESCRIPTION
,hrqANSWEREO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hrqANSWEREO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hrqANSWEREO.CREATED_BY RSC_CREATED_BY
,hrqANSWEREO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM HRQ_QUESTIONNAIRES_VL hrqQUESTIONAIREO
,HRQ_QSTNR_TEMPLATES_VL hrqQSTNRETEMPEO
,HRQ_QSTNR_SECTIONS_VL hrqQSTNRSECTIONEO
,HRQ_QSTNR_QUESTIONS hrqQSTNRQUESTIONEO
,HRQ_QUESTIONS_VL hrqQUESTIONSEO
,HRQ_QSTN_ANSWERS_VL hrqANSWEREO
WHERE hrqQUESTIONAIREO.QUESTIONNAIRE_ID = hrqQSTNRETEMPEO.QUESTIONNAIRE_ID
AND hrqQUESTIONAIREO.QUESTIONNAIRE_ID = hrqQSTNRSECTIONEO.QUESTIONNAIRE_ID
AND hrqQSTNRSECTIONEO.QSTNR_SECTION_ID = hrqQSTNRQUESTIONEO.QSTNR_SECTION_ID
AND hrqQSTNRQUESTIONEO.QUESTION_ID = hrqQUESTIONSEO.QUESTION_ID
AND hrqQSTNRQUESTIONEO.QSTN_VERSION_NUM = hrqQUESTIONSEO.QSTN_VERSION_NUM
AND hrqQUESTIONSEO.QUESTION_ID = hrqANSWEREO.QUESTION_ID
AND hrqANSWEREO.ANSWER_CODE IS NOT NULL
ORDER BY hrqQUESTIONAIREO.QUESTIONNAIRE_CODE
,hrqQUESTIONAIREO.NAME