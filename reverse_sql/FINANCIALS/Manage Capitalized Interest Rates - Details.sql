SELECT pjcCINTRNameEO.RATE_NAME RES_NAME
,HROrgUNITEO.BU_NAME RES_BUSINESS_UNIT
,(SELECT MEANING 
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJC_CINT_EXP_ORG_SOURCE'
	AND LOOKUP_CODE = pjcCINTRInfoEO.EXP_ORG_SOURCE
	) RES_EXPENDITURE_ORGANIZATION_S
,(SELECT MEANING 
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJC_CINT_THRESHOLD_AMT_TYPE'
	AND LOOKUP_CODE = pjcCINTRInfoEO.THRESHOLD_AMT_TYPE
	) RES_AMOUNT_TYPE
,(SELECT NAME 
	FROM PJO_PLAN_TYPES_VL
	WHERE PLAN_TYPE_CODE = 'FINANCIAL_PLAN'
	AND PLAN_TYPE_ID = pjcCINTRInfoEO.FIN_PLAN_TYPE_ID
	) RES_FINANCIAL_PLAN_TYPE
,pjcCINTRInfoEO.PROJ_AMT_THRESHOLD 	RES_PROJECT_AMOUNT
,ledgersE0.CURRENCY_CODE RES_PROJECT_AMOUNT_CURRENCY
,pjcCINTRInfoEO.PROJ_DURATION_THRESHOLD RES_PROJECT_DAYS
,pjcCINTRInfoEO.TASK_AMT_THRESHOLD 	RES_TASK_AMOUNT
,ledgersE0.CURRENCY_CODE RES_TASK_AMOUNT_CURRENCY
,pjcCINTRInfoEO.TASK_DURATION_THRESHOLD RES_TASK_DAYS
,(SELECT MEANING 
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJC_CINT_PERIOD_CONVENTION'
	AND LOOKUP_CODE = pjcCINTRInfoEO.CURR_PERIOD_CONVENTION
	) RES_CURRENT_PERIOD_CALCULATION
,(SELECT MEANING 
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJC_CINT_CALC_METHOD'
	AND LOOKUP_CODE = pjcCINTRInfoEO.INTEREST_CALCULATION_METHOD
	) RES_INTEREST_METHOD
,(SELECT MEANING 
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJC_CINT_PERIOD_RATE_CODE'
	AND LOOKUP_CODE = pjcCINTRInfoEO.PERIOD_RATE_CODE
	) RES_BASIS_METHOD
,pjcCINTRInfoEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,pjcCINTRInfoEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,pjcCINTRInfoEO.CREATED_BY RSC_CREATED_BY
,pjcCINTRInfoEO.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,pjcCINTRInfoEO.ORG_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM FUN_ALL_BUSINESS_UNITS_V HROrgUNITEO
,PJC_CINT_RATE_NAMES pjcCINTRNameEO
,PJC_CINT_RATE_INFO_ALL pjcCINTRInfoEO
,GL_LEDGERS ledgersE0
WHERE pjcCINTRNameEO.RATE_NAME_ID = pjcCINTRInfoEO.RATE_NAME_ID
AND pjcCINTRInfoEO.ORG_ID = HROrgUNITEO.BU_ID
AND HROrgUNITEO.PRIMARY_LEDGER_ID = ledgersE0.LEDGER_ID(+)
ORDER BY HROrgUNITEO.BU_NAME