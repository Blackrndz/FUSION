--RSC_PREREQUISITE_OBJECTS=HRC_DL_LAYOUTS_VL

SELECT (SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = hdcDLLAYOUTEO.LAYOUT_TYPE
		AND LOOKUP_TYPE = 'ORA_HRC_DL_TEMPLATE_TYPES' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_TYPE
,hdcDLLAYOUTEO.LAYOUT_NAME RES_NAME
,hdcDLLAYOUTEO.LAYOUT_CODE RES_CODE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = hdcDLLAYOUTEO.STATUS
		AND LOOKUP_TYPE = 'ORA_HRC_DL_TEMPLT_STATUS' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_STATUS
,(SELECT UI_NAME FROM HRC_DL_UI_BUS_OBJECTS_VL WHERE UI_BUSINESS_OBJECT_ID = hrcDLBUSOBJEO.UI_BUSINESS_OBJECT_ID) RES_BUSINESS_OBJECT
,(SELECT NAME FROM PER_LEGISLATIVE_DATA_GROUPS_VL WHERE LEGISLATIVE_DATA_GROUP_ID = hdcDLLAYOUTEO.LEGISLATIVE_DATA_GROUP_ID) RES_LEGISLATIVE_DATA_GROUP
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = hdcDLLAYOUTEO.LAYOUT_MODE
		AND LOOKUP_TYPE = 'ORA_HRC_SDL_ACTIONS' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_SUPPORTED_ACTION
,hdcDLLAYOUTEO.LAYOUT_DESCRIPTION RES_DESCRIPTION
,hdcDLLAYOUTEO.LAYOUT_CATEGORY_CODE RES_CATEGORY
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = hdcDLLAYOUTEO.RUN_MODE
		AND LOOKUP_TYPE = 'ORA_HRC_SDL_USER_RUN_TYPE' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_UPLOAD_DATA_AS
,(CASE WHEN hdcDLLAYOUTEO.RUN_MODE = 'ORA_ELEVATED' 
	THEN 'Disabled'
	WHEN hdcDLLAYOUTEO.RUN_MODE = 'ORA_SESSION'
	THEN 'Enabled' END)	RES_ASSIGN_ROLES_TO_TEMPLATE

,hdcDLLAYOUTEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hdcDLLAYOUTEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hdcDLLAYOUTEO.CREATED_BY RSC_CREATED_BY
,hdcDLLAYOUTEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,(SELECT LEGISLATION_CODE 
	FROM PER_LEGISLATIVE_DATA_GROUPS
	WHERE LEGISLATIVE_DATA_GROUP_ID = hdcDLLAYOUTEO.LEGISLATIVE_DATA_GROUP_ID
	) RSC_COUNTRY_ID

FROM HRC_DL_LAYOUTS_VL  hdcDLLAYOUTEO
,HRC_DL_BUSINESS_OBJECTS  hrcDLBUSOBJEO
WHERE hdcDLLAYOUTEO.TOP_BUSINESS_OBJECT_ID = hrcDLBUSOBJEO.BUSINESS_OBJECT_ID
ORDER BY hdcDLLAYOUTEO.LAYOUT_NAME