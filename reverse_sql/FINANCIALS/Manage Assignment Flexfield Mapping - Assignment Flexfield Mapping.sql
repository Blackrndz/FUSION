-- RSC_PREREQUISITE_OBJECTS=PER_FLEX_SEGMENT_MAPPINGS

SELECT 'Position Flexfields' RES_SOURCE
,(CASE 
	WHEN mappingE0.SOURCE_CONTEXT_CODE = 'Global Data Elements' THEN 
		NULL
	ELSE 
		(SELECT NAME
			FROM FND_DF_CONTEXTS_VL
			WHERE DESCRIPTIVE_FLEXFIELD_CODE = 'PER_POSITIONS_DFF'
			AND CONTEXT_CODE = mappingE0.SOURCE_CONTEXT_CODE)
	END) RES_SOURCE_CONTEXT
,(SELECT NAME
	FROM FND_DF_SEGMENTS_VL
	WHERE DESCRIPTIVE_FLEXFIELD_CODE = 'PER_POSITIONS_DFF'
	AND SEGMENT_CODE = mappingE0.SOURCE_SEGMENT_CODE 
	) RES_SOURCE_SEGMENT
,'Assignment Flexfields' RES_DESTINATION
,(CASE 
	WHEN mappingE0.DESTINATION_CONTEXT_CODE = 'Global Data Elements' THEN 
		NULL
	ELSE 
		(SELECT NAME
			FROM FND_DF_CONTEXTS_VL
			WHERE DESCRIPTIVE_FLEXFIELD_CODE = 'PER_ASG_DF'
			AND CONTEXT_CODE = mappingE0.DESTINATION_CONTEXT_CODE)
	END) RES_DESTINATION_CONTEXT
,(SELECT NAME
	FROM FND_DF_SEGMENTS_VL
	WHERE DESCRIPTIVE_FLEXFIELD_CODE = 'PER_ASG_DF'
	AND SEGMENT_CODE = mappingE0.DESTINATION_SEGMENT_CODE 
	) RES_DESTINATION_SEGMENT
,(SELECT DECODE(POSITION_SYNC_ENABLED_FLAG,'Y','Yes','No') 
	FROM PER_FLEX_MAPPING_USAGES 
	WHERE mappingE0.FLEX_SEGMENT_MAPPING_ID = FLEX_SEGMENT_MAPPING_ID 
	AND LEGAL_ENTITY_ID IS NULL
	)RES_ENABLE_POSITION_SYNCHRONIZ
,mappingE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,mappingE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,mappingE0.CREATED_BY RSC_CREATED_BY
,mappingE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID
FROM PER_FLEX_SEGMENT_MAPPINGS mappingE0