-- RSC_PREREQUISITE_OBJECTS=BEN_BILL_CYCLE

SELECT benBILLCYCLEO.NAME RES_CALENDAR_NAME
,benBILLCYCLEO.DESCRIPTION RES_DESCRIPTION
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benBILLCYCLEO.BILL_FREQ
	AND LOOKUP_TYPE = 'BEN_ACTY_REF_PERD' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_BILLING_FREQUENCY
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benBILLCYCLEO.TYPE
	AND LOOKUP_TYPE = 'BEN_PERD_TYP' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_PERIOD_TYPE
,benBILLCYCLEO.DUE_DAY RES_PAYMENT_DUE_DAY
,benBILLCYCLEO.DAYS_OVERDUE RES_DAYS_OVERDUE


, benBILLCYCLEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
, benBILLCYCLEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
, benBILLCYCLEO.CREATED_BY RSC_CREATED_BY
, benBILLCYCLEO.CREATION_DATE RSC_CREATION_DATE
, NULL RSC_LEDGER_ID
, NULL RSC_CHART_OF_ACCOUNTS_ID
, NULL RSC_BUSINESS_UNIT_ID
, NULL RSC_LEGAL_ENTITY_ID
, NULL RSC_ORGANIZATION_ID
, NULL RSC_BUSINESS_GROUP_ID
, NULL RSC_ENTERPRISE_ID
, NULL RSC_COUNTRY_ID

FROM BEN_BILL_CYCLE benBILLCYCLEO


