select ARSTATEMENTCYCLES.name RES_NAME
,BUSINESSUNITPEO.BU_NAME RES_BUSINESS_UNIT
,TO_CHAR(ARSTATEMENTCYCLEDATESALL.STATEMENT_DATE,'DD-Mon-YYYY') RES_STATEMENT_DATE
,DECODE(ARSTATEMENTCYCLEDATESALL.PRINTED,'S','Yes','No') RES_SKIP
,to_char(ArStatementCycleDatesAll.PRINT_DATE,'DD-Mon-YYYY') RES_DATE_PRINTED
,ARSTATEMENTCYCLEDATESALL.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,ARSTATEMENTCYCLEDATESALL.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,ARSTATEMENTCYCLEDATESALL.CREATED_BY  RSC_CREATED_BY
,ARSTATEMENTCYCLEDATESALL.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,ARSTATEMENTCYCLEDATESALL.ORG_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

from AR_STATEMENT_CYCLES ARSTATEMENTCYCLES 
,AR_STATEMENT_CYCLE_DATES_ALL ARSTATEMENTCYCLEDATESALL
,FUN_ALL_BUSINESS_UNITS_V BusinessUnitPEO
where  ARSTATEMENTCYCLEDATESALL.ORG_ID = BUSINESSUNITPEO.BU_ID
and ARSTATEMENTCYCLES.STATEMENT_CYCLE_ID =  ARSTATEMENTCYCLEDATESALL.STATEMENT_CYCLE_ID
order by ARSTATEMENTCYCLES.name,BUSINESSUNITPEO.BU_NAME