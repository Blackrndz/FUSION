SELECT lifeEventsE0.NAME RES_NAME
,persE0.NAME RES_RELATED_PERSON_CHANGE
,persE0.SOURCE_TABLE RES_TABLE_NAME
,persE0.SOURCE_COLUMN RES_COLUMN_NAME
,NVL((SELECT MEANING
		FROM FND_LOOKUP_VALUES
		WHERE LANGUAGE = USERENV('LANG')
		AND LOOKUP_TYPE IN ('BEN_ANY_OR_NULL','CONTACT','EMP_CAT')
		AND LOOKUP_CODE = OLD_VAL)
	,(CASE
		WHEN SOURCE_TABLE = 'BEN_PER_BENEFIT_GROUP_F' AND SOURCE_COLUMN = 'BENEFIT_GROUP_ID' THEN
			(SELECT NAME FROM BEN_BENFTS_GRP WHERE TO_CHAR(BENFTS_GRP_ID) = OLD_VAL)
		WHEN SOURCE_TABLE = 'BEN_BENEFIT_RELATIONS_F' AND SOURCE_COLUMN = 'PRIMARY_REL' THEN
			DECODE(OLD_VAL,'Y','Yes','No')
		ELSE
			OLD_VAL
		END)
	) RES_OLD_VALUE
,NVL((SELECT MEANING
		FROM FND_LOOKUP_VALUES
		WHERE LANGUAGE = USERENV('LANG')
		AND LOOKUP_TYPE IN ('BEN_ANY_OR_NULL','CONTACT','EMP_CAT')
		AND LOOKUP_CODE = NEW_VAL)
	,(CASE
		WHEN SOURCE_TABLE = 'BEN_PER_BENEFIT_GROUP_F' AND SOURCE_COLUMN = 'BENEFIT_GROUP_ID' THEN
			(SELECT NAME FROM BEN_BENFTS_GRP WHERE TO_CHAR(BENFTS_GRP_ID) = NEW_VAL)
		WHEN SOURCE_TABLE = 'BEN_BENEFIT_RELATIONS_F' AND SOURCE_COLUMN = 'PRIMARY_REL' THEN
			DECODE(NEW_VAL,'Y','Yes','No')
		ELSE
			NEW_VAL
		END)
	) RES_NEW_VALUE
,DECODE(persE0.RULE_OVERRIDES_FLAG,'Y','Yes','No') RES_FORMULA_OVERRIDES
,(SELECT FORMULA_NAME
			FROM FF_FORMULAS_TL
			WHERE LANGUAGE = USERENV('LANG')
			AND FORMULA_ID = persE0.RLTD_PER_CHG_CS_LER_RL) RES_RELATED_PERSON_CHANGE_FORM
,perChangeRelatesE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,perChangeRelatesE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,perChangeRelatesE0.CREATED_BY RSC_CREATED_BY
,perChangeRelatesE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,lifeEventsE0.BUSINESS_GROUP_ID RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_LER_F lifeEventsE0
,BEN_LER_RLTD_PER_CS_LER perChangeRelatesE0
,BEN_RLTD_PER_CHG_CS_LER persE0
WHERE lifeEventsE0.LER_ID = perChangeRelatesE0.LER_ID
AND perChangeRelatesE0.RLTD_PER_CHG_CS_LER_ID = persE0.RLTD_PER_CHG_CS_LER_ID
ORDER BY lifeEventsE0.NAME
,lifeEventsE0.EFFECTIVE_START_DATE
,persE0.NAME