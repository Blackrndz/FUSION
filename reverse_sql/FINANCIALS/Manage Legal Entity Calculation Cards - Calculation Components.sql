WITH CONTEXT_DTL AS (SELECT dedcxtusgsE0.DIR_CARD_COMP_DEF_ID
    ,dedcxtusgsE0.SEQUENCE
    ,ROW_NUMBER() OVER(PARTITION BY dedcxtusgsE0.DIR_CARD_COMP_DEF_ID ORDER BY dedcxtusgsE0.DIR_CARD_COMP_DEF_ID,dedcxtusgsE0.SEQUENCE) R1
    
    ,cxtusgsE0.LOOKUP_TYPE
    ,cxtusgsE0.VO_NAME
    FROM PAY_DED_CONTEXT_USAGES dedcxtusgsE0
    ,PAY_CONTEXT_USAGES cxtusgsE0
    WHERE dedcxtusgsE0.CONTEXT_ID = cxtusgsE0.CONTEXT_ID
    AND dedcxtusgsE0.LEGISLATION_CODE = cxtusgsE0.LEGISLATION_CODE)
    
    
SELECT TaxRptUnitsE0.LE_NAME RES_LEGAL_ENTITY
,TaxRptUnitsE0.LEGAL_ENTITY_IDENTIFIER RES_LEGAL_ENTITY_INDENTIFIER
,(SELECT DISPLAY_NAME
    FROM PAY_DIR_CARD_DEFINITIONS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND DIR_CARD_DEFINITION_ID = leRptCardsE0.DIR_CARD_DEFINITION_ID
    ) RES_CALCULATION_CARD
--,deductionGrpsE0.NAME
,calCompDtlsE0.COMPONENT_NAME RES_CALCULATION_COMPONENT
,(CASE
    WHEN CONTEXT_DTL1.LOOKUP_TYPE IS NOT NULL THEN
        (SELECT MEANING
            FROM FND_LOOKUP_VALUES
            WHERE LANGUAGE = USERENV('LANG')
            AND LOOKUP_TYPE = CONTEXT_DTL1.LOOKUP_TYPE
            AND LOOKUP_CODE = cardCompsE0.CONTEXT_VALUE1)
    WHEN CONTEXT_DTL1.VO_NAME IS NOT NULL THEN
        cardCompsE0.CONTEXT_VALUE1
    ELSE
        cardCompsE0.CONTEXT_VALUE1
    END) RES_REFERENCE
,(CASE
    WHEN CONTEXT_DTL2.LOOKUP_TYPE IS NOT NULL THEN
        (SELECT MEANING
            FROM FND_LOOKUP_VALUES
            WHERE LANGUAGE = USERENV('LANG')
            AND LOOKUP_TYPE = CONTEXT_DTL2.LOOKUP_TYPE
            AND LOOKUP_CODE = cardCompsE0.CONTEXT_VALUE2)
    WHEN CONTEXT_DTL2.VO_NAME IS NOT NULL THEN
        cardCompsE0.CONTEXT_VALUE2
    ELSE
        cardCompsE0.CONTEXT_VALUE2
    END) RES_PARENT_CALCULATION_COMPONE
,(CASE
    WHEN CONTEXT_DTL3.LOOKUP_TYPE IS NOT NULL THEN
        (SELECT MEANING
            FROM FND_LOOKUP_VALUES
            WHERE LANGUAGE = USERENV('LANG')
            AND LOOKUP_TYPE = CONTEXT_DTL3.LOOKUP_TYPE
            AND LOOKUP_CODE = cardCompsE0.CONTEXT_VALUE3)
    WHEN CONTEXT_DTL3.VO_NAME IS NOT NULL THEN
        cardCompsE0.CONTEXT_VALUE3
    ELSE
        cardCompsE0.CONTEXT_VALUE3
    END) RES_PARENT_REFERENCE
,(CASE
    WHEN CONTEXT_DTL4.LOOKUP_TYPE IS NOT NULL THEN
        (SELECT MEANING
            FROM FND_LOOKUP_VALUES
            WHERE LANGUAGE = USERENV('LANG')
            AND LOOKUP_TYPE = CONTEXT_DTL4.LOOKUP_TYPE
            AND LOOKUP_CODE = cardCompsE0.CONTEXT_VALUE4)
    WHEN CONTEXT_DTL4.VO_NAME IS NOT NULL THEN
        cardCompsE0.CONTEXT_VALUE4
    ELSE
        cardCompsE0.CONTEXT_VALUE4
    END) RES_ELEMENT_NAME
,(CASE
    WHEN CONTEXT_DTL5.LOOKUP_TYPE IS NOT NULL THEN
        (SELECT MEANING
            FROM FND_LOOKUP_VALUES
            WHERE LANGUAGE = USERENV('LANG')
            AND LOOKUP_TYPE = CONTEXT_DTL5.LOOKUP_TYPE
            AND LOOKUP_CODE = cardCompsE0.CONTEXT_VALUE5)
    WHEN CONTEXT_DTL5.VO_NAME IS NOT NULL THEN
        cardCompsE0.CONTEXT_VALUE5
    ELSE
        cardCompsE0.CONTEXT_VALUE5
    END) RES_SUBPROCESSING_ORDER
,TO_CHAR(cardCompsE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,TO_CHAR(cardCompsE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_END_DATE

,cardCompsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,cardCompsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,cardCompsE0.CREATED_BY RSC_CREATED_BY
,cardCompsE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID


FROM PAY_DIR_CARDS_F leRptCardsE0
,(SELECT orgsE0.ORGANIZATION_ID
    ,lesE0.NAME LE_NAME
	,lesE0.LEGAL_ENTITY_IDENTIFIER
    FROM HR_ORGANIZATION_V orgsE0
    ,XLE_ENTITY_PROFILES lesE0
    WHERE orgsE0.CLASSIFICATION_CODE = 'HCM_PSU'
    AND SYSDATE BETWEEN orgsE0.EFFECTIVE_START_DATE AND orgsE0.EFFECTIVE_END_DATE
    AND orgsE0.LEGAL_ENTITY_ID = lesE0.LEGAL_ENTITY_ID
    ) TaxRptUnitsE0
,PAY_DIR_CARD_COMPONENTS_F cardCompsE0
,PAY_DEDUCTION_GROUPS_VL deductionGrpsE0
,PAY_DIR_CARD_COMP_DEFS_VL calCompDtlsE0
,(SELECT DIR_CARD_COMP_DEF_ID
    ,LOOKUP_TYPE
    ,VO_NAME
    FROM CONTEXT_DTL
    WHERE R1 = 1
    ) CONTEXT_DTL1
,(SELECT DIR_CARD_COMP_DEF_ID
    ,LOOKUP_TYPE
    ,VO_NAME
    FROM CONTEXT_DTL
    WHERE R1 = 2
    ) CONTEXT_DTL2
,(SELECT DIR_CARD_COMP_DEF_ID
    ,LOOKUP_TYPE
    ,VO_NAME
    FROM CONTEXT_DTL
    WHERE R1 = 3
    ) CONTEXT_DTL3
,(SELECT DIR_CARD_COMP_DEF_ID
    ,LOOKUP_TYPE
    ,VO_NAME
    FROM CONTEXT_DTL
    WHERE R1 = 4
    ) CONTEXT_DTL4
,(SELECT DIR_CARD_COMP_DEF_ID
    ,LOOKUP_TYPE
    ,VO_NAME
    FROM CONTEXT_DTL
    WHERE R1 = 5
    ) CONTEXT_DTL5
,(SELECT DIR_CARD_COMP_DEF_ID
    ,LOOKUP_TYPE
    ,VO_NAME
    FROM CONTEXT_DTL
    WHERE R1 = 6
    ) CONTEXT_DTL6
WHERE leRptCardsE0.SOURCE_TYPE = 'PSU'
AND SYSDATE BETWEEN leRptCardsE0.EFFECTIVE_START_DATE AND leRptCardsE0.EFFECTIVE_END_DATE
AND leRptCardsE0.SOURCE_ID = TaxRptUnitsE0.ORGANIZATION_ID

AND leRptCardsE0.DIR_CARD_ID = cardCompsE0.DIR_CARD_ID
AND cardCompsE0.DEDUCTION_GROUP_ID = deductionGrpsE0.DEDUCTION_GROUP_ID

AND cardCompsE0.DIR_CARD_COMP_DEF_ID = calCompDtlsE0.DIR_CARD_COMP_DEF_ID

AND calCompDtlsE0.DIR_CARD_COMP_DEF_ID = CONTEXT_DTL1.DIR_CARD_COMP_DEF_ID(+)
AND calCompDtlsE0.DIR_CARD_COMP_DEF_ID = CONTEXT_DTL2.DIR_CARD_COMP_DEF_ID(+)
AND calCompDtlsE0.DIR_CARD_COMP_DEF_ID = CONTEXT_DTL3.DIR_CARD_COMP_DEF_ID(+)
AND calCompDtlsE0.DIR_CARD_COMP_DEF_ID = CONTEXT_DTL4.DIR_CARD_COMP_DEF_ID(+)
AND calCompDtlsE0.DIR_CARD_COMP_DEF_ID = CONTEXT_DTL5.DIR_CARD_COMP_DEF_ID(+)
AND calCompDtlsE0.DIR_CARD_COMP_DEF_ID = CONTEXT_DTL6.DIR_CARD_COMP_DEF_ID(+)

ORDER BY 1,2,3,4
