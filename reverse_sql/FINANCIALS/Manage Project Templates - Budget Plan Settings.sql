WITH RBS AS (SELECT source.NAME SNAME
	,header.NAME HNAME
	,header.RBS_HEADER_ID
	,header.CENTRALLY_CONTROL_FLAG
	FROM PJF_RBS_HEADERS_VL source
	,PJF_RBS_HEADERS_VL header
	WHERE header.SOURCE_RBS_HEADER_ID = source.RBS_HEADER_ID)

SELECT projTemplatesE0.NAME RES_TEMPLATE_NAME
,projTemplatesE0.SEGMENT1 RES_TEMPLATE_NUMBER
,budgetsE0.VERSION_NAME RES_NAME
,(CASE 
	WHEN planningOptionsE0.BUDGET_CREATION_METHOD != 'GENERATE' THEN
		(SELECT 
			(SELECT (CASE
				WHEN CENTRALLY_CONTROL_FLAG = 'I' THEN HNAME
				ELSE SNAME
				END) 
				FROM RBS 
				WHERE RBS_HEADER_ID = version.RBS_HEADER_ID)
			FROM PJF_RBS_VERSIONS_VL version
			WHERE version.RBS_VERSION_ID = planningOptionsE0.RBS_VERSION_ID)
	END) RES_PLANNING_RESOURCE_BREAKDOW
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJO_PLAN_LEVEL_CODE'
	AND LOOKUP_CODE = planningOptionsE0.PLAN_LEVEL_CODE
	) RES_PLANNING_LEVEL
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJO_TIME_PHASED_TYPE'
	AND LOOKUP_CODE = planningOptionsE0.TIME_PHASED_CODE
	) RES_CALENDAR_TYPE
,(SELECT NAME
	FROM PJO_PERIOD_PROFILES_VL
	WHERE PERIOD_PROFILE_ID = planningOptionsE0.PERIOD_PROFILE_ID
	) RES_PERIOD_PROFILE
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE = 'N' AND budgetsE0.PLANNED_FOR_CODE = 'COST' THEN
		(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'PJO_BDGT_RATE_DRV_DATE_TYPE'
			AND LOOKUP_CODE = planningOptionsE0.PFC_COST_RATE_DATE_TYPE)
	END) RES_COST_RATE_DERIVATION_DATE_
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE = 'N' AND budgetsE0.PLANNED_FOR_CODE = 'COST' THEN
		TO_CHAR(planningOptionsE0.PFC_COST_RATE_DATE,'DD-Mon-YYYY')
	END) RES_COST_RATE_DERIVATION_DATE
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE = 'N' AND budgetsE0.PLANNED_FOR_CODE = 'COST_AND_REV_TOGETHER' THEN
		(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'PJO_BDGT_RATE_DRV_DATE_TYPE'
			AND LOOKUP_CODE = planningOptionsE0.PC_COST_RATE_DATE_TYPE)
	END) RES_RATE_DERIVATION_DATE_TYPE
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE = 'N' AND budgetsE0.PLANNED_FOR_CODE = 'COST_AND_REV_TOGETHER' THEN
		TO_CHAR(planningOptionsE0.PC_COST_RATE_DATE,'DD-Mon-YYYY')
	END) RES_RATE_DERIVATION_DATE
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE = 'N' AND budgetsE0.PLANNED_FOR_CODE = 'REVENUE' THEN
		(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'PJO_BDGT_RATE_DRV_DATE_TYPE'
			AND LOOKUP_CODE = planningOptionsE0.PFC_REV_RATE_DATE_TYPE)
	END) RES_BILL_RATE_DERIVATION_DATE_
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE = 'N' AND budgetsE0.PLANNED_FOR_CODE = 'REVENUE' THEN
		TO_CHAR(planningOptionsE0.PFC_REV_RATE_DATE,'DD-Mon-YYYY')
	END) RES_BILL_RATE_DERIVATION_DATE
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE != 'N'  THEN
		planningOptionsE0.CURRENT_PLANNING_PERIOD
	END) RES_CURRENT_PLANNING_PERIOD
,DECODE(planningOptionsE0.PLAN_IN_MULTI_CURR_FLAG,'Y','Yes','No') RES_MULTIPLE_TRANSACTION_CURRE
,DECODE(budgetsE0.BUDGETARY_CONTROLS_FLAG,'Y','Yes','No') RES_ENABLE_BUDGETARY_CONTROLS
,budgetsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,budgetsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,budgetsE0.CREATED_BY  RSC_CREATED_BY
,budgetsE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,projTemplatesE0.ORG_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJF_PROJECTS_ALL_VL projTemplatesE0
,FUN_ALL_BUSINESS_UNITS_V buE0
,PJO_PLAN_VERSIONS_VL budgetsE0
,PJO_PLANNING_OPTIONS planningOptionsE0
WHERE projTemplatesE0.TEMPLATE_FLAG = 'Y'
AND projTemplatesE0.ORG_ID = buE0.BU_ID
AND projTemplatesE0.PROJECT_ID = budgetsE0.PROJECT_ID
AND budgetsE0.PLAN_CLASS_CODE = 'BUDGET'
AND budgetsE0.STRUCTURE_VERSION_ID IS NULL
AND budgetsE0.PLAN_VERSION_ID = planningOptionsE0.PLAN_VERSION_ID
AND planningOptionsE0.PLAN_TYPE_CODE = 'FINANCIAL_PLAN'
ORDER BY projTemplatesE0.NAME
,budgetsE0.VERSION_NAME