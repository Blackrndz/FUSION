SELECT orgSecPflsE0.NAME RES_NAME
--,DECODE(orgSecPflsE0.SECURE_BY_ORG_LIST,'Y','Yes','N','No') RES_SECURE_BY_ORGANIZATION_LIS
,(SELECT NAME
    FROM HR_ORGANIZATION_UNITS
    WHERE SYSDATE BETWEEN DATE_FROM AND DATE_TO
    AND ORGANIZATION_ID = pflOrgsE0.ORGANIZATION_ID
    ) RES_ORGANIZATION
,(SELECT CLASSIFICATION_NAME
	FROM HR_ORG_UNIT_CLASSIFICATIONS_F OrgUnitClassificationDEO2
	,HR_ORG_CLASSIFICATIONS_VL OrgClassificationEO2
	WHERE OrgUnitClassificationDEO2.CLASSIFICATION_CODE = OrgClassificationEO2.CLASSIFICATION_CODE
	AND OrgUnitClassificationDEO2.ORGANIZATION_ID = pflOrgsE0.ORGANIZATION_ID
	AND rownum <= 1
	) RES_CLASSIFICATION_NAME
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'INCLUDE_EXCLUDE'
	AND LOOKUP_CODe = pflOrgsE0.INCLUDE_EXCLUDE_FLAG 
	) RES_INCLUDE_OR_EXCLUDE_ORGANIZ
,pflOrgsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,pflOrgsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,pflOrgsE0.CREATED_BY RSC_CREATED_BY
,pflOrgsE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,orgSecPflsE0.BUSINESS_GROUP_ID RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PER_ORG_SECURITY_PROFILES orgSecPflsE0
,PER_ORG_SEC_PROFILE_ORGS pflOrgsE0
WHERE orgSecPflsE0.ORG_SECURITY_PROFILE_ID = pflOrgsE0.ORG_SECURITY_PROFILE_ID
ORDER BY orgSecPflsE0.NAME