SELECT benPGMEO.NAME RES_PROGRAM_NAME
,TO_CHAR(benPGMEO.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,benPGMEO.NAME RES_PROGRAM_HIERARCHY
,'Programs' RES_PROGRAM_HIERARCHY_DESCRIPT
,TO_CHAR(benENRTPERDEO.STRT_DT,'DD-Mon-YYYY') RES_ENROLLMENT_PERIOD_START_DA
,TO_CHAR(benENRTPERDEO.END_DT,'DD-Mon-YYYY') RES_ENROLLMENT_PERIOD_END_DATE
,TO_CHAR(benENRTPERDEO.ASND_LF_EVT_DT,'DD-Mon-YYYY') RES_ASSIGNED_LIFE_EVENT_DATE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ENRT_TYP_CYCL'
    AND LOOKUP_CODE = benPOPLENRTEO.ENRT_TYP_CYCL_CD
    ) RES_SCHEDULE_TYPE
,TO_CHAR(benENRTPERDEO.DFLT_ENRT_DT,'DD-Mon-YYYY') RES_ASSIGN_DEFAULTS_DATE
,TO_CHAR(benENRTPERDEO.PROCG_END_DT,'DD-Mon-YYYY') RES_PROCESSING_CUTOFF_DATE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_CLS_ENRT_DT_TO_USE'
    AND LOOKUP_CODE = benENRTPERDEO.CLS_ENRT_DT_TO_USE_CD
    ) RES_CLOSE_ENROLLMENT_PERIOD_DA
,SDAY||'-'||SMONTH||'-'||SYEAR||' - '||EDAY||'-'||EMONTH||'-'||EYEAR RES_YEAR_PERIOD
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ENRT_PERD_DET_CD'
    AND LOOKUP_CODE = benENRTPERDEO.ENRT_PERD_DET_OVRLP_BCKDT_CD
    ) RES_PERIOD_DETERMINATION_RULE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ENRT_CVG_STRT'
    AND LOOKUP_CODE = benENRTPERDEO.ENRT_CVG_STRT_DT_CD
    ) RES_COVERAGE_START_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benENRTPERDEO.ENRT_CVG_STRT_DT_RL
    ) RES_COVERAGE_START_FORMULA
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ENRT_CVG_END'
    AND LOOKUP_CODE = benENRTPERDEO.ENRT_CVG_END_DT_CD
    ) RES_PREVIOUS_COVERAGE_END_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benENRTPERDEO.ENRT_CVG_END_DT_RL
    ) RES_PREVIOUS_COVERAGE_END_FORM
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_RT_STRT'
    AND LOOKUP_CODE = benENRTPERDEO.RT_STRT_DT_CD
    ) RES_RATE_START_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benENRTPERDEO.RT_STRT_DT_RL
    ) RES_RATE_START_FORMULA
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_RT_END'
    AND LOOKUP_CODE = benENRTPERDEO.RT_END_DT_CD
    ) RES_PREVIOUS_RATE_END_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benENRTPERDEO.RT_END_DT_RL
    ) RES_PREVIOUS_RATE_END_FORMULA
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_REINSTATE'
    AND LOOKUP_CODE = benENRTPERDEO.REINSTATE_CD	
    ) RES_REINSTATEMENT_RULE	
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_REINSTATE_OVRDN'
    AND LOOKUP_CODE = benENRTPERDEO.REINSTATE_OVRDN_CD
    ) RES_REINSTATEMENT_OVERRIDE_RUL
	


,benENRTPERDEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,benENRTPERDEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,benENRTPERDEO.CREATED_BY RSC_CREATED_BY
,benENRTPERDEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPGMEO.LEGISLATION_CODE RSC_COUNTRY_ID
	
	
FROM BEN_PGM_F benPGMEO
,BEN_POPL_ENRT_TYP_CYCL benPOPLENRTEO
,BEN_ENRT_PERD benENRTPERDEO
,(SELECT YR_PERD_ID,EXTRACT(DAY FROM START_DATE) SDAY
,DECODE(EXTRACT(MONTH FROM START_DATE),1,'Jan',2,'Feb',3,'Mar',4,'Apr',5,'May',6,'Jun',7,'Jul',8,'Aug',9,'Sep',10,'Oct',11,'Nov',12,'Dec') SMONTH
,EXTRACT(YEAR FROM START_DATE) SYEAR
,EXTRACT(DAY FROM END_DATE) EDAY
,DECODE(EXTRACT(MONTH FROM END_DATE),1,'Jan',2,'Feb',3,'Mar',4,'Apr',5,'May',6,'Jun',7,'Jul',8,'Aug',9,'Sep',10,'Oct',11,'Nov',12,'Dec') EMONTH
,EXTRACT(YEAR FROM END_DATE) EYEAR FROM BEN_YR_PERD) benYEAREO
WHERE benPGMEO.PGM_ID = benPOPLENRTEO.PGM_ID 
AND benPOPLENRTEO.POPL_ENRT_TYP_CYCL_ID = benENRTPERDEO.POPL_ENRT_TYP_CYCL_ID
AND benENRTPERDEO.YR_PERD_ID = benYEAREO.YR_PERD_ID(+)
AND benPOPLENRTEO.ENRT_TYP_CYCL_CD in ('O','A')
AND benENRTPERDEO.LER_ID IN (SELECT LER_ID FROM BEN_LER_F_VL WHERE TYP_CD IN ('SCHEDDA','SCHEDDO')
				AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY')))
AND SYSDATE BETWEEN benPGMEO.EFFECTIVE_START_DATE AND NVL(benPGMEO.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))

ORDER BY 1,2,3