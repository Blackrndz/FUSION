SELECT (SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_TYPE = 'FUN_NUMBERING_TYPES'
	AND LOOKUP_CODE = funSYSOPEO.NUMBERING_TYPE
	AND LANGUAGE = USERENV('LANG')
	) RES_INTERCOMPANY_BATCH_NUMBERI
,funSYSOPEO.DEFAULT_CURRENCY RES_INTERCOMPANY_CURRENCY
,funSYSOPEO.MIN_TRX_AMT RES_MINIMUM_TRANSACTION_AMOUNT
,funSYSOPEO.MIN_TRX_AMT_CURRENCY RES_MINIMUM_TRANSACTION_CURREN
,(SELECT USER_CONVERSION_TYPE 
	FROM GL_DAILY_CONVERSION_TYPES
	WHERE CONVERSION_TYPE = funSYSOPEO.EXCHG_RATE_TYPE 
	) RES_CONVERSION_RATE_TYPE
,DECODE(funSYSOPEO.ALLOW_REJECT_FLAG,'Y','Yes','N','No') RES_ALLOW_RECEIVER_TO_REJECT_T
,(SELECT USER_PERIOD_SET_NAME FROM GL_CALENDARS WHERE PERIOD_SET_NAME = funSYSOPEO.INTECO_CALENDAR
	AND PERIOD_TYPE = funSYSOPEO.INTECO_PERIOD_TYPE) RES_INTERCOMPANY_CALENDAR
,funSYSOPEO.INTECO_PERIOD_TYPE RES_PERIOD_TYPE
,(SELECT TRX_TYPE_NAME
	FROM FUN_TRX_TYPES_VL
	WHERE TRX_TYPE_ID = funSYSOPEO.DEFAULT_TRX_TYPE_ID
	) RES_DEFAULT_TRANSACTION_TYPE
,DECODE(funSYSOPEO.APAR_BATCH_FLAG,'Y','Yes','N','No') RES_SUMMARIZE_DISTRIBUTIONS_FO
,funSYSOPEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,funSYSOPEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,funSYSOPEO.CREATED_BY RSC_CREATED_BY
,funSYSOPEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM FUN_SYSTEM_OPTIONS funSYSOPEO