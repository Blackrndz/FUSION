SELECT benELIGYEO.NAME RES_NAME
,benELIGYEO.DESCRIPTION RES_DESCRIPTION
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benELIGYEO.PROFILE_TYPE 
	AND LOOKUP_TYPE = 'BEN_ELIG_PRFL_PERS_TYPE' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_PROFILE_TYPE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benELIGYEO.BNFT_CAGR_PRTN_CD
	AND LOOKUP_TYPE = 'BEN_BNFT_CAGR_PRTN' AND LANGUAGE = USERENV('LANG') 
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_PROFILE_USAGE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benELIGYEO.STAT_CD 
	AND LOOKUP_TYPE = 'BEN_STAT' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_STATUS
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benELIGYEO.ASMT_TO_USE_CD
	AND LOOKUP_TYPE = 'BEN_ASMT_TO_USE' AND LANGUAGE = USERENV('LANG') 
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_ASSIGNMENT_TO_USE


,benELIGYEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,benELIGYEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,benELIGYEO.CREATED_BY  RSC_CREATED_BY
,benELIGYEO.CREATION_DATE  RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_ELIGY_PRFL benELIGYEO
WHERE benELIGYEO.PROFILE_TYPE = 'PRTP'
AND benELIGYEO.ELIGY_PRFL_ID IN 
	(SELECT ELIGY_PRFL_ID FROM BEN_ELIG_BRGNG_UNIT_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
		UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_GEN_CRIT_PRTE WHERE ELIG_CRITERIA_CODE = 'EBU' AND benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
		UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_ORG_UNIT_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_GRD_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_GEN_CRIT_PRTE WHERE ELIG_CRITERIA_CODE = 'HRD' AND benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_HRLY_SLRD_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_GEN_CRIT_PRTE WHERE ELIG_CRITERIA_CODE = 'JFM' AND benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_GEN_CRIT_PRTE WHERE ELIG_CRITERIA_CODE = 'JBF' AND benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_JOB_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_LBR_MMBR_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_LGL_ENTY_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_PYRL_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_GEN_CRIT_PRTE WHERE ELIG_CRITERIA_CODE = 'MNG' AND benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
		UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_PERF_RTNG_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_PSTN_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_GEN_CRIT_PRTE WHERE ELIG_CRITERIA_CODE = 'EPN' AND benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_QUA_IN_GR_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) 
	 UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_SCHEDD_HRS_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_PY_BSS_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_WK_LOC_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_FL_TM_PT_TM_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) 
		UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_EE_STAT_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) 
	 )
AND SYSDATE BETWEEN benELIGYEO.START_DATE AND NVL(benELIGYEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
