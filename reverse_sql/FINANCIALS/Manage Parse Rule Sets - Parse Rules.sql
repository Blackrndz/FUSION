SELECT cePARSINGRULESETE0.PARSING_RULE_SET_NAME RES_NAME
,cePARSINGRULEE0.SEQUENCE_NUM RES_SEQUENCE
,DECODE(cePARSINGRULEE0.ACTIVE_FLAG,'Y','Yes','No') RES_ACTIVE
,(SELECT TRX_CODE 
	FROM CE_TRANSACTION_CODES 
	WHERE TRANSACTION_CODE_ID = cePARSINGRULEE0.TRX_CODE_ID
	) RES_TRANSACTION_CODE
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = cePARSINGRULEE0.SOURCE_FIELD
	AND LOOKUP_TYPE = 'CE_PARSE_SOURCE_FIELD' 
	AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_SOURCE_FIELD
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = cePARSINGRULEE0.TARGET_FIELD
	AND LOOKUP_TYPE = 'CE_PARSE_TARGET_FIELD' 
	AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_TARGET_FIELD
,cePARSINGRULEE0.PARSE_RULE RES_RULE
,DECODE(cePARSINGRULEE0.OVERWRITE_FLAG,'Y','Yes','No') RES_OVERWRITE

,cePARSINGRULEE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,cePARSINGRULEE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,cePARSINGRULEE0.CREATED_BY RSC_CREATED_BY
,cePARSINGRULEE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM CE_PARSING_RULE_SETS cePARSINGRULESETE0
,CE_PARSING_RULES cePARSINGRULEE0
WHERE cePARSINGRULESETE0.PARSING_RULE_SET_ID = cePARSINGRULEE0.PARSING_RULE_SET_ID
ORDER BY cePARSINGRULESETE0.PARSING_RULE_SET_NAME,cePARSINGRULEE0.SEQUENCE_NUM 