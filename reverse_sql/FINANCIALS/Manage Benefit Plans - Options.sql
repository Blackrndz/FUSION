-- RSC_PREREQUISITE_OBJECTS=BEN_PL_F
-- RSC_PREREQUISITE_OBJECTS=BEN_OIPL_F
 
SELECT benPlansE0.NAME RES_PLAN_NAME
,TO_CHAR(benPlansE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY')  RES_EFFECTIVE_START_DATE
--,planTypesE0.NAME RES_PLAN_TYPE
,(SELECT NAME
    FROM BEN_OPT_F
    WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
    AND OPT_ID = optionsE0.OPT_ID
    ) RES_OPTION_NAME
,optionsE0.ORDR_NUM RES_SEQUENCE
,optionsE0.SHORT_NAME RES_SHORT_NAME
,optionsE0.SHORT_CODE RES_SHORT_CODE
,optionsE0.IVR_IDENT RES_INTERACTIVE_VOICE_RESPONSE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_STAT'
    AND LOOKUP_CODE = optionsE0.OIPL_STAT_CD
    ) RES_STATUS
,optionsE0.URL_REF_NAME RES_WEB_ADDRESS
,REPLACE(REPLACE(REPLACE(optionsE0.CONFIG_CHAR_2,'&','#AMP#'),'<','#OPENTAG#'),'>','#CLOSETAG#') RES_INSTRUCTION_TEXT
,DECODE(optionsE0.CONFIG_CHAR_1,'Y','Yes','No') RES_MINIMUM_VALUE
,TO_CHAR(optionsE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_OPTIONS_START_DATE
,TO_CHAR(optionsE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY') RES_OPTIONS_END_DATE

,optionsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,optionsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,optionsE0.CREATED_BY  RSC_CREATED_BY
,optionsE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPlansE0.LEGISLATION_CODE RSC_COUNTRY_ID

FROM BEN_PL_F benPlansE0
,(SELECT NAME
	,PL_TYP_ID
    FROM BEN_PL_TYP_F
    WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
	AND OPT_TYP_CD != 'FLXCR'
	) planTypesE0
,BEN_OIPL_F optionsE0
WHERE SYSDATE BETWEEN benPlansE0.EFFECTIVE_START_DATE AND benPlansE0.EFFECTIVE_END_DATE
AND benPlansE0.PL_TYP_ID = planTypesE0.PL_TYP_ID
AND benPlansE0.PL_ID = optionsE0.PL_ID
AND benPlansE0.EFFECTIVE_END_DATE BETWEEN optionsE0.EFFECTIVE_START_DATE AND optionsE0.EFFECTIVE_END_DATE
ORDER BY benPlansE0.NAME
,benPlansE0.EFFECTIVE_START_DATE
,optionsE0.ORDR_NUM