SELECT gmsINSTITUE0.INSTITUTION_NAME RES_NAME
,hrLOCTIONE0.LOCATION_NAME RES_LOCATION
,DECODE(gmsINSTITULOCE0.PRIMARY_FLAG,'Y','Yes','No') RES_PRIMARY
,TO_CHAR(gmsINSTITULOCE0.START_DATE_ACTIVE,'DD-Mon-YYYY') RES_FROM_DATE
,TO_CHAR(gmsINSTITULOCE0.END_DATE_ACTIVE,'DD-Mon-YYYY') RES_TO_DATE
,DECODE(gmsINSTITULOCE0.AWARD_LOCATION,'Y','Yes','No') RES_AWARD_LOCATION
,DECODE(gmsINSTITULOCE0.BILLING_LOCATION,'Y','Yes','No') RES_BILLING_LOCATION
,REPLACE(REPLACE(REPLACE(gmsINSTITULOCE0.LOCATION_URL,'&','#AMP#'),'<','#OPENTAG#'),'>','#CLOSETAG#') RES_URL
,hz_format_pub.format_address(p_location_id => hrLOCTIONE0.LOCATION_ID, p_line_break => ',') RES_ADDRESS
,hrLOCTIONE0.TELEPHONE_NUMBER_1 RES_MAIN_PHONE
,(CASE WHEN hrLOCTIONE0.FAX_COUNTRY_CODE2 IS NULL AND hrLOCTIONE0.FAX_AREA_CODE2 IS NULL AND hrLOCTIONE0.FAX_SUBSCRIBER_NUM2 IS NULL AND hrLOCTIONE0.FAX_EXTENSION2 IS NULL
	THEN NULL
	ELSE hrLOCTIONE0.FAX_COUNTRY_CODE2||'-'||hrLOCTIONE0.FAX_AREA_CODE2||'-'||hrLOCTIONE0.FAX_SUBSCRIBER_NUM2||'-'||hrLOCTIONE0.FAX_EXTENSION2 
	END) RES_FAX
,hrLOCTIONE0.EMAIL_ADDRESS RES_EMAIL


,gmsINSTITULOCE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,gmsINSTITULOCE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,gmsINSTITULOCE0.CREATED_BY  RSC_CREATED_BY
,gmsINSTITULOCE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM GMS_INSTITUTIONS_VL gmsINSTITUE0
,GMS_INSTITUTION_LOCATIONS gmsINSTITULOCE0
,HR_LOCATIONS hrLOCTIONE0
WHERE gmsINSTITUE0.INSTITUTION_ID = gmsINSTITULOCE0.INSTITUTION_ID
AND gmsINSTITULOCE0.LOCATION_ID = hrLOCTIONE0.LOCATION_ID
ORDER BY gmsINSTITUE0.INSTITUTION_NAME
,hrLOCTIONE0.LOCATION_NAME
