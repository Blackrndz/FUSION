SELECT (SELECT ELEMENT_NAME 
	FROM PAY_ELEMENT_TYPES_TL 
	WHERE LANGUAGE = USERENV('LANG')
	AND ELEMENT_TYPE_ID = elementsE0.ELEMENT_TYPE_ID
	)RES_ELEMENT_NAME
,legislativeDataGrpsE0.NAME RES_LEGISLATIVE_DATA_GROUP
,(SELECT CLASSIFICATION_NAME
    FROM PAY_ELE_CLASSIFICATIONS_VL
	WHERE BASE_CLASSIFICATION_ID = elementsE0.CLASSIFICATION_ID
	AND LEGISLATION_CODE = legislativeDataGrpsE0.LEGISLATION_CODE
	AND SYSDATE BETWEEN DATE_FROM AND NVL(DATE_TO,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_PRIMARY_CLASSIFICATION
,NVL((SELECT CLASSIFICATION_NAME
    FROM PAY_ELE_CLASSIFICATIONS_VL
    WHERE BASE_CLASSIFICATION_ID = elementsE0.SECONDARY_CLASSIFICATION_ID
	AND LEGISLATION_CODE = legislativeDataGrpsE0.LEGISLATION_CODE
	AND NVL(elementsE0.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) BETWEEN DATE_FROM AND DATE_TO)
	,(SELECT CLASSIFICATION_NAME
    FROM PAY_ELE_CLASSIFICATIONS_VL
    WHERE CLASSIFICATION_ID = elementsE0.SECONDARY_CLASSIFICATION_ID
	AND LEGISLATION_CODE = legislativeDataGrpsE0.LEGISLATION_CODE
	AND NVL(elementsE0.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) BETWEEN DATE_FROM AND DATE_TO)
    ) RES_SECONDARY_CLASSIFICATION
,TO_CHAR(inputE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,(SELECT NAME 
	FROM PAY_INPUT_VALUES_TL 
	WHERE INPUT_VALUE_ID = inputE0.INPUT_VALUE_ID  
	AND LANGUAGE = USERENV('LANG')
	)RES_NAME
,inputE0.DISPLAY_SEQUENCE RES_DISPLAY_SEQUENCE
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = inputE0.RESERVED_INPUT_VALUE
	AND LOOKUP_TYPE = 'PAY_RESERVED_INPUT_VALUE' 
	AND LANGUAGE = USERENV('LANG')
	) RES_SPECIAL_PURPOSE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'PAY_UNITS'
    AND LOOKUP_CODE =  inputE0.UOM
    ) RES_UNIT_OF_MEASURE
,DECODE(inputE0.USER_DISPLAY_FLAG,'Y','Yes','No') RES_DISPLAYED
,DECODE(inputE0.USER_ENTERABLE_FLAG,'Y','Yes','No') RES_ALLOW_USER_ENTRY
,DECODE(inputE0.MANDATORY_FLAG,'Y','Yes','No') RES_REQUIRED
,DECODE(inputE0.GENERATE_DB_ITEMS_FLAG,'Y','Yes','No') RES_CREATE_A_DATABASE_ITEM
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_VL
    WHERE FORMULA_ID = inputE0.RATE_FORMULA_ID
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
    ) RES_RATE_FORMULA
,inputE0.DEFAULT_VALUE RES_DEFAULT_VALUE
,DECODE(inputE0.HOT_DEFAULT_FLAG,'Y','Yes','No') RES_APPLY_DEFAULT_AT_RUN_TIME
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_VL
    WHERE FORMULA_ID = inputE0.FORMULA_ID
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
    ) RES_VALIDATION_FORMULA
,inputE0.MIN_VALUE RES_MINIMUM
,inputE0.MAX_VALUE RES_MAXIMUM
,(SELECT CONTEXT_NAME 
	FROM FF_CONTEXTS_VL 
	WHERE CONTEXT_ID = inputE0.CONTEXT_ID
	) RES_REFERENCE
,inputE0.LOOKUP_TYPE RES_LOOKUP_TYPE
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = inputE0.WARNING_OR_ERROR
	AND LOOKUP_TYPE = 'PAY_WARNING_ERROR' 
	AND LANGUAGE = USERENV('LANG')
	) RES_WARNING_OR_ERROR
,inputE0.VO_NAME RES_VALIDATION_SOURCE
,inputE0.VALUE_SET_CODE RES_VALUE_SET
,DECODE(TO_CHAR(inputE0.EFFECTIVE_END_DATE ,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(inputE0.EFFECTIVE_END_DATE ,'DD-Mon-YYYY')) RES_EFFECTIVE_END_DATE

,inputE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,inputE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,inputE0.CREATED_BY RSC_CREATED_BY
,inputE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,legislativeDataGrpsE0.LEGISLATION_CODE RSC_COUNTRY_ID

FROM PAY_ELEMENT_TYPES_F elementsE0
,PAY_INPUT_VALUES_F inputE0
,(SELECT NAME
    ,LEGISLATIVE_DATA_GROUP_ID
	,LEGISLATION_CODE
    FROM PER_LEGISLATIVE_DATA_GROUPS_VL
    ) legislativeDataGrpsE0
WHERE elementsE0.LEGISLATIVE_DATA_GROUP_ID = legislativeDataGrpsE0.LEGISLATIVE_DATA_GROUP_ID
AND elementsE0.ELEMENT_TYPE_ID = inputE0.ELEMENT_TYPE_ID
AND inputE0.EFFECTIVE_START_DATE = elementsE0.EFFECTIVE_START_DATE
--AND SYSDATE BETWEEN inputE0.EFFECTIVE_START_DATE AND NVL(inputE0.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
--AND SYSDATE BETWEEN elementsE0.EFFECTIVE_START_DATE AND NVL(elementsE0.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY RES_ELEMENT_NAME
