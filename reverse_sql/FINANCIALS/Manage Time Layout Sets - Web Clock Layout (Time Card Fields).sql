 -- RSC_PREREQUISITE_OBJECTS=HXT_TCLAY_V

SELECT RES_LAYOUT_NAME
,RES_DISPLAY_SEQUENCE
,RES_NAME
,RES_BUTTON_DEPENDENCE
,RES_EDIT_WITH_CHANGE_REQUEST
,RES_FIELD_TYPE
,RES_BUTTONS
,RES_WEB_CLOCK_LAYOUT_COMPONENT
,RES_TIME_CARD_FIELD

,RSC_LAST_UPDATED_BY
,RSC_LAST_UPDATE_DATE
,RSC_CREATED_BY
,RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM 
(SELECT DISTINCT hxtTCLAYEO.NAME RES_LAYOUT_NAME
,hxtTCLATDEFNSVEO.DISPLAY_SEQUENCE+1 RES_DISPLAY_SEQUENCE
,hxtTCLATDEFNSVEO.NAME RES_NAME
,(SELECT LABEL FROM HXT_TCLAYFLD_DEFNS_VL WHERE TCLAYFLD_DEFNS_ID = webclockATTBE0.TCLAYFLD_VALUE) RES_BUTTON_DEPENDENCE
,DECODE(hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CHAR28,'Y','Yes','No') RES_EDIT_WITH_CHANGE_REQUEST
,'Independent' RES_FIELD_TYPE
,(SELECT LABEL FROM HXT_TCLAYFLD_DEFNS_VL WHERE TCLAYFLD_DEFNS_ID = webclockATTBE0.TCLAYFLD_VALUE) RES_BUTTONS
,(SELECT NAME FROM HXT_TCLAYFLD_DEFNS_VL WHERE TCLAYFLD_DEFNS_ID = webclockATTBE0.TCLAYFLD_VALUE) RES_WEB_CLOCK_LAYOUT_COMPONENT
,hxtTCLATDEFNSVEO.NAME RES_TIME_CARD_FIELD
 
,hxtTCLATDEFNSVEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hxtTCLATDEFNSVEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hxtTCLATDEFNSVEO.CREATED_BY RSC_CREATED_BY
,hxtTCLATDEFNSVEO.CREATION_DATE RSC_CREATION_DATE
FROM HXT_TCLAYST_VL hxtTCLAYEO   
,HXT_TCLAY_B hxtTCLATVEO
,HXT_TCLAYFLD_DEFNS_VL hxtTCLATDEFNSVEO
,HXT_TCLAYFLD_CXT_DEPT webclockATTBE0

WHERE hxtTCLAYEO.TCLAYST_ID =  hxtTCLATVEO.TCLAYST_ID
AND hxtTCLATVEO.TCLAY_ID = hxtTCLATDEFNSVEO.TCLAY_ID
AND hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CHAR26 = 'MATRIX'
AND hxtTCLATVEO.TCLAY_TYPE LIKE 'Web Clock%'
AND hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CATEGORY = 'RAW_INSTANCE'
AND webclockATTBE0.USG_TYPE = 'WebClock'
AND hxtTCLATDEFNSVEO.TCLAYFLD_DEFNS_ID  = webclockATTBE0.TCLAYFLD_DEFNS_ID
)

UNION

SELECT RES_LAYOUT_NAME
,RES_DISPLAY_SEQUENCE
,RES_NAME
,RES_BUTTON_DEPENDENCE
,RES_EDIT_WITH_CHANGE_REQUEST
,RES_FIELD_TYPE
,RES_BUTTONS
,RES_WEB_CLOCK_LAYOUT_COMPONENT
,RES_TIME_CARD_FIELD

,RSC_LAST_UPDATED_BY
,RSC_LAST_UPDATE_DATE
,RSC_CREATED_BY
,RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM 
(SELECT DISTINCT hxtTCLAYEO.NAME RES_LAYOUT_NAME
,hxtTCLATDEFNSVEO.DISPLAY_SEQUENCE+1 RES_DISPLAY_SEQUENCE
,hxtTCLATDEFNSVEO.NAME RES_NAME
,webclockATTBE0.LABEL RES_BUTTON_DEPENDENCE
,DECODE(hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CHAR28,'Y','Yes','No') RES_EDIT_WITH_CHANGE_REQUEST
,'Dependent' RES_FIELD_TYPE
,webclockATTBE0.LABEL RES_BUTTONS
,webclockATTBE0.NAME RES_WEB_CLOCK_LAYOUT_COMPONENT
,hxtTCLATDEFNSVEO.NAME RES_TIME_CARD_FIELD
 
,hxtTCLATDEFNSVEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hxtTCLATDEFNSVEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hxtTCLATDEFNSVEO.CREATED_BY RSC_CREATED_BY
,hxtTCLATDEFNSVEO.CREATION_DATE RSC_CREATION_DATE
FROM HXT_TCLAYST_VL hxtTCLAYEO   
,HXT_TCLAY_B hxtTCLATVEO
,HXT_TCLAYFLD_DEFNS_VL hxtTCLATDEFNSVEO
,(SELECT A.TCLAYFLD_DEFNS_ID,B.TCLAY_ID,B.LABEL,B.NAME FROM HXT_TCLAYFLD_CXT_DEPT A,HXT_TCLAYFLD_DEFNS_VL B WHERE 
	A.USG_TYPE IS NULL AND A.TCLAYFLD_VALUE = B.TCLAYFLD_ATTRIBUTE_NUMBER11 ) webclockATTBE0

WHERE hxtTCLAYEO.TCLAYST_ID =  hxtTCLATVEO.TCLAYST_ID
AND hxtTCLATVEO.TCLAY_ID = hxtTCLATDEFNSVEO.TCLAY_ID
AND hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CHAR26 = 'MATRIX'
AND hxtTCLATVEO.TCLAY_TYPE LIKE 'Web Clock%'
AND hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CATEGORY = 'RAW_INSTANCE'
AND hxtTCLATDEFNSVEO.TCLAYFLD_TMPL_ID  = webclockATTBE0.TCLAYFLD_DEFNS_ID
AND hxtTCLATDEFNSVEO.TCLAY_ID = webclockATTBE0.TCLAY_ID
)

ORDER BY RES_LAYOUT_NAME,RES_DISPLAY_SEQUENCE,RES_FIELD_TYPE





