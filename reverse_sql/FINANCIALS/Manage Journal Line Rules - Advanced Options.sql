-- RSC_PREREQUISITE_OBJECTS=XLA_ACCT_LINE_TYPES_VL
 
SELECT (SELECT APPLICATION_NAME
	FROM XLA_SUBLEDGERS_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND APPLICATION_ID = JELineRulesE0.APPLICATION_ID
	) RES_SUBLEDGER_APPLICATION
,JELineRulesE0.NAME RES_NAME
,(SELECT NAME
	FROM XLA_EVENT_CLASSES_TL
	WHERE ENTITY_CODE = JELineRulesE0.ENTITY_CODE
	AND EVENT_CLASS_CODE = JELineRulesE0.EVENT_CLASS_CODE
	AND APPLICATION_ID = JELineRulesE0.APPLICATION_ID
	AND LANGUAGE = USERENV('LANG')
	) RES_EVENT_CLASS
,(SELECT MEANING
	FROM XLA_LOOKUPS
	WHERE LOOKUP_TYPE = 'XLA_BALANCE_TYPE'
	AND LOOKUP_CODE = JELineRulesE0.ACCOUNTING_ENTRY_TYPE_CODE
	) RES_BALANCE_TYPE
,(SELECT MEANING
	FROM XLA_LOOKUPS
	WHERE LOOKUP_TYPE = 'XLA_ACCOUNTING_CLASS'
	AND LOOKUP_CODE = JELineRulesE0.ROUNDING_CLASS_CODE
	) RES_ROUNDING_CLASS
,DECODE(JELineRulesE0.GAIN_OR_LOSS_FLAG,'Y','Yes','No') RES_SUBLEDGER_GAIN_OR_LOSS
,(SELECT MEANING
	FROM XLA_LOOKUPS
	WHERE LOOKUP_TYPE = 'XLA_BUSINESS_FLOW_METHOD'
	AND LOOKUP_CODE = JELineRulesE0.BUSINESS_METHOD_CODE
	) RES_LINK_JOURNAL_LINES
,(SELECT MEANING
	FROM XLA_LOOKUPS
	WHERE LOOKUP_TYPE = 'XLA_BUSINESS_FLOW_CLASS'
	AND LOOKUP_CODE = JELineRulesE0.BUSINESS_CLASS_CODE
	) RES_BUSINESS_FLOW_CLASS
,DECODE(JELineRulesE0.MULTIPERIOD_OPTION_FLAG,'Y','Yes','No') RES_MULTIPERIOD
,(SELECT MEANING
	FROM XLA_LOOKUPS
	WHERE LOOKUP_TYPE = 'ORA_XLA_MULTIPERIOD_CLASS'
	AND LOOKUP_CODE = JELineRulesE0.MULTIPERIOD_CLASS_CODE
	) RES_MULTIPERIOD_CLASS
,JELineRulesE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,JELineRulesE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,JELineRulesE0.CREATED_BY RSC_CREATED_BY
,JELineRulesE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM XLA_ACCT_LINE_TYPES_VL JELineRulesE0
ORDER BY 1,2