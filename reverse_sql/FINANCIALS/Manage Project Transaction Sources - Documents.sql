-- RSC_PREREQUISITE_OBJECTS=PJF_TXN_DOCUMENT_VL

SELECT trxSourcesE0.USER_TRANSACTION_SOURCE RES_TRANSACTION_SOURCE
,trxDocumentsE0.DOCUMENT_NAME RES_DOCUMENT
,trxDocumentsE0.DESCRIPTION RES_DESCRIPTION
,DECODE(trxDocumentsE0.COMMITMENT_FLAG,'Y','Yes','No') RES_COMMITMENT_SOURCE
,TO_CHAR(trxDocumentsE0.START_DATE_ACTIVE,'DD-Mon-YYYY') RES_FROM_DATE
,TO_CHAR(trxDocumentsE0.END_DATE_ACTIVE,'DD-Mon-YYYY') RES_TO_DATE
,(SELECT MEANING
    FROM FND_LOOKUPS
    WHERE LOOKUP_TYPE = 'PJC_CMT_LINE_TYPE'
    AND LOOKUP_CODE = trxDocumentsE0.COMMITMENT_TYPE
    ) RES_COMMITMENT_TYPE
,DECODE(trxDocumentsE0.COSTED_FLAG,'Y','Yes','No') RES_IMPORT_RAW_COST_AMOUNTS
,DECODE(trxDocumentsE0.ALLOW_DUPLICATE_REF_FLAG,'Y','Yes','No') RES_ALLOW_DUPLICATE_REFERENCE
,DECODE(trxDocumentsE0.TIEBACK_TO_SOURCE,'Y','Yes','No') RES_RECONCILE_WITH_SOURCE
,DECODE(trxDocumentsE0.REVALIDATE_FLAG,'Y','Yes','No') RES_REVALIDATE_DURING_IMPORT
,DECODE(trxDocumentsE0.ALLOW_BURDEN_FLAG,'Y','Yes','No') RES_IMPORT_BURDENED_COST_AMOUN
,DECODE(trxDocumentsE0.ALLOW_EMP_ORG_OVERRIDE_FLAG,'Y','Yes','No') RES_ALLOW_OVERRIDE_OF_PERSON_O
,DECODE(trxDocumentsE0.EXP_BATCH_APPROVAL_FLAG,'Y','Yes','No') RES_REQUIRES_EXPENDITURE_BATCH
,DECODE(trxDocumentsE0.PURGEABLE_FLAG,'Y','Yes','No') RES_ARCHIVE_AFTER_IMPORT
,(SELECT MEANING
    FROM FND_LOOKUPS
    WHERE LOOKUP_TYPE = 'PJF_ACCT_IN_SRC_SYS'
    AND LOOKUP_CODE = trxDocumentsE0.IMPORT_COST_ACC_FLAG
    ) RES_ACCOUNTED_IN_SOURCE_APPLIC
,DECODE(trxDocumentsE0.COST_ACC_JOURNAL_FLAG,'Y','Yes','No') RES_CREATE_RAW_COST_ACCOUNTING
,DECODE(trxDocumentsE0.ADJUST_ACC_JOURNAL_FLAG,'Y','Yes','No') RES_CREATE_ADJUSTMENT_ACCOUNTI
,DECODE(trxDocumentsE0.IMPORT_IN_CLOSED_PERIOD_FLAG,'Y','Yes','No') RES_IMPORT_ACCOUNTED_COST_WHEN
,trxDocumentsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,trxDocumentsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,trxDocumentsE0.CREATED_BY RSC_CREATED_BY
,trxDocumentsE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJF_TXN_SOURCES_VL trxSourcesE0
,PJF_TXN_DOCUMENT_VL trxDocumentsE0
WHERE trxSourcesE0.TRANSACTION_SOURCE_ID = trxDocumentsE0.TRANSACTION_SOURCE_ID
ORDER BY trxSourcesE0.USER_TRANSACTION_SOURCE
,trxDocumentsE0.DOCUMENT_NAME