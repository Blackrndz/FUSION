SELECT  TO_CHAR(SS_DATE_TO_USE,'DD-MM-YYYY') RES_SELF_SERVICE_EFFECTIVE_DAT
,DECODE(benSSCONFIGEO.DISP_SS_PCP_REGION,'Y','Yes','No') RES_DISPLAY_PRIMARY_CARE_PHYSI
,DECODE(benSSCONFIGEO.DISP_PARAM_FLAG,'Y','Yes','No') RES_DISPLAY_PARAMETERS
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benSSCONFIGEO.ALLOW_SS_UNRST_PROCG_CD 
	AND LOOKUP_TYPE = 'BEN_ALLOW_SS_UNRST_PROCG' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_UNRESTRICTED_PROCESSING_EN
,DECODE(benSSCONFIGEO.DSPLY_OVW_CONT_WARN,'Y','Yes','No') RES_DISPLAY_WARNING_TO_REVIEW_
,TO_CHAR(benSSCONFIGEO.CONT_WARN_DAYS,'999') RES_FREQUENCY_IN_DAYS
,DECODE(benSSCONFIGEO.DSPLY_CHC_WARN,'Y','Yes','No') RES_DISPLAY_WARNING_TO_HIGHLIG
,DECODE(benSSCONFIGEO.CONFIG_CHAR_2,'Y','Yes','No') RES_DISPLAY_SEPARATE_DESIGNATI
,DECODE(benSSCONFIGEO.DISP_SIGN_REGION,'Y','Yes','No') RES_DISPLAY_SIGNATURE



,benSSCONFIGEO.LAST_UPDATED_BY 			RSC_LAST_UPDATED_BY
,benSSCONFIGEO.LAST_UPDATE_DATE 			RSC_LAST_UPDATE_DATE
,benSSCONFIGEO.CREATED_BY 				RSC_CREATED_BY
,benSSCONFIGEO.CREATION_DATE 			RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_SS_CONFIGURATION benSSCONFIGEO
