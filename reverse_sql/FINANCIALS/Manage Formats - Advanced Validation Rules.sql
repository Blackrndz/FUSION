SELECT IbyFormatsVl.FORMAT_NAME RES_NAME
,IbyFormatsVl.FORMAT_CODE RES_CODE
,ibyADVANCE0.RULE_NAME RES_VALIDATION_RULES_NAME
,(SELECT LookupPEO.MEANING
	FROM FND_LOOKUPS LookupPEO
	WHERE LookupPEO.LOOKUP_TYPE = 'IBY_VALIDATION_LEVELS'
	AND LookupPEO.LOOKUP_CODE   = ibyADVANCE0.VALIDATION_LEVEL_CODE
	) RES_VALIDATION_POINT
,TO_CHAR(ibyADVANCE0.START_DATE,'DD-Mon-YYYY') RES_FROM_DATE
,DECODE(TO_CHAR(ibyADVANCE0.END_DATE,'DD-MM-YYYY'),'31-12-4712',null,TO_CHAR(ibyADVANCE0.END_DATE,'DD-Mon-YYYY')) RES_TO_DATE
,ibyADVANCE0.CONDITION RES_CONDITION

,IbyFormatsVl.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,IbyFormatsVl.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,IbyFormatsVl.CREATED_BY RSC_CREATED_BY
,IbyFormatsVl.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM IBY_FORMATS_VL IbyFormatsVl
,IBY_ADVANCED_VALIDATIONS_VL ibyADVANCE0
WHERE IbyFormatsVl.FORMAT_CODE = ibyADVANCE0.ASSIGNMENT_ENTITY_ID
ORDER BY IbyFormatsVl.FORMAT_NAME,ibyADVANCE0.RULE_NAME