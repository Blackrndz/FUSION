SELECT benPGMEO.NAME RES_PROGRAM_NAME
,TO_CHAR(benPGMEO.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,(SELECT NAME
    FROM HR_ORGANIZATION_UNITS
    WHERE SYSDATE BETWEEN DATE_FROM AND DATE_TO
    AND ORGANIZATION_ID = benLERESOE0.LEGAL_ENTITY_ID
    ) RES_LEGAL_EMPLOYER 
,TO_CHAR(benLERESOE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_LEGAL_ENTITIES_EFFECTIVE_S
,TO_CHAR(benLERESOE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY') RES_LEGAL_ENTITIES_EFFECTIVE_E

,benLERESOE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,benLERESOE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,benLERESOE0.CREATED_BY RSC_CREATED_BY
,benLERESOE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPGMEO.LEGISLATION_CODE RSC_COUNTRY_ID

FROM BEN_PGM_F benPGMEO
,BEN_LE_RESOLUTIONS_F benLERESOE0
WHERE benPGMEO.PGM_ID = benLERESOE0.MAPPING_TABLE_PK_ID
AND benLERESOE0.MAPPING_TABLE_NAME = 'BEN_PGM_F'
AND benLERESOE0.MAPPING_COLUMN_NAME = 'PGM_ID'
AND SYSDATE BETWEEN benPGMEO.EFFECTIVE_START_DATE AND NVL(benPGMEO.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))
AND SYSDATE BETWEEN benLERESOE0.EFFECTIVE_START_DATE AND NVL(benLERESOE0.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))
ORDER BY RES_PROGRAM_NAME,RES_LEGAL_EMPLOYER
