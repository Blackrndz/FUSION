SELECT 
(SELECT ITEM_CLASS_NAME
	FROM EGP_ITEM_CLASSES_VL
	WHERE ITEM_CLASS_ID = ItemClassPEO.PARENT_ITEM_CLASS_ID
	) RES_PARENT_ITEM_CLASS 
,ItemClassPEO.ITEM_CLASS_NAME RES_ITEM_CLASS
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGO_ITEMPEOPLE_GRANTEE_TYPE'
	AND LOOKUP_CODE = FndGrantsE0.GRANTEE_TYPE) RES_PRINCIPAL
,(CASE
	WHEN FndGrantsE0.GRANTEE_TYPE = 'USER' THEN
		(SELECT USERNAME
		FROM PER_USERS
		WHERE USER_GUID = FndGrantsE0.GRANTEE_KEY)
	WHEN FndGrantsE0.GRANTEE_TYPE = 'GROUP' THEN
		NVL((SELECT ROLE_NAME
		FROM PER_ROLES_DN_VL
		WHERE ROLE_COMMON_NAME = FndGrantsE0.ROLE_NAME),FndGrantsE0.ROLE_NAME)
	END) RES_NAME
,(SELECT ORGANIZATION_CODE
	FROM INV_ORG_PARAMETERS
	WHERE ORGANIZATION_ID = FndGrantsE0.PARAMETER1
	) RES_ORGANIZATION
,(select DESCRIPTION
	from EGP_ITEM_CLASSES_VL
	where ITEM_CLASS_CODE =  ItemClassPEO.ITEM_CLASS_CODE 
	) RES_ITEM_CLASS_PEOPLE_ITEM_CLA
,'' RES_INHERITED -- Not found in Internal Instance
,TO_CHAR(FndGrantsE0.START_DATE,'DD-Mon-YYYY') RES_START_DATE
,TO_CHAR(FndGrantsE0.END_DATE,'DD-Mon-YYYY') RES_END_DATE
,FndGrantsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,FndGrantsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,FndGrantsE0.CREATED_BY  RSC_CREATED_BY
,FndGrantsE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,(SELECT BUSINESS_UNIT_ID
	FROM INV_ORG_PARAMETERS
	WHERE ORGANIZATION_ID = FndGrantsE0.PARAMETER1
	) RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,(CASE
	WHEN EXISTS
		(SELECT DISTINCT MASTER_ORGANIZATION_ID
			FROM INV_ORG_PARAMETERS
			WHERE MASTER_ORGANIZATION_ID = FndGrantsE0.PARAMETER1)
	THEN NULL
	ELSE FndGrantsE0.PARAMETER1
	END) RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM EGP_ITEM_CLASSES_VL ItemClassPEO
,FND_GRANTS FndGrantsE0
WHERE TO_CHAR(ItemClassPEO.ITEM_CLASS_ID) = FndGrantsE0.PARAMETER2
CONNECT BY ItemClassPEO.PARENT_ITEM_CLASS_ID = ItemClassPEO.ITEM_CLASS_ID
ORDER SIBLINGS BY RES_PARENT_ITEM_CLASS,RES_ITEM_CLASS