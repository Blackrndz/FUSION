SELECT (SELECT ITEM_CLASS_NAME
	FROM EGP_ITEM_CLASSES_VL
	WHERE ITEM_CLASS_ID = ItemClassPEO.PARENT_ITEM_CLASS_ID
	) RES_PARENT_ITEM_CLASS 
,ItemClassPEO.ITEM_CLASS_NAME RES_ITEM_CLASS
,ImportFormatEO.MAPPING_NAME RES_NAME
,(CASE
	WHEN EgiIoMapDestination.DESTINATION_TABLE LIKE 'EGO_ITEM_INTF_EFF%' 
		OR EgiIoMapDestination.DESTINATION_TABLE = 'EGP_ITEM_CATEGORIES_INTERFACE' THEN 
		'Item'
	ELSE
		(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'EGP_ENTITY_NAMES'
			AND LOOKUP_CODE = EgiIoMapDestination.DESTINATION_TABLE)
	END) RES_OBJECT
,(CASE
	WHEN EgiIoMapDestination.DESTINATION_TABLE LIKE 'EGO_ITEM_INTF_EFF%' 
		OR EgiIoMapDestination.DESTINATION_TABLE = 'EGP_ITEM_CATEGORIES_INTERFACE' THEN 
		(SELECT NAME
			FROM FND_DF_CONTEXTS_VL
			WHERE DESCRIPTIVE_FLEXFIELD_CODE = 'EGO_ITEM_EFF'
			AND CONTEXT_CODE = EgiIoMapDestination.CONTEXT_CODE)
	WHEN EgiIoMapDestination.DESTINATION_TABLE LIKE 'EGP_SYSTEM_ITEMS_INTERFACE'  THEN 
			(SELECT DISTINCT ATTR_GROUP_DISP_NAME
			FROM EGP_ITEM_ATTRIBUTES_VL
			WHERE ATTR_NAME = EgiIoMapDestination.DESTINATION_COLUMN
			AND NVL(ATTR_GROUP_NAME,'R4C')  = NVL(EgiIoMapDestination.CONTEXT_CODE,'R4C'))
	ELSE	
		(SELECT DISTINCT ATTR_GROUP_DISP_NAME
			FROM EGP_ITEM_ATTRIBUTES_VL
			WHERE ATTR_NAME = EgiIoMapDestination.DESTINATION_COLUMN
			AND NVL(ATTR_GROUP_NAME,'R4C')  = NVL(EgiIoMapDestination.CONTEXT_CODE,'R4C'))
	END)  RES_ATTRIBUTE_GROUP
,(CASE
	WHEN EgiIoMapDestination.DESTINATION_TABLE LIKE 'EGO_ITEM_INTF_EFF%'  THEN 
		(SELECT PROMPT
			FROM FND_DF_SEGMENTS_VL
			WHERE DESCRIPTIVE_FLEXFIELD_CODE = 'EGO_ITEM_EFF'
			AND CONTEXT_CODE = EgiIoMapDestination.CONTEXT_CODE
			AND COLUMN_NAME = EgiIoMapDestination.DESTINATION_COLUMN)
	WHEN EgiIoMapDestination.DESTINATION_TABLE LIKE 'EGP_SYSTEM_ITEMS_INTERFACE'  THEN 
		(CASE
			WHEN EgiIoMapDestination.DESTINATION_COLUMN = 'BUNDLE_ID' THEN
				NULL
			ELSE
				(SELECT DISTINCT ATTR_DISP_NAME
					FROM EGP_ITEM_ATTRIBUTES_VL
					WHERE ATTR_NAME = EgiIoMapDestination.DESTINATION_COLUMN
					AND NVL(INTERFACE_TABLE_NAME,'R4C')  = NVL(EgiIoMapDestination.DESTINATION_TABLE,'R4C')
					
					UNION
					
					SELECT DISTINCT ATTR_DISP_NAME
					FROM EGP_ITEM_ATTRIBUTES_VL
					WHERE ATTR_NAME = EgiIoMapDestination.DESTINATION_COLUMN
					AND NVL(ATTR_GROUP_NAME,'R4C')  = NVL(EgiIoMapDestination.CONTEXT_CODE,'R4C'))
			END)
	WHEN EgiIoMapDestination.DESTINATION_TABLE = 'EGO_ITEM_ASSOCIATIONS_INTF'  THEN 
		(CASE
			WHEN EgiIoMapDestination.DESTINATION_COLUMN = 'STATUS_CODE' THEN
				'Status Code'
			WHEN EgiIoMapDestination.DESTINATION_COLUMN = 'SUPPLIER_SITE_NAME' THEN
				'Address Name'
			ELSE
				(SELECT DISTINCT ATTR_DISP_NAME
					FROM EGP_ITEM_ATTRIBUTES_VL
					WHERE ATTR_NAME = EgiIoMapDestination.DESTINATION_COLUMN
					AND INTERFACE_TABLE_NAME = EgiIoMapDestination.DESTINATION_TABLE)
			END)
	ELSE
		(SELECT DISTINCT ATTR_DISP_NAME
			FROM EGP_ITEM_ATTRIBUTES_VL
			WHERE ATTR_NAME = EgiIoMapDestination.DESTINATION_COLUMN
			AND INTERFACE_TABLE_NAME = EgiIoMapDestination.DESTINATION_TABLE)
	END) RES_ATTRIBUTE
,'' RES_REQUIRED -- Not found in Internal instance
,ImportFormatEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,ImportFormatEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,ImportFormatEO.CREATED_BY  RSC_CREATED_BY
,ImportFormatEO.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM EGP_ITEM_CLASSES_VL ItemClassPEO
,EGI_IO_MAP_VL ImportFormatEO
,EGI_IO_MAP_DESTINATION EgiIoMapDestination
WHERE ItemClassPEO.ITEM_CLASS_ID = ImportFormatEO.ITEM_CLASS_ID
AND ImportFormatEO.MAPPING_ID = EgiIoMapDestination.MAPPING_ID
AND EXISTS (SELECT 1 FROM FND_TABLES WHERE TABLE_NAME = EgiIoMapDestination.DESTINATION_TABLE)
CONNECT BY ItemClassPEO.PARENT_ITEM_CLASS_ID = ItemClassPEO.ITEM_CLASS_ID
ORDER SIBLINGS BY RES_PARENT_ITEM_CLASS
,RES_ITEM_CLASS
,RES_NAME
,RES_OBJECT
,RES_ATTRIBUTE_GROUP