-- RSC_PREREQUISITE_OBJECTS=HRR_TMPL_DISPLAY_OPTN_PREFS

SELECT talentRwTemplatesE0.NAME RES_TEMPLATE_NAME
,DECODE(displayOPTNE0.DISPLAY_OPTN_TYPE,'MOBILITY','Mobility','IMPACT_OF_LOSS','Impact of Loss','RISK_OF_LOSS','Risk of Loss') RES_ANALYTIC_OPTIONS
,(CASE WHEN displayOPTNE0.DISPLAY_OPTN_TYPE = 'MOBILITY' 
	THEN (SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ORA_HRR_ANALYTIC_DISP_OPTNS'
    AND LOOKUP_CODE = talentRwTemplatesE0.MOBILITY_DSPLY_OPTN_PREF ) 
	WHEN displayOPTNE0.DISPLAY_OPTN_TYPE = 'IMPACT_OF_LOSS' 
	THEN (SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ORA_HRR_ANALYTIC_DISP_OPTNS'
    AND LOOKUP_CODE = talentRwTemplatesE0.IOL_DSPLY_OPTN_PREF )  
	WHEN displayOPTNE0.DISPLAY_OPTN_TYPE = 'RISK_OF_LOSS'
	THEN (SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ORA_HRR_ANALYTIC_DISP_OPTNS'
    AND LOOKUP_CODE = talentRwTemplatesE0.ROL_DSPLY_OPTN_PREF )    
	ELSE NULL END)RES_PREFERRED_DISPLAY_OPTION
,DECODE(displayOPTNE0.DISPLAY_OPTN_VALUE,'NO_VALUE','No value','NO_ACCESS','Secured','Y','Yes','N','No',(SELECT RATING_DESCRIPTION FROM HRT_RATING_LEVELS_VL WHERE RATING_LEVEL_ID = displayOPTNE0.DISPLAY_OPTN_VALUE)) RES_ANALYTIC_VALUE
,displayOPTNE0.DISPLAY_VALUE_COLOR RES_COLOR_TO_DISPLAY
--,SUBSTR(displayOPTNE0.DISPLAY_VALUE_COLOR,2,LENGTH(displayOPTNE0.DISPLAY_VALUE_COLOR)-1) RES_COLOR_TO_DISPLAY
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ORA_HRR_DISPLAY_SHAPE_OPTIONS'
    AND LOOKUP_CODE = displayOPTNE0.DISPLAY_VALUE_SHAPE
    ) RES_SHAPE_TO_DISPLAY

,displayOPTNE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,displayOPTNE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,displayOPTNE0.CREATED_BY RSC_CREATED_BY
,displayOPTNE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM HRR_DASHBOARD_TMPLS_VL talentRwTemplatesE0
,HRR_TMPL_DISPLAY_OPTN_PREFS displayOPTNE0
WHERE talentRwTemplatesE0.DASHBOARD_TMPL_ID = displayOPTNE0.DASHBOARD_TMPL_ID
ORDER BY talentRwTemplatesE0.NAME,2