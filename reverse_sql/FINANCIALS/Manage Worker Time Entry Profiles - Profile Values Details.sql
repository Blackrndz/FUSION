--RSC_PREREQUISITE_OBJECTS=HWM_INTERACT_SETS

SELECT hxtSPEO.NAME RES_PROFILE_NAME 
,TO_CHAR(hxtSOVEO.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,TO_CHAR(hxtSOVEO.EFFECTIVE_END_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_END_DATE
,hxtSPEO.DESCRIPTION RES_DESCRIPTION
,hxtTCALYEO.NAME RES_LAYOUT_SET
,hxtTCALYEO.DESCRIPTION RES_LAYOUT_SET_DESCRIPTION
,DECODE(hxtSOVEO.VALUE20,'N','No','Yes') RES_ALLOW_EDITS_TO_WORKER_SCHE
,(SELECT ITR_SET_NAME FROM HWM_INTERACT_SETS WHERE ITR_SET_ID = hxtSOVEO.VALUE31) RES_ATTESTATION_SET
,hxtSOVEO.VALUE24 RES_ALLOW_EDITS_TO_ATTESTATION
,hxtSOVEO.VALUE74 RES_DAYS_BEFORE
,hxtSOVEO.VALUE75 RES_DAYS_AFTER
,(CASE WHEN hxtSOVEO.VALUE26 = 'ORA_HWM_TIME_ENTRY'
	THEN 'Yes'
	ELSE 'No' END) RES_ENABLE_COST_OVERRIDES
,DECODE(hxtSOVEO.VALUE21,'N','No','Yes') RES_ALLOW_EDITS_TO_TIMES_GENER
,hxtSOVEO.CREATED_BY  AS RSC_CREATED_BY
,hxtSOVEO.CREATION_DATE AS RSC_CREATION_DATE
,hxtSOVEO.LAST_UPDATED_BY  AS RSC_LAST_UPDATED_BY
,hxtSOVEO.LAST_UPDATE_DATE AS RSC_LAST_UPDATE_DATE
, null RSC_LEDGER_ID
, null RSC_CHART_OF_ACCOUNTS_ID
, null RSC_BUSINESS_UNIT_ID
, null RSC_LEGAL_ENTITY_ID
, null RSC_ORGANIZATION_ID
, null RSC_BUSINESS_GROUP_ID
, NULL RSC_ENTERPRISE_ID
, NULL RSC_COUNTRY_ID
  

FROM HXT_SETUP_PROFILES_VL hxtSPEO 
,HXT_SETUP_OPTION_VALS hxtSOVEO
,HXT_TCLAYST_VL hxtTCALYEO
WHERE hxtSPEO.PRODUCT_AREA = 'WORKER_TIMECARD' 
AND hxtSOVEO.TYPE = 'WORKER'
AND hxtSPEO.SETUP_PROFILE_ID = hxtSOVEO.SETUP_PROFILE_ID
AND hxtSOVEO.VALUE30 = hxtTCALYEO.TCLAYST_ID
ORDER BY hxtSPEO.PRECEDENCE,hxtSOVEO.EFFECTIVE_START_DATE
