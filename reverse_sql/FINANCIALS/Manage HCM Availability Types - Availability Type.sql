-- RSC_PREREQUISITE_OBJECTS=ANC_AVAILABILITY_TYPES_VL

SELECT (SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'ORA_ANC_AVAILABILITY_TYPES'
	AND LOOKUP_CODE = avalTypesE0.CONTRACTED_HOURS 
	) RES_SELECT_AVAILABILITY_TYPE
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'ORA_ANC_WORK_SHIFT_TYPES'
	AND LOOKUP_CODE = avalTypesE0.WORK_SHIFT_TYPE 
	) RES_AVAILABILITY_ENTRY_METHOD
,avalTypesE0.NAME RES_NAME
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'ACTIVE_INACTIVE'
	AND LOOKUP_CODE = avalTypesE0.ACTIVE_STATUS 
	) RES_STATUS
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'ORA_ANC_PATTERN_TYPES'
	AND LOOKUP_CODE = avalTypesE0.REPEATED_PATTERN_TYPE 
	) RES_CYCLE
,(SELECT TERRITORY_SHORT_NAME 
	FROM FND_TERRITORIES_VL 
	WHERE TERRITORY_CODE = avalTypesE0.LEGISLATION_CODE 
	) RES_COUNTRY
,avalTypesE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,avalTypesE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,avalTypesE0.CREATED_BY  RSC_CREATED_BY
,avalTypesE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,avalTypesE0.LEGISLATION_CODE  RSC_COUNTRY_ID
 
FROM ANC_AVAILABILITY_TYPES_VL avalTypesE0
ORDER BY avalTypesE0.NAME