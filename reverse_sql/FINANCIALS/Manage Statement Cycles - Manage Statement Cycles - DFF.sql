-- IMPORTANT:
-- - the alias names for the Foreign keys must be the same as in the inventory.
-- - the alias name for context code must be RES_CONTEXT_CODE
-- - NO ALIASES for atributes fields. 

SELECT ARSTATEMENTCYCLES.name RES_NAME
,ARSTATEMENTCYCLES.ATTRIBUTE_CATEGORY RES_CONTEXT_CODE
,ARSTATEMENTCYCLES.ATTRIBUTE1
,ARSTATEMENTCYCLES.ATTRIBUTE2
,ARSTATEMENTCYCLES.ATTRIBUTE3
,ARSTATEMENTCYCLES.ATTRIBUTE4
,ARSTATEMENTCYCLES.ATTRIBUTE5
,ARSTATEMENTCYCLES.ATTRIBUTE6
,ARSTATEMENTCYCLES.ATTRIBUTE7
,ARSTATEMENTCYCLES.ATTRIBUTE8
,ARSTATEMENTCYCLES.ATTRIBUTE9
,ARSTATEMENTCYCLES.ATTRIBUTE10
,ARSTATEMENTCYCLES.ATTRIBUTE11
,ARSTATEMENTCYCLES.ATTRIBUTE12
,ARSTATEMENTCYCLES.ATTRIBUTE13
,ARSTATEMENTCYCLES.ATTRIBUTE14
,ARSTATEMENTCYCLES.ATTRIBUTE15
,ARSTATEMENTCYCLES.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,ARSTATEMENTCYCLES.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,ARSTATEMENTCYCLES.CREATED_BY  RSC_CREATED_BY
,ARSTATEMENTCYCLES.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM AR_STATEMENT_CYCLES ARSTATEMENTCYCLES
WHERE (ARSTATEMENTCYCLES.ATTRIBUTE_CATEGORY IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE1 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE2 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE3 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE4 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE5 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE6 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE7 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE8 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE9 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE10 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE11 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE12 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE13 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE14 IS NOT NULL
OR ARSTATEMENTCYCLES.ATTRIBUTE15 IS NOT NULL)
ORDER BY ArStatementCycles.NAME