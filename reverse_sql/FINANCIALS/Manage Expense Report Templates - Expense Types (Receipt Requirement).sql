SELECT ExpenseTemplateEO.NAME RES_EXPENSE_REPORT_TEMPLATES_N
,(SELECT BU_NAME
	FROM FUN_ALL_BUSINESS_UNITS_V
	WHERE BU_ID = ExpenseTemplateEO.ORG_ID
	) RES_BUSINESS_UNIT
,ExpenseTypeEO.NAME RES_EXPENSE_TYPE_NAME
,(
	CASE
		WHEN ExpenseTypeEO.CATEGORY_CODE <> 'MILEAGE'
		THEN
			(SELECT DISPLAYED_FIELD
			FROM EXM_LOOKUP_VALUES
			WHERE LOOKUP_TYPE = 'EXM_RECEIPT_REQ_CASH_CC'
			AND LOOKUP_CODE   = ExpenseTypeEO.RECEIPT_REQUIRED_FLAG
			)
	END) RES_REQUIRE_RECEIPTS_FOR_CASH_
,(
	CASE
		WHEN ExpenseTypeEO.CATEGORY_CODE <> 'MILEAGE'
		THEN ExpenseTypeEO.RECEIPT_THRESHOLD
	END) RES_CASH_EXPENSE_LINES_DEFINE_
,(
	CASE
		WHEN ExpenseTypeEO.CATEGORY_CODE <> 'MILEAGE'
		THEN
			(SELECT DISPLAYED_FIELD
			FROM EXM_LOOKUP_VALUES
			WHERE LOOKUP_TYPE = 'EXM_RECEIPT_REQ_CASH_CC'
			AND LOOKUP_CODE   = ExpenseTypeEO.CC_RECEIPT_REQUIRED_FLAG
			)
	END) RES_REQUIRE_RECEIPTS_FOR_CORPO
,(
	CASE
		WHEN ExpenseTypeEO.CATEGORY_CODE <> 'MILEAGE'
		THEN ExpenseTypeEO.CC_RECEIPT_THRESHOLD
	END) RES_CORPORATE_CARD_EXPENSE_LIN
,(
	CASE
		WHEN ExpenseTypeEO.CATEGORY_CODE <> 'MILEAGE'
		THEN
			(SELECT DISPLAYED_FIELD
			FROM EXM_LOOKUP_VALUES
			WHERE LOOKUP_TYPE = 'EXM_RECEIPT_REQ_NEG_WARN'
			AND LOOKUP_CODE   = ExpenseTypeEO.NEGATIVE_RCPT_REQ_CODE
			)
	END) RES_APPLY_RECEIPT_REQUIREMENT_
,(
	CASE
		WHEN ExpenseTypeEO.CATEGORY_CODE <> 'MILEAGE'
		THEN
			(SELECT DISPLAYED_FIELD
			FROM EXM_LOOKUP_VALUES
			WHERE LOOKUP_TYPE = 'EXM_RECEIPT_REQ_NEG_WARN'
			AND LOOKUP_CODE   = ExpenseTypeEO.DISP_RCPT_VIOLATION_FLAG
			)
	END) RES_DISPLAY_RECEIPT_MISSING_PO
,(
	CASE
		WHEN ExpenseTypeEO.CATEGORY_CODE = 'MILEAGE'
		THEN DECODE(ExpenseTypeEO.RECEIPT_REQUIRED_FLAG,'Y','Yes','No')
	END) RES_SUPPORTING_DOCUMENTS_REQUI
,(
	CASE
		WHEN ExpenseTypeEO.CATEGORY_CODE = 'MILEAGE'
		THEN
			(SELECT DISPLAYED_FIELD
			FROM EXM_LOOKUP_VALUES
			WHERE LOOKUP_TYPE = 'EXM_RECEIPT_REQ_NEG_WARN'
			AND LOOKUP_CODE   = ExpenseTypeEO.DISP_RCPT_VIOLATION_FLAG
			)
	END) RES_DISPLAY_MISSING_DOCUMENTS_
,ExpenseTypeEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ExpenseTypeEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ExpenseTypeEO.CREATED_BY RSC_CREATED_BY
,ExpenseTypeEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,ExpenseTemplateEO.ORG_ID RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM EXM_EXPENSE_TEMPLATES ExpenseTemplateEO
,EXM_EXPENSE_TYPES ExpenseTypeEO
WHERE ExpenseTemplateEO.EXPENSE_TEMPLATE_ID = ExpenseTypeEO.EXPENSE_TEMPLATE_ID
ORDER BY ExpenseTemplateEO.NAME
,RES_BUSINESS_UNIT
,ExpenseTypeEO.NAME