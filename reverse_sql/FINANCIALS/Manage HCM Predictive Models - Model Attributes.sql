SELECT pModelsE0.MODEL_NAME RES_PREDICTIVE_MODEL
,attrsE0.ATTR_NAME RES_ATTRIBUTE_NAME
,attrsE0.ATTR_CODE RES_ATTRIBUTE_CODE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'HWP_ATTR_DATA_TYPE'
    AND LOOKUP_CODE = attrsE0.DATA_TYPE
    ) RES_DATA_TYPE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'HWP_ATTR_CATEGORY'
    AND LOOKUP_CODE = attrsE0.ATTR_CATEGORY
    ) RES_CATEGORY
,attrsE0.ATTR_DESCRIPTION RES_DESCRIPTION
,(SELECT GROUP_NAME
    FROM FF_DBI_GROUPS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND DBI_GROUP_ID = attrsE0.DBI_GROUP_ID
    ) RES_DATABASE_ITEM_GROUP
,DECODE(attrsE0.UI_WHATIF_FLAG,'Y','Yes','No') RES_WHAT_IF_ENABLED
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'HWP_ATTR_UI_TYPE'
    AND LOOKUP_CODE = attrsE0.UI_DISPLAY_TYPE
    ) RES_DISPLAY_TYPE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'HWP_ATTR_RANGE_TYPE'
    AND LOOKUP_CODE = attrsE0.RANGE_TYPE
    ) RES_RANGE_TYPE
,attrsE0.MIN_VALUE RES_MINIMUM_VALUE
,attrsE0.MAX_VALUE RES_MAXIMUM_VALUE
,attrsE0.INCR_VALUE RES_INCREMENT_VALUE
,'Yes' RES_PREDICTIVE_MODEL_MODEL_ATT
,attrsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,attrsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,attrsE0.CREATED_BY RSC_CREATED_BY
,attrsE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,attrsE0.ENTERPRISE_ID RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID
 
FROM HWP_MODELS_VL pModelsE0
,HWP_MODEL_ATTRIBUTE_MAPPING modelAttrsE0
,HWP_ATTRIBUTES_VL attrsE0
WHERE pModelsE0.MODEL_ID = modelAttrsE0.MODEL_ID
AND modelAttrsE0.ATTRIBUTE_ID = attrsE0.ATTRIBUTE_ID
ORDER BY pModelsE0.MODEL_NAME
,attrsE0.ATTR_NAME