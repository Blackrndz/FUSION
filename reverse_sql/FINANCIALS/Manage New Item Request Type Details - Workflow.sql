-- RSC_PREREQUISITE_OBJECTS=EGO_CHANGE_TYPES_VL
 
SELECT changeOrderTypesE0.TYPE_NAME RES_NAME
,workflowsE0.SEQUENCE_NUMBER RES_SEQUENCE_NUMBER
,changeStatusE0.STATUS_NAME RES_STATUS
,(SELECT STATUS_NAME
	FROM EGO_CHANGE_STATUSES_VL
	WHERE STATUS_CODE = changeStatusE0.STATUS_TYPE
	) RES_STATUS_TYPE
,DECODE(workflowsE0.SKIP_REQUEST_COMMENT,'Y','Yes','No') RES_SKIP_REQUEST_COMMENT
,(SELECT STATUS_NAME
	FROM EGO_CHANGE_STATUSES_VL
	WHERE STATUS_CODE = workflowsE0.AUTO_PROMOTE_STATUS
	) RES_AUTOMATIC_PROMOTION_STATUS
,(SELECT STATUS_NAME
	FROM EGO_CHANGE_STATUSES_VL
	WHERE STATUS_CODE = workflowsE0.AUTO_DEMOTE_STATUS
	) RES_AUTOMATIC_DEMOTION_STATUS
,workflowsE0.ENTRY_CRITERIA_RULE_SET_NAME RES_ENTRY_CRITERIA
,workflowsE0.EXIT_CRITERIA_RULE_SET_NAME RES_EXIT_CRITERIA
,workflowsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,workflowsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,workflowsE0.CREATED_BY  RSC_CREATED_BY
,workflowsE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID
 
FROM EGO_CHANGE_TYPES_VL changeOrderTypesE0
,(SELECT ENTITY_ID1
	,STATUS_CODE
	,SEQUENCE_NUMBER
	,SKIP_REQUEST_COMMENT
	,AUTO_PROMOTE_STATUS
	,AUTO_DEMOTE_STATUS
	,ENTRY_CRITERIA_RULE_SET_NAME
	,EXIT_CRITERIA_RULE_SET_NAME
	,CHANGE_EDITABLE_FLAG
	,LAST_UPDATED_BY
	,LAST_UPDATE_DATE
	,CREATED_BY
	,CREATION_DATE
	FROM EGO_LIFECYCLE_STATUSES
	WHERE ENTITY_NAME = 'EGO_CHANGE_TYPE'
	) workflowsE0
,EGO_CHANGE_STATUSES_VL changeStatusE0
WHERE changeOrderTypesE0.CHANGE_TYPE_ID = workflowsE0.ENTITY_ID1
AND workflowsE0.STATUS_CODE = changeStatusE0.STATUS_CODE
AND changeOrderTypesE0.TYPE_INTERNAL_NAME = 'NewItemRequest'
ORDER BY changeOrderTypesE0.TYPE_NAME
,workflowsE0.SEQUENCE_NUMBER