SELECT 
(SELECT ITEM_CLASS_NAME
	FROM EGP_ITEM_CLASSES_VL
	WHERE ITEM_CLASS_ID = ItemClassPEO.PARENT_ITEM_CLASS_ID
	) RES_PARENT_ITEM_CLASS 
,ItemClassPEO.ITEM_CLASS_NAME RES_ITEM_CLASS
,ItemVersionEO.TEMPLATE_NAME RES_NAME
,(SELECT ORGANIZATION_CODE
	FROM INV_ORG_PARAMETERS
	WHERE ORGANIZATION_ID = ItemVersionEO.ORGANIZATION_ID
	) RES_ORGANIZATION
,(SELECT UNIT_OF_MEASURE
	FROM INV_UNITS_OF_MEASURE_VL
	WHERE UOM_CODE = ItemVersionEO.DIMENSION_UOM_CODE
	) RES_DIMENSIONS_UNIT
,ItemVersionEO.UNIT_WIDTH RES_DIMENSIONS_WIDTH
,ItemVersionEO.UNIT_LENGTH RES_DIMENSIONS_LENGTH
,ItemVersionEO.UNIT_HEIGHT RES_DIMENSIONS_HEIGHT
,(SELECT UNIT_OF_MEASURE
	FROM INV_UNITS_OF_MEASURE_VL
	WHERE UOM_CODE = ItemVersionEO.WEIGHT_UOM_CODE
	) RES_WEIGTH_UNIT
,ItemVersionEO.UNIT_WEIGHT RES_WEIGTH_UNIT_WEIGHT
,(SELECT UNIT_OF_MEASURE
	FROM INV_UNITS_OF_MEASURE_VL
	WHERE UOM_CODE = ItemVersionEO.VOLUME_UOM_CODE
	) RES_VOLUME_UNIT
,ItemVersionEO.UNIT_VOLUME RES_VOLUME_UNIT_VOLUME
,DECODE(ItemVersionEO.CONTAINER_ITEM_FLAG,'Y','Yes','N','No') RES_CONTAINER
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_CONTAINER_TYPE'
	AND LOOKUP_CODE = ItemVersionEO.CONTAINER_TYPE_CODE
	) RES_CONTAINER_TYPE
,ItemVersionEO.MAXIMUM_LOAD_WEIGHT RES_MAXIMUM_LOAD_WEIGHT
,DECODE(ItemVersionEO.VEHICLE_ITEM_FLAG,'Y','Yes','N','No') RES_VEHICLE
,ItemVersionEO.INTERNAL_VOLUME RES_INTERNAL_VOLUME
,ItemVersionEO.MINIMUM_FILL_PERCENT RES_MINIMUM_FILL_PERCENT
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_COMMON_YES_NO_NUM'
	AND LOOKUP_CODE = ItemVersionEO.EQUIPMENT_TYPE
	) RES_WAREHOUSE_EQUIPMENT
,DECODE(ItemVersionEO.EVENT_FLAG,'Y','Yes','N','No') RES_EVENT
,DECODE(ItemVersionEO.COLLATERAL_FLAG,'Y','Yes','N','No') RES_COLLATERAL
,ItemVersionEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ItemVersionEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ItemVersionEO.CREATED_BY RSC_CREATED_BY
,ItemVersionEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,(SELECT BUSINESS_UNIT_ID
	FROM INV_ORG_PARAMETERS
	WHERE ORGANIZATION_ID = ItemVersionEO.ORGANIZATION_ID
	) RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,(CASE
	WHEN EXISTS
		(SELECT DISTINCT MASTER_ORGANIZATION_ID
			FROM INV_ORG_PARAMETERS
			WHERE MASTER_ORGANIZATION_ID = ItemVersionEO.ORGANIZATION_ID)
	THEN NULL
	ELSE ItemVersionEO.ORGANIZATION_ID
	END) RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM EGP_ITEM_CLASSES_VL ItemClassPEO
--,EGP_SYSTEM_ITEMS_V ItemVersionEO
,(SELECT TL.TEMPLATE_NAME 
	,B.* 
  FROM EGP_SYSTEM_ITEMS_TL TL
	,EGP_SYSTEM_ITEMS_B B
	WHERE TL.INVENTORY_ITEM_ID = B.INVENTORY_ITEM_ID
	AND TL.ORGANIZATION_ID = B.ORGANIZATION_ID
	AND LANGUAGE = USERENV('LANG')
	AND TEMPLATE_NAME IS NOT NULL
 ) ItemVersionEO
WHERE /*ItemVersionEO.TEMPLATE_NAME IS NOT NULL
AND*/ ItemClassPEO.ITEM_CLASS_ID = ItemVersionEO.ITEM_CATALOG_GROUP_ID
CONNECT BY ItemClassPEO.PARENT_ITEM_CLASS_ID = ItemClassPEO.ITEM_CLASS_ID
ORDER SIBLINGS BY RES_PARENT_ITEM_CLASS,RES_ITEM_CLASS,RES_NAME