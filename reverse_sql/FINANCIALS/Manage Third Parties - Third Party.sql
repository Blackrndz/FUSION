SELECT hzPATIESE0.PARTY_NAME RES_PARTY_NAME
,hzPATIESE0.PARTY_TYPE RES_PARTY_TYPE
,(SELECT PARTY_USAGE_NAME  FROM HZ_PARTY_USAGES_VL WHERE PARTY_USAGE_CODE = hzPARTYUSEASSE0.PARTY_USAGE_CODE) RES_PARTY_USAGE_CODE
,hzPATIESE0.DUNS_NUMBER_C RES_D_U_N_S_NUMBER
,hzPATIESE0.PARTY_NUMBER RES_REGISTRY_ID
,hzPATIESE0.PERSON_PRE_NAME_ADJUNCT RES_PREFIX
,hzPATIESE0.PERSON_FIRST_NAME RES_FIRST_NAME
,hzPATIESE0.PERSON_MIDDLE_NAME RES_MIDDLE_NAME
,hzPATIESE0.PERSON_LAST_NAME RES_LAST_NAME
,hzPATIESE0.PERSON_TITLE RES_TITLE
,TO_CHAR(hzPATIESE0.DATE_OF_BIRTH,'DD-Mon-YYYY') RES_DATE_OF_BIRTH

,hzPATIESE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hzPATIESE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hzPATIESE0.CREATED_BY RSC_CREATED_BY
,hzPATIESE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID


FROM HZ_PARTIES hzPATIESE0
,HZ_PARTY_USG_ASSIGNMENTS hzPARTYUSEASSE0

WHERE hzPATIESE0.PARTY_ID = hzPARTYUSEASSE0.PARTY_ID 
AND hzPARTYUSEASSE0.PARTY_USAGE_CODE IN ('PAYMENT_ISSUING_AUTHORITY','PENSION_PROVIDER','PROFESSIONAL_BODY','BARGAINING_ASSOCIATION','DISABILITY_ORGANIZATION','EXTERNAL_PAYEE')
ORDER BY hzPATIESE0.PARTY_NAME
