SELECT (SELECT NAME FROM PER_LEGISLATIVE_DATA_GROUPS_VL WHERE LEGISLATIVE_DATA_GROUP_ID = payBALEXEREPORTO.LEGISLATIVE_DATA_GROUP_ID) RES_LEGISLATIVE_DATA_GROUP
,payBALEXEREPORTO.BAL_EXCEP_REPORT_NAME RES_EXCEPTION_REPORT_NAME
,(SELECT CONSOLIDATION_SET_NAME FROM PAY_CONSOLIDATION_SETS WHERE  CONSOLIDATION_SET_ID = payBALEXEREPORTO.CONSOLIDATION_SET_ID) RES_CONSOLIDATION_GROUP
,(SELECT PAYROLL_NAME FROM PAY_ALL_PAYROLLS_F WHERE PAYROLL_ID = payBALEXEREPORTO.PAYROLL_ID
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_PAYROLL

,payBALEXEREPORTO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,payBALEXEREPORTO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,payBALEXEREPORTO.CREATED_BY RSC_CREATED_BY
,payBALEXEREPORTO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PAY_BAL_EXCEPTION_REPORTS_VL  payBALEXEREPORTO
ORDER BY RES_LEGISLATIVE_DATA_GROUP,payBALEXEREPORTO.LEGISLATION_CODE,RES_EXCEPTION_REPORT_NAME