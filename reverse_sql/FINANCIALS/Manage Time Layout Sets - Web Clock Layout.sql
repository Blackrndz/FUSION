-- RSC_PREREQUISITE_OBJECTS=HXT_TCLAY_V

SELECT RES_LAYOUT_NAME
,RES_HIDE_SECONDS_ON_CLOCK
,RES_ENABLE_BUTTON_LOGIC_RULE
,RES_ENABLE_VIEWING_OF_DAILY_TI
,RES_ENABLE_EDITING_OF_CLOCK_TI
,RES_TIME_TO_USE
,RES_ALLOWABLE_DIFFERENCE_BETWE

,RSC_LAST_UPDATED_BY
,RSC_LAST_UPDATE_DATE
,RSC_CREATED_BY
,RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM 
(SELECT DISTINCT hxtTCLAYEO.NAME RES_LAYOUT_NAME
,DECODE(matrixE0.TCLAYFLD_ATTRIBUTE_CHAR18,'Y','Yes','No') RES_HIDE_SECONDS_ON_CLOCK
,DECODE(matrixE0.TCLAYFLD_ATTRIBUTE_CHAR15,'Y','Yes','No') RES_ENABLE_BUTTON_LOGIC_RULE
,DECODE(matrixE0.TCLAYFLD_ATTRIBUTE_CHAR17,'Y','Yes','No') RES_ENABLE_VIEWING_OF_DAILY_TI
,DECODE(matrixE0.TCLAYFLD_ATTRIBUTE_CHAR19,'Y','Yes','No') RES_ENABLE_EDITING_OF_CLOCK_TI
,DECODE(matrixE0.TCLAYFLD_ATTRIBUTE_CHAR20,'ORA_HXT_DEVICE_TIME','Device time','ORA_HXT_SERVER_TIME','Server time') RES_TIME_TO_USE
,matrixE0.TCLAYFLD_ATTRIBUTE_NUMBER2 RES_ALLOWABLE_DIFFERENCE_BETWE

,matrixE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,matrixE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,matrixE0.CREATED_BY RSC_CREATED_BY
,matrixE0.CREATION_DATE RSC_CREATION_DATE

FROM HXT_TCLAYST_VL hxtTCLAYEO   
,HXT_TCLAY_B hxtTCLATVEO
,(SELECT * FROM HXT_TCLAYFLD_DEFNS_VL WHERE TCLAYFLD_ATTRIBUTE_CATEGORY IN 'MATRIX_PARAMS') matrixE0


WHERE hxtTCLAYEO.TCLAYST_ID =  hxtTCLATVEO.TCLAYST_ID
AND hxtTCLATVEO.TCLAY_ID = matrixE0.TCLAY_ID
AND hxtTCLATVEO.TCLAY_TYPE LIKE 'Web Clock%'
)

ORDER BY RES_LAYOUT_NAME


