-- RSC_PREREQUISITE_OBJECTS=BEN_PL_F
 
SELECT benPlansE0.NAME RES_PLAN_NAME
,TO_CHAR(benPlansE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,TO_CHAR(benPlansE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_ADDITIONAL_CONFIGURATION_E
,DECODE(TO_CHAR(benPlansE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY'),'31-12-4712',NULL,TO_CHAR(benPlansE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY')) RES_ADDITIONAL_CONFIGURATION_0
,benPlansE0.CARRIER_PL_NAME RES_BENEFITS_EXTRACT_PLAN_CODE
,planTypesE0.NAME RES_PLAN_TYPE
,DECODE(benPlansE0.CONFIG_CHAR_1,'Y','Yes','No') RES_VISIBLE_ON_SELF_SERVICE_PA
,(SELECT NAME
    FROM BEN_PL_CARRIER
    WHERE CARRIER_ID = benPlansE0.CARRIER_ID
    ) RES_PLAN_CARRIER
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ORA_BEN_PLAN_CATEGORY'
    AND LOOKUP_CODE = benPlansE0.PL_CAT
    ) RES_PLAN_CATEGORY
,DECODE(benPlansE0.SUBJ_TO_IMPTD_INCM_CD,'Y','Yes','No') RES_MINIMUM_ESSENTIAL_COVERAGE
,DECODE(benPlansE0.FRFS_APLY_FLAG,'Y','Yes','No') RES_MINIMUM_VALUE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_HLTH_CARE_SVC_TYP'
    AND LOOKUP_CODE = benPlansE0.HC_SVC_TYP_CD
    ) RES_HEALTH_SERVICES_CATEGORY
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_PRMRY_FNDG_MTHD'
    AND LOOKUP_CODE = benPlansE0.PRMRY_FNDG_MTHD_CD
    ) RES_PRIMARY_FUNDING_METHOD
,DECODE(benPlansE0.ALWS_TMPRY_ID_CRD_FLAG,'Y','Yes','No') RES_ENABLE_TEMPORARY_IDENTIFIC
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_WTG_PERD_UOM'
    AND LOOKUP_CODE = benPlansE0.MX_WTG_PERD_PRTE_UOM
    ) RES_UNIT_OF_MEASURE
,benPlansE0.MX_WTG_PERD_PRTE_VAL RES_NUMBER_OF_UNITS_IN_PERIOD
, (SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benPlansE0.MX_WTG_PERD_PRTE_DET_RL
    ) RES_WAITING_PERIOD_FORMULA
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_MX_WTG_DT_TO_USE'
    AND LOOKUP_CODE = benPlansE0.MX_WTG_DT_TO_USE_CD
    ) RES_PERIOD_START_DATE
, (SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benPlansE0.MX_WTG_DT_TO_USE_RL
    ) RES_PERIOD_START_DATE_FORMULA
,benPlansE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,benPlansE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,benPlansE0.CREATED_BY  RSC_CREATED_BY
,benPlansE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPlansE0.LEGISLATION_CODE RSC_COUNTRY_ID

FROM BEN_PL_F benPlansE0
,(SELECT NAME
	,PL_TYP_ID
    FROM BEN_PL_TYP_F
    WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
	AND OPT_TYP_CD != 'FLXCR'
	) planTypesE0
WHERE SYSDATE BETWEEN benPlansE0.EFFECTIVE_START_DATE AND benPlansE0.EFFECTIVE_END_DATE
AND benPlansE0.PL_TYP_ID = planTypesE0.PL_TYP_ID
ORDER BY benPlansE0.NAME
,benPlansE0.EFFECTIVE_START_DATE