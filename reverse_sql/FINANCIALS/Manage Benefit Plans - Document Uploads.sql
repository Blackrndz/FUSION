-- RSC_PREREQUISITE_OBJECTS=BEN_PL_F
-- RSC_PREREQUISITE_OBJECTS=BEN_CTFN_DOC_MAP
 
SELECT benPlansE0.NAME RES_PLAN_NAME
,TO_CHAR(benPlansE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ORA_BEN_DOC_CTFN_TYP'
    AND LOOKUP_CODE = planDOCE0.CTFN_TYPE_CD
    ) RES_CERTIFICATION_TYPE
,(SELECT DOCUMENT_TYPE FROM HR_DOCUMENT_TYPES_VL WHERE DOCUMENT_TYPE_ID =planDOCE0.DOCUMENT_TYPE_ID) RES_DOCUMENT_TYPE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ORA_BEN_DOC_VALIDITY'
    AND LOOKUP_CODE = planDOCE0.VALIDITY_CD
    ) RES_VALIDITY_RULE
,planDOCE0.VALIDITY_TM_NUM RES_NUMBER_OF_DAYS
,TO_CHAR(planDOCE0.START_DATE,'DD-Mon-YYYY') RES_DOCUMENT_UPLOADS_START_DAT
,TO_CHAR(planDOCE0.END_DATE,'DD-Mon-YYYY') RES_DOCUMENT_UPLOADS_END_DATE

,planDOCE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,planDOCE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,planDOCE0.CREATED_BY  RSC_CREATED_BY
,planDOCE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPlansE0.LEGISLATION_CODE RSC_COUNTRY_ID

FROM BEN_PL_F benPlansE0
,(SELECT NAME
	,PL_TYP_ID
    FROM BEN_PL_TYP_F
    WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
	AND OPT_TYP_CD != 'FLXCR'
	) planTypesE0
,BEN_CTFN_DOC_MAP planDOCE0
WHERE SYSDATE BETWEEN benPlansE0.EFFECTIVE_START_DATE AND benPlansE0.EFFECTIVE_END_DATE
AND benPlansE0.PL_TYP_ID = planTypesE0.PL_TYP_ID
AND benPlansE0.PL_ID = planDOCE0.MAPPING_TABLE_PK_ID
AND planDOCE0.MAPPING_TABLE_NAME = 'BEN_PL_F'
AND planDOCE0.MAPPING_COLUMN_NAME = 'PL_ID'
ORDER BY benPlansE0.NAME
,benPlansE0.EFFECTIVE_START_DATE