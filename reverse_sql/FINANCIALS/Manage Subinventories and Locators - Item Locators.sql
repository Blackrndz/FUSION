SELECT (SELECT ORGANIZATION_CODE
	FROM INV_ORG_PARAMETERS
	WHERE ORGANIZATION_ID = ItemSubInvE0.ORGANIZATION_ID
	) RES_ORGANIZATION
,ItemSubInvE0.SECONDARY_INVENTORY RES_SUBINVENTORY
,(SELECT ITEM_NUMBER
	FROM EGP_SYSTEM_ITEMS_B
	WHERE INVENTORY_ITEM_ID = ItemSubInvE0.INVENTORY_ITEM_ID 
	AND ORGANIZATION_ID = ItemSubInvE0.ORGANIZATION_ID 
	) RES_ITEM
,FND_FLEX_EXT.GET_SEGS(APPLICATION_SHORT_NAME => 'INV',KEY_FLEX_CODE => 'MTLL',STRUCTURE_NUMBER =>
	ItemLocatorsE0.STRUCTURE_INSTANCE_NUMBER,COMBINATION_ID => InvSubLocatorsE0.SECONDARY_LOCATOR,DATA_SET_NUMBER => ItemLocatorsE0.SUBINVENTORY_ID) RES_LOCATOR
,ItemSubInvE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ItemSubInvE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ItemSubInvE0.CREATED_BY RSC_CREATED_BY
,ItemSubInvE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,ItemSubInvE0.ORGANIZATION_ID RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM INV_ITEM_SUB_INVENTORIES ItemSubInvE0
,INV_SECONDARY_LOCATORS InvSubLocatorsE0
,INV_ITEM_LOCATIONS ItemLocatorsE0
WHERE ItemSubInvE0.INVENTORY_ITEM_ID = InvSubLocatorsE0.INVENTORY_ITEM_ID
AND ItemSubInvE0.ORGANIZATION_ID = InvSubLocatorsE0.ORGANIZATION_ID
AND ItemSubInvE0.SECONDARY_INVENTORY = InvSubLocatorsE0.SUBINVENTORY_CODE
AND InvSubLocatorsE0.SECONDARY_LOCATOR = ItemLocatorsE0.INVENTORY_LOCATION_ID
AND InvSubLocatorsE0.ORGANIZATION_ID = ItemLocatorsE0.ORGANIZATION_ID
AND InvSubLocatorsE0.SUBINVENTORY_CODE = ItemLocatorsE0.SUBINVENTORY_CODE