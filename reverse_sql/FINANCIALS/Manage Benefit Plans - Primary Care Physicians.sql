SELECT benPlansE0.NAME RES_PLAN_NAME
,TO_CHAR(benPlansE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_PCP_DSGN'
    AND LOOKUP_CODE = primariesE0.PCP_DSGN_CD
    ) RES_PARTICIPANT_DESIGNATION_RU
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_PCP_DSGN'
    AND LOOKUP_CODE = primariesE0.PCP_DPNT_DSGN_CD
    ) RES_DEPENDENT_DESIGNATION_RULE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_PCP_STRT_DT'
    AND LOOKUP_CODE = primariesE0.PCP_STRT_DT_CD
    ) RES_START_DATE

--Participant Information
,TO_CHAR(pcpInfoE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_PARTICIPANT_EFFECTIVE_STAR
,DECODE(TO_CHAR(pcpInfoE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY'),'31-12-4712',NULL,TO_CHAR(pcpInfoE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY')) RES_PARTICIPANT_EFFECTIVE_END_
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ACTN_TYP'
    AND LOOKUP_CODE = pcpInfoE0.ACTN_TYP_CD
    ) RES_PARTICIPANT_ACTION_ITEM
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ACTN_TYP_DUE_DT'
    AND LOOKUP_CODE = pcpInfoE0.ACTN_TYP_DUE_DT_CD
    ) RES_PARTICIPANT_DUE_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = pcpInfoE0.ACTN_TYP_DUE_DT_RL
    ) RES_PARTICIPANT_DUE_DATE_FORMU
,DECODE(pcpInfoE0.SSPND_FLAG,'Y','Yes','No') RES_PARTICIPANT_SUSPEND_ENROLL
,DECODE(pcpInfoE0.RQD_FLAG,'Y','Yes','No') RES_PARTICIPANT_REQUIRED

--Dependent Information
,TO_CHAR(dpntInfoE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_DEPENDENT_EFFECTIVE_START_
,DECODE(TO_CHAR(dpntInfoE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY'),'31-12-4712',NULL,TO_CHAR(dpntInfoE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY')) RES_DEPENDENT_EFFECTIVE_END_DA
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ACTN_TYP'
    AND LOOKUP_CODE = dpntInfoE0.ACTN_TYP_CD
    ) RES_DEPENDENT_ACTION_ITEM
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ACTN_TYP_DUE_DT'
    AND LOOKUP_CODE = dpntInfoE0.ACTN_TYP_DUE_DT_CD
    ) RES_DEPENDENT_DUE_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = dpntInfoE0.ACTN_TYP_DUE_DT_RL
    ) RES_DEPENDENT_DUE_DATE_FORMULA
,DECODE(dpntInfoE0.SSPND_FLAG,'Y','Yes','No') RES_DEPENDENT_SUSPEND_ENROLLME
,DECODE(dpntInfoE0.RQD_FLAG,'Y','Yes','No') RES_DEPENDENT_REQUIRED
,dpntInfoE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,dpntInfoE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,dpntInfoE0.CREATED_BY  RSC_CREATED_BY
,dpntInfoE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPlansE0.LEGISLATION_CODE RSC_COUNTRY_ID

FROM BEN_PL_F benPlansE0
,(SELECT NAME
	,PL_TYP_ID
    FROM BEN_PL_TYP_F
    WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
	AND OPT_TYP_CD != 'FLXCR'
	) planTypesE0
,BEN_PL_PCP primariesE0
,(SELECT *
    FROM BEN_POPL_ACTN_TYP_F
    WHERE ACTN_TYP_CD = 'PCPPRTT'
     AND ACTN_TYP_LVL_CD = 'PCP'
    ) pcpInfoE0
,(SELECT *
    FROM BEN_POPL_ACTN_TYP_F
    WHERE ACTN_TYP_CD = 'PCPDPNT'
     AND ACTN_TYP_LVL_CD = 'PCP'
    ) dpntInfoE0
WHERE SYSDATE BETWEEN benPlansE0.EFFECTIVE_START_DATE AND benPlansE0.EFFECTIVE_END_DATE
AND benPlansE0.PL_TYP_ID = planTypesE0.PL_TYP_ID
AND benPlansE0.PL_ID = primariesE0.PL_ID(+)
AND benPlansE0.PL_ID = pcpInfoE0.PL_ID(+)
AND benPlansE0.PL_ID = dpntInfoE0.PL_ID(+)
ORDER BY benPlansE0.NAME
,benPlansE0.EFFECTIVE_START_DATE