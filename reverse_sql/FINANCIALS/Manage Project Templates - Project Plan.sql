SELECT projTemplatesE0.NAME RES_TEMPLATE_NAME
,projTemplatesE0.SEGMENT1 RES_TEMPLATE_NUMBER
,(SELECT ELEMENT_NUMBER
	FROM PJF_PROJ_ELEMENTS_VL
	WHERE PROJ_ELEMENT_ID = projElementsE0.DENORM_PARENT_ELEMENT_ID
	) RES_PARENT_TASK_NUMBER
,projElementsE0.ELEMENT_NUMBER RES_TASK_NUMBER
,projElementsE0.NAME RES_TASK_NAME
,projElementsE0.DESCRIPTION RES_DESCRIPTION
,(SELECT NAME
	FROM HR_ALL_ORGANIZATION_UNITS
	WHERE ORGANIZATION_ID = projElementsE0.CARRYING_OUT_ORGANIZATION_ID
	) RES_ORGANIZATION
,(SELECT FULL_NAME
	FROM PER_PERSON_NAMES_F
	WHERE NAME_TYPE = 'GLOBAL'
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
	AND PERSON_ID = projElementsE0.MANAGER_PERSON_ID
	) RES_TASK_MANAGER
,DECODE(projElementsE0.MILESTONE_FLAG,'Y','Yes','No') RES_MILESTONE
,DECODE(projElementsE0.CRITICAL_FLAG,'Y','Yes','No') RES_CRITICAL
,TO_CHAR(projElementsE0.PLANNING_START_DATE,'DD-Mon-YYYY') RES_PLANNED_START_DATE
,TO_CHAR(projElementsE0.PLANNING_END_DATE,'DD-Mon-YYYY') RES_PLANNED_FINISH_DATE
,'' RES_CASCADE_OPTION
,TO_CHAR(projElementsE0.START_DATE,'DD-Mon-YYYY') RES_TRANSACTION_START_DATE
,TO_CHAR(projElementsE0.COMPLETION_DATE,'DD-Mon-YYYY') RES_TRANSACTION_FINISH_DATE
,DECODE(projElementsE0.ALLOW_CROSS_CHARGE_FLAG,'Y','Yes','No') RES_ALLOW_CROSS_CHARGE_TRANSAC
,DECODE(projElementsE0.CC_PROCESS_LABOR_FLAG,'Y','Yes','No') RES_PROCESS_CROSS_CHARGE_TRANS
,(SELECT TP_SCHEDULE_NAME
	FROM PJF_TP_SCHEDULES
	WHERE TP_SCHEDULE_ID = projElementsE0.LABOR_TP_SCHEDULE_ID
	) RES_TRANSFER_PRICE_SCHEDULE_FO
,TO_CHAR(projElementsE0.LABOR_TP_FIXED_DATE,'DD-Mon-YYYY') RES_FIXED_DATE_FOR_LABOR
,DECODE(projElementsE0.CC_PROCESS_NL_FLAG,'Y','Yes','No') RES_PROCESS_CROSS_CHARGE_TRA_0
,(SELECT TP_SCHEDULE_NAME
	FROM PJF_TP_SCHEDULES
	WHERE TP_SCHEDULE_ID = projElementsE0.NL_TP_SCHEDULE_ID
	) RES_TRANSFER_PRICE_SCHEDULE__1
,TO_CHAR(projElementsE0.NL_TP_FIXED_DATE,'DD-Mon-YYYY') RES_FIXED_DATE_FOR_NONLABOR
,DECODE(projElementsE0.CINT_ELIGIBLE_FLAG,'Y','Yes','No') RES_ALLOW_CAPITAL_INTEREST
,TO_CHAR(projElementsE0.CINT_STOP_DATE,'DD-Mon-YYYY') RES_CAPITAL_INTEREST_STOP_DATE
,(SELECT NAME
    FROM PJF_WORK_TYPES_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND WORK_TYPE_ID = projTemplatesE0.WORK_TYPE_ID
    ) RES_WORK_TYPE
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJF_SERVICE_TYPE'
	AND LOOKUP_CODE = projElementsE0.SERVICE_TYPE_CODE
	) RES_SERVICE_TYPE
,DECODE(projElementsE0.RETIREMENT_COST_FLAG,'Y','Yes','No') RES_RETIREMENT_COST
,projElementsE0.SITE_USE_ID RES_CUSTOMER_WORK_SITE --No data to test
,DECODE(projElementsE0.BILLABLE_FLAG,'Y','Yes','No') RES_BILLABLE
,DECODE(projElementsE0.CHARGEABLE_FLAG,'Y','Yes','No') RES_CHARGEABLE
,DECODE(projElementsE0.CAPITALIZABLE_FLAG,'Y','Yes','No') RES_CAPITALIZABLE
,DECODE(projElementsE0.RECEIVE_PROJECT_INVOICE_FLAG,'Y','Yes','No') RES_RECEIVE_INTERCOMPANY_AND_I
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJF_PERCENT_COMP_METHOD'
	AND LOOKUP_CODE = projElementsE0.PERCENT_COMP_CALC_METHOD
	) RES_PHYSICAL_PERCENT_COMPLETE_
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJF_ETC_METHOD'
	AND LOOKUP_CODE = projElementsE0.ETC_CALC_METHOD
	) RES_PROGRESS_ETC_METHOD
,projElementsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,projElementsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,projElementsE0.CREATED_BY  RSC_CREATED_BY
,projElementsE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,projTemplatesE0.ORG_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJF_PROJECTS_ALL_VL projTemplatesE0
,FUN_ALL_BUSINESS_UNITS_V buE0
,PJF_PROJ_ELEMENTS_VL projElementsE0
WHERE projTemplatesE0.TEMPLATE_FLAG = 'Y'
AND projTemplatesE0.ORG_ID = buE0.BU_ID
AND projTemplatesE0.PROJECT_ID = projElementsE0.PROJECT_ID
ORDER BY projTemplatesE0.NAME
,DECODE(projElementsE0.OBJECT_TYPE,'PJF_STRUCTURES',1,2)
,projElementsE0.DENORM_WBS_LEVEL
,projElementsE0.ELEMENT_NUMBER