--RSC_PREREQUISITE_OBJECTS=CMP_PLANS_VL
 
SELECT cmpPLANEO.PLAN_NAME RES_PLAN
,DECODE(cmpPLANEO.MODEL_ENABLED_FLAG,'N','No','Yes') RES_ENABLE_MODELING
,(CASE
	WHEN cmpPLANEO.MODEL_ENABLED_FLAG != 'N' THEN
		(SELECT MEANING
			FROM FND_LOOKUP_VALUES 
			WHERE LANGUAGE = USERENV('LANG')
			AND LOOKUP_TYPE = 'CMP_SETUP_ENABLE_MODELING'
			AND LOOKUP_CODE = cmpPLANEO.MODEL_ENABLED_FLAG)
	END) RES_ROLES_WITH_MODEL_ACCESS
,(CASE
	WHEN cmpPLANEO.MODEL_ENABLED_FLAG = 'Y' THEN
		DECODE(cmpPLANEO.CREATE_MODEL_ENABLED_FLAG,'N','No','Yes')
	END) RES_MANAGERS_CAN_CREATE_THEIR_
,(CASE
	WHEN cmpPLANEO.MODEL_ENABLED_FLAG = 'Y' THEN
		DECODE(cmpPLANEO.MANAGE_MODEL_DI_ENABLED_FLAG,'N','No','Yes')
	END) RES_ENABLE_MANAGE_MODEL_IN_WOR
,(CASE
	WHEN cmpPLANEO.MODEL_ENABLED_FLAG != 'N' THEN
		cmpPLANEO.MODEL_BATCH_LIMIT 
	END) RES_APPLY_MODEL_BATCH_LIMIT
,'' RES_MODEL_SHARING_ENABLE
,'' RES_MODEL_SHARING_SHARING_REST
,'' RES_MODEL_USAGE_ENABLE
,'' RES_MODEL_USAGE_MANAGERS
,'' RES_MODEL_USAGE_ADMINISTRATORS

,cmpPLANEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,cmpPLANEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,cmpPLANEO.CREATED_BY RSC_CREATED_BY
,cmpPLANEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM CMP_PLANS_VL cmpPLANEO
WHERE cmpPLANEO.COMP_TYPE = 'CWB'
ORDER BY cmpPLANEO.PLAN_NAME