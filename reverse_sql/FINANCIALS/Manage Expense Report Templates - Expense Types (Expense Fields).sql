-- RSC_PREREQUISITE_OBJECTS=EXM_AIRFARE_POLICIES
-- RSC_PREREQUISITE_OBJECTS=EXM_CARRENTAL_POLICIES
-- RSC_PREREQUISITE_OBJECTS=EXM_PERDIEM_POLICIES
 

SELECT ExpenseTemplateEO.NAME RES_EXPENSE_REPORT_TEMPLATES_N
,(SELECT BU_NAME
	FROM FUN_ALL_BUSINESS_UNITS_V
	WHERE BU_ID = ExpenseTemplateEO.ORG_ID
	) RES_BUSINESS_UNIT
,ExpenseTypeEO.NAME RES_EXPENSE_TYPE_NAME
,NVL((SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = ExpenseTypeEO.DESCRIPTION_BEHAVIOUR_CODE
		AND LOOKUP_TYPE = 'EXM_FIELD_BEHAVIOUR'
		AND LANGUAGE = USERENV('LANG')),'Use setup from expense template') RES_DESCRIPTION
,NVL((SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = ExpenseTypeEO.LOCATION_BEHAVIOUR_CODE
		AND LOOKUP_TYPE = 'EXM_FIELD_BEHAVIOUR'
		AND LANGUAGE = USERENV('LANG')),'Use setup from expense template') RES_EXPENSE_LOCATION
,NVL((SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = ExpenseTypeEO.NUM_OF_DAYS_BEHAVIOUR_CODE
		AND LOOKUP_TYPE = 'EXM_FIELD_BEHAVIOUR'
		AND LANGUAGE = USERENV('LANG')),'Use setup from expense template') RES_NUMBER_OF_DAYS
,NVL((SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = ExpenseTypeEO.MERCHANT_BEHAVIOUR_CODE
		AND LOOKUP_TYPE = 'EXM_FIELD_BEHAVIOUR'
		AND LANGUAGE = USERENV('LANG')),'Use setup from expense template') RES_MERCHANT_NAME
,ExpenseTypeEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ExpenseTypeEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ExpenseTypeEO.CREATED_BY RSC_CREATED_BY
,ExpenseTypeEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,ExpenseTemplateEO.ORG_ID RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM EXM_EXPENSE_TEMPLATES ExpenseTemplateEO
,EXM_EXPENSE_TYPES ExpenseTypeEO
WHERE ExpenseTemplateEO.EXPENSE_TEMPLATE_ID = ExpenseTypeEO.EXPENSE_TEMPLATE_ID
ORDER BY ExpenseTemplateEO.NAME
,RES_BUSINESS_UNIT