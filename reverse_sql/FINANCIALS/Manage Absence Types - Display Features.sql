-- RSC_PREREQUISITE_OBJECTS=ANC_ABSENCE_TYPES_VL

SELECT DISTINCT ancABSENTYPEEO.NAME RES_NAME
,(SELECT TERRITORY_SHORT_NAME 
	FROM FND_TERRITORIES_TL 
	WHERE LANGUAGE = USERENV('LANG')
	AND TERRITORY_CODE = ancABSENTYPEEO.LEGISLATION_CODE
	) RES_LEGISLATION
,(SELECT NAME 
	FROM PER_LEGISLATIVE_DATA_GROUPS_TL 
	WHERE LANGUAGE = USERENV('LANG')
	AND LEGISLATIVE_DATA_GROUP_ID = ancABSENTYPEEO.LEGISLATIVE_DATA_GROUP_ID
	) RES_LEGISLATIVE_DATA_GROUP
,TO_CHAR(ancABSENTYPEEO.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,DECODE(primaryabsence.EMP_USAGE_CD,'ENABLE','Yes','No') RES_PRIMARY_ABSENCE_DETAILS_EM
,DECODE(primaryabsence.MGR_USAGE_CD,'ENABLE','Yes','No') RES_PRIMARY_ABSENCE_DETAILS_MA
,DECODE(primaryabsence.ADMIN_USAGE_CD,'ENABLE','Yes','No') RES_PRIMARY_ABSENCE_DETAILS_AD
,DECODE(qualien.EMP_USAGE_CD,'ENABLE','Yes','No') RES_QUALIFIED_ENTITLEMENTS_EMP
,DECODE(qualien.MGR_USAGE_CD,'ENABLE','Yes','No') RES_QUALIFIED_ENTITLEMENTS_MAN
,DECODE(qualien.ADMIN_USAGE_CD,'ENABLE','Yes','No') RES_QUALIFIED_ENTITLEMENTS_ADM
,DECODE(approval.EMP_USAGE_CD,'ENABLE','Yes','No') RES_APPROVAL_AND_PROCESSING_RU
,DECODE(approval.MGR_USAGE_CD,'ENABLE','Yes','No') RES_APPROVAL_AND_PROCESSING__0
,DECODE(approval.ADMIN_USAGE_CD,'ENABLE','Yes','No') RES_APPROVAL_AND_PROCESSING__1
,DECODE(projectbal.EMP_USAGE_CD,'ENABLE','Yes','No') RES_PROJECTED_BALANCES_EMPLOYE
,DECODE(projectbal.MGR_USAGE_CD,'ENABLE','Yes','No') RES_PROJECTED_BALANCES_MANAGER
,DECODE(projectbal.ADMIN_USAGE_CD,'ENABLE','Yes','No') RES_PROJECTED_BALANCES_ADMINIS
,DECODE(dateanddua.EMP_USAGE_CD,'ENABLE','Yes','No') RES_DATES_AND_DURATION_EMPLOYE
,DECODE(dateanddua.MGR_USAGE_CD,'ENABLE','Yes','No') RES_DATES_AND_DURATION_MANAGER
,DECODE(dateanddua.ADMIN_USAGE_CD,'ENABLE','Yes','No') RES_DATES_AND_DURATION_ADMINIS
,DECODE(supplemental.EMP_USAGE_CD,'ENABLE','Yes','No') RES_SUPPLEMENTAL_ABSENCE_DETAI
,DECODE(supplemental.MGR_USAGE_CD,'ENABLE','Yes','No') RES_SUPPLEMENTAL_ABSENCE_DET_2
,DECODE(supplemental.ADMIN_USAGE_CD,'ENABLE','Yes','No') RES_SUPPLEMENTAL_ABSENCE_DET_3
,ancABSENTYPEEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ancABSENTYPEEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ancABSENTYPEEO.CREATED_BY RSC_CREATED_BY
,ancABSENTYPEEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,ancABSENTYPEEO.LEGISLATION_CODE RSC_COUNTRY_ID

FROM ANC_ABSENCE_TYPES_VL ancABSENTYPEEO
,(SELECT * FROM ANC_ABSENCE_TYPE_REGIONS_F	WHERE  ABSENCE_REGION_CD = 'ADDITIONAL_DETAILS') primaryabsence
,(SELECT * FROM ANC_ABSENCE_TYPE_REGIONS_F	WHERE  ABSENCE_REGION_CD = 'ENTITLEMENTS') qualien
,(SELECT * FROM ANC_ABSENCE_TYPE_REGIONS_F	WHERE  ABSENCE_REGION_CD = 'PROCESSING') approval
,(SELECT * FROM ANC_ABSENCE_TYPE_REGIONS_F	WHERE  ABSENCE_REGION_CD = 'REMAIN_BALANCE') projectbal
,(SELECT * FROM ANC_ABSENCE_TYPE_REGIONS_F	WHERE  ABSENCE_REGION_CD = 'SCHEDULING') dateanddua
,(SELECT * FROM ANC_ABSENCE_TYPE_REGIONS_F	WHERE  ABSENCE_REGION_CD = 'SUPPLEMENTAL_DETAILS') supplemental
WHERE ancABSENTYPEEO.ABSENCE_TYPE_ID = primaryabsence.ABSENCE_TYPE_ID
AND ancABSENTYPEEO.ABSENCE_TYPE_ID = qualien.ABSENCE_TYPE_ID
AND ancABSENTYPEEO.ABSENCE_TYPE_ID = approval.ABSENCE_TYPE_ID
AND ancABSENTYPEEO.ABSENCE_TYPE_ID = projectbal.ABSENCE_TYPE_ID
AND ancABSENTYPEEO.ABSENCE_TYPE_ID = dateanddua.ABSENCE_TYPE_ID
AND ancABSENTYPEEO.ABSENCE_TYPE_ID = supplemental.ABSENCE_TYPE_ID
AND NVL(ancABSENTYPEEO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) BETWEEN primaryabsence.EFFECTIVE_START_DATE AND NVL(primaryabsence.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND NVL(ancABSENTYPEEO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) BETWEEN qualien.EFFECTIVE_START_DATE AND NVL(qualien.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND NVL(ancABSENTYPEEO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) BETWEEN approval.EFFECTIVE_START_DATE AND NVL(approval.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND NVL(ancABSENTYPEEO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) BETWEEN projectbal.EFFECTIVE_START_DATE AND NVL(projectbal.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND NVL(ancABSENTYPEEO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) BETWEEN dateanddua.EFFECTIVE_START_DATE AND NVL(dateanddua.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND NVL(ancABSENTYPEEO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD')) BETWEEN supplemental.EFFECTIVE_START_DATE AND NVL(supplemental.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY ancABSENTYPEEO.NAME,4