-- RSC_PREREQUISITE_OBJECTS=PER_FLEX_SEGMENT_MAPPINGS
-- RSC_PREREQUISITE_OBJECTS=PER_FLEX_MAPPING_USAGES

SELECT (CASE 
	WHEN mappingE0.SOURCE_CONTEXT_CODE = 'Global Data Elements' THEN 
		NULL
	ELSE 
		(SELECT NAME
			FROM FND_DF_CONTEXTS_VL
			WHERE DESCRIPTIVE_FLEXFIELD_CODE = 'PER_POSITIONS_DFF'
			AND CONTEXT_CODE = mappingE0.SOURCE_CONTEXT_CODE)
	END) RES_SOURCE_CONTEXT
,(SELECT NAME
	FROM FND_DF_SEGMENTS_VL
	WHERE DESCRIPTIVE_FLEXFIELD_CODE = 'PER_POSITIONS_DFF'
	AND SEGMENT_CODE = mappingE0.SOURCE_SEGMENT_CODE 
	) RES_SOURCE_SEGMENT
,(CASE 
	WHEN mappingE0.DESTINATION_CONTEXT_CODE = 'Global Data Elements' THEN 
		NULL
	ELSE 
		(SELECT NAME
			FROM FND_DF_CONTEXTS_VL
			WHERE DESCRIPTIVE_FLEXFIELD_CODE = 'PER_ASG_DF'
			AND CONTEXT_CODE = mappingE0.DESTINATION_CONTEXT_CODE)
	END) RES_DESTINATION_CONTEXT
,(SELECT NAME
	FROM FND_DF_SEGMENTS_VL
	WHERE DESCRIPTIVE_FLEXFIELD_CODE = 'PER_ASG_DF'
	AND SEGMENT_CODE = mappingE0.DESTINATION_SEGMENT_CODE 
	) RES_DESTINATION_SEGMENT
,(SELECT NAME
   FROM HR_ORGANIZATION_V
   WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))
   AND CLASSIFICATION_CODE = 'HCM_LEMP'
   AND ORGANIZATION_ID = leConfigsE0.LEGAL_ENTITY_ID
   ) RES_LEGAL_EMPLOYER
,DECODE(leConfigsE0.POSITION_SYNC_ENABLED_FLAG,'Y','Yes','N','No') RES_ENABLE_POSITION_SYNCHRONIZ
,leConfigsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,leConfigsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,leConfigsE0.CREATED_BY RSC_CREATED_BY
,leConfigsE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID
FROM PER_FLEX_SEGMENT_MAPPINGS mappingE0
,PER_FLEX_MAPPING_USAGES leConfigsE0
WHERE mappingE0.FLEX_SEGMENT_MAPPING_ID = leConfigsE0.FLEX_SEGMENT_MAPPING_ID
AND LEGAL_ENTITY_ID IS NOT NULL