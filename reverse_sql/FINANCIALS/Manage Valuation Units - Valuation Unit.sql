SELECT CostOrgPEO.COST_ORG_NAME RES_COST_ORGANIZATION
,CostBookPEO.COST_BOOK_CODE RES_COST_BOOK
,ValUnitEO.VAL_UNIT_CODE RES_VALUATION_UNIT
,ValUnitEO.VAL_UNIT_DESC RES_VALUATION_UNIT_DESCRIPTION
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'CST_VALUATION_STRUCTURE_MODES'
	AND LOOKUP_CODE = ValUnitEO.VAL_STRUCTURE_MODE_CODE
	) RES_VALUATION_STRUCTURE_MODE
,ValStructurePEO.VAL_STRUCTURE_CODE RES_VALUATION_STRUCTURE_CODE
,ValStructurePEO.VAL_STRUCTURE_DESC RES_VALUATION_STRUCTURE_CODE_D
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'CST_VALUATION_STRUCTURE_TYPES'
	AND LOOKUP_CODE = ValStructurePEO.VAL_STRUCTURE_TYPE_CODE
	) RES_VALUATION_STRUCTURE_TYPE
,(SELECT STRUCTURE_INSTANCE_CODE
	FROM FND_KF_STR_INSTANCES_VL
	WHERE STRUCTURE_INSTANCE_NUMBER = ValStructurePEO.STRUCTURE_INSTANCE_NUMBER
	) RES_FLEXFIELD_STRUCTURE
,ValUnitEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ValUnitEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ValUnitEO.CREATED_BY RSC_CREATED_BY
,ValUnitEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,ValUnitEO.COST_ORG_ID RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM CST_VAL_UNITS_VL ValUnitEO
,CST_VAL_STRUCTURES_VL ValStructurePEO
,CST_COST_ORGS_V CostOrgPEO
,CST_COST_BOOKS_VL CostBookPEO
WHERE ValUnitEO.VAL_STRUCTURE_ID      = ValStructurePEO.VAL_STRUCTURE_ID
AND ValUnitEO.COST_ORG_ID              = CostOrgPEO.COST_ORG_ID
AND ValUnitEO.COST_BOOK_ID             = CostBookPEO.COST_BOOK_ID
ORDER BY CostOrgPEO.COST_ORG_NAME