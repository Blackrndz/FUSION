SELECT RESOURCEROLE.ROLE_NAME RES_ROLE_NAME
,RESOURCEROLE.ROLE_CODE RES_ROLE_CODE
,HZLOOKUPPEO.MEANING RES_ROLE_TYPE
,DECODE(RESOURCEROLE.SEEDED_FLAG,'Y','Yes','No') RES_SYSTEM
,DECODE(RESOURCEROLE.LEAD_FLAG,'Y','Yes','No') RES_LEAD
,DECODE(RESOURCEROLE.MEMBER_FLAG,'Y','Yes','No') RES_MEMBER
,DECODE(RESOURCEROLE.MANAGER_FLAG,'Y','Yes','No') RES_MANAGER
,DECODE(RESOURCEROLE.admin_flag,'Y','Yes','No') RES_ADMINISTRATOR
,RESOURCEROLE.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,RESOURCEROLE.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,RESOURCEROLE.CREATED_BY RSC_CREATED_BY
,RESOURCEROLE.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM JTF_RS_ROLES_VL RESOURCEROLE
,HZ_LOOKUPS HZLOOKUPPEO
WHERE HZLOOKUPPEO.LOOKUP_TYPE   = 'JTF_RS_ROLE_TYPE'
AND RESOURCEROLE.ROLE_TYPE_CODE = HZLOOKUPPEO.LOOKUP_CODE
ORDER BY RESOURCEROLE.ROLE_NAME