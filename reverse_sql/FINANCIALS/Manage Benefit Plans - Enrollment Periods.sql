SELECT benPlansE0.NAME RES_PLAN_NAME
,TO_CHAR(benPlansE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,benPlansE0.NAME RES_PLAN_OR_OPTION_NAME
,'Plan' RES_PLAN_OR_OPTION_DESCRIPTION
,(SELECT NAME
    FROM BEN_LER_F_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND LER_ID = restrictsE0.LER_ID
    ) RES_SCHEDULED
,TO_CHAR(periodsE0.STRT_DT,'DD-Mon-YYYY') RES_ENROLLMENT_PERIOD_START_DA
,TO_CHAR(periodsE0.END_DT,'DD-Mon-YYYY') RES_ENROLLMENT_PERIOD_END_DATE
,TO_CHAR(periodsE0.DFLT_ENRT_DT,'DD-Mon-YYYY') RES_ASSIGN_DEFAULTS_DATE
,TO_CHAR(periodsE0.ASND_LF_EVT_DT,'DD-Mon-YYYY') RES_ASSIGNED_LIFE_EVENT_DATE
,TO_CHAR(periodsE0.PROCG_END_DT,'DD-Mon-YYYY') RES_PROCESSING_CUTOFF_DATE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_CLS_ENRT_DT_TO_USE'
    AND LOOKUP_CODE = periodsE0.CLS_ENRT_DT_TO_USE_CD
    ) RES_CLOSE_ENROLLMENT_PERIOD_DA
,(SELECT TO_CHAR(START_DATE,'DD')
	||' '||DECODE(TO_CHAR(START_DATE,'MM'),'01','Jan','02','Feb','03','Mar','04','Apr','05','May','06','Jun'
																,'07','Jul','08','Aug','09','Sep','10','Oct','11','Nov','12','Dec')
	||' '||TO_CHAR(START_DATE,'YYYY')
	||' - '||TO_CHAR(END_DATE,'DD')
	||' '||DECODE(TO_CHAR(END_DATE,'MM'),'01','Jan','02','Feb','03','Mar','04','Apr','05','May','06','Jun'
																,'07','Jul','08','Aug','09','Sep','10','Oct','11','Nov','12','Dec')
	||' '||TO_CHAR(END_DATE,'YYYY')
	FROM BEN_YR_PERD
	WHERE YR_PERD_ID = periodsE0.YR_PERD_ID) RES_YEAR_PERIOD
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ENRT_PERD_DET_CD'
    AND LOOKUP_CODE = periodsE0.ENRT_PERD_DET_OVRLP_BCKDT_CD
    ) RES_PERIOD_DETERMINATION_RULE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ENRT_CVG_STRT'
    AND LOOKUP_CODE = periodsE0.ENRT_CVG_STRT_DT_CD
    ) RES_COVERAGE_START_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = periodsE0.ENRT_CVG_STRT_DT_RL
    ) RES_COVERAGE_START_FORMULA
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ENRT_CVG_END'
    AND LOOKUP_CODE = periodsE0.ENRT_CVG_END_DT_CD
    ) RES_PREVIOUS_COVERAGE_END_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = periodsE0.ENRT_CVG_END_DT_RL
    ) RES_PREVIOUS_COVERAGE_END_FORM
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_RT_STRT'
    AND LOOKUP_CODE = periodsE0.RT_STRT_DT_CD
    ) RES_RATE_START_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = periodsE0.RT_STRT_DT_RL
    ) RES_RATE_START_FORMULA
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_RT_END'
    AND LOOKUP_CODE = periodsE0.RT_END_DT_CD
    ) RES_PREVIOUS_RATE_END_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = periodsE0.RT_END_DT_RL
    ) RES_PREVIOUS_RATE_END_FORMULA
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_REINSTATE'
    AND LOOKUP_CODE = periodsE0.REINSTATE_CD
    ) RES_REINSTATEMENT_RULE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_REINSTATE_OVRDN'
    AND LOOKUP_CODE = periodsE0.REINSTATE_OVRDN_CD
    ) RES_REINSTATEMENT_OVERRIDE_RUL
,periodsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,periodsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,periodsE0.CREATED_BY  RSC_CREATED_BY
,periodsE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPlansE0.LEGISLATION_CODE RSC_COUNTRY_ID

FROM BEN_PL_F benPlansE0
,(SELECT NAME
	,PL_TYP_ID
    FROM BEN_PL_TYP_F
    WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
	AND OPT_TYP_CD != 'FLXCR'
	) planTypesE0
,(SELECT *
    FROM BEN_LER_CHG_PL_NIP_ENRT_F
    WHERE LER_ID IN (SELECT LER_ID FROM BEN_LER_F WHERE TYP_CD = 'SCHEDDUO')
    ) restrictsE0
,(SELECT *
	FROM BEN_POPL_ENRT_TYP_CYCL 
	WHERE COMP_OBJ_TYPE = 'PLN'
	) schedulPeriodsE0
,BEN_ENRT_PERD periodsE0
WHERE SYSDATE BETWEEN benPlansE0.EFFECTIVE_START_DATE AND benPlansE0.EFFECTIVE_END_DATE
AND benPlansE0.PL_TYP_ID = planTypesE0.PL_TYP_ID
AND benPlansE0.PL_ID = restrictsE0.PL_ID
AND benPlansE0.PL_ID = schedulPeriodsE0.PL_ID
AND restrictsE0.LER_ID = periodsE0.LER_ID
AND schedulPeriodsE0.POPL_ENRT_TYP_CYCL_ID = periodsE0.POPL_ENRT_TYP_CYCL_ID
ORDER BY 1,2