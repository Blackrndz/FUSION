--RSC_PREREQUISITE_OBJECTS=HRA_CHECK_IN_TMPLS_VL
--RSC_PREREQUISITE_OBJECTS=HRA_CHECK_IN_TMPL_RV_PRDS
--RSC_PREREQUISITE_OBJECTS=HRT_REVIEW_PERIODS_TL

SELECT hraCHECKINTMPE0.NAME RES_NAME
,TO_CHAR(hraCHECKINTMPE0.DATE_FROM,'DD-Mon-YYYY') RES_FROM_DATE
,TO_CHAR(hraCHECKINTMPE0.DATE_TO,'DD-Mon-YYYY') RES_TO_DATE
,DECODE(hraCHECKINTMPE0.STATUS_CODE,'A','Active','I','Inactive') RES_STATUS
,(CASE
	WHEN EXISTS (SELECT 1 FROM HRA_CHECK_IN_MEETINGS WHERE CHECK_IN_TEMPLATE_ID = hraCHECKINTMPE0.CHECK_IN_TEMPLATE_ID) THEN
		'Yes'
	ELSE
		'No'
	END) RES_IN_USE
,hraCHECKINTMPE0.COMMENTS RES_COMMENTS
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ORA_HRT_ALL_REVIEW_PERIODS'
    AND LOOKUP_CODE = hraCHECKINTMPE0.ALL_REVIEW_PERIODS_FLAG
    ) RES_REVIEW_PERIODS
,DECODE(hraCHECKINTMPE0.INCLUDE_IN_PERF_DOC_FLAG,'Y','Yes','No') RES_INCLUDE_IN_PERFORMANCE_DOC
,DECODE(hraCHECKINTMPE0.PERFORMANCE_GOALS_FLAG,'Y','Yes','No') RES_PERFORMANCE_GOALS
,(CASE 
	WHEN hraCHECKINTMPE0.PERFORMANCE_GOALS_FLAG = 'Y' THEN 
		DECODE(hraCHECKINTMPE0.PERF_GOALS_AUTO_POPULATE_FLAG,'Y','Yes','No')
	END) RES_ADD_DEFAULT_DISCUSSION_TOP
,DECODE(hraCHECKINTMPE0.DEVELOPMENT_GOALS_FLAG,'Y','Yes','No') RES_DEVELOPMENT_GOALS
,(CASE 
	WHEN hraCHECKINTMPE0.DEVELOPMENT_GOALS_FLAG = 'Y' THEN 
		DECODE(hraCHECKINTMPE0.DEV_GOALS_AUTO_POPULATE_FLAG,'Y','Yes','No')
	END) RES_ADD_DEFAULT_DISCUSSION_T_0
,DECODE(hraCHECKINTMPE0.QUESTIONNAIRE_FLAG,'Y','Yes','No') RES_QUESTIONNAIRE
,(SELECT NAME 
	FROM HRQ_QUESTIONNAIRES_VL 
	WHERE QUESTIONNAIRE_ID = hraCHECKINTMPE0.WORKER_QUESTIONNAIRE_ID
	AND QSTNR_VERSION_NUM = 1
	) RES_QUESTIONNAIRE_FOR_WORKER_R
,(SELECT NAME 
	FROM HRQ_QUESTIONNAIRES_VL 
	WHERE QUESTIONNAIRE_ID = hraCHECKINTMPE0.MANAGER_QUESTIONNAIRE_ID
	AND QSTNR_VERSION_NUM = 1
	) RES_QUESTIONNAIRE_FOR_MANAGER_
,DECODE(hraCHECKINTMPE0.GENERAL_DISCUSSION_TOPIC_FLAG,'Y','Yes','No') RES_GENERAL_DISCUSSION_TOPICS

,hraCHECKINTMPE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hraCHECKINTMPE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hraCHECKINTMPE0.CREATED_BY RSC_CREATED_BY
,hraCHECKINTMPE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM HRA_CHECK_IN_TMPLS_VL hraCHECKINTMPE0
ORDER BY hraCHECKINTMPE0.NAME