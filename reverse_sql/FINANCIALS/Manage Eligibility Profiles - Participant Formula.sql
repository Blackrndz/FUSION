SELECT benELIGYEO.NAME RES_NAME
,benELIGPRFLRLEO.ORDR_TO_APLY_NUM RES_SEQUENCE
,ffFOREO.FORMULA_NAME  RES_FORMULA
,(SELECT NAME 
	FROM PER_LEGISLATIVE_DATA_GROUPS_VL 
	WHERE LEGISLATIVE_DATA_GROUP_ID = ffFOREO.LEGISLATIVE_DATA_GROUP_ID
	) RES_LEGISLATIVE_DATA_GROUP
,DECODE(benELIGPRFLRLEO.DRVBL_FCTR_APLS_FLAG,'Y','Yes','No') RES_DERIVED_FACTORS_APPLY
,TO_CHAR(benELIGPRFLRLEO.START_DATE,'DD-Mon-YYYY') RES_START_DATE
,TO_CHAR(benELIGPRFLRLEO.END_DATE,'DD-Mon-YYYY') RES_END_DATE
,benELIGPRFLRLEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,benELIGPRFLRLEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,benELIGPRFLRLEO.CREATED_BY  RSC_CREATED_BY
,benELIGPRFLRLEO.CREATION_DATE  RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_ELIGY_PRFL benELIGYEO
,BEN_ELIGY_PRFL_RL benELIGPRFLRLEO
,FF_FORMULAS_VL ffFOREO
WHERE SYSDATE BETWEEN benELIGYEO.START_DATE AND NVL(benELIGYEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND benELIGYEO.ELIGY_PRFL_ID = benELIGPRFLRLEO.ELIGY_PRFL_ID 
AND benELIGYEO.END_DATE BETWEEN benELIGPRFLRLEO.START_DATE AND NVL(benELIGPRFLRLEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND benELIGPRFLRLEO.FORMULA_ID = ffFOREO.FORMULA_ID
AND benELIGPRFLRLEO.END_DATE BETWEEN ffFOREO.EFFECTIVE_START_DATE AND NVL(ffFOREO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY benELIGYEO.NAME
,benELIGPRFLRLEO.ORDR_TO_APLY_NUM