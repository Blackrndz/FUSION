select COAMAPPINGEO.name RES_NAME
,(
	(SELECT seg.NAME
	FROM FND_KF_SEGMENTS_VL seg
	,FND_KF_STR_INSTANCES_VL strInst
	WHERE seg.STRUCTURE_ID                 = strInst.STRUCTURE_ID
	AND seg.ENABLED_FLAG                   = 'Y'
	AND strInst.ENABLED_FLAG               = 'Y'
	AND strInst.APPLICATION_ID             = 101
	AND strInst.KEY_FLEXFIELD_CODE         = 'GL#'
	AND strInst.STRUCTURE_INSTANCE_NUMBER IN
		(SELECT TO_COA_ID
		FROM GL_COA_MAPPINGS
		WHERE COA_MAPPING_ID = COAMAPPINGEO.COA_MAPPING_ID
		)
	and SEG.COLUMN_NAME = COAMAPPINGEO.COLUMN_NAME
	)) RES_TARGET_SEGMENT_NAME
,(
	(SELECT seg.SEGMENT_CODE 
	FROM FND_KF_SEGMENTS_VL seg
	,FND_KF_STR_INSTANCES_VL strInst
	WHERE seg.STRUCTURE_ID                 = strInst.STRUCTURE_ID
	AND seg.ENABLED_FLAG                   = 'Y'
	AND strInst.ENABLED_FLAG               = 'Y'
	AND strInst.APPLICATION_ID             = 101
	AND strInst.KEY_FLEXFIELD_CODE         = 'GL#'
	AND strInst.STRUCTURE_INSTANCE_NUMBER IN
		(SELECT TO_COA_ID
		FROM GL_COA_MAPPINGS
		WHERE COA_MAPPING_ID = COAMAPPINGEO.COA_MAPPING_ID
		)
	and SEG.COLUMN_NAME = COAMAPPINGEO.COLUMN_NAME
	)) RES_TARGET_SEGMENT_CODE
,NVL((select distinct  decode(MEANING,'Equals to','Equals',MEANING) from GL_LOOKUPS where LOOKUP_TYPE='SEGMENT_MAP_TYPE' and LOOKUP_CODE = COAMAPSEGMENTRULEEO.SEGMENT_MAP_TYPE),'Not assigned')	 RES_MAPPING_METHOD 
,COAMAPSEGMENTRULEEO.SINGLE_VALUE RES_TARGET_SEGMENT_VALUE
,(
	(SELECT seg.NAME
	FROM FND_KF_SEGMENTS_VL seg
	,FND_KF_STR_INSTANCES_VL strInst
	WHERE seg.STRUCTURE_ID                 = strInst.STRUCTURE_ID
	AND seg.ENABLED_FLAG                   = 'Y'
	AND strInst.ENABLED_FLAG               = 'Y'
	AND strInst.APPLICATION_ID             = 101
	AND strInst.KEY_FLEXFIELD_CODE         = 'GL#'
	AND strInst.STRUCTURE_INSTANCE_NUMBER IN
		(SELECT FROM_COA_ID
		FROM GL_COA_MAPPINGS
		WHERE COA_MAPPING_ID = CoaMapSegmentRuleEO.COA_MAPPING_ID
		)
	and SEG.COLUMN_NAME = COAMAPSEGMENTRULEEO.FROM_SEG_COLUMN_NAME
	)) RES_SOURCE_SEGMENT
,CoaMapSegmentRuleEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,CoaMapSegmentRuleEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,CoaMapSegmentRuleEO.CREATED_BY  RSC_CREATED_BY
,CoaMapSegmentRuleEO.CREATION_DATE  RSC_CREATION_DATE
, null RSC_LEDGER_ID
, (SELECT DISTINCT LedgerE0.CHART_OF_ACCOUNTS_ID 
	FROM GL_LEDGER_RELATIONSHIPS GlRelation
	,GL_LEDGERS LedgerE0
	WHERE GlRelation.SL_COA_MAPPING_ID = CoaMappingEO.COA_MAPPING_ID
	AND GlRelation.PRIMARY_LEDGER_ID = LedgerE0.LEDGER_ID
	) RSC_CHART_OF_ACCOUNTS_ID
, null RSC_BUSINESS_UNIT_ID
, null RSC_LEGAL_ENTITY_ID
, null RSC_ORGANIZATION_ID
, null RSC_BUSINESS_GROUP_ID
, NULL RSC_ENTERPRISE_ID
, NULL RSC_COUNTRY_ID
	
from GL_COA_MAP_SEGMENT_RULES COAMAPSEGMENTRULEEO
,(
  SELECT COAMAPPINGEO.NAME
,COAMAPPINGEO.COA_MAPPING_ID
,CoaMappingEO.TO_COA_ID
,QRSLT.COLUMN_NAME
,QRSLT.STRUCTURE_ID
FROM GL_COA_MAPPINGS COAMAPPINGEO
    ,(SELECT TREE_CODE
    ,COLUMN_NAME
    ,STRUCTURE_INSTANCE_NUMBER
    ,si.STRUCTURE_ID
    FROM FND_KF_SEGMENT_INSTANCES segi
    ,FND_KF_STR_INSTANCES_B si
    ,FND_KF_SEGMENTS_VL segvl
    WHERE si.APPLICATION_ID        = 101
    AND si.KEY_FLEXFIELD_CODE      = 'GL#'
    AND segi.STRUCTURE_INSTANCE_ID = si.STRUCTURE_INSTANCE_ID
    AND segi.SEGMENT_CODE          = segvl.SEGMENT_CODE
    AND segvl.STRUCTURE_ID         = si.STRUCTURE_ID
    ) QRSLT
WHERE QRSLT.STRUCTURE_INSTANCE_NUMBER = COAMAPPINGEO.TO_COA_ID) COAMAPPINGEO
where COAMAPPINGEO.COA_MAPPING_ID =  COAMAPSEGMENTRULEEO.COA_MAPPING_ID(+)
AND COAMAPPINGEO.COLUMN_NAME = COAMAPSEGMENTRULEEO.TO_SEG_COLUMN_NAME(+)
order by CoaMappingEO.NAME