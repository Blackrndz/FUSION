--RSC_PREREQUISITE_OBJECTS=PJR_RES_POOL_USER_ROLE

SELECT (SELECT NAME 
	FROM PJR_RESOURCE_POOLS_VL
	WHERE POOL_ID = pjrREPOOLSEO.PARENT_POOL_ID 
	) RES_PARENT_RESOURCE_POOLS
,pjrREPOOLSEO.NAME RES_RESOURCE_POOLS
,pjtPRJENTRESOURCEEO.DISPLAY_NAME RES_RESOURCE_POOL_MANAGERS
,DECODE(pjrRESPOOLUSEROLEEO.INHERITED_FLAG,'Y','Yes','No') RES_INGERITED
,pjrRESPOOLUSEROLEEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,pjrRESPOOLUSEROLEEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,pjrRESPOOLUSEROLEEO.CREATED_BY  RSC_CREATED_BY
,pjrRESPOOLUSEROLEEO.CREATION_DATE  RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJR_RESOURCE_POOLS_VL pjrREPOOLSEO
,PJR_RES_POOL_USER_ROLE pjrRESPOOLUSEROLEEO
,PJT_PRJ_ENTERPRISE_RESOURCE_VL pjtPRJENTRESOURCEEO
WHERE pjrREPOOLSEO.POOL_ID = pjrRESPOOLUSEROLEEO.RESOURCE_POOL_ID
AND pjrRESPOOLUSEROLEEO.RESOURCE_ID = pjtPRJENTRESOURCEEO.RESOURCE_ID
AND pjtPRJENTRESOURCEEO.LANGUAGE = USERENV('LANG')
AND SYSDATE BETWEEN pjrREPOOLSEO.FROM_DATE AND NVL(pjrREPOOLSEO.TO_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))
ORDER BY 1 ,2