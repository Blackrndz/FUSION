SELECT benELIGYEO.NAME RES_NAME
,benELIGTLVGRSNEO.ORDR_NUM RES_SEQUENCE
,(SELECT ACTION_NAME FROM PER_ACTIONS_VL WHERE ACTION_ID = benELIGTLVGRSNEO.ACTION_ID
	AND ACTION_CODE = benELIGTLVGRSNEO.LVG_RSN_CD 
	AND SYSDATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_TERMINATION_REASON
,DECODE(benELIGTLVGRSNEO.EXCLD_FLAG,'Y','Yes','No') RES_EXCLUDE
,TO_CHAR(benELIGTLVGRSNEO.START_DATE,'DD-Mon-YYYY') RES_START_DATE
,TO_CHAR(benELIGTLVGRSNEO.END_DATE,'DD-Mon-YYYY') RES_END_DATE


,benELIGTLVGRSNEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,benELIGTLVGRSNEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,benELIGTLVGRSNEO.CREATED_BY  RSC_CREATED_BY
,benELIGTLVGRSNEO.CREATION_DATE  RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,benELIGTLVGRSNEO.BUSINESS_GROUP_ID RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_ELIGY_PRFL benELIGYEO
,BEN_ELIG_LVG_RSN_PRTE benELIGTLVGRSNEO
WHERE benELIGYEO.ELIGY_PRFL_ID = benELIGTLVGRSNEO.ELIGY_PRFL_ID 
AND benELIGYEO.END_DATE BETWEEN benELIGTLVGRSNEO.START_DATE AND NVL(benELIGTLVGRSNEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND SYSDATE BETWEEN benELIGYEO.START_DATE AND NVL(benELIGYEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY benELIGYEO.NAME,benELIGTLVGRSNEO.ORDR_NUM