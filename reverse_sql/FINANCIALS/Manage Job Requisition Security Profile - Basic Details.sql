SELECT ircREQSECPROE0.NAME RES_NAME
,DECODE(ircREQSECPROE0.ENABLED_FLAG,'Y','Yes','No') RES_ENABLE
,DECODE(ircREQSECPROE0.VIEW_ALL_FLAG,'Y','Yes','No') RES_VIEW_ALL
,DECODE(ircREQSECPROE0.SECURE_BY_JOB_FAMILY_FLAG,'Y','Yes','No') RES_SECURE_BY_JOB_FAMILY
,DECODE(ircREQSECPROE0.SECURE_BY_JOB_FUNCTION_FLAG,'Y','Yes','No') RES_SECURE_BY_JOB_FUNCTION
,DECODE(ircREQSECPROE0.SECURE_BY_LOCATION_FLAG,'Y','Yes','No') RES_SECURE_BY_LOCATION
,DECODE(ircREQSECPROE0.SECURE_BY_ORGANIZATION_FLAG,'Y','Yes','No') RES_SECURE_BY_ORGANIZATION
,DECODE(ircREQSECPROE0.SECURE_BY_REC_TYPE_FLAG,'Y','Yes','No') RES_SECURE_BY_RECRUITING_TYPE
 
,ircREQSECPROE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,ircREQSECPROE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,ircREQSECPROE0.CREATED_BY  RSC_CREATED_BY
,ircREQSECPROE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID
   
FROM IRC_REQ_SEC_PROFILES ircREQSECPROE0
ORDER BY ircREQSECPROE0.NAME
