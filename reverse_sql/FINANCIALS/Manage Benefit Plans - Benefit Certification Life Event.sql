-- RSC_PREREQUISITE_OBJECTS=BEN_PL_F
 
SELECT benPlansE0.NAME RES_PLAN_NAME
,TO_CHAR(benPlansE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,benPlansE0.NAME RES_PLAN_OR_OPTION_NAME
,'Plan' RES_PLAN_OR_OPTION_DESCRIPTION
,TO_CHAR(enrollBenLeE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_LIFE_EVENT_EFFECTIVE_START
,DECODE(TO_CHAR(enrollBenLeE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY'),'31-12-4712',NULL,TO_CHAR(benPlansE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY')) RES_LIFE_EVENT_EFFECTIVE_END_D
,(SELECT NAME
    FROM BEN_LER_F_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND LER_ID = enrollBenLeE0.LER_ID
    ) RES_LIFE_EVENT
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_BNFT_R_OPT_RSTRN'
    AND LOOKUP_CODE = benPlansE0.BNFT_OR_OPTION_RSTRCTN_CD
    ) RES_RESTRICTION_TYPE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_CVG_INCR_R_DECR_ONLY'
    AND LOOKUP_CODE = benPlansE0.CVG_INCR_R_DECR_ONLY_CD
    ) RES_RESTRICTION_ON_CHANGES
	
,enrollBenLeE0.MN_CVG_RQD_AMT RES_MINIMUM
,enrollBenLeE0.MX_CVG_ALWD_AMT RES_MAXIMUM
,enrollBenLeE0.MX_CVG_WCFN_AMT RES_MAXIMUM_WITH_CERTIFICATION
,enrollBenLeE0.MX_CVG_INCR_ALWD_AMT RES_MAXIMUM_INCREASE
,enrollBenLeE0.MX_CVG_INCR_WCF_ALWD_AMT RES_MAXIMUM_INCREASE_WITH_CERT
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = enrollBenLeE0.CVG_AMT_LMT_RL
    ) RES_AMOUNT_LIMIT_FORMULA
,enrollBenLeE0.MN_CVG_RQD_MLT_NUM RES_MINIMUM_SEQUENCE_NUMBER
,enrollBenLeE0.MX_CVG_ALWD_MLT_NUM RES_MAXIMUM_SEQUENCE_NUMBER
,enrollBenLeE0.MX_CVG_WCFN_MLT_NUM RES_MAXIMUM_SEQUENCE_NUMBER_WI
,enrollBenLeE0.MX_CVG_MLT_INCR_NUM RES_MAXIMUM_SEQUENCE_NUMBER_IN
,enrollBenLeE0.MX_CVG_MLT_INCR_WCF_NUM RES_MAXIMUM_SEQUENCE_NUMBER__0
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = enrollBenLeE0.CVG_MLT_LMT_RL
    ) RES_SEQUENCE_NUMBER_LIMIT_FORM
,TO_CHAR(enrollBenLeCertsE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_CERTIFICATIONS_EFFECTIVE_S
,DECODE(TO_CHAR(enrollBenLeCertsE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY'),'31-12-4712',NULL,TO_CHAR(enrollBenLeCertsE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY')) RES_CERTIFICATIONS_EFFECTIVE_E
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ACTN_TYP'
    AND LOOKUP_CODE = enrollBenLeCertsE0.ACTN_TYP_CD
    ) RES_ACTION_ITEM
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_ACTN_TYP_DUE_DT'
    AND LOOKUP_CODE = enrollBenLeCertsE0.ACTN_TYP_DUE_DT_CD
    ) RES_DUE_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = enrollBenLeCertsE0.ACTN_TYP_DUE_DT_RL
    ) RES_DUE_DATE_FORMULA
,DECODE(enrollBenLeCertsE0.SSPND_FLAG,'Y','Yes','No') RES_SUSPEND_ENROLLMENT
,DECODE(enrollBenLeCertsE0.RQD_FLAG,'Y','Yes','No') RES_REQUIRED
,enrollBenLeCertsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,enrollBenLeCertsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,enrollBenLeCertsE0.CREATED_BY  RSC_CREATED_BY
,enrollBenLeCertsE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPlansE0.LEGISLATION_CODE RSC_COUNTRY_ID

FROM BEN_PL_F benPlansE0
,(SELECT NAME
	,PL_TYP_ID
    FROM BEN_PL_TYP_F
    WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
	AND OPT_TYP_CD != 'FLXCR'
	) planTypesE0
,BEN_LER_BNFT_RSTRN_F enrollBenLeE0
,(SELECT *
    FROM BEN_LER_POPL_ACTN_TYP_F
    WHERE ACTN_TYP_LVL_CD = 'RSTRNCTFN'
    ) enrollBenLeCertsE0
WHERE SYSDATE BETWEEN benPlansE0.EFFECTIVE_START_DATE AND benPlansE0.EFFECTIVE_END_DATE
AND benPlansE0.PL_TYP_ID = planTypesE0.PL_TYP_ID
AND benPlansE0.PL_ID = enrollBenLeE0.PL_ID
AND enrollBenLeE0.PL_ID = enrollBenLeCertsE0.PL_ID(+)
AND enrollBenLeE0.LER_ID = enrollBenLeCertsE0.LER_ID(+)
ORDER BY benPlansE0.NAME
,benPlansE0.EFFECTIVE_START_DATE