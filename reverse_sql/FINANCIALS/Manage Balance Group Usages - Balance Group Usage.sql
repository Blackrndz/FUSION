SELECT (SELECT NAME FROM PER_LEGISLATIVE_DATA_GROUPS_VL WHERE LEGISLATIVE_DATA_GROUP_ID = payBALGROUPUSAGEO.LEGISLATIVE_DATA_GROUP_ID) RES_LEGISLATIVE_DATA_GROUP
,payBALGROUPUSAGEO.GROUP_USAGE_NAME RES_NAME
,payBALGROUPUSAGEO.DESCRIPTION RES_DESCRIPTION
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = payBALGROUPUSAGEO.BALANCE_REPORT_TYPE
		AND LOOKUP_TYPE = 'PAY_BALANCE_REPORT_TYPE' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_REPORT_TYPE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = payBALGROUPUSAGEO.FORMAT_TYPE
		AND LOOKUP_TYPE = 'PAY_BAL_GRP_FORMAT_TYPE' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_FORMAT
,(SELECT GROUP_NAME FROM PAY_BALANCE_GROUPS_TL WHERE BALANCE_GROUP_ID = payBALGROUPUSAGEO.BALANCE_GROUP_ID
	AND LANGUAGE = USERENV('LANG')) RES_BALANCE_GROUP
	

,payBALGROUPUSAGEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,payBALGROUPUSAGEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,payBALGROUPUSAGEO.CREATED_BY RSC_CREATED_BY
,payBALGROUPUSAGEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PAY_BAL_GRP_USAGES_VL  payBALGROUPUSAGEO
ORDER BY RES_LEGISLATIVE_DATA_GROUP,payBALGROUPUSAGEO.LEGISLATION_CODE,RES_NAME