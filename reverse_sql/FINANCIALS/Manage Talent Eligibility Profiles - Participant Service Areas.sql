SELECT benELIGYEO.NAME RES_NAME
,benELIGSVCAREAEO.ORDR_NUM RES_SEQUENCE
,(SELECT NAME FROM BEN_SVC_AREA WHERE SVC_AREA_ID = benELIGSVCAREAEO.SVC_AREA_ID) RES_SERVICE_AREA
,DECODE(benELIGSVCAREAEO.EXCLD_FLAG,'Y','Yes','No') RES_EXCLUDE
,TO_CHAR(benELIGSVCAREAEO.START_DATE,'DD-Mon-YYYY') RES_START_DATE
,TO_CHAR(benELIGSVCAREAEO.END_DATE,'DD-Mon-YYYY') RES_END_DATE


,benELIGSVCAREAEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,benELIGSVCAREAEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,benELIGSVCAREAEO.CREATED_BY  RSC_CREATED_BY
,benELIGSVCAREAEO.CREATION_DATE  RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,benELIGSVCAREAEO.BUSINESS_GROUP_ID RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_ELIGY_PRFL benELIGYEO
,BEN_ELIG_SVC_AREA_PRTE benELIGSVCAREAEO
WHERE benELIGYEO.ELIGY_PRFL_ID = benELIGSVCAREAEO.ELIGY_PRFL_ID 
AND benELIGYEO.END_DATE BETWEEN benELIGSVCAREAEO.START_DATE AND NVL(benELIGSVCAREAEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND SYSDATE BETWEEN benELIGYEO.START_DATE AND NVL(benELIGYEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY benELIGYEO.NAME,benELIGSVCAREAEO.ORDR_NUM