--RSC_PREREQUISITE_OBJECTS=HRT_REVIEW_PERIODS_TL 

SELECT (SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'ORA_HRG_GOAL_PLAN_TYPE'
	AND LOOKUP_CODE = goldPlansE0.GOAL_PLAN_TYPE_CODE 
	) RES_GOAL_PLAN_TYPE
,goldPlansE0.GOAL_PLAN_NAME RES_GOAL_PLAN
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'ACTIVE_INACTIVE'
	AND LOOKUP_CODE = goldPlansE0.GOAL_PLAN_ACTIVE_FLAG 
	) RES_STATUS
,goldPlansE0.DESCRIPTION RES_DESCRIPTION
,(SELECT REVIEW_PERIOD_NAME 
	FROM HRT_REVIEW_PERIODS_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND REVIEW_PERIOD_ID = goldPlansE0.REVIEW_PERIOD_ID 
	) RES_REVIEW_PERIOD
,TO_CHAR(goldPlansE0.START_DATE,'DD-Mon-YYYY') RES_START_DATE
,TO_CHAR(goldPlansE0.END_DATE,'DD-Mon-YYYY') RES_END_DATE
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'HRG_SUB_TYPE_CODE'
	AND LOOKUP_CODE = goldPlansE0.GOAL_SUB_TYPE_CODE 
	) RES_SUBTYPE
,goldPlansE0.GOAL_PLAN_EXT_ID RES_EXTERNAL_ID
,DECODE(goldPlansE0.ENABLE_WEIGHTING_FLAG,'Y','Yes','No') RES_ENABLE_WEIGHTS
,DECODE(goldPlansE0.ENFORCE_GOAL_WEIGHT_FLAG,'Y','Yes','No') RES_ENFORCE_SUM_OF_GOAL_WEIGHT
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'HRG_GOAL_ACCESS_LEVELS'
	AND LOOKUP_CODE = goldPlansE0.GOAL_ACCESS_LEVEL_CODE 
	) RES_ALLOW_UPDATES_TO_GOALS_BY
,goldPlansE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,goldPlansE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,goldPlansE0.CREATED_BY  RSC_CREATED_BY
,goldPlansE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,goldPlansE0.BUSINESS_GROUP_ID RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM HRG_GOAL_PLANS_VL goldPlansE0
ORDER BY goldPlansE0.GOAL_PLAN_NAME