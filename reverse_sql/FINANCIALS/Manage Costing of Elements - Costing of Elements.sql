SELECT (SELECT CLASSIFICATION_NAME FROM PAY_ELE_CLASSIFICATIONS_VL 
			WHERE CLASSIFICATION_ID = elementsTYPE0.CLASSIFICATION_ID) RES_PRIMARY_CLASSIFICATION
,elementsTYPE0.ELEMENT_NAME RES_ELEMENT_NAME
,elementsE0.ELEMENT_LINK_NAME RES_ELEMENT_ELIGIBILITY_NAME
,(SELECT NAME
    FROM PER_LEGISLATIVE_DATA_GROUPS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND LEGISLATIVE_DATA_GROUP_ID = costElesE0.LEGISLATIVE_DATA_GROUP_ID
    ) RES_LEGISLATIVE_DATA_GROUP
,TO_CHAR(costElesE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,TO_CHAR(costElesE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_END_DATE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'PAY_COSTABLE_TYPE'
    AND LOOKUP_CODE = costElesE0.COSTABLE_TYPE
    ) RES_COSTING_TYPE
,costElesE0.DISTRIBUTION_SET_ID RES_DISTRIBUTION_GROUP
,'Yes' RES_COSTING_EXISTS 
,DECODE(costElesE0.TRANSFER_TO_GL_FLAG,'Y','Yes','No') RES_TRANSFER_TO_GENERAL_LEDGER


,costElesE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,costElesE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,costElesE0.CREATED_BY RSC_CREATED_BY
,costElesE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID



FROM PAY_COST_INFO_F costElesE0
,PAY_ELEMENT_LINKS_F elementsE0
,PAY_ELEMENT_TYPES_VL elementsTYPE0
WHERE costElesE0.SOURCE_TYPE = 'EL'
AND costElesE0.SOURCE_ID = elementsE0.ELEMENT_LINK_ID
AND elementsE0.ELEMENT_TYPE_ID = elementsTYPE0.ELEMENT_TYPE_ID(+)

