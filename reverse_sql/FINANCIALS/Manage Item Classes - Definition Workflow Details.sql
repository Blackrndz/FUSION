SELECT 
(SELECT ITEM_CLASS_NAME
	FROM EGP_ITEM_CLASSES_VL
	WHERE ITEM_CLASS_ID = ItemClassPEO.PARENT_ITEM_CLASS_ID
	) RES_PARENT_ITEM_CLASS 
,ItemClassPEO.ITEM_CLASS_NAME RES_ITEM_CLASS
,RouteStepEO.STEP_SEQ_NUMBER RES_STEP_SEQUENCE_NUMBER
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGO_ROUTE_STEP_RES_REQ'
	AND LOOKUP_CODE = RouteStepEO.RESPONSE_REQUIRED_FROM
	) RES_RESPONSE_REQUIRED_FROM
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGO_ROUTE_ASSIGNEE_TYPES'
	AND LOOKUP_CODE = RoutePeopleEO.ASSIGNEE_TYPE_CODE
	) RES_ASSIGNEE_TYPE
,(CASE
	WHEN RoutePeopleEO.ASSIGNEE_TYPE_CODE = 'EGO_PERSON' THEN
		(SELECT USERNAME
		FROM PER_USERS
		WHERE USER_GUID = RoutePeopleEO.ASSIGNEE_ID)
	WHEN RoutePeopleEO.ASSIGNEE_TYPE_CODE = 'EGO_ROLE' THEN
		(SELECT ROLE_NAME
		FROM PER_ROLES_DN_VL
		WHERE ROLE_GUID = RoutePeopleEO.ASSIGNEE_ID)
	END) RES_ASSIGNED_TO
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGO_ITEM_DEFINITION_ENTITIES'
	AND LOOKUP_CODE = RouteAssocEO.DEFINITION_ENTITY_NAME
	) RES_BUSINESS_ENTITY
,(CASE
	WHEN RouteAssocEO.DEFINITION_ENTITY_NAME = 'EGO_ITEM_ATTACHMENT' THEN
		(SELECT USER_NAME
			FROM FND_DOCUMENT_CATEGORIES_VL
			WHERE CATEGORY_ID = RouteAssocEO.DEFINITION_ENTITY_PK1)	
	WHEN RouteAssocEO.DEFINITION_ENTITY_NAME = 'EGO_ITEM_ASSOCIATIONS' THEN
		(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'EGO_ITEM_ASSOCIATIONS'
			AND LOOKUP_CODE = RouteAssocEO.DEFINITION_ENTITY_PK1)
	WHEN RouteAssocEO.DEFINITION_ENTITY_NAME = 'EGO_ITEM_CATALOG_ASSIGN' THEN
		(SELECT CATEGORY_SET_NAME
			FROM EGP_CATEGORY_SETS_VL
			WHERE CATEGORY_SET_ID = RouteAssocEO.DEFINITION_ENTITY_PK1)	
	WHEN RouteAssocEO.DEFINITION_ENTITY_NAME = 'EGO_ITEM_OPERATIONAL_ATTRS' THEN
		(SELECT DISTINCT ATTR_GROUP_DISP_NAME
			FROM EGP_ITEM_ATTRIBUTES_TL
			WHERE ATTR_GROUP_NAME = RouteAssocEO.DEFINITION_ENTITY_PK1
			AND LANGUAGE = 'US')	
	WHEN RouteAssocEO.DEFINITION_ENTITY_NAME = 'EGO_ITEM_RELATIONSHIPS' THEN
		(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'EGP_ITEM_RELATIONSHIP_TYPES'
			AND LOOKUP_CODE = RouteAssocEO.DEFINITION_ENTITY_PK1)
	WHEN RouteAssocEO.DEFINITION_ENTITY_NAME = 'EGO_BOM_PACK' THEN
		(SELECT DISPLAY_NAME
			FROM EGP_STRUCTURE_NAMES_VL
			WHERE ALTERNATE_DESIGNATOR_ID = RouteAssocEO.DEFINITION_ENTITY_PK1)
	WHEN RouteAssocEO.DEFINITION_ENTITY_NAME = 'EGO_BOM_STRUCTURE' THEN
		(SELECT DISPLAY_NAME
			FROM EGP_STRUCTURE_NAMES_VL
			WHERE ALTERNATE_DESIGNATOR_ID = RouteAssocEO.DEFINITION_ENTITY_PK1)
	ELSE
		RouteAssocEO.DEFINITION_ENTITY_PK1 -- Not found in Internal
	END) RES_ASSOCIATED_ENTITY
,DECODE(RouteAssocEO.MANDATORY_FLAG,'Y','Yes','No') RES_ALL_VALUES_REQUIRED
,ItemClassPEO.ITEM_CLASS_CODE RES_DEFINITION_WORKFLOW_DETAIL
,DECODE(RouteAssocEO.INHERITED_FLAG,'Y','Yes','No') RES_INHERITED
,RouteEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,RouteEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,RouteEO.CREATED_BY RSC_CREATED_BY
,RouteEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM EGP_ITEM_CLASSES_VL ItemClassPEO
,EGO_ROUTES RouteEO
,EGO_ROUTE_STEPS RouteStepEO
,EGO_ROUTE_PEOPLE RoutePeopleEO
,EGO_ROUTE_ASSOCS RouteAssocEO
WHERE ItemClassPEO.ITEM_CLASS_ID = RouteEO.OBJECT_PK1
AND RouteEO.OBJECT_NAME     = 'EGP_CATALOG_GROUP'
AND RouteEO.ROUTE_TYPE_CODE = 'DEFINITION'
AND (RouteStepEO.ROUTE_ID        = RouteEO.ROUTE_ID)
AND(RouteStepEO.STEP_ID           = RoutePeopleEO.STEP_ID(+))
AND(RoutePeopleEO.ROUTE_PEOPLE_ID = RouteAssocEO.ROUTE_PEOPLE_ID(+))
AND RouteEO.OBJECT_PK1 = RouteStepEO.OBJECT_PK1
CONNECT BY ItemClassPEO.PARENT_ITEM_CLASS_ID = ItemClassPEO.ITEM_CLASS_ID
ORDER SIBLINGS BY RES_PARENT_ITEM_CLASS,RES_ITEM_CLASS,RES_STEP_SEQUENCE_NUMBER