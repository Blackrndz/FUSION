 -- syntax: APPLICATION_ID##RES##DFF_CODE (Descriptive Flexfield name)
-- DFF_LOGIC_TO_APPLY=222##RES##AR_APPROVAL_USER_LIMITS
--  DFF_ATTRIBUTES=ATTRIBUTE
-- IMPORTANT: 
-- 	- the alias names for the Foreign keys must be the same as in the inventory.
--  - the alias name for context code must be RES_CONTEXT_CODE
--  - NO ALIASES for atributes fields.

SELECT UserPEO.USERNAME RES_USER_NAME
,DECODE(AR.DOCUMENT_TYPE,'ADJ','Adjustment','CMREF','Credit Memo Refund','WRTOFF','Receipt write off') RES_DOCUMENT_TYPE
,(SELECT curr.CURRENCY_CODE || ' - ' || curr.NAME
	FROM fusion.fnd_currencies_vl curr
	WHERE curr.CURRENCY_CODE = AR.CURRENCY_CODE
	)  RES_CURRENCY
,AR.ATTRIBUTE_CATEGORY RES_CONTEXT_CODE
,AR.ATTRIBUTE1 
,AR.ATTRIBUTE2
,AR.ATTRIBUTE3
,AR.ATTRIBUTE4
,AR.ATTRIBUTE5
,AR.ATTRIBUTE6
,AR.ATTRIBUTE7
,AR.ATTRIBUTE8
,AR.ATTRIBUTE9
,AR.ATTRIBUTE10
,AR.ATTRIBUTE11
,AR.ATTRIBUTE12
,AR.ATTRIBUTE13
,AR.ATTRIBUTE14
,AR.ATTRIBUTE15
,AR.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,AR.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,AR.CREATED_BY RSC_CREATED_BY
,AR.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,(SELECT DISTINCT EMPLOYEEASSIGNMENTDEO.BUSINESS_UNIT_ID
	FROM PER_ALL_ASSIGNMENTS_M EMPLOYEEASSIGNMENTDEO
	WHERE EMPLOYEEASSIGNMENTDEO.PERSON_ID = UserPEO.PERSON_ID
	) RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID
       
FROM fusion.AR_APPROVAL_USER_LIMITS AR
,fusion.PER_USERS UserPEO
,fusion.PER_PERSON_NAMES_F_V PER 
WHERE AR.user_id = UserPEO.user_guid(+)
AND AR.document_type NOT IN('CM')
AND UserPEO.person_id = PER.person_id(+)
AND (AR.ATTRIBUTE_CATEGORY IS NOT NULL OR
AR.ATTRIBUTE1 IS NOT NULL OR 
AR.ATTRIBUTE2 IS NOT NULL OR 
AR.ATTRIBUTE3 IS NOT NULL OR 
AR.ATTRIBUTE4 IS NOT NULL OR 
AR.ATTRIBUTE5 IS NOT NULL OR 
AR.ATTRIBUTE6 IS NOT NULL OR 
AR.ATTRIBUTE7 IS NOT NULL OR 
AR.ATTRIBUTE8 IS NOT NULL OR 
AR.ATTRIBUTE9 IS NOT NULL OR 
AR.ATTRIBUTE10 IS NOT NULL OR 
AR.ATTRIBUTE11 IS NOT NULL OR 
AR.ATTRIBUTE12 IS NOT NULL OR 
AR.ATTRIBUTE13 IS NOT NULL OR 
AR.ATTRIBUTE14 IS NOT NULL OR 
AR.ATTRIBUTE15 IS NOT NULL )
ORDER BY UserPEO.USERNAME
,RES_DOCUMENT_TYPE 
,AR.CURRENCY_CODE
,AR.AMOUNT_FROM