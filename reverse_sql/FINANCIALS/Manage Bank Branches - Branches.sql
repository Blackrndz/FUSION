SELECT BankBranchEO.BANK_NAME RES_BANK
/*
		(SELECT MEANING
		FROM FND_LOOKUP_VALUES
		WHERE LOOKUP_TYPE = 'HZ_DOMAIN_SUFFIX_LIST'
		AND LOOKUP_CODE   = BankBranchEO.BANK_HOME_COUNTRY
		AND ENABLED_FLAG = 'Y'
		and LANGUAGE = USERENV('LANG')
		)
    */
     ,(select TERRITORYPEO.TERRITORY_SHORT_NAME	
	   from FND_TERRITORIES_VL TERRITORYPEO
	   where TERRITORYPEO.TERRITORY_CODE =   BankBranchEO.BANK_HOME_COUNTRY
	  )	
    	  RES_COUNTRY
,BankBranchEO.BANK_BRANCH_NAME RES_BRANCH_NAME
,BankBranchEO.BANK_BRANCH_NAME_ALT RES_ALTERNATE_BRANCH_NAME
,BankBranchEO.BRANCH_NUMBER RES_BRANCH_NUMBER
,BankBranchEO.DESCRIPTION RES_DESCRIPTION
,BankBranchEO.EFT_SWIFT_CODE RES_BIC_CODE
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_TYPE = 'BANK_CODE_US'
	AND LOOKUP_CODE   = BankBranchEO.BANK_CODE
	AND ENABLED_FLAG = 'Y'
	and LANGUAGE = USERENV('LANG')
	) RES_BRANCH_NUMBER_TYPE
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_TYPE = 'BANK_BRANCH_TYPE'
	AND LOOKUP_CODE   = BankBranchEO.BANK_BRANCH_TYPE
	AND ENABLED_FLAG = 'Y'
	and LANGUAGE = USERENV('LANG')
	)						 RES_BANK_BRANCH_TYPE
,BankBranchEO.EDI_ID_NUMBER RES_EDI_ID_NUMBER
,BankBranchEO.EFT_USER_NUMBER RES_EFT_NUMBER
,BankBranchEO.EDI_LOCATION RES_EDI_LOCATION
,(SELECT MEANING
	FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_TYPE = 'RFC_IDENTIFIER'
	AND LOOKUP_CODE   = BankBranchEO.RFC
	AND ENABLED_FLAG = 'Y'
	and LANGUAGE = USERENV('LANG')
	) RES_RFC_IDENTIFIER
,BankBranchEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,BankBranchEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,BankBranchEO.CREATED_BY RSC_CREATED_BY
,BankBranchEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM CE_BANK_BRANCHES_V BankBranchEO
ORDER BY BANK_BRANCH_NAME
,BANK_NAME
,BANK_HOME_COUNTRY