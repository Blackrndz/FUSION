SELECT applicationsE0.APPLICATION_NAME RES_SUBLEDGER_APPLICATION
,accountingMethodsE0.NAME RES_NAME
,accountingMethodsE0.ACCOUNTING_METHOD_CODE RES_SHORT_NAME
,(SELECT NAME 
	FROM XLA_EVENT_CLASSES_TL
	WHERE EVENT_CLASS_CODE = jeRuleSetsAssE0.EVENT_CLASS_CODE
	AND APPLICATION_ID = jeRuleSetsAssE0.APPLICATION_ID
	AND LANGUAGE = USERENV('LANG')
	) RES_EVENT_CLASS
,(SELECT NAME 
	FROM XLA_EVENT_TYPES_TL
	WHERE EVENT_CLASS_CODE = jeRuleSetsAssE0.EVENT_CLASS_CODE
	AND EVENT_TYPE_CODE = jeRuleSetsAssE0.EVENT_TYPE_CODE
	AND APPLICATION_ID = jeRuleSetsAssE0.APPLICATION_ID
	AND LANGUAGE = USERENV('LANG')	
	) RES_EVENT_TYPE
,jeRuleSetsE0.NAME RES_RULE_SET
,(SELECT MEANING 
	FROM XLA_LOOKUPS
	WHERE LOOKUP_TYPE = 'XLA_JOURNAL_RULE_SET_STATUS'
	AND LOOKUP_CODE = jeRuleSetsE0.STATUS_CODE
	) RES_STATUS
,TO_CHAR(jeRuleSetsAssE0.START_DATE_ACTIVE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,TO_CHAR(jeRuleSetsAssE0.END_DATE_ACTIVE,'DD-Mon-YYYY') RES_EFFECTIVE_END_DATE
,jeRuleSetsAssE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,jeRuleSetsAssE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,jeRuleSetsAssE0.CREATED_BY RSC_CREATED_BY
,jeRuleSetsAssE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,accountingMethodsE0.ACCOUNTING_COA_ID RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM XLA_ACCTG_METHODS_VL accountingMethodsE0
,XLA_SLAM_JE_RULE_SETS jeRuleSetsAssE0
,XLA_SUBLEDGERS_TL applicationsE0
,XLA_JE_RULE_SETS_VL jeRuleSetsE0
WHERE accountingMethodsE0.ACCOUNTING_METHOD_CODE = jeRuleSetsAssE0.ACCOUNTING_METHOD_CODE
AND accountingMethodsE0.ACCOUNTING_METHOD_TYPE_CODE = jeRuleSetsAssE0.ACCOUNTING_METHOD_TYPE_CODE
AND jeRuleSetsAssE0.APPLICATION_ID = applicationsE0.APPLICATION_ID
AND applicationsE0.LANGUAGE = USERENV('LANG')
AND jeRuleSetsAssE0.APPLICATION_ID = jeRuleSetsE0.APPLICATION_ID
AND jeRuleSetsAssE0.AMB_CONTEXT_CODE = jeRuleSetsE0.AMB_CONTEXT_CODE
AND jeRuleSetsAssE0.JOURNAL_RULE_SET_TYPE_CODE = jeRuleSetsE0.JERS_TYPE_CODE
AND jeRuleSetsAssE0.JOURNAL_RULE_SET_CODE = jeRuleSetsE0.JERS_CODE
AND jeRuleSetsAssE0.EVENT_CLASS_CODE = jeRuleSetsE0.EVENT_CLASS_CODE
AND jeRuleSetsAssE0.EVENT_TYPE_CODE = jeRuleSetsE0.EVENT_TYPE_CODE
ORDER BY applicationsE0.APPLICATION_NAME
,accountingMethodsE0.NAME
,4,5,jeRuleSetsAssE0.START_DATE_ACTIVE