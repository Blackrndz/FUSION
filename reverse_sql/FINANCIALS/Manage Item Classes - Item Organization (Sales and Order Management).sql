-- FIELD_TO_APPLY_FUNCTION=RES_SALES_ACCOUNT/GET_CODE_COMBINATION

SELECT 
(SELECT ITEM_CLASS_NAME
	FROM EGP_ITEM_CLASSES_VL
	WHERE ITEM_CLASS_ID = ItemClassPEO.PARENT_ITEM_CLASS_ID
	) RES_PARENT_ITEM_CLASS 
,ItemClassPEO.ITEM_CLASS_NAME RES_ITEM_CLASS
,ItemVersionEO.TEMPLATE_NAME RES_NAME
,(SELECT ORGANIZATION_CODE
	FROM INV_ORG_PARAMETERS
	WHERE ORGANIZATION_ID = ItemVersionEO.ORGANIZATION_ID
	) RES_ORGANIZATION
,DECODE(ItemVersionEO.CUSTOMER_ORDER_FLAG,'Y','Yes','N','No') RES_CUSTOMER_ORDERED
,DECODE(ItemVersionEO.CUSTOMER_ORDER_ENABLED_FLAG,'Y','Yes','N','No') RES_CUSTOMER_ORDERS_ENABLED
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_ORDATPCHECKVS_TYPE'
	AND LOOKUP_CODE = ItemVersionEO.ATP_COMPONENTS_FLAG
	) RES_ATP_COMPONENTS
,(SELECT RULE_NAME
	FROM INV_RULES_VL
	WHERE RULE_ID = ItemVersionEO.PICKING_RULE_ID
	) RES_PICKING_RULE
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_COMMON_YES_NO_NUM'
	AND LOOKUP_CODE = ItemVersionEO.RETURN_INSPECTION_REQUIREMENT
	) RES_RMA_INSPECTION_REQUIRED
,DECODE(ItemVersionEO.ELIGIBILITY_COMPATIBILITY_RULE,'Y','Yes','N','No') RES_ELIGIBILITY_RULE
,DECODE(ItemVersionEO.INTERNAL_ORDER_FLAG,'Y','Yes','N','No') RES_INTERNALLY_TRANSFERABLE
,DECODE(ItemVersionEO.INTERNAL_ORDER_ENABLED_FLAG,'Y','Yes','N','No') RES_TRANSFER_ORDERS_ENABLED
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_ORDATPCHECKVS_TYPE'
	AND LOOKUP_CODE = ItemVersionEO.ATP_FLAG
	) RES_CHECK_ATP
,DECODE(ItemVersionEO.RETURNABLE_FLAG,'Y','Yes','N','No') RES_RETURNABLE
,DECODE(ItemVersionEO.FINANCING_ALLOWED_FLAG,'Y','Yes','N','No') RES_FINANCING_ALLOWED
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'QSC_SALES_PRODUCT_TYPE'
	AND LOOKUP_CODE = ItemVersionEO.SALES_PRODUCT_TYPE
	) RES_SALES_PRODUCT_TYPE
,DECODE(ItemVersionEO.BACK_TO_BACK_ENABLED,'Y','Yes','N','No') RES_BACK_TO_BACK_ENABLED
,DECODE(ItemVersionEO.SO_TRANSACTIONS_FLAG,'Y','Yes','N','No') RES_ORDER_MANAGEMENT_TRANSACTI
,DECODE(ItemVersionEO.INDIVISIBLE_FLAG,'Y','Yes','N','No') RES_ORDER_MANAGEMENT_INDIVISIB
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_ORDDEFSOSRCVS_TYPE'
	AND LOOKUP_CODE = ItemVersionEO.DEFAULT_SO_SOURCE_TYPE
	) RES_DEFAULT_SALES_ORDER_SOURCE
,DECODE(ItemVersionEO.ELECTRONIC_FLAG,'Y','Yes','N','No') RES_ELECTRONIC_FORMAT
,DECODE(ItemVersionEO.SHIPPABLE_ITEM_FLAG,'Y','Yes','N','No') RES_SHIPPABLE
,(SELECT NAME
	FROM HR_ALL_ORGANIZATION_UNITS_F_VL
	WHERE ORGANIZATION_ID = ItemVersionEO.DEFAULT_SHIPPING_ORG
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
	) RES_DEFAULT_SHIPPING_ORGANIZAT
,DECODE(ItemVersionEO.SHIP_MODEL_COMPLETE_FLAG,'Y','Yes','N','No') RES_SHIP_MODEL_COMPLETE
,DECODE(ItemVersionEO.DOWNLOADABLE_FLAG,'Y','Yes','N','No') RES_DOWNLOADABLE
,ItemVersionEO.OVER_SHIPMENT_TOLERANCE RES_OVER_SHIPMENT_TOLERANCE
,ItemVersionEO.UNDER_SHIPMENT_TOLERANCE RES_UNDER_SHIPMENT_TOLERANCE
,ItemVersionEO.OVER_RETURN_TOLERANCE RES_OVER_RETURN_TOLERANCE
,ItemVersionEO.UNDER_RETURN_TOLERANCE RES_UNDER_RETURN_TOLERANCE
,DECODE(ItemVersionEO.INVOICEABLE_ITEM_FLAG,'Y','Yes','N','No') RES_INVOICED
,(SELECT NAME
	FROM RA_RULES
	WHERE RULE_ID = ItemVersionEO.ACCOUNTING_RULE_ID
	) RES_ACCOUNTING_RULE
,(SELECT NAME
	FROM RA_TERMS_VL
	WHERE TERM_ID = ItemVersionEO.PAYMENT_TERMS_ID
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,SYSDATE)
	) RES_PAYMENT_TERMS
,ItemVersionEO.TAX_CODE RES_OUTPUT_TAX_CLASSIFICATION_
,DECODE(ItemVersionEO.INVOICE_ENABLED_FLAG,'Y','Yes','N','No') RES_INVOICE_ENABLED
,(SELECT NAME
	FROM RA_RULES
	WHERE RULE_ID = ItemVersionEO.INVOICING_RULE_ID
	) RES_INVOICING_RULE
,ItemVersionEO.SALES_ACCOUNT RES_SALES_ACCOUNT
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EGP_IBE_ITEM_STATUS'
	AND LOOKUP_CODE = ItemVersionEO.WEB_STATUS
	) RES_WEB_STATUS
,DECODE(ItemVersionEO.BACK_ORDERABLE_FLAG,'Y','Yes','N','No') RES_BACK_ORDERABLE
,DECODE(ItemVersionEO.ORDERABLE_ON_WEB_FLAG,'Y','Yes','N','No') RES_ORDERABLE_ON_THE_WEB
,ItemVersionEO.MINIMUM_LICENSE_QUANTITY RES_MINIMUM_LICENSE_QUANTITY
,ItemVersionEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ItemVersionEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ItemVersionEO.CREATED_BY RSC_CREATED_BY
,ItemVersionEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,(SELECT BUSINESS_UNIT_ID
	FROM INV_ORG_PARAMETERS
	WHERE ORGANIZATION_ID = ItemVersionEO.ORGANIZATION_ID
	) RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,(CASE
	WHEN EXISTS
		(SELECT DISTINCT MASTER_ORGANIZATION_ID
			FROM INV_ORG_PARAMETERS
			WHERE MASTER_ORGANIZATION_ID = ItemVersionEO.ORGANIZATION_ID)
	THEN NULL
	ELSE ItemVersionEO.ORGANIZATION_ID
	END) RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM EGP_ITEM_CLASSES_VL ItemClassPEO
,(SELECT TL.TEMPLATE_NAME 
	,B.* 
  FROM EGP_SYSTEM_ITEMS_TL TL
	,EGP_SYSTEM_ITEMS_B B
	WHERE TL.INVENTORY_ITEM_ID = B.INVENTORY_ITEM_ID
	AND TL.ORGANIZATION_ID = B.ORGANIZATION_ID
	AND LANGUAGE = USERENV('LANG')
	AND TEMPLATE_NAME IS NOT NULL
 ) ItemVersionEO
WHERE /*ItemVersionEO.TEMPLATE_NAME IS NOT NULL
AND*/ ItemClassPEO.ITEM_CLASS_ID = ItemVersionEO.ITEM_CATALOG_GROUP_ID
CONNECT BY ItemClassPEO.PARENT_ITEM_CLASS_ID = ItemClassPEO.ITEM_CLASS_ID
ORDER SIBLINGS BY RES_PARENT_ITEM_CLASS,RES_ITEM_CLASS,RES_NAME