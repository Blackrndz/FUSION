SELECT hraFEEDBACKE0.NAME RES_NAME
,hraFEEDBACKE0.COMMENTS RES_COMMENTS
,(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'CODE_STATUS'
			AND LOOKUP_CODE = hraFEEDBACKE0.STATUS_CODE
			) RES_STATUS
,DECODE(hraFEEDBACKE0.TEMPLATE_TYPE,'GENERAL','General Feedback','PERF_GOAL','Performance Goal Feedback') RES_TEMPLATE_TYPE
,(SELECT NAME
			FROM HRQ_QUESTIONNAIRES_VL
			WHERE LATEST_VERSION = 'Y'
			AND QUESTIONNAIRE_ID = hraFEEDBACKE0.QUESTIONNAIRE_ID) RES_QUESTIONNAIRE
,DECODE(hraFEEDBACKE0.INCLUDE_IN_PERF_DOC_FLAG,'Y','Yes','No') RES_INCLUDE_IN_PERFORMANCE_DOC
,hraFEEDBACKE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hraFEEDBACKE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hraFEEDBACKE0.CREATED_BY RSC_CREATED_BY
,hraFEEDBACKE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM HRA_FEEDBACK_REQ_TMPLS_VL hraFEEDBACKE0