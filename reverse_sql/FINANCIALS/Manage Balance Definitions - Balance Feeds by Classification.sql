SELECT (SELECT NAME FROM PER_LEGISLATIVE_DATA_GROUPS_VL WHERE LEGISLATIVE_DATA_GROUP_ID = payBALNTYPEO.LEGISLATIVE_DATA_GROUP_ID)  RES_LEGISLATIVE_DATA_GROUP
,payBALNTYPEO.BALANCE_NAME RES_NAME
,payELECLASSEO.CLASSIFICATION_NAME RES_BALANCE_CLASSIFICATION
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = payBALCALEO.SCALE
		AND LOOKUP_TYPE = 'PAY_ADD_SUBTRACT' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_ADD_OR_SUBTRACT

,payBALCALEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,payBALCALEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,payBALCALEO.CREATED_BY RSC_CREATED_BY
,payBALCALEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PAY_BALANCE_TYPES_VL  payBALNTYPEO
,PAY_BAL_CLASSIFICATIONS  payBALCALEO
,PAY_ELE_CLASSIFICATIONS_VL payELECLASSEO 
WHERE payBALNTYPEO.BALANCE_TYPE_ID = payBALCALEO.BALANCE_TYPE_ID
AND payBALCALEO.CLASSIFICATION_ID = payELECLASSEO.CLASSIFICATION_ID
AND SYSDATE BETWEEN payELECLASSEO.DATE_FROM AND NVL(payELECLASSEO.DATE_TO,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY RES_LEGISLATIVE_DATA_GROUP,payBALNTYPEO.LEGISLATION_CODE,RES_NAME