--RSC_PREREQUISITE_OBJECTS=PAY_FLOWS_VL 

SELECT payFLOWEO.FLOW_NAME RES_FLOW_PATTERN
,payFLOWEO.DESCRIPTION RES_DESCRIPTION
,(SELECT NAME FROM PER_LEGISLATIVE_DATA_GROUPS_VL WHERE LEGISLATIVE_DATA_GROUP_ID = payFLOWEO.LEGISLATIVE_DATA_GROUP_ID) RES_LEGISLATIVE_DATA_GROUP
,DECODE(payFLOWEO.LDG_RQD_FLAG,'Y','Yes','No') RES_LDG_REQUIRED
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = payFLOWEO.CONNECTOR_STATUS
		AND LOOKUP_TYPE = 'ORA_PAY_FLOW_CONNECTOR_STATUS' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY')))  RES_CONNECTOR_STATUS
,(CASE WHEN CONNECTOR_TAG IS NULL THEN
	payFLOWEO.FLOW_NAME||' | '||payFLOWEO.BASE_FLOW_NAME 
	ELSE 
	payFLOWEO.FLOW_NAME||' | '||payFLOWEO.CONNECTOR_TAG 
	END) RES_CONNECTOR_NAME
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = payFLOWEO.DEFAULT_FLOW_FLAG
		AND LOOKUP_TYPE = 'ORA_PAY_FLOW_SUBMISSION_STATUS' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY')))  RES_FLOW_STATUS


,payFLOWEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,payFLOWEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,payFLOWEO.CREATED_BY RSC_CREATED_BY
,payFLOWEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PAY_FLOWS_VL payFLOWEO
ORDER BY RES_LEGISLATIVE_DATA_GROUP,payFLOWEO.LEGISLATION_CODE,payFLOWEO.FLOW_NAME