-- RSC_PREREQUISITE_OBJECTS=AP_PAYMENT_TEMPLATES

SELECT apTemplatesE0.TEMPLATE_NAME RES_NAME
,DECODE(apTemplatesE0.PAYMENT_DATE_OPTION,'RD','Use request date','Calculate based on request date') RES_PAYMENT_DATE
,apTemplatesE0.ADDL_PAYMENT_DAYS RES_PAYMENT_DATE_CALCULATE_BAS
,(SELECT BANK_ACCOUNT_NAME
	FROM CE_BANK_ACCOUNTS
	WHERE BANK_ACCOUNT_ID = apTemplatesE0.BANK_ACCOUNT_ID
	) RES_DISBURSEMENT_BANK_ACCOUNT
,(SELECT PAYMENT_DOCUMENT_NAME 
	FROM CE_PAYMENT_DOCUMENTS
	WHERE PAYMENT_DOCUMENT_ID = apTemplatesE0.PAYMENT_DOCUMENT_ID
	) RES_PAYMENT_DOCUMENT
,(SELECT PAYMENT_PROFILE_NAME 
	FROM IBY_ACCT_PMT_PROFILES_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND PAYMENT_PROFILE_ID = apTemplatesE0.PAYMENT_PROFILE_ID
	) RES_PAYMENT_PROCESS_PROFILE
,(SELECT MEANING
	FROM AP_LOOKUP_VALUES
	WHERE LOOKUP_TYPE = 'TRANSFER_PRIORITY'
	AND LOOKUP_CODE = apTemplatesE0.SETTLEMENT_PRIORITY
	) RES_SETTLEMENT_PRIORITY_OVERRI
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'IBY_BANK_CHARGE_BEARER'
	AND LOOKUP_CODE = apTemplatesE0.BANK_CHARGE_BEARER
	) RES_BANK_CHARGE_BEARER_OVERRID
,apTemplatesE0.FIRST_VOUCHER_NUMBER RES_STARTING_VOUCHER_NUMBER
,(SELECT USER_CONVERSION_TYPE 
	FROM GL_DAILY_CONVERSION_TYPES
	WHERE CONVERSION_TYPE = apTemplatesE0.PAYMENT_EXCHANGE_RATE_TYPE
	) RES_PAYMENT_CONVERSION_RATE_TY
,(SELECT USER_CONVERSION_TYPE 
	FROM GL_DAILY_CONVERSION_TYPES
	WHERE CONVERSION_TYPE = apTemplatesE0.X_CURR_RATE_TYPE
	) RES_CROSS_CURRENCY_RATE_TYPE
,DECODE(apTemplatesE0.ZERO_AMOUNTS_ALLOWED,'Y','Yes','No') RES_APPLY_CREDITS_UP_TO_ZERO_A
,DECODE(apTemplatesE0.PAYABLES_REVIEW_SETTINGS,'Y','Yes','No') RES_REVIEW_INSTALLMENTS
,DECODE(apTemplatesE0.PAYMENTS_REVIEW_SETTINGS,'Y','Yes','No') RES_REVIEW_PROPOSED_PAYMENTS
,DECODE(apTemplatesE0.CREATE_INSTRS_FLAG,'Y','Yes','No') RES_CREATE_PAYMENT_FILES_IMMED
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'IBY_DOCUMENT_REJECTION_LEVELS'
	AND LOOKUP_CODE = apTemplatesE0.DOCUMENT_REJECTION_LEVEL_CODE
	) RES_DOCUMENT
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'IBY_PAYMENT_REJECTION_LEVELS'
	AND LOOKUP_CODE = apTemplatesE0.PAYMENT_REJECTION_LEVEL_CODE
	) RES_PAYMENT
,(SELECT LIST_NAME
	FROM PER_PERSON_NAMES_F
	WHERE NAME_TYPE = 'GLOBAL'
	AND PERSON_ID = apTemplatesE0.FIRST_APPROVER_ID
	) RES_FIRST_APPROVER
,apTemplatesE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,apTemplatesE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,apTemplatesE0.CREATED_BY RSC_CREATED_BY
,apTemplatesE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM AP_PAYMENT_TEMPLATES apTemplatesE0
ORDER BY apTemplatesE0.TEMPLATE_NAME