-- RSC_PREREQUISITE_OBJECTS=HXT_TCLAY_V


SELECT hxtTCLAYEO.NAME RES_LAYOUT_NAME
,DECODE(hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CHAR2,'TIME','Display start and end time','HOUR','Display hours only','BOTH','Display hours and times') RES_TIME_ENTRY_FORMAT
,DECODE(hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CHAR26,'Y','Yes','No')  RES_DISPLAY_HOURS_SLIDER
,DECODE(hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CHAR27,'Y','Yes','No')  RES_DISPLAY_TIME_SLIDER
,DECODE(hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CHAR1,'Y','Yes','No') RES_ENABLE_ENTRY_OF_NEGATIVE_H
,DECODE(hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CHAR9,'Y','Yes','No') RES_ENABLE_TIME_TO_SPAN_MIDNIG
,hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_NUMBER1  RES_TIME_CARD_FIELDS_DECIMAL_P
,NULL RES_OVERTIME_TIME_CATEGORY
,(SELECT TCLAYFLD_ATTRIBUTE_NUMBER1 FROM HXT_TCLAYFLD_DEFNS_VL WHERE TCLAYFLD_ATTRIBUTE_CATEGORY = 'TIME_TOTALS'
	AND TCLAY_ID = hxtTCLATDEFNSVEO.TCLAY_ID) RES_TIME_TOTALS_DECIMAL_PLACES

,hxtTCLAYEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hxtTCLAYEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hxtTCLAYEO.CREATED_BY RSC_CREATED_BY
,hxtTCLAYEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM HXT_TCLAYST_VL hxtTCLAYEO   
,HXT_TCLAYST_PROP hxtTCLAYPROPEO
,HWM_TL_TASK_RESULTS hxtTLTASKREEO
,(SELECT TL_TASK_FEATURES_ID FROM HWM_TL_TASK_FEATURES_VL WHERE TASK_NAME ='Calendar Entry Layout' OR TASK_SHORT_NAME = 'FCL') TMP_NAMEEO
,HXT_TCLAY_V hxtTCLATVEO
,HXT_TCLAYFLD_DEFNS_VL hxtTCLATDEFNSVEO

WHERE hxtTCLAYEO.TCLAYST_ID =  hxtTCLAYPROPEO.ANS_SET_ID     
AND hxtTCLAYPROPEO.ANS_SET_ID = hxtTLTASKREEO.TL_ANSWER_SET_ID
AND hxtTLTASKREEO.TL_TASK_FEATURES_ID = TMP_NAMEEO.TL_TASK_FEATURES_ID
AND hxtTLTASKREEO.TL_TASK_RESULTS_ID = hxtTCLATVEO.TL_TASK_RESULTS_ID
AND hxtTCLATVEO.TCLAY_ID = hxtTCLATDEFNSVEO.TCLAY_ID
AND hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CATEGORY = 'MATRIX_PARAMS'
ORDER BY hxtTCLAYEO.NAME



