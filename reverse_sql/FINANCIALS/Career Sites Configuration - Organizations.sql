WITH ORG AS (SELECT * FROM (SELECT PK1_START_VALUE ORG_ID
    ,SUBSTR(REVERSE(SYS_CONNECT_BY_PATH(REVERSE(ORG_DISPLAY),' ,')),1,LENGTH(REVERSE(SYS_CONNECT_BY_PATH(REVERSE(ORG_DISPLAY),', ')))-2) ORG_DISPLAY
    FROM (SELECT PK1_START_VALUE
        ,PARENT_TREE_NODE_ID
        ,TREE_NODE_ID
        ,PARENT_DATA_SOURCE_ID
        ,DATA_SOURCE_ID
        ,(SELECT NAME
            FROM HR_ORGANIZATION_UNITS
            WHERE SYSDATE BETWEEN DATE_FROM AND DATE_TO
            AND ORGANIZATION_ID = PK1_START_VALUE
            ) ORG_DISPLAY
        FROM PER_ORG_TREE_NODE)
    START WITH NVL(PARENT_TREE_NODE_ID,'1') = '1'
    CONNECT BY PRIOR TREE_NODE_ID = PARENT_TREE_NODE_ID
    AND DATA_SOURCE_ID = PARENT_DATA_SOURCE_ID))

SELECT cxSitesE0.SITE_NAME RES_SITE_NAME
,(SELECT ORG_DISPLAY
    FROM ORG
    WHERE ORG_ID = contextsE0.ORGANIZATION_ID
	AND ROWNUM = 1
    ) RES_ORGANIZATIONS
,contextsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,contextsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,contextsE0.CREATED_BY RSC_CREATED_BY
,contextsE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM IRC_CX_SITES_VL cxSitesE0
,IRC_AVAILABILITY_CONTEXT contextsE0
WHERE cxSitesE0.SITE_ID = contextsE0.OBJECT_ID
AND contextsE0.OBJECT_TYPE_CODE = 'ORA_CE_SITES'
AND contextsE0.DIMENSION_TYPE_CODE = 'ORA_ORGANIZATION'
ORDER BY cxSitesE0.SEQUENCE_NUMBER