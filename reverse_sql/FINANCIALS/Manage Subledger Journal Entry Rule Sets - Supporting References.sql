SELECT (SELECT APPLICATION_NAME
	FROM XLA_SUBLEDGERS_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND APPLICATION_ID = SubledgerRuleSetsE0.APPLICATION_ID
	) RES_SUBLEDGER_APPLICATION
,SubledgerRuleSetsE0.NAME  RES_NAME
,(SELECT MEANING 
	FROM XLA_LOOKUPS
	WHERE LOOKUP_TYPE = 'XLA_JLT_SIDE'
	AND LOOKUP_CODE = SubledgerRSLinesE0.SIDE_CODE
	) RES_LINE_TYPE
,JELineRulesE0.NAME RES_JOURNAL_LINE_RULE
,(SELECT NAME
	FROM XLA_ANALYTICAL_HDRS_TL
	WHERE ANALYTICAL_CRITERION_CODE = SubledgerRSLinesSRE0.ANALYTICAL_CRITERION_CODE
	AND ANALYTICAL_CRITERION_TYPE_CODE = SubledgerRSLinesSRE0.ANALYTICAL_CRITERION_TYPE_CODE
	AND LANGUAGE = USERENV('LANG')
	) RES_SUPPORTING_REFERENCES
,SubledgerRSLinesSRE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,SubledgerRSLinesSRE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,SubledgerRSLinesSRE0.CREATED_BY RSC_CREATED_BY
,SubledgerRSLinesSRE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,SubledgerRuleSetsE0.ACCOUNTING_COA_ID RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM XLA_JE_RULE_SETS_VL SubledgerRuleSetsE0
,XLA_JERS_JLT_LINES SubledgerRSLinesE0
,XLA_ACCT_LINE_TYPES_VL JELineRulesE0
,XLA_JERS_SR_LINE_ASSGNS SubledgerRSLinesSRE0
WHERE SubledgerRuleSetsE0.JERS_CODE = SubledgerRSLinesE0.JERS_CODE
AND SubledgerRuleSetsE0.JERS_TYPE_CODE = SubledgerRSLinesE0.JERS_TYPE_CODE
AND SubledgerRuleSetsE0.AMB_CONTEXT_CODE = SubledgerRSLinesE0.AMB_CONTEXT_CODE
AND SubledgerRuleSetsE0.APPLICATION_ID = SubledgerRSLinesE0.APPLICATION_ID
AND JELineRulesE0.ACCOUNTING_LINE_CODE = SubledgerRSLinesE0.ACCOUNTING_LINE_CODE
AND JELineRulesE0.ACCOUNTING_LINE_TYPE_CODE = SubledgerRSLinesE0.ACCOUNTING_LINE_TYPE_CODE
AND JELineRulesE0.APPLICATION_ID = SubledgerRSLinesE0.APPLICATION_ID
AND JELineRulesE0.AMB_CONTEXT_CODE = SubledgerRSLinesE0.AMB_CONTEXT_CODE
AND JELineRulesE0.EVENT_CLASS_CODE = SubledgerRSLinesE0.EVENT_CLASS_CODE
AND SubledgerRSLinesE0.APPLICATION_ID = SubledgerRSLinesSRE0.APPLICATION_ID
AND SubledgerRSLinesE0.AMB_CONTEXT_CODE = SubledgerRSLinesSRE0.AMB_CONTEXT_CODE
AND SubledgerRSLinesE0.JERS_TYPE_CODE = SubledgerRSLinesSRE0.JERS_TYPE_CODE
AND SubledgerRSLinesE0.JERS_CODE = SubledgerRSLinesSRE0.JERS_CODE
AND SubledgerRSLinesE0.EVENT_CLASS_CODE = SubledgerRSLinesSRE0.EVENT_CLASS_CODE
AND SubledgerRSLinesE0.EVENT_TYPE_CODE = SubledgerRSLinesSRE0.EVENT_TYPE_CODE
AND SubledgerRSLinesE0.ACCOUNTING_LINE_TYPE_CODE = SubledgerRSLinesSRE0.ACCOUNTING_LINE_TYPE_CODE
AND SubledgerRSLinesE0.ACCOUNTING_LINE_CODE = SubledgerRSLinesSRE0.ACCOUNTING_LINE_CODE