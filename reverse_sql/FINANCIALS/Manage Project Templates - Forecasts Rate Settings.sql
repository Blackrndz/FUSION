SELECT projTemplatesE0.NAME RES_TEMPLATE_NAME
,projTemplatesE0.SEGMENT1 RES_TEMPLATE_NUMBER
,budgetsE0.VERSION_NAME RES_NAME
,DECODE(planningOptionsE0.USE_PLANNING_RATES_FLAG,'Y','Yes','No') RES_USE_PLANNING_RATES
,(CASE
	WHEN planningOptionsE0.USE_PLANNING_RATES_FLAG = 'N' THEN	
		(SELECT RATE_SCHEDULE_NAME 
			FROM PJF_RATE_SCHEDULES_TL  
			WHERE RATE_SCHEDULE_ID = planningOptionsE0.RES_CLASS_RAW_COST_SCH_ID
			AND LANGUAGE = USERENV('LANG'))
	END) RES_RESOURCE_CLASS_RAW_COST_RA
,(CASE
	WHEN planningOptionsE0.USE_PLANNING_RATES_FLAG = 'N' THEN	
		(SELECT RATE_SCHEDULE_NAME 
			FROM PJF_RATE_SCHEDULES_TL  
			WHERE RATE_SCHEDULE_ID = planningOptionsE0.RES_CLASS_BILL_RATE_SCH_ID
			AND LANGUAGE = USERENV('LANG'))
	END)  RES_RESOURCE_CLASS_BILL_RATE
,(CASE
	WHEN planningOptionsE0.USE_PLANNING_RATES_FLAG = 'Y' AND budgetsE0.PLANNED_FOR_CODE != 'REVENUE' THEN	
		(SELECT IND_SCH_NAME 
			FROM PJF_IND_RATE_SCH_TL  
			WHERE IND_RATE_SCH_ID = planningOptionsE0.COST_BRDND_RATE_SCH_ID
			AND LANGUAGE = USERENV('LANG')) 
	END) RES_BURDEN_SCHEDULE
,(CASE
	WHEN planningOptionsE0.USE_PLANNING_RATES_FLAG = 'Y' AND budgetsE0.PLANNED_FOR_CODE != 'REVENUE' THEN	
		(SELECT RATE_SCHEDULE_NAME 
			FROM PJF_RATE_SCHEDULES_TL  
			WHERE RATE_SCHEDULE_ID = planningOptionsE0.COST_EMP_RATE_SCH_ID
			AND LANGUAGE = USERENV('LANG'))
	END) RES_PERSON
,(CASE
	WHEN planningOptionsE0.USE_PLANNING_RATES_FLAG = 'Y' AND budgetsE0.PLANNED_FOR_CODE != 'REVENUE' THEN	
		(SELECT RATE_SCHEDULE_NAME 
			FROM PJF_RATE_SCHEDULES_TL  
			WHERE RATE_SCHEDULE_ID = planningOptionsE0.COST_JOB_RATE_SCH_ID
			AND LANGUAGE = USERENV('LANG'))
	END) RES_JOB
,(CASE
	WHEN planningOptionsE0.USE_PLANNING_RATES_FLAG = 'Y' AND budgetsE0.PLANNED_FOR_CODE != 'REVENUE' THEN	
		(SELECT RATE_SCHEDULE_NAME 
			FROM PJF_RATE_SCHEDULES_TL  
			WHERE RATE_SCHEDULE_ID = planningOptionsE0.COST_NON_LBR_RES_RATE_SCH_ID
			AND LANGUAGE = USERENV('LANG')) 
	END) RES_NONLABOR_RESOURCE
,(CASE
	WHEN planningOptionsE0.USE_PLANNING_RATES_FLAG = 'Y' AND budgetsE0.PLANNED_FOR_CODE != 'REVENUE' THEN	
		(SELECT RATE_SCHEDULE_NAME 
			FROM PJF_RATE_SCHEDULES_TL  
			WHERE RATE_SCHEDULE_ID = planningOptionsE0.RES_CLASS_RAW_COST_SCH_ID
			AND LANGUAGE = USERENV('LANG')) 
	END) RES_RESOURCE_CLASS
,(CASE
	WHEN planningOptionsE0.USE_PLANNING_RATES_FLAG = 'Y' AND budgetsE0.PLANNED_FOR_CODE = 'REVENUE' THEN	
		(SELECT RATE_SCHEDULE_NAME 
			FROM PJF_RATE_SCHEDULES_TL  
			WHERE RATE_SCHEDULE_ID = planningOptionsE0.REV_EMP_RATE_SCH_ID
			AND LANGUAGE = USERENV('LANG')) 
	END) RES_REVENUE_PERSON
,(CASE
	WHEN planningOptionsE0.USE_PLANNING_RATES_FLAG = 'Y' AND budgetsE0.PLANNED_FOR_CODE = 'REVENUE' THEN	
		(SELECT RATE_SCHEDULE_NAME 
			FROM PJF_RATE_SCHEDULES_TL  
			WHERE RATE_SCHEDULE_ID = planningOptionsE0.REV_JOB_RATE_SCH_ID
			AND LANGUAGE = USERENV('LANG')) 
	END) RES_REVENUE_JOB
,(CASE
	WHEN planningOptionsE0.USE_PLANNING_RATES_FLAG = 'Y' AND budgetsE0.PLANNED_FOR_CODE = 'REVENUE' THEN	
		(SELECT RATE_SCHEDULE_NAME 
			FROM PJF_RATE_SCHEDULES_TL  
			WHERE RATE_SCHEDULE_ID = planningOptionsE0.REV_NON_LBR_RES_RATE_SCH_ID
			AND LANGUAGE = USERENV('LANG')) 
	END) RES_REVENUE_NONLABOR_RESOURCE
,(CASE
	WHEN planningOptionsE0.USE_PLANNING_RATES_FLAG = 'Y' AND budgetsE0.PLANNED_FOR_CODE = 'REVENUE' THEN	
		(SELECT RATE_SCHEDULE_NAME 
			FROM PJF_RATE_SCHEDULES_TL  
			WHERE RATE_SCHEDULE_ID = planningOptionsE0.RES_CLASS_BILL_RATE_SCH_ID
			AND LANGUAGE = USERENV('LANG')) 
	END) RES_REVENUE_RESOURCE_CLASS
,budgetsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,budgetsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,budgetsE0.CREATED_BY  RSC_CREATED_BY
,budgetsE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,projTemplatesE0.ORG_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJF_PROJECTS_ALL_VL projTemplatesE0
,PJO_PLAN_VERSIONS_VL budgetsE0
,PJO_PLANNING_OPTIONS planningOptionsE0
WHERE projTemplatesE0.TEMPLATE_FLAG = 'Y'
AND projTemplatesE0.PROJECT_ID = budgetsE0.PROJECT_ID
AND budgetsE0.PLAN_CLASS_CODE = 'FORECAST'
AND budgetsE0.STRUCTURE_VERSION_ID IS NULL
AND budgetsE0.PLAN_VERSION_ID = planningOptionsE0.PLAN_VERSION_ID
ORDER BY projTemplatesE0.NAME
,budgetsE0.VERSION_NAME