    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_MX_WTG_DT_TO_USE'
    AND LOOKUP_CODE = benPRTNELIGEO.WAIT_PERD_DT_TO_USE_CD
    ) RES_WAITING_PERIOD_PERIOD_STAR
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benPRTNELIGEO.WAIT_PERD_DT_TO_USE_RL
    ) RES_WAITING_PERIOD_START_DATE_

,benPRTNELIGEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,benPRTNELIGEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,benPRTNELIGEO.CREATED_BY RSC_CREATED_BY
,benPRTNELIGEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPGMEO.LEGISLATION_CODE RSC_COUNTRY_ID
	
	
FROM BEN_PGM_F benPGMEO
,BEN_PRTN_ELIG_F benPRTNELIGEO
WHERE benPGMEO.PGM_ID = benPRTNELIGEO.PGM_ID
AND SYSDATE BETWEEN benPGMEO.EFFECTIVE_START_DATE AND NVL(benPGMEO.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))

UNION

SELECT benPGMEO.NAME RES_PROGRAM_NAME
,benPLT.NAME RES_PROGRAM_HIERARCHY
,TO_CHAR(benPT.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,'Plan Type' RES_PROGRAM_HIERARCHY_DESCRIPT
,'2' SEQUENCE
,TO_CHAR(benPRTNELIGEO.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_PARTICIPATION_ELIGIBILITY_
,DECODE(TO_CHAR(benPRTNELIGEO.EFFECTIVE_END_DATE ,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(benPRTNELIGEO.EFFECTIVE_END_DATE ,'DD-Mon-YYYY')) RES_PARTICIPATION_ELIGIBILIT_0
