SELECT benPGMEO.NAME RES_PROGRAM_NAME
,benPL.NAME RES_PROGRAM_HIERARCHY
,TO_CHAR(benPLIP.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,'Plan' RES_PROGRAM_HIERARCHY_DESCRIPT
,'3' SEQUENCE
,TO_CHAR(benPRTNELIGEO.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_PARTICIPATION_ELIGIBILITY_
,DECODE(TO_CHAR(benPRTNELIGEO.EFFECTIVE_END_DATE ,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(benPRTNELIGEO.EFFECTIVE_END_DATE ,'DD-Mon-YYYY')) RES_PARTICIPATION_ELIGIBILIT_0
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_PRTN_ELIG_STRT'
    AND LOOKUP_CODE = benPRTNELIGEO.PRTN_EFF_STRT_DT_CD
    ) RES_PARTICIPATION_START_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benPRTNELIGEO.PRTN_EFF_STRT_DT_RL
    ) RES_PARTICIPATION_START_DATE_F
