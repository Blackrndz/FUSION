    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_PRTN_ELIG_STRT'
    AND LOOKUP_CODE = benPRTNELIGEO.PRTN_EFF_STRT_DT_CD
    ) RES_PARTICIPATION_START_DATE
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benPRTNELIGEO.PRTN_EFF_STRT_DT_RL
    ) RES_PARTICIPATION_START_DATE_F
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_PRTN_ELIG_END'
    AND LOOKUP_CODE = benPRTNELIGEO.PRTN_EFF_END_DT_CD
    ) RES_PARTICIPATION_PREVIOUS_END
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benPRTNELIGEO.PRTN_EFF_END_DT_RL
    ) RES_PARTICIPATION_PREVIOUS_E_1
,benPRTNELIGEO.WAIT_PERD_VAL RES_WAITING_PERIOD_VALUE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_WTG_PERD_UOM'
    AND LOOKUP_CODE = benPRTNELIGEO.WAIT_PERD_UOM
    ) RES_WAITING_PERIOD_UNIT_OF_MEA
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benPRTNELIGEO.WAIT_PERD_RL
    ) RES_WAITING_PERIOD_WAITING_PER
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_MX_WTG_DT_TO_USE'
    AND LOOKUP_CODE = benPRTNELIGEO.WAIT_PERD_DT_TO_USE_CD
    ) RES_WAITING_PERIOD_PERIOD_STAR
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benPRTNELIGEO.WAIT_PERD_DT_TO_USE_RL
    ) RES_WAITING_PERIOD_START_DATE_

,benPRTNELIGEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,benPRTNELIGEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,benPRTNELIGEO.CREATED_BY RSC_CREATED_BY
,benPRTNELIGEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
