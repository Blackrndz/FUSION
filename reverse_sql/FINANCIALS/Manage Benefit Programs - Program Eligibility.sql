,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = benPRTNELIGEO.PRTN_EFF_END_DT_RL
    ) RES_PARTICIPATION_PREVIOUS_E_1
,benPRTNELIGEO.WAIT_PERD_VAL RES_WAITING_PERIOD_VALUE
