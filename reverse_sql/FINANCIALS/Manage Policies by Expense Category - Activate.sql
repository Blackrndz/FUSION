SELECT 'Accommodations' RES_EXPENSE_CATEGORY
,ExmAccomPolicies.POLICY_NAME RES_POLICY_NAME
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EXM_POLICY_STATUS'
	AND LOOKUP_CODE = ExmAccomPolicies.STATUS
	) RES_STATUS
,ExmAccomPolicies.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ExmAccomPolicies.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ExmAccomPolicies.CREATED_BY RSC_CREATED_BY
,ExmAccomPolicies.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM EXM_ACCOM_POLICIES ExmAccomPolicies
WHERE ExmAccomPolicies.STATUS = 'ACTIVE'

UNION ALL 

