-- RSC_PREREQUISITE_OBJECTS=HWM_RULES

SELECT hwnRuleEO.RULE_NAME  RES_NAME
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = hwnRuleTmpEO.TEMPLATE_TYPE
		AND LOOKUP_TYPE = 'HWM_RULE_TYPE' AND LANGUAGE = USERENV('LANG')) RES_TEMPLATE_TYPE
,hwnRuleTmpEO.TEMPLATE_NAME RES_RULE_TEMPLATE_NAME
,hwnRuleEO.DESCRIPTION  RES_DESCRIPTION
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = hwnRuleEO.RULE_SUB_TYPE
		AND LOOKUP_TYPE = 'ORA_HWM_RULE_SUB_TYPE' AND LANGUAGE = USERENV('LANG')) RES_FORMULA_SUBTYPE
,(SELECT ALLOCATION_NAME FROM HWM_ALLOCATIONS_HDR_VL 
	WHERE ALLOCATION_ID = hwnRuleEO.DFLT_ALLOCATION_ID AND (SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE) )RES_ALLOCATION_NAME
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = hwnRuleEO.CLASSIFICATION
		AND LOOKUP_TYPE = 'HWM_RULE_CLASSIFICATION' AND LANGUAGE = USERENV('LANG')) RES_RULE_CLASSIFICATION
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = hwnRuleEO.RULE_EXEC_TYPE
		AND LOOKUP_TYPE = 'HWM_RULE_EXEC_TYPE' AND LANGUAGE = USERENV('LANG')) RES_RULE_EXECUTION_TYPE		
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = hwnRuleEO.RUN_SUMMATION_LEVEL
		AND LOOKUP_TYPE = 'HWM_RULE_TBB_PROCESS_LEVEL' AND LANGUAGE = USERENV('LANG')) RES_SUMMATION_LEVEL
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = hwnRuleEO.RUN_TBB_LEVEL
		AND LOOKUP_TYPE = 'HWM_RULE_TBB_PROCESS_LEVEL' AND LANGUAGE = USERENV('LANG')) RES_REPORTING_LEVEL
,DECODE(hwnRuleEO.SUPPRESS_DUP_MSGS,'Y','Yes','No') RES_SUPPRESS_DUPLICATE_MESSAGE
,DECODE(hwnRuleEO.INCLUDE_EMPTY_TC,'Y','Yes','No') RES_PROCESS_EMPTY_TIME_CARD
,DECODE(hwnRuleEO.RUN_EVENT_SAVE,'Y','Yes','No') RES_SAVE
,DECODE(hwnRuleEO.RUN_EVENT_SUB,'Y','Yes','No') RES_SUBMIT
,DECODE(hwnRuleEO.RUN_EVENT_RESUB,'Y','Yes','No') RES_RESUBMIT
,DECODE(hwnRuleEO.RUN_EVENT_DELETE,'Y','Yes','No') RES_DELETE
,hwnRuleTmpEO.RT_EXPLANATION RES_EXPLANATION
,hwnRuleEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hwnRuleEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hwnRuleEO.CREATED_BY RSC_CREATED_BY
,hwnRuleEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM HWM_RULES hwnRuleEO
,HWM_RULE_TMPLTS_VL hwnRuleTmpEO

WHERE hwnRuleEO.RULE_TMPLTS_ID = hwnRuleTmpEO.RULE_TMPLTS_ID(+)
ORDER BY hwnRuleEO.RULE_NAME
