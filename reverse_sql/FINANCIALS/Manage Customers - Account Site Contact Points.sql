SELECT (SELECT MEANING 
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PARTY_TYPE'
	AND LOOKUP_CODE = HzPartiesE0.PARTY_TYPE
	) RES_CUSTOMER_TYPE
,HzPartiesE0.PARTY_NUMBER RES_REGISTRY_ID
,HzCustAccountsE0.ACCOUNT_NUMBER RES_ACCOUNT_NUMBER
,HzPartySiteE0.PARTY_SITE_NUMBER RES_SITE_NUMBER
,(SELECT SET_NAME
	FROM FND_SETID_SETS
	WHERE LANGUAGE = USERENV('LANG')
	AND SET_ID = HzCustAcctSitesAllE0.SET_ID
	) RES_ACCOUNT_ADDRESS_SET
--,HZ_FORMAT_PUB.FORMAT_ADDRESS(P_LOCATION_ID => HzPartySiteE0.LOCATION_ID, P_LINE_BREAK => ',')  RES_ACCOUNT_ADDRESS_SET
,ContactDetailE0.PERSON_FIRST_NAME RES_FIRST_NAME
,ContactDetailE0.PERSON_LAST_NAME RES_LAST_NAME
,HzOrgContactsE0.CONTACT_NUMBER RES_CONTACT_NUMBER
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'RESPONSIBILITY'
	AND LOOKUP_CODE = HzOrgContactsE0.JOB_TITLE_CODE
	) RES_JOB_TITLE_CODE
,DECODE(HzContactPointsE0.PRIMARY_FLAG,'Y','Yes','N','No') RES_PRIMARY
,DECODE(HzContactPointsE0.PRIMARY_BY_PURPOSE,'Y','Yes','N','No') RES_PRIMARY_BY_PURPOSE
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'COMMUNICATION_TYPE'
	AND LOOKUP_CODE = HzContactPointsE0.CONTACT_POINT_TYPE
	) RES_CONTACT_POINT_TYPE
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PHONE_LINE_TYPE'
	AND LOOKUP_CODE = HzContactPointsE0.PHONE_LINE_TYPE
	) RES_TYPE
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'CONTACT_POINT_PURPOSE'
	AND LOOKUP_CODE = HzContactPointsE0.CONTACT_POINT_PURPOSE
	) RES_PURPOSE
,HzContactPointsE0.PHONE_COUNTRY_CODE RES_PHONE_COUNTRY_CODE
,HzContactPointsE0.PHONE_AREA_CODE RES_AREA_CODE
,HzContactPointsE0.PHONE_NUMBER RES_PHONE
,HzContactPointsE0.PHONE_EXTENSION RES_EXTENSION
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EMAIL_FORMAT'
	AND LOOKUP_CODE = HzContactPointsE0.EMAIL_FORMAT
	) RES_E_MAIL_FORMAT
,HzContactPointsE0.EMAIL_ADDRESS RES_E_MAIL
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'HZ_INSTANT_MESSENGER_TYPE'
	AND LOOKUP_CODE = HzContactPointsE0.INSTANT_MESSENGER_TYPE
	) RES_SOCIAL_NETWORK
,HzContactPointsE0.INSTANT_MESSAGING_ADDRESS RES_USER_ID
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'HZ_URL_TYPES'
	AND LOOKUP_CODE = HzContactPointsE0.WEB_TYPE
	) RES_PROTOCOL
,HzContactPointsE0.URL RES_URL
,TO_CHAR(HzContactPointsE0.START_DATE,'DD-Mon-YYYY') RES_FROM_DATE
,DECODE(TO_CHAR(HzContactPointsE0.END_DATE,'DD-Mon-YYYY'),'31-12-4712',NULL,TO_CHAR(HzContactPointsE0.END_DATE,'DD-Mon-YYYY')) RES_TO_DATE
,HzContactPointsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,HzContactPointsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,HzContactPointsE0.CREATED_BY  RSC_CREATED_BY
,HzContactPointsE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,buE0.BU_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID
   
FROM HZ_PARTIES HzPartiesE0
,(SELECT PARTY_ID 
	FROM HZ_PARTY_USG_ASSIGNMENTS
	WHERE PARTY_USAGE_CODE = 'CUSTOMER'
	) HzPartyUsgAssignmentsE0
,HZ_CUST_ACCOUNTS HzCustAccountsE0
,HZ_CUST_ACCT_SITES_ALL HzCustAcctSitesAllE0
,HZ_PARTY_SITES HzPartySiteE0
,HZ_CUST_ACCOUNT_ROLES HzCustAccountRolesE0
,HZ_RELATIONSHIPS HzRelationshipE0
,HZ_ORG_CONTACTS HzOrgContactsE0
,HZ_PARTIES ContactDetailE0
,(SELECT * FROM HZ_CONTACT_POINTS
	WHERE STATUS = 'A'
	) HzContactPointsE0
,FUN_ALL_BUSINESS_UNITS_V buE0
WHERE HzPartiesE0.PARTY_ID = HzPartyUsgAssignmentsE0.PARTY_ID
AND HzPartiesE0.PARTY_ID = HzCustAccountsE0.PARTY_ID
AND HzCustAccountsE0.CUST_ACCOUNT_ID = HzCustAcctSitesAllE0.CUST_ACCOUNT_ID
AND HzCustAcctSitesAllE0.PARTY_SITE_ID = HzPartySiteE0.PARTY_SITE_ID
AND HzCustAccountsE0.CUST_ACCOUNT_ID = HzCustAccountRolesE0.CUST_ACCOUNT_ID
AND HzCustAccountRolesE0.RELATIONSHIP_ID = HzRelationshipE0.RELATIONSHIP_ID
AND HzCustAccountRolesE0.CUST_ACCT_SITE_ID = HzCustAcctSitesAllE0.CUST_ACCT_SITE_ID
AND HzRelationshipE0.DIRECTIONAL_FLAG = 'B'
AND HzRelationshipE0.RELATIONSHIP_ID = HzOrgContactsE0.PARTY_RELATIONSHIP_ID
AND HzRelationshipE0.OBJECT_ID = ContactDetailE0.PARTY_ID
AND HzRelationshipE0.RELATIONSHIP_ID = HzContactPointsE0.RELATIONSHIP_ID
AND HzCustAcctSitesAllE0.SET_ID = buE0.DEFAULT_SET_ID(+)
ORDER BY HzPartiesE0.PARTY_NUMBER
,HzCustAccountsE0.ACCOUNT_NUMBER
,HzPartySiteE0.PARTY_SITE_NUMBER
,ContactDetailE0.PERSON_FIRST_NAME