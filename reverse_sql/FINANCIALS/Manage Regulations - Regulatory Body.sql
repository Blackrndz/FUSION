SELECT benREGNEO.NAME RES_NAME
,(SELECT NAME FROM HR_ORGANIZATION_UNITS WHERE ORGANIZATION_ID = benREGNFORREGYEO.ORGANIZATION_ID 
	AND SYSDATE BETWEEN DATE_FROM AND NVL(DATE_TO,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_REGULATORY_BODY
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benREGNFORREGYEO.REGN_ADMIN_CD 
	AND LANGUAGE = USERENV('LANG') AND LOOKUP_TYPE = 'BEN_REGN_ADMN'
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_ADMINISTRATIVE_TYPE
,TO_CHAR(benREGNFORREGYEO.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,TO_CHAR(benREGNFORREGYEO.EFFECTIVE_END_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_END_DATE

,benREGNFORREGYEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,benREGNFORREGYEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,benREGNFORREGYEO.CREATED_BY RSC_CREATED_BY
,benREGNFORREGYEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_REGN_F benREGNEO
,BEN_REGN_FOR_REGY_BODY_F benREGNFORREGYEO
WHERE benREGNEO.REGN_ID = benREGNFORREGYEO.REGN_ID
AND SYSDATE BETWEEN benREGNEO.EFFECTIVE_START_DATE AND NVL(benREGNEO.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))
AND SYSDATE BETWEEN benREGNFORREGYEO.EFFECTIVE_START_DATE AND NVL(benREGNFORREGYEO.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))
ORDER BY benREGNEO.NAME,2

