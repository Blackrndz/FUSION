SELECT (SELECT APPLICATION_NAME
	FROM XLA_SUBLEDGERS_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND APPLICATION_ID = JELineRulesE0.APPLICATION_ID
	) RES_SUBLEDGER_APPLICATION
,JELineRulesE0.NAME RES_NAME
,JELineRulesE0.ACCOUNTING_LINE_CODE RES_SHORT_NAME
,JELineRulesE0.DESCRIPTION RES_DESCRIPTION
,(SELECT NAME
	FROM XLA_EVENT_CLASSES_TL
	WHERE ENTITY_CODE = JELineRulesE0.ENTITY_CODE
	AND EVENT_CLASS_CODE = JELineRulesE0.EVENT_CLASS_CODE
	AND APPLICATION_ID = JELineRulesE0.APPLICATION_ID
	AND LANGUAGE = USERENV('LANG')
	) RES_EVENT_CLASS
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'JA_CN_DEBIT_CREDIT'
	AND LOOKUP_CODE = JELineRulesE0.NATURAL_SIDE_CODE
	) RES_SIDE
,(SELECT MEANING
	FROM XLA_LOOKUPS
	WHERE LOOKUP_TYPE = 'XLA_ACCOUNTING_CLASS'
	AND LOOKUP_CODE = JELineRulesE0.ACCOUNTING_CLASS_CODE
	) RES_ACCOUNTING_CLASS
,DECODE(JELineRulesE0.SWITCH_SIDE_FLAG,'Y','Yes','No') RES_SWITCH_DEBIT_AND_CREDIT
,(SELECT MEANING
	FROM XLA_LOOKUPS
	WHERE LOOKUP_TYPE = 'XLA_MERGE_MATCHING_TYPE'
	AND LOOKUP_CODE = JELineRulesE0.MERGE_DUPLICATE_CODE
	) RES_MERGE_MATCHING_LINES
,DECODE(JELineRulesE0.ENABLED_FLAG,'Y','Active','Inactive') RES_STATUS
,JELineRulesE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,JELineRulesE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,JELineRulesE0.CREATED_BY RSC_CREATED_BY
,JELineRulesE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM XLA_ACCT_LINE_TYPES_VL JELineRulesE0
ORDER BY 1,2