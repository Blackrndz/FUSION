SELECT (CASE WHEN QpMatrixDimensionsE0.DYNAMIC_MATRIX_DIMENSION_NAME IN ('Pricing Segment','Precedence')
		THEN QpMatrixDimensionsE0.DYNAMIC_MATRIX_DIMENSION_NAME
		ELSE QpMatrixDimensionsE0.DYNAMIC_MATRIX_DIMENSION_NAME||' (=)' END)RES_MATRIX_NAME
,'Yes' RES_MATRIX_VALUE
,QpMatrixInteritedDimensionsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,QpMatrixInteritedDimensionsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,QpMatrixInteritedDimensionsE0.CREATED_BY RSC_CREATED_BY
,QpMatrixInteritedDimensionsE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM QP_MATRIX_DIMENSIONS_VL QpMatrixDimensionsE0
,QP_MATRIX_INHERITED_DIMENSIONS QpMatrixInteritedDimensionsE0
WHERE QpMatrixDimensionsE0.DYNAMIC_MATRIX_DIMENSION_ID = QpMatrixInteritedDimensionsE0.DYNAMIC_MATRIX_DIMENSION_ID
AND QpMatrixInteritedDimensionsE0.DYNAMIC_MATRIX_ID IN 
	(SELECT DYNAMIC_MATRIX_ID 
		FROM QP_MATRICES 
		WHERE DYNAMIC_MATRIX_CLASS_ID IN 
		(SELECT DYNAMIC_MATRIX_CLASS_ID
			FROM QP_MATRIX_CLASSES_TL
			WHERE DYNAMIC_MATRIX_CLASS_NAME = 'Pricing Segment'
			AND LANGUAGE = USERENV('LANG'))
	)
