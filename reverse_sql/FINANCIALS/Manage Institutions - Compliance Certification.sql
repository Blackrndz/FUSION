-- REPLACE_HTML_KEYWORD = RES_COMMENTS

 
SELECT gmsINSTITUE0.INSTITUTION_NAME RES_NAME
,gmsCERTE0.CERTIFICATION_NAME RES_CERTIFICATION_NAME
,gmsCERTE0.DESCRIPTION  RES_DESCRIPTION
,(SELECT partiesE0.PARTY_NAME
	FROM GMS_SPONSORS_VL sponsorsE0,HZ_PARTIES partiesE0
	WHERE sponsorsE0.PARTY_ID = partiesE0.PARTY_ID
    AND sponsorsE0.SPONSOR_ID = gmsINSTITUCERTE0.SPONSOR_ID
	) RES_SPONSOR
,gmsINSTITUCERTE0.STATUS RES_STATUS
,TO_CHAR(gmsINSTITUCERTE0.CERTIFICATION_DATE,'DD-Mon-YYYY') RES_CERTIFICATION_DATE
,TO_CHAR(gmsINSTITUCERTE0.EXPIRATION_DATE,'DD-Mon-YYYY') RES_EXPIRATION_DATE
,gmsINSTITUCERTE0.REFERENCE_NUMBER RES_REFERENCE_NUMBER
,REPLACE(REPLACE(REPLACE(gmsINSTITUCERTE0.COMMENTS,'&','#AMP#'),'<','#OPENTAG#'),'>','#CLOSETAG#') RES_COMMENTS

,gmsINSTITUCERTE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,gmsINSTITUCERTE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,gmsINSTITUCERTE0.CREATED_BY  RSC_CREATED_BY
,gmsINSTITUCERTE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM GMS_INSTITUTIONS_VL gmsINSTITUE0
,GMS_INSTITUTION_CERTS_VL gmsINSTITUCERTE0
,GMS_CERTIFICATIONS_TL gmsCERTE0
WHERE gmsINSTITUE0.INSTITUTION_ID = gmsINSTITUCERTE0.INSTITUTION_ID
AND gmsINSTITUCERTE0.CERTIFICATION_ID = gmsCERTE0.CERTIFICATION_ID
AND gmsCERTE0.LANGUAGE = USERENV('LANG')
ORDER BY gmsINSTITUE0.INSTITUTION_NAME
,gmsCERTE0.CERTIFICATION_NAME
