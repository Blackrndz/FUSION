SELECT benELIGYEO.NAME RES_NAME
,benELIGPOSITIONEO.ORDR_NUM RES_SEQUENCE
,hrALLPOSITIONEO.NAME RES_POSITION
,(SELECT NAME FROM HR_ALL_ORGANIZATION_UNITS_F_VL WHERE hrALLPOSITIONEO.BUSINESS_UNIT_ID = ORGANIZATION_ID
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_BUSINESS_UNIT
,DECODE(benELIGPOSITIONEO.EXCLD_FLAG,'Y','Yes','No') RES_EXCLUDE
,TO_CHAR(benELIGPOSITIONEO.START_DATE,'DD-Mon-YYYY') RES_START_DATE
,TO_CHAR(benELIGPOSITIONEO.END_DATE,'DD-Mon-YYYY') RES_END_DATE

,benELIGPOSITIONEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,benELIGPOSITIONEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,benELIGPOSITIONEO.CREATED_BY  RSC_CREATED_BY
,benELIGPOSITIONEO.CREATION_DATE  RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_ELIGY_PRFL benELIGYEO
,BEN_ELIG_PSTN_PRTE benELIGPOSITIONEO
,HR_ALL_POSITIONS_F_VL hrALLPOSITIONEO
WHERE benELIGYEO.ELIGY_PRFL_ID = benELIGPOSITIONEO.ELIGY_PRFL_ID 
AND benELIGPOSITIONEO.POSITION_ID =  hrALLPOSITIONEO.POSITION_ID
AND SYSDATE BETWEEN hrALLPOSITIONEO.EFFECTIVE_START_DATE AND NVL(hrALLPOSITIONEO.EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND benELIGYEO.END_DATE BETWEEN benELIGPOSITIONEO.START_DATE AND NVL(benELIGPOSITIONEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
AND SYSDATE BETWEEN benELIGYEO.START_DATE AND NVL(benELIGYEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY benELIGYEO.NAME,benELIGPOSITIONEO.ORDR_NUM