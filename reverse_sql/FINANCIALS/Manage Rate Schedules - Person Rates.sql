SELECT rateSchedulesE0.RATE_SCHEDULE_NAME RES_NAME
,TO_CHAR(rateScheduleLinesE0.START_DATE_ACTIVE,'DD-Mon-YYYY') RES_FROM_DATE
,TO_CHAR(rateScheduleLinesE0.END_DATE_ACTIVE,'DD-Mon-YYYY') RES_TO_DATE
,personNamesE0.FULL_NAME RES_PERSON_NAME
,(SELECT EMAIL_ADDRESS FROM PER_EMAIL_ADDRESSES WHERE PERSON_ID = rateScheduleLinesE0.PERSON_ID
	AND SYSDATE BETWEEN DATE_FROM AND DATE_TO) RES_PERSON_EMAIL
,(SELECT PERSON_NUMBER 
	FROM PER_ALL_PEOPLE_F 
	WHERE PERSON_ID = rateScheduleLinesE0.PERSON_ID
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
	) RES_PERSON_NUMBER
,(SELECT NAME
	FROM PER_JOBS_F_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND JOB_ID = rateScheduleLinesE0.PERSON_JOB_ID
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
	) RES_PERSON_ASSIGNMENT_JOB_NAME
,(SELECT NAME FROM HR_ORGANIZATION_V WHERE ORGANIZATION_ID = rateScheduleLinesE0.PERSON_ORG_ID 
	AND ROWNUM = 1
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_PERSON_ASSIGNMENT_JOB_DEPA
,(SELECT NAME
	FROM PER_JOBS_F_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND JOB_ID = rateScheduleLinesE0.PERSON_JOB_ID
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
	) RES_PERSON_ASSIGNMENT_JOB_ASSI
,(SELECT NAME 
	FROM HR_ORGANIZATION_UNITS 
	WHERE ORGANIZATION_ID = rateScheduleLinesE0.PERSON_ORG_ID 
	) RES_PERSON_ASSIGNMENT_ORGANIZA
,(CASE
	WHEN rateScheduleLinesE0.RATE_UNIT = 'HOURS' THEN
		'Hours'
	WHEN rateScheduleLinesE0.RATE_UNIT = 'DOLLARS' THEN
		'Currency'
	ELSE
		(SELECT UNIT_OF_MEASURE 
		FROM INV_UNITS_OF_MEASURE_VL
		WHERE UOM_CODE = rateScheduleLinesE0.RATE_UNIT)
	END) RES_UNIT_OF_MEASURE
,rateScheduleLinesE0.RATE RES_RATE
,rateScheduleLinesE0.MARKUP_PERCENTAGE RES_MARKUP_PERCENT

,rateScheduleLinesE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,rateScheduleLinesE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,rateScheduleLinesE0.CREATED_BY RSC_CREATED_BY
,rateScheduleLinesE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJF_RATE_SCHEDULES_VL rateSchedulesE0
,PJF_RATE_SCHEDULE_LINES rateScheduleLinesE0
,PER_PERSON_NAMES_F personNamesE0
WHERE rateSchedulesE0.SCHEDULE_TYPE = 'EMPLOYEE'
AND rateSchedulesE0.RATE_SCHEDULE_ID = rateScheduleLinesE0.RATE_SCHEDULE_ID
AND rateScheduleLinesE0.PERSON_ID = personNamesE0.PERSON_ID
AND personNamesE0.NAME_TYPE = 'GLOBAL'
ORDER BY rateSchedulesE0.RATE_SCHEDULE_NAME
,personNamesE0.FULL_NAME