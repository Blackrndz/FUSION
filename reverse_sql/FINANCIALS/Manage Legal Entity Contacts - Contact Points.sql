--RSC_PREREQUISITE_OBJECTS=HZ_CONTACT_POINTS 
 
SELECT xleENTITYPROFILES.NAME RES_LEGAL_ENTITY_NAME
,xleENTITYPROFILES.LEGAL_ENTITY_IDENTIFIER RES_LEGAL_ENTITY_IDENTIFIER
,hzPATIESE0.PARTY_NAME RES_CONTACT_NAME
,hzORGCONTACTE0.CONTACT_NUMBER RES_CONTACT_NUMBER
,DECODE(hzcontactPOINTE0.PRIMARY_FLAG,'Y','Yes','No') RES_PRIMARY
,DECODE(hzcontactPOINTE0.PRIMARY_BY_PURPOSE,'Y','Yes','No') RES_PRIMARY_BY_PURPOSE
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'COMMUNICATION_TYPE'
	AND LOOKUP_CODE = hzcontactPOINTE0.CONTACT_POINT_TYPE
	) RES_CONTACT_POINT_TYPE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = hzcontactPOINTE0.PHONE_LINE_TYPE AND LANGUAGE = USERENV('LANG')
		AND LOOKUP_TYPE = 'PHONE_LINE_TYPE' AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_TYPE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = hzcontactPOINTE0.PHONE_TYPE AND LANGUAGE = USERENV('LANG')
		AND LOOKUP_TYPE = 'ORA_HZ_PHONE_TYPE' AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_PURPOSE
,(CASE WHEN hzcontactPOINTE0.PHONE_AREA_CODE IS NULL
	THEN '+'||hzcontactPOINTE0.PHONE_COUNTRY_CODE||' '||hzcontactPOINTE0.PHONE_NUMBER||' x'||hzcontactPOINTE0.PHONE_EXTENSION
	WHEN hzcontactPOINTE0.PHONE_EXTENSION IS NULL
	THEN '+'||hzcontactPOINTE0.PHONE_COUNTRY_CODE||' ('||hzcontactPOINTE0.PHONE_AREA_CODE||')'||hzcontactPOINTE0.PHONE_NUMBER
	WHEN hzcontactPOINTE0.PHONE_AREA_CODE IS NULL AND hzcontactPOINTE0.PHONE_EXTENSION IS NULL
	THEN '+'||hzcontactPOINTE0.PHONE_COUNTRY_CODE||' '||hzcontactPOINTE0.PHONE_NUMBER 
	ELSE '+'||hzcontactPOINTE0.PHONE_COUNTRY_CODE||' ('||hzcontactPOINTE0.PHONE_AREA_CODE||')'||hzcontactPOINTE0.PHONE_NUMBER||' x'||hzcontactPOINTE0.PHONE_EXTENSION
	END) RES_VALUE
,hzcontactPOINTE0.PHONE_COUNTRY_CODE RES_PHONE_COUNTRY_CODE
,hzcontactPOINTE0.PHONE_AREA_CODE RES_AREA_CODE
,hzcontactPOINTE0.PHONE_NUMBER RES_PHONE
,hzcontactPOINTE0.PHONE_EXTENSION RES_EXTENSION
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'EMAIL_FORMAT'
	AND LOOKUP_CODE = hzcontactPOINTE0.EMAIL_FORMAT
	) RES_E_MAIL_FORMAT
,hzcontactPOINTE0.EMAIL_ADDRESS RES_E_MAIL
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'HZ_INSTANT_MESSENGER_TYPE'
	AND LOOKUP_CODE = hzcontactPOINTE0.INSTANT_MESSENGER_TYPE
	) RES_SOCIAL_NETWORK
,hzcontactPOINTE0.INSTANT_MESSAGING_ADDRESS RES_USER_ID
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'HZ_URL_TYPES'
	AND LOOKUP_CODE = hzcontactPOINTE0.WEB_TYPE
	) RES_PROTOCOL
,hzcontactPOINTE0.URL RES_URL
,TO_CHAR(hzcontactPOINTE0.START_DATE,'DD-Mon-YYYY') RES_FROM_DATE
,DECODE(TO_CHAR(hzcontactPOINTE0.END_DATE,'DD-Mon-YYYY'),'31-12-4712',NULL,TO_CHAR(hzcontactPOINTE0.END_DATE,'DD-Mon-YYYY')) RES_TO_DATE

,hzcontactPOINTE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hzcontactPOINTE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hzcontactPOINTE0.CREATED_BY RSC_CREATED_BY
,hzcontactPOINTE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM FUSION.XLE_ENTITY_PROFILES xleENTITYPROFILES 
,HZ_RELATIONSHIPS hzRELATIONE0 
,HZ_ORG_CONTACTS hzORGCONTACTE0
,HZ_PARTIES hzPATIESE0
,HZ_CONTACT_POINTS hzcontactPOINTE0
WHERE xleENTITYPROFILES.PARTY_ID = hzRELATIONE0.OBJECT_ID
AND hzRELATIONE0.SUBJECT_ID = hzPATIESE0.PARTY_ID
AND hzRELATIONE0.RELATIONSHIP_ID = hzORGCONTACTE0.PARTY_RELATIONSHIP_ID
AND hzPATIESE0.PARTY_ID = hzcontactPOINTE0.OWNER_TABLE_ID
ORDER BY xleENTITYPROFILES.NAME,hzPATIESE0.PARTY_NAME