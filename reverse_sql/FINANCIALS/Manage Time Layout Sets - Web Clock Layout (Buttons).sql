-- RSC_PREREQUISITE_OBJECTS=HXT_TCLAY_V

SELECT RES_LAYOUT_NAME
,RES_ORDER
,RES_WEB_CLOCK_LAYOUT_COMPONENT
,RES_BUTTON
,RES_LABEL
,RES_ICON
,RES_EVENT

,RSC_LAST_UPDATED_BY
,RSC_LAST_UPDATE_DATE
,RSC_CREATED_BY
,RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM 
(SELECT DISTINCT hxtTCLAYEO.NAME RES_LAYOUT_NAME
,hxtTCLATDEFNSVEO.DISPLAY_SEQUENCE RES_ORDER
,hxtTCLATDEFNSVEO.NAME RES_WEB_CLOCK_LAYOUT_COMPONENT
,hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CHAR23 RES_BUTTON
,hxtTCLATDEFNSVEO.LABEL RES_LABEL
,DECODE(hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CHAR20,'HXT_BREAK_ICON','Coffee Break','HXT_CLOCK_ICON','Clock Time','HXT_CLOCK_IN_ICON'
,'Clock Time In','HXT_CLOCK_OUT_ICON','Clock Time Out','HXT_MEAL_ICON','Lunch','HXT_TRANSFER_ICON','Exchange') RES_ICON
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = hwnALTNAME0.WFM_EVENT_IN_OUT 
	AND LOOKUP_TYPE = 'ORA_HWM_TCD_EVENTS' AND LANGUAGE = USERENV('LANG')) RES_EVENT

,hxtTCLATDEFNSVEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hxtTCLATDEFNSVEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hxtTCLATDEFNSVEO.CREATED_BY RSC_CREATED_BY
,hxtTCLATDEFNSVEO.CREATION_DATE RSC_CREATION_DATE

FROM HXT_TCLAYST_VL hxtTCLAYEO   
,HXT_TCLAY_B hxtTCLATVEO
,HXT_TCLAYFLD_DEFNS_VL hxtTCLATDEFNSVEO
,HWM_TE_ALT_NAME_VALS_VL hwnALTNAME0

WHERE hxtTCLAYEO.TCLAYST_ID =  hxtTCLATVEO.TCLAYST_ID
AND hxtTCLATVEO.TCLAY_ID = hxtTCLATDEFNSVEO.TCLAY_ID
AND hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CHAR26 = 'WEBCLOCKBUTTONS'
AND hxtTCLATVEO.TCLAY_TYPE LIKE 'Web Clock%'
AND hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_CATEGORY = 'RAW_INSTANCE'
AND hxtTCLATDEFNSVEO.TCLAYFLD_ATTRIBUTE_NUMBER1 = hwnALTNAME0.TE_ALT_NAME_ID 
)

ORDER BY RES_LAYOUT_NAME,RES_ORDER


