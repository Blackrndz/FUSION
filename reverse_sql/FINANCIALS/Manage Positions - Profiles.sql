SELECT TO_CHAR(hrALLPOSTIONEO.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,(SELECT BU_NAME
	FROM FUN_ALL_BUSINESS_UNITS_V
	WHERE BU_ID = hrALLPOSTIONEO.BUSINESS_UNIT_ID 
	) RES_BUSINESS_UNIT
,hrALLPOSTIONEO.NAME RES_NAME
,hrALLPOSTIONEO.POSITION_CODE RES_CODE
,hrPROFILEREO.DESCRIPTION RES_PROFILE_NAME
,hrPROFILEREO.PROFILE_CODE RES_PROFILE_CODE
,hrPROFILEREO.SUMMARY RES_PROFILE_DESCRIPTION
,TO_CHAR(hrPROFILERELAEO.OBJECT_EFF_START_DATE,'DD-Mon-YYYY') RES_PROFILE_EFFECTIVE_START_DA
,TO_CHAR(hrPROFILERELAEO.OBJECT_EFF_END_DATE,'DD-Mon-YYYY') RES_PROFILE_EFFECTIVE_END_DATE
,hrPROFILERELAEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hrPROFILERELAEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hrPROFILERELAEO.CREATED_BY RSC_CREATED_BY
,hrPROFILERELAEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,hrALLPOSTIONEO.BUSINESS_UNIT_ID RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM HR_ALL_POSITIONS_F_VL hrALLPOSTIONEO
,HRT_PROFILE_RELATIONS hrPROFILERELAEO	
,HRT_PROFILES_VL hrPROFILEREO
WHERE hrALLPOSTIONEO.POSITION_ID = hrPROFILERELAEO.OBJECT_ID
AND hrPROFILEREO.PROFILE_ID = hrPROFILERELAEO.PROFILE_ID
ORDER BY hrALLPOSTIONEO.NAME
,hrALLPOSTIONEO.POSITION_CODE
,hrALLPOSTIONEO.EFFECTIVE_START_DATE
,hrPROFILERELAEO.OBJECT_EFF_START_DATE 