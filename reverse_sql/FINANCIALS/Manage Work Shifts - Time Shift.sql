SELECT shiftsE0.SHIFT_NAME RES_NAME
,shiftsE0.SHIFT_DESC RES_DESCRIPTION
,shiftsE0.SHORT_TXT RES_CODE
,TO_CHAR(TIMESTAMP '1970-01-01 00:00:00.000' + NUMTODSINTERVAL(shiftsE0.START_TIME_MS_NUM/ 1000, 'SECOND' ),'HH24:MI:SS') RES_START_TIME
,(CASE
	WHEN shiftsE0.DURATION_UOM_CODE = 'MINUTES' THEN
		shiftsE0.DURATION_MS_NUM/1000/60 
	WHEN shiftsE0.DURATION_UOM_CODE = 'HOURS' THEN
		shiftsE0.DURATION_MS_NUM/1000/60/60
	WHEN shiftsE0.DURATION_UOM_CODE = 'DAYS' THEN
		shiftsE0.DURATION_MS_NUM/1000/60/60/24
	WHEN shiftsE0.DURATION_UOM_CODE = 'WEEKS' THEN
		shiftsE0.DURATION_MS_NUM/1000/60/60/24/7
	END) RES_DURATION_VALUE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ZMM_SR_DURATION_UOM'
    AND LOOKUP_CODE = shiftsE0.DURATION_UOM_CODE
    ) RES_DURATION_UNIT
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ZMM_SR_SHIFT_CATEGORY'
    AND LOOKUP_CODE = shiftsE0.CATEGORY_CODE
    ) RES_CATEGORY
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ZMM_SR_SHIFT_DETAIL_TYPE'
    AND LOOKUP_CODE = shiftsE0.SHIFT_DTL_TYPE_CODE 
	) RES_SHIFT_DETAIL_TYPE
,shiftsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,shiftsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,shiftsE0.CREATED_BY RSC_CREATED_BY
,shiftsE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM ZMM_SR_SHIFTS_VL shiftsE0
WHERE shiftsE0.SHIFT_TYPE_CODE = 'TIME'
ORDER BY shiftsE0.SHIFT_NAME