--RSC_PREREQUISITE_OBJECTS=WLF_RESOURCES_TL
--RSC_PREREQUISITE_OBJECTS=WLF_RESOURCES_B

SELECT wlfRESOURCTLE0.NAME RES_TITLE
,wlfRESOURCTLE0.DESCRIPTION RES_DESCRIPTION
,wlfRESOURCE0.RESOURCE_NUMBER RES_RESOURCE_NUMBER
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = wlfRESOURCE0.RESOURCE_TYPE 
	AND LOOKUP_TYPE = 'ORA_WLF_RESOURCE_TYPE' AND LANGUAGE = USERENV('LANG'))RES_RESOURCE_TYPE
,(SELECT DISPLAY_NAME FROM PER_PERSON_NAMES_F WHERE PERSON_ID = wlfRESOURCE0.CONTACT_ID 
	AND NAME_TYPE = 'GLOBAL') RES_CONTACT
,NULL RES_RELATED_MATERIALS

,wlfRESOURCE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,wlfRESOURCE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,wlfRESOURCE0.CREATED_BY  RSC_CREATED_BY
,wlfRESOURCE0.CREATION_DATE  RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM WLF_RESOURCES_B wlfRESOURCE0
,WLF_RESOURCES_TL wlfRESOURCTLE0
WHERE wlfRESOURCE0.RESOURCE_ID = wlfRESOURCTLE0.RESOURCE_ID
AND wlfRESOURCE0.RESOURCE_TYPE = 'ORA_TRNG_VENDOR'
AND wlfRESOURCTLE0.LANGUAGE = USERENV('LANG')
