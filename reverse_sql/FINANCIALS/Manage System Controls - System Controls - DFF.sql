-- DFF_LOGIC_TO_APPLY=140##RES##FA_SYSTEM_CONTROLS
-- DFF_ATTRIBUTES=ATTRIBUTE

-- IMPORTANT:
-- - the alias names for the Foreign keys must be the same as in the inventory.
-- - the alias name for context code must be RES_CONTEXT_CODE
-- - NO ALIASES for atributes fields.

SELECT QRSLT.COMPANY_NAME RES_ENTERPRISE_NAME
,QRSLT.ATTRIBUTE_CATEGORY_CODE RES_CONTEXT_CODE
,QRSLT.ATTRIBUTE1
,QRSLT.ATTRIBUTE2
,QRSLT.ATTRIBUTE3
,QRSLT.ATTRIBUTE4
,QRSLT.ATTRIBUTE5
,QRSLT.ATTRIBUTE6
,QRSLT.ATTRIBUTE7
,QRSLT.ATTRIBUTE8
,QRSLT.ATTRIBUTE9
,QRSLT.ATTRIBUTE10
,QRSLT.ATTRIBUTE11
,QRSLT.ATTRIBUTE12
,QRSLT.ATTRIBUTE13
,QRSLT.ATTRIBUTE14
,QRSLT.ATTRIBUTE15
,QRSLT.LAST_UPDATED_BY  AS RSC_LAST_UPDATED_BY
,QRSLT.LAST_UPDATE_DATE AS RSC_LAST_UPDATE_DATE
,QRSLT.CREATED_BY RSC_CREATED_BY
,QRSLT.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM
	(SELECT SystemControlEO.COMPANY_NAME
	,SystemControlEO.DATE_PLACED_IN_SERVICE
	,SystemControlEO.CATEGORY_FLEX_STRUCTURE
	,SystemControlEO.LOCATION_FLEX_STRUCTURE
	,SystemControlEO.ASSET_KEY_FLEX_STRUCTURE
	,SystemControlEO.INITIAL_ASSET_ID
	,(SELECT MAX(asset_id)
		FROM FA_ADDITIONS_B
		) AS TRANS_LAST_ASSET_ID
	,SystemControlEO.CREATED_BY
	,SystemControlEO.CREATION_DATE
	,SystemControlEO.LAST_UPDATE_DATE
	,SystemControlEO.LAST_UPDATE_LOGIN
	,SystemControlEO.LAST_UPDATED_BY
	,SystemControlEO.ATTRIBUTE1
	,SystemControlEO.ATTRIBUTE2
	,SystemControlEO.ATTRIBUTE3
	,SystemControlEO.ATTRIBUTE4
	,SystemControlEO.ATTRIBUTE5
	,SystemControlEO.ATTRIBUTE6
	,SystemControlEO.ATTRIBUTE7
	,SystemControlEO.ATTRIBUTE8
	,SystemControlEO.ATTRIBUTE9
	,SystemControlEO.ATTRIBUTE10
	,SystemControlEO.ATTRIBUTE11
	,SystemControlEO.ATTRIBUTE12
	,SystemControlEO.ATTRIBUTE13
	,SystemControlEO.ATTRIBUTE14
	,SystemControlEO.ATTRIBUTE15
	,SystemControlEO.ATTRIBUTE_CATEGORY_CODE
	,SystemControlEO.USE_CUSTOM_ASSET_NUMBERS_FLAG
	,SystemControlEO.OBJECT_VERSION_NUMBER
	FROM FA_SYSTEM_CONTROLS SYSTEMCONTROLEO
	) QRSLT
WHERE (QRSLT.ATTRIBUTE_CATEGORY_CODE IS NOT NULL OR
QRSLT.ATTRIBUTE1 IS NOT NULL OR 
QRSLT.ATTRIBUTE2 IS NOT NULL OR 
QRSLT.ATTRIBUTE3 IS NOT NULL OR 
QRSLT.ATTRIBUTE4 IS NOT NULL OR 
QRSLT.ATTRIBUTE5 IS NOT NULL OR 
QRSLT.ATTRIBUTE6 IS NOT NULL OR 
QRSLT.ATTRIBUTE7 IS NOT NULL OR 
QRSLT.ATTRIBUTE8 IS NOT NULL OR 
QRSLT.ATTRIBUTE9 IS NOT NULL OR 
QRSLT.ATTRIBUTE10 IS NOT NULL OR 
QRSLT.ATTRIBUTE11 IS NOT NULL OR 
QRSLT.ATTRIBUTE12 IS NOT NULL OR 
QRSLT.ATTRIBUTE13 IS NOT NULL OR 
QRSLT.ATTRIBUTE14 IS NOT NULL OR 
QRSLT.ATTRIBUTE15 IS NOT NULL)
ORDER BY QRSLT.COMPANY_NAME