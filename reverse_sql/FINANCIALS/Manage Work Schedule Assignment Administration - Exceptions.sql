SELECT schedulesE0.SCHEDULE_NAME RES_NAME
,TO_CHAR(assignmentsE0.START_DATE,'DD-Mon-YYYY') RES_ASSIGNMENT_START_DATE
,TO_CHAR(assignmentsE0.END_DATE,'DD-Mon-YYYY') RES_ASSIGNMENT_END_DATE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'PER_AVL_RESOURCE_TYPES'
    AND LOOKUP_CODE = assignmentsE0.RESOURCE_TYPE
    ) RES_ASSIGNMENT_RESOURCE_TYPE
,(CASE
    WHEN assignmentsE0.RESOURCE_TYPE IN ('ASSIGN','EMPTERM') THEN
        (SELECT person.FULL_NAME
            FROM PER_ALL_ASSIGNMENTS_M assignment
			,PER_PERSON_NAMES_F person
            WHERE SYSDATE BETWEEN assignment.EFFECTIVE_START_DATE AND assignment.EFFECTIVE_END_DATE
			AND SYSDATE BETWEEN person.EFFECTIVE_START_DATE AND person.EFFECTIVE_END_DATE
			AND person.NAME_TYPE = 'GLOBAL'          
			AND person.PERSON_ID = assignment.PERSON_ID  
			AND assignment.ASSIGNMENT_ID = assignmentsE0.RESOURCE_ID)
    WHEN assignmentsE0.RESOURCE_TYPE IN ('DEP','ENT','LEGALEMP') THEN
        (SELECT NAME
            FROM HR_ORGANIZATION_UNITS
            WHERE SYSDATE BETWEEN DATE_FROM AND DATE_TO
            AND ORGANIZATION_ID = assignmentsE0.RESOURCE_ID)
    WHEN assignmentsE0.RESOURCE_TYPE = 'JOB' THEN
        (SELECT NAME
            FROM PER_JOBS_F_TL
            WHERE LANGUAGE = USERENV('LANG')
            AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
            AND JOB_ID = assignmentsE0.RESOURCE_ID)
    WHEN assignmentsE0.RESOURCE_TYPE = 'LOC' THEN
        (SELECT LOCATION_NAME
            FROM PER_LOCATION_DETAILS_F_VL
            WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
            AND LOCATION_ID = assignmentsE0.RESOURCE_ID)
    WHEN assignmentsE0.RESOURCE_TYPE = 'POS' THEN
        (SELECT NAME
            FROM HR_ALL_POSITIONS_F_TL
            WHERE LANGUAGE = USERENV('LANG')
            AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
            AND POSITION_ID = assignmentsE0.RESOURCE_ID)
    END) RES_ASSIGNMENT_RESOURCE_NAME
,(CASE
    WHEN assignmentsE0.RESOURCE_TYPE = 'POS' THEN
        (SELECT POSITION_CODE
            FROM HR_ALL_POSITIONS_F
            WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
            AND POSITION_ID = assignmentsE0.RESOURCE_ID)
    WHEN assignmentsE0.RESOURCE_TYPE = 'JOB' THEN
		(SELECT JOB_CODE 
			FROM PER_JOBS_F
			WHERE JOB_ID = assignmentsE0.RESOURCE_ID
			AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE ,TO_DATE('31-12-4712','DD-MM-YYYY')))
	END) RES_ASSIGNMENT_RESOURCE_CODE
,(CASE
    WHEN assignmentsE0.RESOURCE_TYPE IN ('ASSIGN','EMPTERM') THEN
        (SELECT ASSIGNMENT_NUMBER
            FROM PER_ALL_ASSIGNMENTS_M
            WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
            AND ASSIGNMENT_ID = assignmentsE0.RESOURCE_ID)
	END) RES_ASSIGNMENT_RESOURCE_NUMBER
,(CASE
    WHEN assignmentsE0.RESOURCE_TYPE = 'POS' THEN 
		(SELECT BUS.BU_NAME 
			FROM HR_ALL_POSITIONS_F_VL POS
			,FUN_ALL_BUSINESS_UNITS_V BUS
			WHERE POS.BUSINESS_UNIT_ID = BUS.BU_ID
			AND POS.POSITION_ID = assignmentsE0.RESOURCE_ID
			AND SYSDATE BETWEEN POS.EFFECTIVE_START_DATE AND NVL(POS.EFFECTIVE_END_DATE ,TO_DATE('31-12-4712','DD-MM-YYYY')))
	END) RES_ASSIGNMENT_RESOURCE_BUSINE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'PER_SHDL_EXCEPTION_TYPES'
    AND LOOKUP_CODE = exceptionsE0.EXCEPTION_TYPE
    ) RES_EXCEPTION_TYPE
,(CASE
    WHEN exceptionsE0.EXCEPTION_TYPE = 'EVENTCATEGORY' THEN
        (SELECT MEANING
            FROM FND_LOOKUP_VALUES
            WHERE LANGUAGE = USERENV('LANG')
            AND LOOKUP_TYPE = 'PER_CAL_EVENT_CATEGORY'
            AND LOOKUP_CODE = exceptionsE0.EXCEPTION_CODE)
    WHEN exceptionsE0.EXCEPTION_TYPE = 'RESEXCEPTION' THEN
        (SELECT EXCEPTION_NAME
            FROM PER_RESOURCE_EXCEPTIONS
            WHERE EXCEPTION_ID = exceptionsE0.EXCEPTION_ID)
    WHEN exceptionsE0.EXCEPTION_TYPE = 'EVENT' THEN
        (SELECT NAME
            FROM PER_CALENDAR_EVENTS_TL
            WHERE LANGUAGE = USERENV('LANG')
            AND CALENDAR_EVENT_ID = exceptionsE0.EXCEPTION_ID)
    END) RES_EXCEPTION_NAME
,(CASE
    WHEN exceptionsE0.EXCEPTION_TYPE = 'EVENTCATEGORY' THEN
        NULL
    WHEN exceptionsE0.EXCEPTION_TYPE = 'RESEXCEPTION' THEN
        (SELECT TO_CHAR(START_DATE_TIME,'DD-Mon-YYYY')
            FROM PER_RESOURCE_EXCEPTIONS
            WHERE EXCEPTION_ID = exceptionsE0.EXCEPTION_ID)
    WHEN exceptionsE0.EXCEPTION_TYPE = 'EVENT' THEN
        (SELECT TO_CHAR(START_DATE_TIME,'DD-Mon-YYYY')
            FROM PER_CALENDAR_EVENTS_VL
            WHERE CALENDAR_EVENT_ID = exceptionsE0.EXCEPTION_ID)
    END) RES_START_DATE
,(CASE
    WHEN exceptionsE0.EXCEPTION_TYPE = 'EVENTCATEGORY' THEN
        NULL
    WHEN exceptionsE0.EXCEPTION_TYPE = 'RESEXCEPTION' THEN
        (SELECT TO_CHAR(END_DATE_TIME ,'DD-Mon-YYYY')
            FROM PER_RESOURCE_EXCEPTIONS
            WHERE EXCEPTION_ID = exceptionsE0.EXCEPTION_ID)
    WHEN exceptionsE0.EXCEPTION_TYPE = 'EVENT' THEN
        (SELECT TO_CHAR(END_DATE_TIME ,'DD-Mon-YYYY')
            FROM PER_CALENDAR_EVENTS_VL
            WHERE CALENDAR_EVENT_ID = exceptionsE0.EXCEPTION_ID)
    END) RES_END_DATE
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'ZMM_SR_AVAILABILITY'
    AND LOOKUP_CODE = exceptionsE0.AVAILABILITY_CODE
    ) RES_AVAILABILITY
,exceptionsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,exceptionsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,exceptionsE0.CREATED_BY RSC_CREATED_BY
,exceptionsE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM ZMM_SR_SCHEDULES_VL schedulesE0
,PER_SCHEDULE_ASSIGNMENTS assignmentsE0
,PER_SCHEDULE_EXCEPTIONS exceptionsE0
WHERE schedulesE0.SCHEDULE_ID = assignmentsE0.SCHEDULE_ID
AND assignmentsE0.SCHEDULE_ASSIGNMENT_ID = exceptionsE0.SCHEDULE_ASSIGNMENT_ID
ORDER BY schedulesE0.SCHEDULE_NAME