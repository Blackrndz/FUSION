-- RSC_PREREQUISITE_OBJECTS=XCC_CONTROL_BUDGETS

SELECT xccCONTROLBUDE0.NAME  RES_NAME
,xccCONTROLBUDE0.DESCRIPTION RES_DESCRIPTION
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = xccCONTROLBUDE0.STATUS_CODE
	AND LOOKUP_TYPE = 'XCC_CB_STATUS' 
	AND LANGUAGE = USERENV('LANG')
	) RES_STATUS
,(SELECT USER_PERIOD_SET_NAME 
	FROM GL_CALENDARS 
	WHERE PERIOD_SET_NAME = xccCONTROLBUDE0.PERIOD_SET_NAME 
	AND PERIOD_TYPE = xccCONTROLBUDE0.PERIOD_TYPE
	) RES_BUDGET_CALENDAR
,xccCONTROLBUDE0.START_PERIOD_NAME RES_FROM_PERIOD
,xccCONTROLBUDE0.END_PERIOD_NAME RES_TO_PERIOD
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = xccCONTROLBUDE0.SOURCE_BUDGET_SYSTEM_CODE
	AND LOOKUP_TYPE = 'XCC_CB_SRC_BUDGET_SYSTEM' 
	AND LANGUAGE = USERENV('LANG')
	) RES_SOURCE_BUDGET_TYPE
,(CASE
	WHEN xccCONTROLBUDE0.SOURCE_BUDGET_SYSTEM_CODE = 'XCC' THEN
		(SELECT NAME
			FROM XCC_CONTROL_BUDGETS
			WHERE CONTROL_BUDGET_ID = CAST(xccCONTROLBUDE0.SOURCE_BUDGET AS NUMBER))
	WHEN xccCONTROLBUDE0.SOURCE_BUDGET_SYSTEM_CODE = 'PPM' THEN
		(SELECT NAME
			FROM PJF_PROJECTS_ALL_TL
			WHERE LANGUAGE = USERENV('LANG')
			AND PROJECT_ID = CAST(xccCONTROLBUDE0.SOURCE_BUDGET AS NUMBER))
	ELSE 
		xccCONTROLBUDE0.PARENT_SOURCE_BUDGET
	END) RES_SOURCE_BUDGET_NAME
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = xccCONTROLBUDE0.FUNDS_RELEASE_TIMEFRAME
	AND LOOKUP_TYPE = 'ORA_XCC_CB_FUNDS_REL_TIMEFRAME' 
	AND LANGUAGE = USERENV('LANG')
	) RES_RELEASE_BUDGET_FOR_CONSUMP
,(SELECT NAME
    FROM FND_CURRENCIES_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND CURRENCY_CODE = xccCONTROLBUDE0.CURRENCY_CODE
    ) RES_CURRENCY
,(SELECT USER_CONVERSION_TYPE 
    FROM GL_DAILY_CONVERSION_TYPES
    WHERE CONVERSION_TYPE = xccCONTROLBUDE0.DEFAULT_RATE_TYPE
    ) RES_DEFAULT_RATE_TYPE
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = xccCONTROLBUDE0.CONTROL_LEVEL_CODE
	AND LOOKUP_TYPE = 'XCC_CB_CONTR_LVL' 
	AND LANGUAGE = USERENV('LANG')
	) RES_CONTROL_LEVEL
,xccCONTROLBUDE0.TOLERANCE_PERCENT RES_TOLERANCE_PERCENTAGE
,xccCONTROLBUDE0.TOLERANCE_AMOUNT RES_TOLERANCE_AMOUNT
,(SELECT NAME
	FROM GL_LEDGERS
	WHERE LEDGER_ID = xccCONTROLBUDE0.LEDGER_ID
	) RES_LEDGER
,(CASE
	WHEN xccCONTROLBUDE0.PROJECT_ID IN (-98,-99) THEN
		(SELECT MEANING 
			FROM FND_LOOKUP_VALUES 
			WHERE LOOKUP_CODE = DECODE(xccCONTROLBUDE0.PROJECT_ID,'-98','NO_ANY_PROJECT','-99','NO_PROJ_ASSGN')
			AND LOOKUP_TYPE = 'PJO_XCC_NO_PROJ_ASSGN' 
			AND LANGUAGE = USERENV('LANG'))
	ELSE
		(SELECT NAME
			FROM PJF_PROJECTS_ALL_TL
			WHERE LANGUAGE = USERENV('LANG')
			AND PROJECT_ID = xccCONTROLBUDE0.PROJECT_ID)
	END) RES_PROJECT
,(SELECT DISPLAY_NAME
    FROM PER_PERSON_NAMES_F
    WHERE NAME_TYPE = 'GLOBAL'
    AND PERSON_ID = (SELECT PERSON_ID FROM PER_USERS WHERE xccCONTROLBUDE0.BUDGET_MANAGER_ID = USER_ID)
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
    ) RES_BUDGET_MANAGER
,DECODE(xccCONTROLBUDE0.BALANCE_INCREASES_ALLOWED_FLAG,'Y','Yes','No') RES_ALLOW_BUDGET_INCREASE_ADJU
,DECODE(xccCONTROLBUDE0.BALANCE_DECREASES_ALLOWED_FLAG,'Y','Yes','No') RES_ALLOW_BUDGET_DECREASE_ADJU
,DECODE(xccCONTROLBUDE0.NEGATIVE_BALANCES_ALLOWED_FLAG,'Y','Yes','No') RES_ALLOW_BUDGET_DECREASE_AD_0
,xccCONTROLBUDE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,xccCONTROLBUDE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,xccCONTROLBUDE0.CREATED_BY RSC_CREATED_BY
,xccCONTROLBUDE0.CREATION_DATE RSC_CREATION_DATE
,xccCONTROLBUDE0.LEDGER_ID RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID
FROM XCC_CONTROL_BUDGETS xccCONTROLBUDE0
ORDER BY (CASE
	WHEN xccCONTROLBUDE0.SOURCE_BUDGET_SYSTEM_CODE = 'XCC' THEN
		2
	ELSE
		1
	END)
,xccCONTROLBUDE0.NAME 