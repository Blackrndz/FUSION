 -- RSC_PREREQUISITE_OBJECTS=QP_ROUNDING_RULES_VL
 

SELECT qpROUNDRULE0.NAME RES_NAME
,qpROUNDRULE0.DESCRIPTION RES_DESCRIPTION
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = qpROUNDRULE0.ROUNDING_RULE_TYPE_CODE 
			AND LOOKUP_TYPE = ' ORA_QP_ROUND_TYPES' AND LANGUAGE = USERENV('LANG')
			AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_TYPE
,qpROUNDRULE0.ROUND_VALUE RES_ROUND_TO
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = qpROUNDRULE0.ROUNDING_RULE_DIRECTION_CODE 
			AND LOOKUP_TYPE = 'ORA_QP_ROUND_DIRECTIONS' AND LANGUAGE = USERENV('LANG')
			AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_DIRECTION
,qpROUNDRULE0.INCREMENT_VALUE RES_INCREMENTAL_VALUE
,DECODE(qpROUNDRULE0.SETUP_ENABLED_FLAG,'Y','Yes','No') RES_SETUP_ENABLED
,DECODE(qpROUNDRULE0.ACTIVE_FLAG,'Y','Yes','No') RES_ACTIVE


,qpROUNDRULE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,qpROUNDRULE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,qpROUNDRULE0.CREATED_BY RSC_CREATED_BY
,qpROUNDRULE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID


FROM QP_ROUNDING_RULES_VL qpROUNDRULE0
ORDER BY qpROUNDRULE0.NAME