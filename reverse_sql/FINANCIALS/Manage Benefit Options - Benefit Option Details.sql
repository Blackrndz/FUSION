SELECT optionsE0.NAME RES_OPTION_NAME
,TO_CHAR(optionsE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,DECODE(TO_CHAR(optionsE0.EFFECTIVE_END_DATE ,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(optionsE0.EFFECTIVE_END_DATE ,'DD-Mon-YYYY')) RES_EFFECTIVE_END_DATE
,optionsE0.CARRIER_OPT_NAME RES_BENEFITS_EXTRACT_OPTION_NA
,optionsE0.SHORT_NAME RES_SHORT_NAME
,optionsE0.SHORT_CODE RES_SHORT_CODE
,DECODE(optionsE0.INVK_WV_OPT_FLAG,'Y','Yes','No') RES_WAIVE_OPTION
,(SELECT MEANING
    FROM FND_LOOKUP_VALUES
    WHERE LANGUAGE = USERENV('LANG')
    AND LOOKUP_TYPE = 'BEN_RQD_PERD_ENRT_NENRT_TM_UOM'
    AND LOOKUP_CODE = optionsE0.RQD_PERD_ENRT_NENRT_UOM
    ) RES_UNITS
,optionsE0.RQD_PERD_ENRT_NENRT_VAL RES_NUMBER_OF_UNITS_IN_PERIOD
,(SELECT FORMULA_NAME
    FROM FF_FORMULAS_TL
    WHERE LANGUAGE = USERENV('LANG')
    AND FORMULA_ID = optionsE0.RQD_PERD_ENRT_NENRT_RL
    ) RES_REQUIRED_PERIOD_OF_ENROLLM
,optionsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,optionsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,optionsE0.CREATED_BY RSC_CREATED_BY
,optionsE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,optionsE0.LEGISLATION_CODE RSC_COUNTRY_ID
FROM BEN_OPT_F optionsE0
ORDER BY optionsE0.NAME
,optionsE0.EFFECTIVE_START_DATE