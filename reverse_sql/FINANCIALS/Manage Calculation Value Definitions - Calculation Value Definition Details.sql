 -- RSC_PREREQUISITE_OBJECTS=PAY_VALUE_DEFINITIONS_VL
 
SELECT payVALUEDEFIEO.NAME RES_NAME
,legislativesE0.NAME RES_LEGISLATIVE_DATA_GROUP
,TO_CHAR(payVALUEDEFIEO.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE 
,DECODE(TO_CHAR(payVALUEDEFIEO.EFFECTIVE_END_DATE,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(payVALUEDEFIEO.EFFECTIVE_END_DATE,'DD-Mon-YYYY')) RES_EFFECTIVE_END_DATE
,(SELECT NAME 
	FROM PAY_VALUE_DEFINITIONS_VL 
	WHERE VALUE_DEFN_ID = payVALUEDEFIEO.PARENT_VALUE_DEFN_ID 
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE ,TO_DATE('31-12-4712','DD-MM-YYYY'))
	) RES_DEFINITION_GROUP
,(SELECT NAME 
	FROM PAY_VALUE_GROUPS_VL 
	WHERE VALUE_GROUP_ID = payVALUEDEFIEO.VALUE_GROUP_ID
	) RES_VALUE_DEFINITION_GROUP
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = payVALUEDEFIEO.DATE_MODE
	AND LOOKUP_TYPE = 'PAY_DED_DATE_MODE' 
	AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))
	) RES_RETRIEVAL_DATE
,calTYPEO.NAME RES_CALCULATION_TYPE
,(CASE WHEN calTYPEO.NAME IN ('Flat Amount Times Multiplier') THEN 
		(SELECT USER_NAME 
			FROM FF_DATABASE_ITEMS_VL 
			WHERE DATABASE_ITEM_ID = payVALUEDEFIEO.DATABASE_ITEM_ID)
	ELSE 
		NULL 
	END) RES_MULTIPLIER_OR_CONDITION
,(CASE WHEN calTYPEO.NAME NOT IN ('Flat Amount Times Multiplier') THEN 
		(SELECT USER_NAME 
			FROM FF_DATABASE_ITEMS_VL 
			WHERE DATABASE_ITEM_ID = payVALUEDEFIEO.DATABASE_ITEM_ID)
	ELSE 
		NULL 
	END) RES_BASIS_OF_CALCULATION_VALUE
,(SELECT USER_NAME 
	FROM FF_DATABASE_ITEMS_VL 
	WHERE DATABASE_ITEM_ID = payVALUEDEFIEO.OVERRIDDEN_DB_ITEM_ID
	) RES_OVERRIDING_RATE_IDENTIFIER
,payVALUEDEFIEO.CURRENCY_CODE RES_CURRENCY
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = payVALUEDEFIEO.UOM
	AND LOOKUP_TYPE = 'PAY_UNITS' 
	AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))
	) RES_UNIT_OF_MEASURE
,payVALUEDEFIEO.PERIODICITY RES_PERIODICITY
,(CASE 
	WHEN calTYPEO.NAME IN ('Flat Amount','Flat Amount Times Multiplier') THEN 
		payVALUEDEFIEO.VO_NAME1 
	ELSE 
		NULL 
	END) RES_FLAT_AMOUNT
,(CASE 
	WHEN calTYPEO.NAME IN ('Flat Rate','Flat Rate by Derived Base','Incremental Rate') THEN 
		payVALUEDEFIEO.VO_NAME1 
	ELSE 
		NULL 
	END) RES_RATE
,(CASE 
	WHEN calTYPEO.NAME IN ('Text') THEN 
		payVALUEDEFIEO.VO_NAME1 
	ELSE 
		NULL 
	END) RES_VALUE

,payVALUEDEFIEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,payVALUEDEFIEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,payVALUEDEFIEO.CREATED_BY RSC_CREATED_BY
,payVALUEDEFIEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,payVALUEDEFIEO.LEGISLATION_CODE RSC_COUNTRY_ID

FROM PAY_VALUE_DEFINITIONS_VL  payVALUEDEFIEO
,PER_LEGISLATIVE_DATA_GROUPS_VL legislativesE0
,(SELECT CALC_TYPE_ID,NAME FROM PAY_CALC_TYPES_VL) calTYPEO
WHERE payVALUEDEFIEO.PARENT_VALUE_DEFN_ID IS NULL
AND payVALUEDEFIEO.LEGISLATION_CODE = legislativesE0.LEGISLATION_CODE
AND payVALUEDEFIEO.CALC_TYPE_ID = calTYPEO.CALC_TYPE_ID
ORDER BY payVALUEDEFIEO.NAME
,legislativesE0.NAME 
,payVALUEDEFIEO.EFFECTIVE_START_DATE