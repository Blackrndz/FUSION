SELECT benELIGYEO.NAME RES_NAME
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benELIGYEO.PROFILE_TYPE 
	AND LOOKUP_TYPE = 'BEN_ELIG_PRFL_PERS_TYPE' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_PROFILE_TYPE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benELIGYEO.BNFT_CAGR_PRTN_CD
	AND LOOKUP_TYPE = 'BEN_BNFT_CAGR_PRTN' AND LANGUAGE = USERENV('LANG') 
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_PROFILE_USAGE
,benELIGYEO.DESCRIPTION RES_DESCRIPTION
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benELIGYEO.ASMT_TO_USE_CD
	AND LOOKUP_TYPE = 'BEN_ASMT_TO_USE' AND LANGUAGE = USERENV('LANG') 
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_ASSIGNMENT_TO_USE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benELIGYEO.STAT_CD 
	AND LOOKUP_TYPE = 'BEN_STAT' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_STATUS

,benELIGYEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,benELIGYEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,benELIGYEO.CREATED_BY  RSC_CREATED_BY
,benELIGYEO.CREATION_DATE  RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_ELIGY_PRFL benELIGYEO
WHERE benELIGYEO.PROFILE_TYPE = 'PRTP'
AND benELIGYEO.ELIGY_PRFL_ID IN 
	(SELECT ELIGY_PRFL_ID FROM BEN_ELIG_COMPTNCY_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
		UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_DSBLD_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
		UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_GNDR_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_LOA_RSN_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_MRTL_STS_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_PER_TYP_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_PSTL_CD_R_RNG_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_QUAL_TITL_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_GEN_CRIT_PRTE WHERE ELIG_CRITERIA_CODE = 'REL' AND benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_SVC_AREA_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_LVG_RSN_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_TBCO_USE_PRTE WHERE benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 	UNION
	 SELECT ELIGY_PRFL_ID FROM BEN_ELIG_GEN_CRIT_PRTE WHERE ELIG_CRITERIA_CODE = 'GEO' AND benELIGYEO.END_DATE BETWEEN START_DATE AND NVL(END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	 )
AND SYSDATE BETWEEN benELIGYEO.START_DATE AND NVL(benELIGYEO.END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY benELIGYEO.NAME