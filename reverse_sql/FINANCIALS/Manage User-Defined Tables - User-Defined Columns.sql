SELECT  (SELECT NAME 
	FROM PER_LEGISLATIVE_DATA_GROUPS_VL 
	WHERE LEGISLATIVE_DATA_GROUP_ID = ffUSERTABLEO.LEGISLATIVE_DATA_GROUP_ID
	) RES_LEGISLATIVE_DATA_GROUP
,ffUSERTABLEO.USER_TABLE_NAME RES_NAME
,ffUSERCOLUMNEO.USER_COLUMN_NAME RES_COLUMN_NAME
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = ffUSERCOLUMNEO.DATA_TYPE
	AND LOOKUP_TYPE = 'FF_DATA_TYPE' 
	AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_DATA_TYPE
,(SELECT FORMULA_NAME
	FROM FF_FORMULAS_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND FORMULA_ID = ffUSERCOLUMNEO.FORMULA_ID
	) RES_FORMULA
,ffUSERCOLUMNEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ffUSERCOLUMNEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ffUSERCOLUMNEO.CREATED_BY RSC_CREATED_BY
,ffUSERCOLUMNEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,(SELECT LEGISLATION_CODE 
	FROM PER_LEGISLATIVE_DATA_GROUPS
	WHERE LEGISLATIVE_DATA_GROUP_ID = ffUSERTABLEO.LEGISLATIVE_DATA_GROUP_ID
	) RSC_COUNTRY_ID
FROM FF_USER_TABLES_VL ffUSERTABLEO
,FF_USER_COLUMNS_VL ffUSERCOLUMNEO
WHERE ffUSERTABLEO.USER_TABLE_ID = ffUSERCOLUMNEO.USER_TABLE_ID
ORDER BY RES_NAME