-- RSC_PREREQUISITE_OBJECTS=PO_HEADERS_ALL

SELECT (SELECT BU_NAME
	FROM FUN_ALL_BUSINESS_UNITS_V
	WHERE BU_ID = poHeadersE0.PRC_BU_ID
	) RES_PROCUREMENT_BU
,poHeadersE0.SEGMENT1 RES_AGREEMENT
,poLinesE0.LINE_NUM RES_LINE
,(SELECT LINE_TYPE 
	FROM PO_LINE_TYPES_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND LINE_TYPE_ID = poLinesE0.LINE_TYPE_ID
	) RES_LINE_TYPE
,(SELECT DISTINCT ITEM_NUMBER
	FROM EGP_SYSTEM_ITEMS_B
	WHERE INVENTORY_ITEM_ID = poLinesE0.ITEM_ID
	AND ITEM_NUMBER IS NOT NULL
	) RES_ITEM
,poLinesE0.ITEM_DESCRIPTION RES_DESCRIPTION
,DECODE(poLinesE0.ALLOW_DESCRIPTION_UPDATE_FLAG,'Y','Yes','No') RES_ALLOW_ITEM_DESCRIPTION_UPD
,poLinesE0.VENDOR_PRODUCT_NUM RES_SUPPLIER_ITEM
,(SELECT CATEGORY_NAME
	FROM EGP_CATEGORIES_TL
	WHERE LANGUAGE = USERENV('LANG')
	AND CATEGORY_ID = poLinesE0.CATEGORY_ID
	) RES_CATEGORY_NAME
,(SELECT UNIT_OF_MEASURE
	FROM INV_UNITS_OF_MEASURE
	WHERE UOM_CODE = poLinesE0.UOM_CODE
	) RES_UOM
,poLinesE0.UNIT_PRICE RES_PRICE
,DECODE(poLinesE0.ALLOW_PRICE_OVERRIDE_FLAG,'Y','Yes','No') RES_ALLOW_PRICE_OVERRIDE
,poLinesE0.NOT_TO_EXCEED_PRICE RES_PRICE_LIMIT
,(SELECT DISPLAYED_FIELD
	FROM PO_LOOKUP_CODES
	WHERE LOOKUP_TYPE = 'PRICE BREAK TYPE'
	AND LOOKUP_CODE = poLinesE0.PRICE_BREAK_LOOKUP_CODE
	) RES_PRICE_BREAK_TYPE
,TO_CHAR(poLinesE0.EXPIRATION_DATE,'DD-Mon-YYYY') RES_EXPIRATION_DATE
,poLinesE0.MIN_RELEASE_AMOUNT RES_MINIMUM_RELEASE_AMOUNT
,poLinesE0.COMMITTED_AMOUNT RES_AGREEMENT_AMOUNT
,poLinesE0.QUANTITY_COMMITTED RES_AGREEMENT_QUANTITY
,DECODE(poLinesE0.CONSIGNMENT_LINE_FLAG,'Y','Yes','No') RES_CONSIGNMENT_LINE
,poLinesE0.AGING_PERIOD_DAYS RES_AGING_PERIOD_DAYS
,poAttrValuesTlpE0.LONG_DESCRIPTION RES_LONG_DESCRIPTION
,poLinesE0.SUPPLIER_PART_AUXID RES_SUPPLIER_ITEM_AUXILIARY_ID
,poAttrValuesE0.SUPPLIER_URL RES_SUPPLIER_URL
,poAttrValuesTlpE0.MANUFACTURER RES_MANUFACTURER
,poAttrValuesE0.MANUFACTURER_URL RES_MANUFACTURER_URL
,poAttrValuesE0.MANUFACTURER_PART_NUM RES_MANUFACTURER_PART_NUMBER
,poAttrValuesE0.ATTACHMENT_URL RES_ATTACHMENT_URL
,poAttrValuesTlpE0.ALIAS RES_ALIAS
,poAttrValuesE0.LEAD_TIME RES_LEAD_TIME_DAYS
,poAttrValuesE0.UNSPSC RES_UNSPSC
,poAttrValuesE0.PICTURE RES_IMAGE_URL
,poAttrValuesE0.THUMBNAIL_IMAGE RES_THUMBNAIL_IMAGE
,poAttrValuesE0.AVAILABILITY RES_AVAILABILITY
,poAttrValuesE0.ROUNDING_FACTOR RES_ROUNDING_FACTOR
,poAttrValuesTlpE0.COMMENTS RES_COMMENTS
,poLinesE0.NOTE_TO_VENDOR RES_NOTE_TO_SUPPLIER
,poLinesE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,poLinesE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,poLinesE0.CREATED_BY RSC_CREATED_BY
,poLinesE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,poHeadersE0.PRC_BU_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID
   
FROM PO_HEADERS_ALL poHeadersE0
,PO_LINES_ALL poLinesE0
,PO_ATTRIBUTE_VALUES poAttrValuesE0
,PO_ATTRIBUTE_VALUES_TLP poAttrValuesTlpE0
WHERE poHeadersE0.TYPE_LOOKUP_CODE IN ('BLANKET','CONTRACT')
AND poHeadersE0.PO_HEADER_ID = poLinesE0.PO_HEADER_ID
AND poLinesE0.PO_HEADER_ID = poAttrValuesE0.PO_HEADER_ID(+)
AND poLinesE0.PO_LINE_ID = poAttrValuesE0.PO_LINE_ID(+)
AND poLinesE0.PO_HEADER_ID = poAttrValuesTlpE0.PO_HEADER_ID(+)
AND poLinesE0.PO_LINE_ID = poAttrValuesTlpE0.PO_LINE_ID(+)
ORDER BY 1,poHeadersE0.SEGMENT1
,poLinesE0.LINE_NUM