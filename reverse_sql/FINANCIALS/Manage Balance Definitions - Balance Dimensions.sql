SELECT (SELECT NAME FROM PER_LEGISLATIVE_DATA_GROUPS_VL WHERE LEGISLATIVE_DATA_GROUP_ID = payBALNTYPEO.LEGISLATIVE_DATA_GROUP_ID) RES_LEGISLATIVE_DATA_GROUP
,payBALNTYPEO.BALANCE_NAME RES_NAME 
,SUBSTR(payBALDIMENEO.BASE_DIMENSION_NAME,5,LENGTH(payBALDIMENEO.BASE_DIMENSION_NAME)-4) RES_DIMENSION_NAME
,payBALDIMENEO.DESCRIPTION  RES_DESCRIPTION
,DECODE(payBALDIMENEO.SAVE_RUN_BALANCE_ENABLED,'Y','Valid') RES_RUN_BALANCE_STATUS
,TO_CHAR(payBALDIMENEO.INITIAL_DATE,'DD-Mon-YYYY') RES_BALANCE_VALID_FROM_DATE
,DECODE(payDEFINEDBALEO.GROSSUP_ALLOWED_FLAG,'Y','Yes','No') RES_ENABLE
,DECODE(payDEFINEDBALEO.GROSSUP_EXCLUDE_BY_DEFAULT,'Y','Yes','No') RES_INCLUDE
--,DECODE(payDEFINEDBALEO.SAVE_RUN_BALANCE,'Y','Valid','Invalid') RES_SAVE_RUN_BALANCE

,payDEFINEDBALEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,payDEFINEDBALEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,payDEFINEDBALEO.CREATED_BY RSC_CREATED_BY
,payDEFINEDBALEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PAY_BALANCE_TYPES_VL  payBALNTYPEO
,PAY_DEFINED_BALANCES payDEFINEDBALEO
,PAY_BALANCE_DIMENSIONS payBALDIMENEO
WHERE payBALNTYPEO.BALANCE_TYPE_ID = payDEFINEDBALEO.BALANCE_TYPE_ID
AND payBALNTYPEO.LEGISLATIVE_DATA_GROUP_ID = payDEFINEDBALEO.LEGISLATIVE_DATA_GROUP_ID
AND payDEFINEDBALEO.BALANCE_DIMENSION_ID = payBALDIMENEO.BALANCE_DIMENSION_ID
ORDER BY RES_LEGISLATIVE_DATA_GROUP,payBALNTYPEO.LEGISLATION_CODE,RES_NAME,RES_DIMENSION_NAME