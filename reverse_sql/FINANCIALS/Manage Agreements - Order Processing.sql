SELECT (SELECT BU_NAME
	FROM FUN_ALL_BUSINESS_UNITS_V
	WHERE BU_ID = poHeadersE0.PRC_BU_ID
	) RES_PROCUREMENT_BU
,poHeadersE0.SEGMENT1 RES_AGREEMENT
,DECODE(poHeadersE0.GENERATE_ORDERS_AUTOMATIC,'Y','Yes','No') RES_AUTOMATICALLY_GENERATE_ORD
,DECODE(poHeadersE0.SUBMIT_APPROVAL_AUTOMATIC,'Y','Yes','No') RES_AUTOMATICALLY_SUBMIT_FOR_A
,DECODE(poHeadersE0.GROUP_REQUISITIONS,'Y','Yes','No') RES_GROUP_REQUISITIONS
,DECODE(poHeadersE0.USE_SALES_ORDER_NUMBER_FLAG,'Y','Yes','No') RES_USE_CUSTOMER_SALES_ORDER
,DECODE(poHeadersE0.GROUP_REQUISITION_LINES,'Y','Yes','No') RES_GROUP_REQUISITION_LINES
,DECODE(poHeadersE0.USE_NEED_BY_DATE,'Y','Yes','No') RES_USE_REQUESTED_DATE
,DECODE(poHeadersE0.USE_SHIP_TO,'Y','Yes','No') RES_USE_SHIP_TO_ORGANIZATION_A
,DECODE(poHeadersE0.ALLOW_MULTIPLE_SITES_FLAG,'Y','Yes','No') RES_ALLOW_ORDERING_FROM_UNASSI
,DECODE(poHeadersE0.RETRO_PRICE_APPLY_UPDATES_FLAG,'Y','Yes','No') RES_ENABLE_RETROACTIVE_PRICING
,DECODE(poHeadersE0.RETRO_PRICE_INIT_APPROVAL_FLAG,'Y','Yes','No') RES_INITIATE_PROCESS_UPON_AGRE
,DECODE(poHeadersE0.RETRO_PRICE_OPEN_ORDER_FLAG,'Y','Yes','No') RES_REPRICE_OPEN_ORDERS_ONLY
,DECODE(poHeadersE0.RETRO_PRICE_COMM_UPDATES_FLAG,'Y','Yes','No') RES_COMMUNICATE_PRICE_UPDATES
,poHeadersE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,poHeadersE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,poHeadersE0.CREATED_BY RSC_CREATED_BY
,poHeadersE0.CREATION_DATE RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,poHeadersE0.PRC_BU_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID
   
FROM PO_HEADERS_ALL poHeadersE0
WHERE poHeadersE0.TYPE_LOOKUP_CODE IN ('BLANKET','CONTRACT')
ORDER BY 1,poHeadersE0.SEGMENT1