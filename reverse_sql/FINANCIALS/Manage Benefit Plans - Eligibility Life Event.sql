-- RSC_PREREQUISITE_OBJECTS=BEN_PL_F
 
SELECT * 
FROM (SELECT benPlansE0.NAME RES_PLAN_NAME
    ,TO_CHAR(benPlansE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
    ,benPlansE0.NAME RES_PLAN_OR_OPTION_NAME
	,'Plan' RES_PLAN_OR_OPTION_DESCRIPTION
	,(SELECT NAME
         FROM BEN_LER_F
         WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
         AND LER_ID = lifeEventsE0.LER_ID
         ) RES_LIFE_EVENT
    ,TO_CHAR(lifeEventsE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_LIFE_EVENT_EFFECTIVE_START
	,DECODE(TO_CHAR(lifeEventsE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY'),'31-12-4712',NULL,TO_CHAR(lifeEventsE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY')) RES_LIFE_EVENT_EFFECTIVE_END_D
    ,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_PRTN_ELIG_STRT'
         AND LOOKUP_CODE = lifeEventsE0.PRTN_EFF_STRT_DT_CD
         ) RES_PARTICIPATION_START_DATE
    ,(SELECT FORMULA_NAME
         FROM FF_FORMULAS_TL
         WHERE LANGUAGE = USERENV('LANG')
         AND FORMULA_ID = lifeEventsE0.PRTN_EFF_STRT_DT_RL
         ) RES_PARTICIPATION_START_DATE_F
    ,DECODE(lifeEventsE0.IGNR_PRTN_OVRID_FLAG,'Y','Yes','No') RES_PARTICIPATION_IGNORE_PARTI
    ,DECODE(lifeEventsE0.PRTN_OVRIDBL_FLAG,'Y','Yes','No') RES_PARTICIPATION_ENABLE_PARTI
    ,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_PRTN_ELIG_END'
         AND LOOKUP_CODE = lifeEventsE0.PRTN_EFF_END_DT_CD
         ) RES_PARTICIPATION_PREVIOUS_END
    ,(SELECT FORMULA_NAME
         FROM FF_FORMULAS_TL
         WHERE LANGUAGE = USERENV('LANG')
         AND FORMULA_ID = lifeEventsE0.PRTN_EFF_END_DT_RL
         ) RES_PARTICIPATION_PREVIOUS_E_0
    ,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_ELIG_INELIG'
         AND LOOKUP_CODE = lifeEventsE0.ELIG_INELIG_CD
         ) RES_PARTICIPATION_PARTICIPANT_
    ,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_WTG_PERD_UOM'
         AND LOOKUP_CODE = lifeEventsE0.WAIT_PERD_UOM
         ) RES_WAITING_PERIOD_UNIT_OF_MEA
    ,lifeEventsE0.WAIT_PERD_VAL RES_WAITING_PERIOD_NUMBER_OF_U
    ,(SELECT FORMULA_NAME
         FROM FF_FORMULAS_TL
         WHERE LANGUAGE = USERENV('LANG')
         AND FORMULA_ID = lifeEventsE0.WAIT_PERD_RL
         ) RES_WAITING_PERIOD_WAITING_PER
    ,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_MX_WTG_DT_TO_USE'
         AND LOOKUP_CODE = lifeEventsE0.WAIT_PERD_DT_TO_USE_CD
         ) RES_WAITING_PERIOD_PERIOD_STAR
    ,(SELECT FORMULA_NAME
         FROM FF_FORMULAS_TL
         WHERE LANGUAGE = USERENV('LANG')
         AND FORMULA_ID = lifeEventsE0.WAIT_PERD_DT_TO_USE_RL
         ) RES_WAITING_PERIOD_START_DATE_
    ,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_FMLY_MMBR'
         AND LOOKUP_CODE = lifeEventsE0.VRFY_FMLY_MMBR_CD
         ) RES_FAMILY_MEMBER_RULE
    ,(SELECT FORMULA_NAME
         FROM FF_FORMULAS_TL
         WHERE LANGUAGE = USERENV('LANG')
         AND FORMULA_ID = lifeEventsE0.VRFY_FMLY_MMBR_RL
         ) RES_FAMILY_MEMBER_FORMULA
	,lifeEventsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
	,lifeEventsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
	,lifeEventsE0.CREATED_BY  RSC_CREATED_BY
	,lifeEventsE0.CREATION_DATE  RSC_CREATION_DATE
	,null RSC_LEDGER_ID
	,null RSC_CHART_OF_ACCOUNTS_ID
	,null RSC_BUSINESS_UNIT_ID
	,null RSC_LEGAL_ENTITY_ID
	,null RSC_ORGANIZATION_ID
	,null RSC_BUSINESS_GROUP_ID
	,NULL RSC_ENTERPRISE_ID
	,benPlansE0.LEGISLATION_CODE RSC_COUNTRY_ID

    FROM BEN_PL_F benPlansE0
	,(SELECT NAME
		,PL_TYP_ID
		FROM BEN_PL_TYP_F
		WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
		AND OPT_TYP_CD != 'FLXCR'
		) planTypesE0
    ,BEN_ELIG_TO_PRTE_RSN_F lifeEventsE0
    WHERE SYSDATE BETWEEN benPlansE0.EFFECTIVE_START_DATE AND benPlansE0.EFFECTIVE_END_DATE
	AND benPlansE0.PL_TYP_ID = planTypesE0.PL_TYP_ID
	AND benPlansE0.PL_ID = lifeEventsE0.PL_ID
    
    UNION
    
	SELECT benPlansE0.NAME RES_PLAN_NAME
    ,TO_CHAR(benPlansE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
    ,(SELECT NAME
         FROM BEN_OPT_F
         WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
         AND OPT_ID = planOptionsE0.OPT_ID
         ) RES_PLAN_OR_OPTION_NAME
	,'Options' RES_PLAN_OR_OPTION_DESCRIPTION
	,(SELECT NAME
         FROM BEN_LER_F
         WHERE SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
         AND LER_ID = lifeEventsE0.LER_ID
         ) RES_LIFE_EVENT
    ,TO_CHAR(lifeEventsE0.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_LIFE_EVENT_EFFECTIVE_START
	,DECODE(TO_CHAR(lifeEventsE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY'),'31-12-4712',NULL,TO_CHAR(lifeEventsE0.EFFECTIVE_END_DATE,'DD-Mon-YYYY')) RES_LIFE_EVENT_EFFECTIVE_END_D
    ,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_PRTN_ELIG_STRT'
         AND LOOKUP_CODE = lifeEventsE0.PRTN_EFF_STRT_DT_CD
         ) RES_PARTICIPATION_START_DATE
    ,(SELECT FORMULA_NAME
         FROM FF_FORMULAS_TL
         WHERE LANGUAGE = USERENV('LANG')
         AND FORMULA_ID = lifeEventsE0.PRTN_EFF_STRT_DT_RL
         ) RES_PARTICIPATION_START_DATE_F
    ,DECODE(lifeEventsE0.IGNR_PRTN_OVRID_FLAG,'Y','Yes','No') RES_PARTICIPATION_IGNORE_PARTI
    ,DECODE(lifeEventsE0.PRTN_OVRIDBL_FLAG,'Y','Yes','No') RES_PARTICIPATION_ENABLE_PARTI
    ,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_PRTN_ELIG_END'
         AND LOOKUP_CODE = lifeEventsE0.PRTN_EFF_END_DT_CD
         ) RES_PARTICIPATION_PREVIOUS_END
    ,(SELECT FORMULA_NAME
         FROM FF_FORMULAS_TL
         WHERE LANGUAGE = USERENV('LANG')
         AND FORMULA_ID = lifeEventsE0.PRTN_EFF_END_DT_RL
         ) RES_PARTICIPATION_PREVIOUS_E_0
    ,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_ELIG_INELIG'
         AND LOOKUP_CODE = lifeEventsE0.ELIG_INELIG_CD
         ) RES_PARTICIPATION_PARTICIPANT_
    ,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_WTG_PERD_UOM'
         AND LOOKUP_CODE = lifeEventsE0.WAIT_PERD_UOM
         ) RES_WAITING_PERIOD_UNIT_OF_MEA
    ,lifeEventsE0.WAIT_PERD_VAL RES_WAITING_PERIOD_NUMBER_OF_U
    ,(SELECT FORMULA_NAME
         FROM FF_FORMULAS_TL
         WHERE LANGUAGE = USERENV('LANG')
         AND FORMULA_ID = lifeEventsE0.WAIT_PERD_RL
         ) RES_WAITING_PERIOD_WAITING_PER
    ,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_MX_WTG_DT_TO_USE'
         AND LOOKUP_CODE = lifeEventsE0.WAIT_PERD_DT_TO_USE_CD
         ) RES_WAITING_PERIOD_PERIOD_STAR
    ,(SELECT FORMULA_NAME
         FROM FF_FORMULAS_TL
         WHERE LANGUAGE = USERENV('LANG')
         AND FORMULA_ID = lifeEventsE0.WAIT_PERD_DT_TO_USE_RL
         ) RES_WAITING_PERIOD_START_DATE_
    ,(SELECT MEANING
         FROM FND_LOOKUP_VALUES
         WHERE LANGUAGE = USERENV('LANG')
         AND LOOKUP_TYPE = 'BEN_FMLY_MMBR'
         AND LOOKUP_CODE = lifeEventsE0.VRFY_FMLY_MMBR_CD
         ) RES_FAMILY_MEMBER_RULE
    ,(SELECT FORMULA_NAME
         FROM FF_FORMULAS_TL
         WHERE LANGUAGE = USERENV('LANG')
         AND FORMULA_ID = lifeEventsE0.VRFY_FMLY_MMBR_RL
         ) RES_FAMILY_MEMBER_FORMULA
	,lifeEventsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
	,lifeEventsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
	,lifeEventsE0.CREATED_BY  RSC_CREATED_BY
	,lifeEventsE0.CREATION_DATE  RSC_CREATION_DATE
	,null RSC_LEDGER_ID
	,null RSC_CHART_OF_ACCOUNTS_ID
	,null RSC_BUSINESS_UNIT_ID
	,null RSC_LEGAL_ENTITY_ID
	,null RSC_ORGANIZATION_ID
	,null RSC_BUSINESS_GROUP_ID
	,NULL RSC_ENTERPRISE_ID
	,benPlansE0.LEGISLATION_CODE RSC_COUNTRY_ID

    FROM BEN_PL_F benPlansE0
    ,BEN_OIPL_F planOptionsE0
    ,BEN_ELIG_TO_PRTE_RSN_F lifeEventsE0
    WHERE benPlansE0.PL_ID = planOptionsE0.PL_ID
    AND planOptionsE0.OIPL_ID = lifeEventsE0.OIPL_ID
    )
    
ORDER BY RES_PLAN_NAME