 --RSC_PREREQUISITE_OBJECTS=PER_SENIORITY_DATES_SETUP

SELECT benLOSFACEO.NAME RES_NAME
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benLOSFACEO.LOS_UOM
	AND LOOKUP_TYPE = 'BEN_TM_UOM' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_UNIT_OF_MEASURE
,(SELECT FORMULA_NAME FROM FF_FORMULAS_VL 
	WHERE FORMULA_ID = benLOSFACEO.LOS_CALC_RL 
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_LENGTH_OF_SERVICE_FACTOR_F
,benLOSFACEO.MN_LOS_NUM RES_GREATER_THAN_OR_EQUAL_TO_L
,benLOSFACEO.MX_LOS_NUM RES_LESS_THAN_LENGTH_OF_SERVIC
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benLOSFACEO.LOS_DT_TO_USE_CD
	AND LOOKUP_TYPE = 'BEN_LOS_DT_TO_USE' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_PERIOD_START_DATE_RULE 
,(SELECT FORMULA_NAME FROM FF_FORMULAS_VL 
	WHERE FORMULA_ID = benLOSFACEO.LOS_DT_TO_USE_RL 
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_START_DATE_FORMULA
--,HRS_ALT_VAL_TO_USE_CD RES_SENIORITY_DATE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = perSENIORITYEO.SENIORITY_DATE_CODE
	AND LOOKUP_TYPE = 'ORA_PER_SENIORITY_ITEMS' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_WORKER_SENIORITY_DATE
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benLOSFACEO.LOS_DET_CD
	AND LOOKUP_TYPE = 'BEN_AGE_DET' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_DETERMINATION_RULE
,(SELECT FORMULA_NAME FROM FF_FORMULAS_VL 
	WHERE FORMULA_ID = benLOSFACEO.LOS_DET_RL 
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_DETERMINATION_FORMULA
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benLOSFACEO.RNDG_CD
	AND LOOKUP_TYPE = 'BEN_RNDG' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD')))  RES_ROUNDING_RULE 
,(SELECT FORMULA_NAME FROM FF_FORMULAS_VL 
	WHERE FORMULA_ID = benLOSFACEO.RNDG_RL 
	AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND NVL(EFFECTIVE_END_DATE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_ROUNDING_FORMULA


,benLOSFACEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,benLOSFACEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,benLOSFACEO.CREATED_BY RSC_CREATED_BY
,benLOSFACEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,benLOSFACEO.BUSINESS_GROUP_ID RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM BEN_LOS_FCTR benLOSFACEO
,PER_SENIORITY_DATES_SETUP perSENIORITYEO
WHERE benLOSFACEO.CONFIG_ID_1 = perSENIORITYEO.SENIORITY_DATE_SETUP_ID(+)
ORDER BY benLOSFACEO.NAME
