SELECT FNDTREEEO.TREE_NAME RES_TREE_NAME
,FNDTREEEO.TREE_CODE RES_CODE
,(SELECT TREE_STRUCTURE_NAME
	FROM FND_TREE_STRUCTURE_VL
	WHERE TREE_STRUCTURE_CODE = FNDTREEEO.TREE_STRUCTURE_CODE
	) RES_TREE_STRUCTURE
,FNDTREEEO.TREE_STRUCTURE_CODE RES_TREE_STRUCTURE_CODE
,FNDTSDATASOURCEEO.DATA_SOURCE_NAME RES_DATA_SOURCE
,FNDLABELEO.LABEL_SHORT_NAME RES_SHORT_NAME
,FNDLABELEO.LABEL_NAME RES_NAME
,FNDLABELEO.DESCRIPTION RES_DESCRIPTION
,FndLabelEO.ICON_NAME RES_ICON_NAME
,TO_CHAR(FNDLABELEO.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
,DECODE(TO_CHAR(FNDLABELEO.EFFECTIVE_END_DATE,'DD-MM-YYYY'),'31-12-4712',NULL,TO_CHAR(FNDLABELEO.EFFECTIVE_END_DATE,
	'DD-Mon-YYYY')) RES_EFFECTIVE_END_DATE
,FNDTREELABELEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,FNDTREELABELEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,FNDTREELABELEO.CREATED_BY RSC_CREATED_BY
,FNDTREELABELEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,FNDTREELABELEO.ENTERPRISE_ID RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM FND_TREE_VL FNDTREEEO
,FND_TS_DATA_SOURCE_VL FNDTSDATASOURCEEO
,FND_LABEL_VL FNDLABELEO
,FND_TREE_LABEL FNDTREELABELEO
WHERE FNDTREEEO.TREE_STRUCTURE_CODE  = 'PER_DEPT_TREE_STRUCTURE'
AND FNDTREEEO.TREE_CODE              = FNDTREELABELEO.TREE_CODE
AND FNDTSDATASOURCEEO.DATA_SOURCE_ID = FNDLABELEO.DATA_SOURCE_ID
AND FNDLABELEO.TREE_STRUCTURE_CODE   = FNDTREELABELEO.LABEL_PK1_VALUE
AND FNDLABELEO.DATA_SOURCE_ID        = FNDTREELABELEO.LABEL_PK2_VALUE
AND FNDLABELEO.LABEL_ID              = FNDTREELABELEO.LABEL_PK3_VALUE
ORDER BY FNDTREEEO.TREE_NAME
,FNDLABELEO.LABEL_SHORT_NAME