-- RSC_PREREQUISITE_OBJECTS=HRQ_QUESTIONNAIRES_VL
 
SELECT hrqQUESTIONAIREO.QUESTIONNAIRE_CODE RES_QUESTIONNAIRE_TEMPLATE_ID
,hrqQUESTIONAIREO.NAME RES_NAME
,(SELECT NAME 
	FROM HRQ_SUBSCRIBERS_TL	
	WHERE LANGUAGE = USERENV('LANG') 
	AND SUBSCRIBER_ID = hrqQUESTIONAIREO.SUBSCRIBER_ID
	) RES_SUBSCRIBER
,hrqQSTNRSECTIONEO.NAME RES_TITLE
,hrqQSTNRSECTIONEO.DESCRIPTION RES_DESCRIPTION
,DECODE(ALLOW_ADHOC,'Y','Yes','No') RES_ALLOW_ADDITIONAL_QUESTIONS
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = hrqQSTNRSECTIONEO.QUESTION_ORDER
	AND LOOKUP_TYPE = 'HRQ_QUESTION_ORDER' 
	AND LANGUAGE = USERENV('LANG') 
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_QUESTION_ORDER
,(SELECT MEANING 
	FROM FND_LOOKUP_VALUES 
	WHERE LOOKUP_CODE = hrqQSTNRSECTIONEO.RESPONSE_ORDER
	AND LOOKUP_TYPE = 'HRQ_QUESTION_ORDER' 
	AND LANGUAGE = USERENV('LANG') 
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))
	) RES_RESPONSE_ORDER
,DECODE(hrqQSTNRSECTIONEO.MANDATORY,'Y','Yes','No') RES_REQUIRED


,hrqQSTNRSECTIONEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hrqQSTNRSECTIONEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hrqQSTNRSECTIONEO.CREATED_BY RSC_CREATED_BY
,hrqQSTNRSECTIONEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM HRQ_QUESTIONNAIRES_VL hrqQUESTIONAIREO
,HRQ_QSTNR_TEMPLATES_VL hrqQSTNRETEMPEO
,HRQ_QSTNR_SECTIONS_VL hrqQSTNRSECTIONEO
WHERE hrqQUESTIONAIREO.QUESTIONNAIRE_ID = hrqQSTNRETEMPEO.QUESTIONNAIRE_ID
AND hrqQUESTIONAIREO.QUESTIONNAIRE_ID = hrqQSTNRSECTIONEO.QUESTIONNAIRE_ID
ORDER BY hrqQUESTIONAIREO.QUESTIONNAIRE_CODE
,hrqQUESTIONAIREO.NAME