SELECT cmpPLANEO.PLAN_NAME RES_PLAN
,cmpSTMTGROUPE0.NAME RES_NAME
,(SELECT FILE_NAME FROM CMP_STMT_TEMPLATES WHERE STMT_TEMPLATE_ID = cmpSTMTGROUPTMPE0.STMT_TEMPLATE_ID 
	AND GROUP_TYPE = cmpSTMTGROUPE0.GROUP_TYPE) RES_TEMPLATE
,cmpSTMTGROUPTMPE0.NAME RES_DISPLAY_NAME
,cmpCALEXPRE0.EXPR_COND RES_CRITERIA
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = cmpSTMTGROUPTMPE0.STMT_WORKER_ACK_ACTION 
	AND LOOKUP_TYPE = 'ORA_CMP_STMT_WORKER_ACK_ACTION' AND LANGUAGE = USERENV('LANG')
	AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))) RES_WORKER_STATEMENT_ACTION
,cmpINSTUCE0.INSTRUCTION_TEXT RES_TEXT_SHOWN_TO_WORKERS

,cmpSTMTGROUPTMPE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,cmpSTMTGROUPTMPE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,cmpSTMTGROUPTMPE0.CREATED_BY RSC_CREATED_BY
,cmpSTMTGROUPTMPE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM CMP_PLANS_VL cmpPLANEO
,CMP_STMT_GROUPS_VL cmpSTMTGROUPE0
,CMP_STMT_GROUP_TEMPLATES_VL cmpSTMTGROUPTMPE0
,CMP_INSTRUCTIONS_VL cmpINSTUCE0
,CMP_CALC_EXPRESSIONS cmpCALEXPRE0
WHERE cmpPLANEO.STATEMENT_GROUP_ID = cmpSTMTGROUPE0.STMT_GROUP_ID
AND cmpSTMTGROUPE0.STMT_GROUP_ID = cmpSTMTGROUPTMPE0.STMT_GROUP_ID
AND cmpSTMTGROUPE0.GROUP_TYPE = cmpPLANEO.COMP_TYPE
AND cmpSTMTGROUPTMPE0.STMT_GROUP_TEMPLATE_ID = cmpCALEXPRE0.EXPR_KEY
AND cmpSTMTGROUPTMPE0.STMT_GROUP_TEMPLATE_ID = cmpINSTUCE0.INSTRUCTION_KEY(+)
AND cmpINSTUCE0.KEY_TYPE = 'STMT_GRP_TEMPLATE' 
AND cmpCALEXPRE0.KEY_TYPE = 'GROUPTMPL'
AND cmpPLANEO.COMP_TYPE = 'CWB'
ORDER BY cmpPLANEO.PLAN_NAME