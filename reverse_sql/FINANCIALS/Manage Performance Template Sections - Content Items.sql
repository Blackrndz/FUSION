  -- RSC_PREREQUISITE_OBJECTS=HRA_SECTION_DEFNS_VL
 
SELECT pfSectionsE0.NAME RES_NAME
,TO_CHAR(pfSectionsE0.DATE_FROM,'DD-Mon-YYYY') RES_FROM_DATE
,TO_CHAR(pfSectionsE0.DATE_TO,'DD-Mon-YYYY') RES_TO_DATE
,(SELECT NAME
    FROM HRT_CONTENT_ITEMS_VL
    WHERE CONTENT_ITEM_ID = pfSecContentsE0.CONTENT_ITEM_ID
    AND SYSDATE BETWEEN DATE_FROM AND NVL(DATE_TO,TO_DATE('4712-12-31','YYYY-MM-DD'))
     ) RES_ITEM_NAME
,(SELECT ITEM_DESCRIPTION 
	FROM HRT_CONTENT_ITEMS_VL
    WHERE CONTENT_ITEM_ID = pfSecContentsE0.CONTENT_ITEM_ID
    AND SYSDATE BETWEEN DATE_FROM AND NVL(DATE_TO,TO_DATE('4712-12-31','YYYY-MM-DD'))
     ) RES_DESCRIPTION
,(SELECT RATING_NAME
    FROM HRT_RATING_MODELS_VL
    WHERE RATING_MODEL_ID = pfSecContentsE0.PROF_RATING_MODEL_ID
	AND SYSDATE BETWEEN DATE_FROM AND NVL(DATE_TO,TO_DATE('4712-12-31','YYYY-MM-DD'))
     ) RES_PROFICIENCY_LEVEL_MODEL
,(SELECT RATING_SHORT_DESCR
    FROM HRT_RATING_LEVELS_VL
    WHERE RATING_LEVEL_ID = pfSecContentsE0.TARGET_PROF_REV_RATING_ID
     AND SYSDATE BETWEEN DATE_FROM AND NVL(DATE_TO,TO_DATE('4712-12-31','YYYY-MM-DD'))
     ) RES_TARGET_PROFICIENCY_LEVEL
,(CASE
    WHEN pfSectionsE0.SHOW_TARGET_PERF_RTG = 'Y' THEN
        (SELECT RATING_NAME
            FROM HRT_RATING_MODELS_VL
            WHERE  RATING_MODEL_ID = pfSecContentsE0.PERF_RATING_MODEL_ID
			AND SYSDATE BETWEEN DATE_FROM AND NVL(DATE_TO,TO_DATE('4712-12-31','YYYY-MM-DD')))
    END) RES_PERFORMANCE_RATING_MODEL
,(CASE
    WHEN pfSectionsE0.SHOW_TARGET_PERF_RTG = 'Y' THEN
        (SELECT RATING_SHORT_DESCR
            FROM HRT_RATING_LEVELS_VL
            WHERE RATING_LEVEL_ID = pfSecContentsE0.TARGET_PERF_REV_RATING_ID
			AND SYSDATE BETWEEN DATE_FROM AND NVL(DATE_TO,TO_DATE('4712-12-31','YYYY-MM-DD')) )
    END) RES_TARGET_PERFORMANCE_RATING
,(CASE WHEN pfSectionsE0.SHOW_SECTION_WEIGHT = 'Y' THEN
		pfSecContentsE0.WEIGHT
		END)RES_WEIGHT
,(CASE WHEN pfSectionsE0.SHOW_SECTION_MIN_WEIGHT = 'Y' THEN
		pfSecContentsE0.MIN_WEIGHT
		END)RES_MINIMUM_WEIGHT
,DECODE(pfSecContentsE0.MANDATORY_FLAG,'Y','Yes','No') RES_REQUIRED
	 
,pfSecContentsE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,pfSecContentsE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,pfSecContentsE0.CREATED_BY RSC_CREATED_BY
,pfSecContentsE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,pfSecContentsE0.BUSINESS_GROUP_ID RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID



FROM HRA_SECTION_DEFNS_VL pfSectionsE0
,HRA_SECTION_ITEMS_VL pfSecContentsE0
WHERE pfSectionsE0.SECTION_DEF_ID = pfSecContentsE0.SECTION_DEF_ID
ORDER BY pfSectionsE0.NAME