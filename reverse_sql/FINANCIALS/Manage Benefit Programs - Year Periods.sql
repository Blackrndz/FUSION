SELECT benPGMEO.NAME RES_PROGRAM_NAME
,TO_CHAR(benPGMEO.EFFECTIVE_START_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_START_DATE
--,TO_CHAR(benPGMEO.EFFECTIVE_END_DATE,'DD-Mon-YYYY') RES_EFFECTIVE_END_DATE
,TO_CHAR(benYEAREO.START_DATE,'DD-Mon-YYYY') RES_YEAR_PERIODS_START_DATE
,TO_CHAR(benYEAREO.END_DATE,'DD-Mon-YYYY') RES_YEAR_PERIODS_END_DATE
,benYEAREO.RES_TYPE RES_PERIOD_TYPE
,TO_CHAR(benYEAREO.START_DATE,'DD')||'-'||DECODE(TO_CHAR(benYEAREO.START_DATE,'MM'),'01','Jan','02','Feb','03','Mar'
	,'04','Apr','05','May','06','Jun','07','Jul','08','Aug','09','Sep','10','Oct','11','Nov','12','Dec')||'-'||TO_CHAR(benYEAREO.START_DATE,'YYYY') 
	||' - '||TO_CHAR(benYEAREO.END_DATE,'DD')||'-'||DECODE(TO_CHAR(benYEAREO.END_DATE,'MM'),'01','Jan','02','Feb','03','Mar'
	,'04','Apr','05','May','06','Jun','07','Jul','08','Aug','09','Sep','10','Oct','11','Nov','12','Dec')||'-'||TO_CHAR(benYEAREO.END_DATE,'YYYY') RES_SELECT_DATE_START_DATE_END 

,benYEAREO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,benYEAREO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,benYEAREO.CREATED_BY RSC_CREATED_BY
,benYEAREO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,benPGMEO.LEGISLATION_CODE RSC_COUNTRY_ID

FROM BEN_PGM_F benPGMEO
,(SELECT benPOPLEO.PGM_ID
	,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = benYREO.PERD_TYP_CD
		AND LOOKUP_TYPE = 'BEN_PERD_TYP' AND LANGUAGE = USERENV('LANG')
		AND SYSDATE BETWEEN START_DATE_ACTIVE AND NVL(END_DATE_ACTIVE ,TO_DATE('31-12-4712','DD-MM-YYYY'))) RES_TYPE
	,benYREO.START_DATE 
	,benYREO.END_DATE 
	,benPOPLEO.LAST_UPDATED_BY LAST_UPDATED_BY
	,benPOPLEO.LAST_UPDATE_DATE LAST_UPDATE_DATE
	,benPOPLEO.CREATED_BY CREATED_BY
	,benPOPLEO.CREATION_DATE CREATION_DATE
	FROM BEN_POPL_YR_PERD benPOPLEO
	,BEN_YR_PERD benYREO
	WHERE benPOPLEO.YR_PERD_ID = benYREO.YR_PERD_ID
	ORDER BY PGM_ID ) benYEAREO
WHERE benPGMEO.PGM_ID = benYEAREO.PGM_ID
AND SYSDATE BETWEEN benPGMEO.EFFECTIVE_START_DATE AND NVL(benPGMEO.EFFECTIVE_END_DATE,TO_DATE('31-12-4712','DD-MM-YYYY'))
ORDER BY RES_PROGRAM_NAME
