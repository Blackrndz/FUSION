SELECT	DEPRECIATIONMETHODEO.METHOD_CODE RES_NAME
,SETIDSETSEO.SET_NAME RES_REFERENCE_DATA_SET
,LookupMethodTypePEO.MEANING RES_METHOD_TYPE
,TRUNC(DEPRECIATIONMETHODEO.LIFE_IN_MONTHS/12) RES_LIFE_IN_YEARS
,MOD(DEPRECIATIONMETHODEO.LIFE_IN_MONTHS,12) RES_LIFE_IN_MONTHS
,DECODE(USE_LIFE_IN_PERIODS_FLAG,'Y',DEPRECIATIONMETHODEO.LIFE_IN_MONTHS,NULL) RES_LIFE_IN_PERIODS
,DEPRECIATIONRATEEO.YEAR RES_YEAR
,DEPRECIATIONRATEEO.PERIOD_PLACED_IN_SERVICE RES_PERIOD
,DepreciationRateEO.RATE RES_ANNUAL_RATE
,DepreciationRateEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,DepreciationRateEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,DepreciationRateEO.CREATED_BY RSC_CREATED_BY
,DepreciationRateEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

from FA_RATES DEPRECIATIONRATEEO ,
FA_METHODS DEPRECIATIONMETHODEO ,
FND_SETID_SETS_VL SETIDSETSEO ,
FND_LOOKUPS LookupMethodTypePEO
where DEPRECIATIONRATEEO.METHOD_ID = DEPRECIATIONMETHODEO.METHOD_ID
and DEPRECIATIONMETHODEO.SET_ID                    = SETIDSETSEO.SET_ID(+)
and LookupMethodTypePEO.LOOKUP_TYPE (+)          = 'FA_RATE_SOURCE_RULE'
AND DepreciationMethodEO.RATE_SOURCE_RULE          = LookupMethodTypePEO.LOOKUP_CODE (+)
ORDER BY DEPRECIATIONMETHODEO.METHOD_CODE
,SETIDSETSEO.SET_NAME
,TRUNC(DEPRECIATIONMETHODEO.LIFE_IN_MONTHS/12) 
,MOD(DEPRECIATIONMETHODEO.LIFE_IN_MONTHS,12)
,DepreciationRateEO.PERIOD_PLACED_IN_SERVICE
,DepreciationRateEO.YEAR