SELECT pjfPROJECTstatEO.PROJECT_STATUS_NAME RES_PROJECT_STATUS
 ,(SELECT MEANING FROM FND_LOOKUPS
			WHERE LOOKUP_CODE = pjfPROJECTstatEO.STATUS_TYPE
			AND LOOKUP_TYPE = 'PJF_STATUS_TYPE')RES_STATUS_TYPE
 ,(CASE 
	WHEN pjfPROJECTstatEO.STATUS_TYPE = 'PROGRESS' THEN
		(SELECT MEANING FROM FND_LOOKUPS
			WHERE LOOKUP_CODE = pjfPROJECTstatEO.PROJECT_SYSTEM_STATUS_CODE
			AND LOOKUP_TYPE = 'PJF_PROGRESS_SYSTEM_STATUS')
	ELSE
		(SELECT MEANING FROM FND_LOOKUPS
			WHERE LOOKUP_CODE = pjfPROJECTstatEO.PROJECT_SYSTEM_STATUS_CODE
			AND LOOKUP_TYPE = 'PJF_PROJECT_SYSTEM_STATUS')
	END) RES_SYSTEM_STATUS
 ,pjfPROJECTstatEO.DESCRIPTION	RES_DESCRIPTION
 ,TO_CHAR(pjfPROJECTstatEO.START_DATE_ACTIVE,'DD-Mon-YYYY') RES_FROM_DATE
 ,TO_CHAR(pjfPROJECTstatEO.END_DATE_ACTIVE,'DD-Mon-YYYY') RES_TO_DATE
 ,DECODE(pjfPROJECTstatEO.STARTING_STATUS_FLAG,'Y','Yes','No') RES_INITIAL_PROJECT_STATUS
 ,DECODE(pjfPROJECTstatEO.ENABLE_BP_FLAG,'Y','Yes','No') RES_ENABLE_WORKFLOW
 ,(SELECT PROJECT_STATUS_NAME FROM PJF_PROJECT_STATUSES_VL 
	WHERE PROJECT_STATUS_CODE = pjfPROJECTstatEO.BP_SUCCESS_STATUS_CODE ) RES_STATUS_AFTER_CHANGE_ACCEPT
 ,(SELECT PROJECT_STATUS_NAME FROM PJF_PROJECT_STATUSES_VL A_TMP
	WHERE PROJECT_STATUS_CODE = pjfPROJECTstatEO.BP_FAILURE_STATUS_CODE ) RES_STATUS_AFTER_CHANGE_REJECT
 ,pjfPROJECTstatEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
 ,pjfPROJECTstatEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
 ,pjfPROJECTstatEO.CREATED_BY RSC_CREATED_BY
 ,pjfPROJECTstatEO.CREATION_DATE RSC_CREATION_DATE
 ,NULL RSC_LEDGER_ID
 ,NULL RSC_CHART_OF_ACCOUNTS_ID
 ,NULL RSC_BUSINESS_UNIT_ID
 ,NULL RSC_LEGAL_ENTITY_ID
 ,NULL RSC_ORGANIZATION_ID
 ,NULL RSC_BUSINESS_GROUP_ID
 ,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJF_PROJECT_STATUSES_VL pjfPROJECTstatEO

 