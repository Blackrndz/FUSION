WITH RBS AS (SELECT source.NAME SNAME
	,header.NAME HNAME
	,header.RBS_HEADER_ID
	,header.CENTRALLY_CONTROL_FLAG
	FROM PJF_RBS_HEADERS_VL source
	,PJF_RBS_HEADERS_VL header
	WHERE header.SOURCE_RBS_HEADER_ID = source.RBS_HEADER_ID)

SELECT projTemplatesE0.NAME RES_TEMPLATE_NAME
,projTemplatesE0.SEGMENT1 RES_TEMPLATE_NUMBER
,forecastsE0.VERSION_NAME RES_NAME
,(CASE
	WHEN forecastsE0.PLANNED_FOR_CODE = 'COST' THEN	
		DECODE(pjoFinPlanAMTEO.COST_QTY_FLAG,'Y','Yes','No') 
	END) RES_COST_QUANTITY
,(CASE
	WHEN forecastsE0.PLANNED_FOR_CODE = 'COST_AND_REV_TOGETHER' THEN	
		DECODE(pjoFinPlanAMTEO.ALL_QTY_FLAG,'Y','Yes','No') 
	END) RES_QUANTITY
,(CASE
	WHEN forecastsE0.PLANNED_FOR_CODE != 'REVENUE' THEN	
		DECODE(pjoFinPlanAMTEO.RAW_COST_FLAG,'Y','Yes','No')
	END) RES_RAW_COST
,(CASE
	WHEN forecastsE0.PLANNED_FOR_CODE != 'REVENUE' THEN	
		DECODE(pjoFinPlanAMTEO.COST_RATE_FLAG,'Y','Yes','No') 
	END) RES_RAW_COST_RATE
,(CASE
	WHEN forecastsE0.PLANNED_FOR_CODE != 'COST' THEN	
		DECODE(pjoFinPlanAMTEO.REVENUE_FLAG,'Y','Yes','No') 
	END) RES_REVENUE
,(CASE
	WHEN forecastsE0.PLANNED_FOR_CODE = 'REVENUE' THEN	
		DECODE(pjoFinPlanAMTEO.REVENUE_QTY_FLAG,'Y','Yes','No') 
	END) RES_REVENUE_QUANTITY
,(CASE
	WHEN forecastsE0.PLANNED_FOR_CODE != 'REVENUE' THEN	
		DECODE(pjoFinPlanAMTEO.BRDND_COST_FLAG,'Y','Yes','No') 
	END) RES_BURDENED_COST
,(CASE
	WHEN forecastsE0.PLANNED_FOR_CODE != 'REVENUE' THEN	
		DECODE(pjoFinPlanAMTEO.BRDND_RATE_FLAG,'Y','Yes','No')
	END) RES_BURDENED_COST_RATE
,(CASE
	WHEN forecastsE0.PLANNED_FOR_CODE != 'COST' THEN	
		DECODE(pjoFinPlanAMTEO.BILL_RATE_FLAG,'Y','Yes','No') 
	END) RES_BILL_RATE
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJO_PLAN_LEVEL_CODE'
	AND LOOKUP_CODE = planningOptionsE0.PLAN_LEVEL_CODE
	) RES_PLANNING_LEVEL
,(CASE 
	WHEN planningOptionsE0.BUDGET_CREATION_METHOD != 'GENERATE' THEN
		(SELECT 
			(SELECT (CASE
				WHEN CENTRALLY_CONTROL_FLAG = 'I' THEN HNAME
				ELSE SNAME
				END) 
				FROM RBS 
				WHERE RBS_HEADER_ID = version.RBS_HEADER_ID)
			FROM PJF_RBS_VERSIONS_VL version
			WHERE version.RBS_VERSION_ID = planningOptionsE0.RBS_VERSION_ID)
	END) RES_PLANNING_RESOURCE_BREAKDOW
,(SELECT MEANING
	FROM FND_LOOKUPS
	WHERE LOOKUP_TYPE = 'PJO_TIME_PHASED_TYPE'
	AND LOOKUP_CODE = planningOptionsE0.TIME_PHASED_CODE
	) RES_CALENDAR_TYPE
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE = 'N' AND forecastsE0.PLANNED_FOR_CODE = 'COST' THEN
		(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'PJO_BDGT_RATE_DRV_DATE_TYPE'
			AND LOOKUP_CODE = planningOptionsE0.NTP_RATE_DATE_TYPE)
	END) RES_COST_RATE_DERIVATION_DATE_
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE = 'N' AND forecastsE0.PLANNED_FOR_CODE = 'COST' THEN
		TO_CHAR(planningOptionsE0.NTP_RATE_DATE,'DD-Mon-YYYY')
	END) RES_COST_RATE_DERIVATION_DATE
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE = 'N' AND forecastsE0.PLANNED_FOR_CODE = 'REVENUE' THEN
		(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'PJO_BDGT_RATE_DRV_DATE_TYPE'
			AND LOOKUP_CODE = planningOptionsE0.NTP_RATE_DATE_TYPE)
	END) RES_BILL_RATE_DERIVATION_DATE_
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE = 'N' AND forecastsE0.PLANNED_FOR_CODE = 'REVENUE' THEN
		TO_CHAR(planningOptionsE0.NTP_RATE_DATE,'DD-Mon-YYYY')
	END) RES_BILL_RATE_DERIVATION_DATE
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE = 'N' AND forecastsE0.PLANNED_FOR_CODE = 'COST_AND_REV_TOGETHER' THEN
		(SELECT MEANING
			FROM FND_LOOKUPS
			WHERE LOOKUP_TYPE = 'PJO_FCST_RATE_DRV_DATE_TYPE'
			AND LOOKUP_CODE = planningOptionsE0.NTP_RATE_DATE_TYPE)
	END) RES_RATE_DERIVATION_DATE_TYPE
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE = 'N' AND forecastsE0.PLANNED_FOR_CODE = 'COST_AND_REV_TOGETHER' THEN
		TO_CHAR(planningOptionsE0.NTP_RATE_DATE,'DD-Mon-YYYY')
	END) RES_RATE_DERIVATION_DATE
,(SELECT NAME
	FROM PJO_PERIOD_PROFILES_VL
	WHERE PERIOD_PROFILE_ID = planningOptionsE0.PERIOD_PROFILE_ID
	) RES_PERIOD_PROFILE
,(CASE
	WHEN planningOptionsE0.TIME_PHASED_CODE != 'N'  THEN
		planningOptionsE0.CURRENT_PLANNING_PERIOD
	END) RES_CURRENT_PLANNING_PERIOD
,forecastsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,forecastsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,forecastsE0.CREATED_BY  RSC_CREATED_BY
,forecastsE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,projTemplatesE0.ORG_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJF_PROJECTS_ALL_VL projTemplatesE0
,FUN_ALL_BUSINESS_UNITS_V buE0
,PJO_PLAN_VERSIONS_VL forecastsE0
,PJO_PLANNING_OPTIONS planningOptionsE0
,PJO_FIN_PLAN_AMT_SETS pjoFinPlanAMTEO
,PJO_PLAN_TYPES_VL pjoPlanTypeEO
WHERE projTemplatesE0.TEMPLATE_FLAG = 'Y'
AND projTemplatesE0.ORG_ID = buE0.BU_ID
AND projTemplatesE0.PROJECT_ID = forecastsE0.PROJECT_ID
AND forecastsE0.PLAN_CLASS_CODE = 'FORECAST'
AND forecastsE0.STRUCTURE_VERSION_ID IS NULL
AND forecastsE0.PLAN_VERSION_ID = planningOptionsE0.PLAN_VERSION_ID
AND planningOptionsE0.PLAN_TYPE_CODE = 'FINANCIAL_PLAN'
AND planningOptionsE0.AMOUNT_SET_ID = pjoFinPlanAMTEO.FIN_PLAN_AMT_SET_ID
AND pjoPlanTypeEO.PLAN_TYPE_ID = planningOptionsE0.PLAN_TYPE_ID
ORDER BY projTemplatesE0.NAME
,forecastsE0.VERSION_NAME