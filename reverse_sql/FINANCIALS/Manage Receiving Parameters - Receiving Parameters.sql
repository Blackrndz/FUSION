--RSC_PREREQUISITE_OBJECTS=RCV_PARAMETERS

SELECT IODefinitionE0.ORGANIZATION_CODE RES_ORGANIZATION
,(SELECT LookupPEO.MEANING
FROM FND_LOOKUPS LookupPEO
WHERE LookupPEO.LOOKUP_TYPE = 'RCV_RECEIVING_OPTION'
AND LookupPEO.LOOKUP_CODE = ReceivingParameterEO.ENFORCE_SHIP_TO_LOCATION_CODE) RES_SHIP_TO_EXCEPTION_ACTION
,(SELECT LookupPEO.MEANING
FROM FND_LOOKUPS LookupPEO
WHERE LookupPEO.LOOKUP_TYPE = 'RCV_RECEIVING_OPTION'
AND LookupPEO.LOOKUP_CODE = ReceivingParameterEO.RECEIPT_ASN_EXISTS_CODE) RES_ASN_CONTROL_ACTION
,ReceivingParameterEO.DAYS_EARLY_RECEIPT_ALLOWED RES_EARLY_RECEIPT_TOLERANCE_IN
,ReceivingParameterEO.DAYS_LATE_RECEIPT_ALLOWED RES_LATE_RECEIPT_TOLERANCE_IN_
,(SELECT LookupPEO.MEANING
FROM FND_LOOKUPS LookupPEO
WHERE LookupPEO.LOOKUP_TYPE = 'RCV_RECEIVING_OPTION'
AND LookupPEO.LOOKUP_CODE = ReceivingParameterEO.RECEIPT_DAYS_EXCEPTION_CODE) RES_RECEIPT_DAYS_EXCEED_ACTION
,ReceivingParameterEO.QTY_RCV_TOLERANCE RES_OVER_RECEIPT_TOLERANCE
,(SELECT LookupPEO.MEANING
FROM FND_LOOKUPS LookupPEO
WHERE LookupPEO.LOOKUP_TYPE = 'RCV_RECEIVING_OPTION'
AND LookupPEO.LOOKUP_CODE = ReceivingParameterEO.QTY_RCV_EXCEPTION_CODE ) RES_OVER_RECEIPT_ACTION
,(SELECT LookupPEO.MEANING
FROM FND_LOOKUPS LookupPEO
WHERE LookupPEO.LOOKUP_TYPE = 'RCV_ROUTING_HEADERS'
AND LookupPEO.LOOKUP_CODE = ReceivingParameterEO.RECEIVING_ROUTING_ID) RES_GENERAL_RECEIPT_ROUTING
,DECODE(ReceivingParameterEO.INSPECTION_METHOD,'QUALITY','Yes','RECEIVING','No') RES_USE_QUALITY_INSPECTION_PLA
,DECODE(NVL(ReceivingParameterEO.ALLOW_SUBSTITUTE_RECEIPTS_FLAG,'N'),'Y','Yes','N','No') RES_ALLOW_SUBSTITUTE_RECEIPTS
,DECODE(NVL(ReceivingParameterEO.ALLOW_UNORDERED_RECEIPTS_FLAG,'N'),'Y','Yes','N','No') RES_ALLOW_UNORDERED_RECEIPTS
,DECODE(NVL(ReceivingParameterEO.BLIND_RECEIVING_FLAG,'N'),'Y','Yes','N','No') RES_ENFORCE_BLIND_RECEIVING
,DECODE(NVL(ReceivingParameterEO.PRINT_RECEIPT_TRAVELER,'N'),'Y','Yes','N','No') RES_PRINT_RECEIPT_TRAVELER
,DECODE(NVL(ReceivingParameterEO.INCLUDE_CLOSED_PO_RECEIPT,'N'),'Y','Yes','N','No') RES_INCLUDE_CLOSED_PURCHASE_OR
,DECODE(NVL(ReceivingParameterEO.ALLOW_ROUTING_OVERRIDE,'N'),'Y','Yes','N','No') RES_ALLOW_ROUTING_OVERRIDE
,DECODE(NVL(ReceivingParameterEO.PROCESS_ASN_LINES_TOGETHER,'N'),'Y','Yes','N','No') RES_PROCESS_ALL_LINES_TOGETHER
,DECODE(NVL(ReceivingParameterEO.INTF_RETURNS_TO_SHIPPING,'N'),'Y','Yes','N','No') RES_PRINT_SHIPPING_DOCUMENTS_F
,DECODE(NVL(ReceivingParameterEO.INTF_DROPSHIPS_TO_SHIPPING,'N'),'Y','Yes','N','No') RES_PRINT_SHIPPING_DOCUMENTS_0
,DECODE(NVL(ReceivingParameterEO.INTF_RMAS_TO_SHIPPING,'N'),'Y','Yes','N','No') RES_PRINT_SHIPPING_DOCUMENTS_1
,DECODE(NVL(ReceivingParameterEO.PUBLISH_TXN_EVENTS,'N'),'Y','Yes','N','No') RES_PUBLISH_TRANSACTIONAL_BUSI
,DECODE(NVL(ReceivingParameterEO.ALLOW_DEFAULT_LOTSER_FROM_ASN,'N'),'Y','Yes','N','No') RES_ALLOW_DEFAULTING_OF_LOTS_A
,(SELECT LookupPEO.MEANING
FROM FND_LOOKUPS LookupPEO
WHERE LookupPEO.LOOKUP_TYPE = 'RCV_USER_DEF_RCPT_NUM_CODE'
AND LookupPEO.LOOKUP_CODE = ReceivingParameterEO.USER_DEFINED_RECEIPT_NUM_CODE) RES_GENERATION
,(SELECT LookupPEO.MEANING
FROM FND_LOOKUPS LookupPEO
WHERE LookupPEO.LOOKUP_TYPE = 'RCV_MANUAL_RECEIPT_NUM_TYPE'
AND LookupPEO.LOOKUP_CODE = ReceivingParameterEO.MANUAL_RECEIPT_NUM_TYPE) RES_TYPE
,ReceivingParameterEO.NEXT_RECEIPT_NUM RES_NEXT_NUMBER
,(SELECT LookupPEO.MEANING
FROM FND_LOOKUPS LookupPEO
WHERE LookupPEO.LOOKUP_TYPE = 'RCV_ROUTING_HEADERS'
AND LookupPEO.LOOKUP_CODE = ReceivingParameterEO.RMA_RECEIPT_ROUTING_ID) RES_RMA_RECEIPT_ROUTING
,(SELECT LookupPEO.MEANING
FROM FND_LOOKUPS LookupPEO
WHERE LookupPEO.LOOKUP_TYPE = 'RCV_ENFORCE_RMA_LOT_NUM'
AND LookupPEO.LOOKUP_CODE = ReceivingParameterEO.ENFORCE_RMA_LOT_NUM) RES_RMA_VALIDATE_LOTS
,DECODE(NVL(ReceivingParameterEO.ENFORCE_RMA_SERIAL_NUM,'N'),'Y','Yes','N','No') RES_VALIDATE_SERIAL_NUMBERS
,ReceivingParameterEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,ReceivingParameterEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,ReceivingParameterEO.CREATED_BY RSC_CREATED_BY
,ReceivingParameterEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,ReceivingParameterEO.ORGANIZATION_ID RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM RCV_PARAMETERS ReceivingParameterEO
,INV_ORGANIZATION_DEFINITIONS_V IODefinitionE0
WHERE ReceivingParameterEO.ORGANIZATION_ID = IODefinitionE0.ORGANIZATION_ID