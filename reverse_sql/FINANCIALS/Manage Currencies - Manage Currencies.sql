 SELECT CurrencyEO.CURRENCY_CODE RES_CURRENCY_CODE
,CurrencyEO.NAME RES_CURRENCY_NAME
,CurrencyEO.DESCRIPTION RES_DESCRIPTION
,DECODE(CURRENCYEO.ENABLED_FLAG,'Y','Yes','No')  RES_ENABLED
,TO_CHAR(CURRENCYEO.START_DATE_ACTIVE,'DD-Mon-YYYY') RES_START_DATE
,TO_CHAR(CURRENCYEO.END_DATE_ACTIVE,'DD-Mon-YYYY') RES_END_DATE
,TerritoryPEO.TERRITORY_SHORT_NAME as RES_ISSUING_TERRITORY
,CURRENCYEO.SYMBOL as RES_SYMBOL
,CURRENCYEO.precision as RES_PRECISION
,CURRENCYEO.EXTENDED_PRECISION as RES_EXTENDED_PRECISION
,CURRENCYEO.MINIMUM_ACCOUNTABLE_UNIT as RES_MINIMUM_ACCOUNTABLE_UNIT
,DECODE(CURRENCYEO.ISO_FLAG,'Y','Yes','No') as RES_ISO_CURRENCY
,DECODE(CURRENCYEO.CURRENCY_FLAG,'Y','Currency','Statistical Unit') as RES_TYPE
,(select LOOKUPPEO.MEANING from FND_LOOKUPS LOOKUPPEO
where LOOKUPPEO.LOOKUP_TYPE = 'FND_CURRENCIES_DERIVATION_TYPE'
and CURRENCYEO.DERIVE_TYPE = LookupPEO.LOOKUP_CODE) as RES_DERIVATION_TYPE
,CURRENCYEO.DERIVE_FACTOR as RES_DERIVATION_FACTOR
,to_Char(DERIVE_EFFECTIVE,'DD-Mon-YYYY') as RES_DERIVATION_EFFECTIVE_DATE
,CURRENCYEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,CURRENCYEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,CURRENCYEO.CREATED_BY  RSC_CREATED_BY
,CURRENCYEO.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

from FND_CURRENCIES_VL CURRENCYEO
,FND_TERRITORIES_VL TERRITORYPEO
where CURRENCYEO.ISSUING_TERRITORY_CODE = TERRITORYPEO.TERRITORY_CODE(+)
ORDER BY CurrencyEO.CURRENCY_CODE