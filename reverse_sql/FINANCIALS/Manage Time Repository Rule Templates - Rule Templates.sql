-- RSC_PREREQUISITE_OBJECTS=HWM_ALLOCATIONS_HDR_VL

SELECT hwnRuleTMPEO.TEMPLATE_NAME  RES_NAME
,hwnRuleTMPEO.DESCRIPTION  RES_DESCRIPTION
,hwnRuleTMPEO.PROCESS_NAME RES_FORMULA_NAME
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = hwnRuleTMPEO.TEMPLATE_TYPE
		AND LOOKUP_TYPE = 'HWM_RULE_TYPE' AND LANGUAGE = USERENV('LANG')) RES_TEMPLATE_TYPE
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = hwnRuleTMPEO.PROCESS_TYPE
		AND LOOKUP_TYPE = 'HWM_RULE_CODE_PROCESS_TYPE' AND LANGUAGE = USERENV('LANG')) RES_FORMULA_TYPE
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = hwnRuleTMPEO.CLASSIFICATION
		AND LOOKUP_TYPE = 'HWM_RULE_CLASSIFICATION' AND LANGUAGE = USERENV('LANG')) RES_RULE_CLASSIFICATION	
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = hwnRuleTMPEO.RULE_EXEC_TYPE
		AND LOOKUP_TYPE = 'HWM_RULE_EXEC_TYPE' AND LANGUAGE = USERENV('LANG')) RES_RULE_EXECUTION_TYPE
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = hwnRuleTMPEO.RUN_SUMMATION_LEVEL
		AND LOOKUP_TYPE = 'HWM_RULE_TBB_PROCESS_LEVEL' AND LANGUAGE = USERENV('LANG')) RES_SUMMATION_LEVEL
,(SELECT MEANING FROM FND_LOOKUP_VALUES
	WHERE LOOKUP_CODE = hwnRuleTMPEO.RUN_TBB_LEVEL 
	AND LOOKUP_TYPE = 'HWM_RULE_TBB_PROCESS_LEVEL' AND LANGUAGE = USERENV('LANG')) RES_REPORTING_LEVEL
,DECODE(hwnRuleTMPEO.SUPPRESS_DUP_MSGS,'Y','Yes','No') RES_SUPPRESS_DUPLICATE_MESSAGE
,DECODE(hwnRuleTMPEO.INCLUDE_EMPTY_TC,'Y','Yes','No') RES_PROCESS_EMPTY_TIME_CARD
,(SELECT ALLOCATION_NAME FROM HWM_ALLOCATIONS_HDR_VL 
	WHERE ALLOCATION_ID = hwnRuleTMPEO.DFLT_ALLOCATION_ID AND (SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE) ) RES_DEFAULT_ALLOCATION
,DECODE(hwnRuleTMPEO.RUN_EVENT_SAVE,'Y','Yes','No') RES_SAVE
,DECODE(hwnRuleTMPEO.RUN_EVENT_SUB,'Y','Yes','No') RES_SUBMIT
,DECODE(hwnRuleTMPEO.RUN_EVENT_RESUB,'Y','Yes','No') RES_RESUBMIT
,DECODE(hwnRuleTMPEO.RUN_EVENT_DELETE,'Y','Yes','No') RES_DELETE
,hwnRuleTMPEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hwnRuleTMPEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hwnRuleTMPEO.CREATED_BY RSC_CREATED_BY
,hwnRuleTMPEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM HWM_RULE_TMPLTS_VL hwnRuleTMPEO
ORDER BY hwnRuleTMPEO.TEMPLATE_NAME
