-- RSC_PREREQUISITE_OBJECTS=PJO_PLANNING_OPTIONS

WITH CHECK_SAME_CONV AS(SELECT PLANNING_OPTION_ID 
	,(CASE
	WHEN 	((NVL(PFC_COST_RATE_TYPE,'X') = NVL(PC_COST_RATE_TYPE,'X') AND NVL(PFC_COST_RATE_TYPE,'X') = NVL(RATE_SCH_COST_RATE_TYPE,'X'))
		AND (NVL(PFC_COST_RATE_DATE_TYPE,'X') = NVL(PC_COST_RATE_DATE_TYPE,'X') AND NVL(PFC_COST_RATE_DATE_TYPE,'X') = NVL(RATE_SCH_COST_RATE_DATE_TYPE,'X')))
		AND ( NVL(TO_CHAR(PFC_COST_RATE_DATE),'0') = NVL(TO_CHAR(PC_COST_RATE_DATE),'0') AND NVL(TO_CHAR(PFC_COST_RATE_DATE),'0') = NVL(TO_CHAR(RATE_SCH_COST_RATE_DATE),'0'))
		THEN 'Y'
		ELSE 'N'
		END) USE_SAME_CONV_COST
	,(CASE
	WHEN ((NVL(PFC_REV_RATE_TYPE,'X') = NVL(PC_REV_RATE_TYPE,'X') AND NVL(PFC_REV_RATE_TYPE,'X') = NVL(RATE_SCH_REV_RATE_TYPE,'X'))
		AND (NVL(PFC_REV_RATE_DATE_TYPE,'X') = NVL(PC_REV_RATE_DATE_TYPE,'X') AND NVL(PFC_REV_RATE_DATE_TYPE,'X') = NVL(RATE_SCH_REV_RATE_DATE_TYPE,'X')))
		AND (NVL(TO_CHAR(PFC_REV_RATE_DATE),'0') = NVL(TO_CHAR(PC_REV_RATE_DATE),'0') AND NVL(TO_CHAR(PFC_REV_RATE_DATE),'0') = NVL(TO_CHAR(RATE_SCH_REV_RATE_DATE),'0'))
		THEN 'Y'
		ELSE 'N'
		END) USE_SAME_CONV_REV
	FROM PJO_PLANNING_OPTIONS
	WHERE PROJECT_ID IN (SELECT PROJECT_ID FROM PJF_PROJECTS_ALL_B WHERE TEMPLATE_FLAG = 'Y')
	)

SELECT projTemplatesE0.NAME RES_TEMPLATE_NAME
,projTemplatesE0.SEGMENT1 RES_TEMPLATE_NUMBER
,budgetsE0.VERSION_NAME RES_NAME
,(CASE
	WHEN planningOptionsE0.PLANNED_FOR_CODE = 'COST' THEN
		DECODE(checkSameConvsE0.USE_SAME_CONV_COST,'Y','Yes','No')
	WHEN planningOptionsE0.PLANNED_FOR_CODE = 'REVENUE' THEN
		DECODE(checkSameConvsE0.USE_SAME_CONV_REV,'Y','Yes','No')
	WHEN planningOptionsE0.PLANNED_FOR_CODE = 'COST_AND_REV_TOGETHER' THEN
		(CASE
			WHEN checkSameConvsE0.USE_SAME_CONV_COST = 'Y' AND checkSameConvsE0.USE_SAME_CONV_REV = 'Y' THEN
				'Yes'
			ELSE 'No'
			END)
	END) RES_USE_SAME_CONVERSION_ATTRIB
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_COST = 'Y' AND checkSameConvsE0.USE_SAME_CONV_REV = 'Y' AND (planningOptionsE0.PLANNED_FOR_CODE != 'REVENUE') THEN 
		(SELECT USER_CONVERSION_TYPE 
				FROM GL_DAILY_CONVERSION_TYPES
				WHERE CONVERSION_TYPE = planningOptionsE0.PFC_COST_RATE_TYPE )				
	END) RES_RATE_TYPE
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_COST = 'Y' AND checkSameConvsE0.USE_SAME_CONV_REV = 'Y' AND (planningOptionsE0.PLANNED_FOR_CODE != 'REVENUE') THEN 
		(SELECT MEANING 
			FROM FND_LOOKUPS
			WHERE LOOKUP_CODE = planningOptionsE0.PFC_COST_RATE_DATE_TYPE
			AND LOOKUP_TYPE = 'PJO_RATE_DRV_DATE_TYPE') 					
	END) RES_DATE_TYPE
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_COST = 'Y' AND checkSameConvsE0.USE_SAME_CONV_REV = 'Y' AND (planningOptionsE0.PLANNED_FOR_CODE != 'REVENUE') THEN 
		TO_CHAR(planningOptionsE0.PFC_COST_RATE_DATE,'DD-Mon-YYYY') 				
	END) RES_FIXED_DATE
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_COST = 'N' AND (planningOptionsE0.PLANNED_FOR_CODE != 'REVENUE') THEN 
		(SELECT USER_CONVERSION_TYPE 
			FROM GL_DAILY_CONVERSION_TYPES
			WHERE CONVERSION_TYPE = planningOptionsE0.PC_COST_RATE_TYPE) 
	END) RES_RATE_TYPE_FOR_PROJECT_CURR
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_COST = 'N' AND (planningOptionsE0.PLANNED_FOR_CODE != 'REVENUE') THEN 
		(SELECT MEANING 
			FROM FND_LOOKUPS
			WHERE LOOKUP_CODE = planningOptionsE0.PC_COST_RATE_DATE_TYPE
			AND LOOKUP_TYPE = 'PJO_RATE_DRV_DATE_TYPE') 					
	END) RES_DATE_TYPE_FOR_PROJECT_CURR
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_COST = 'N' AND (planningOptionsE0.PLANNED_FOR_CODE != 'REVENUE') THEN 
		TO_CHAR(planningOptionsE0.PC_COST_RATE_DATE,'DD-Mon-YYYY') 				
	END) RES_FIXED_DATE_FOR_PROJECT_CUR
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_COST = 'N' AND (planningOptionsE0.PLANNED_FOR_CODE != 'REVENUE') THEN 
		(SELECT USER_CONVERSION_TYPE 
			FROM GL_DAILY_CONVERSION_TYPES
			WHERE CONVERSION_TYPE = planningOptionsE0.RATE_SCH_COST_RATE_TYPE) 
	END) RES_RATE_TYPE_FOR_PLANNING_CUR
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_COST = 'N' AND (planningOptionsE0.PLANNED_FOR_CODE != 'REVENUE') THEN 
		(SELECT MEANING 
			FROM FND_LOOKUPS
			WHERE LOOKUP_CODE = planningOptionsE0.RATE_SCH_COST_RATE_DATE_TYPE
			AND LOOKUP_TYPE = 'PJO_RATE_DRV_DATE_TYPE') 					
	END) RES_DATE_TYPE_FOR_PLANNING_CUR
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_COST = 'N' AND (planningOptionsE0.PLANNED_FOR_CODE != 'REVENUE') THEN 
		TO_CHAR(planningOptionsE0.RATE_SCH_COST_RATE_DATE,'DD-Mon-YYYY') 				
	END) RES_FIXED_DATE_FOR_PLANNING_CU
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_COST = 'Y' AND checkSameConvsE0.USE_SAME_CONV_REV = 'Y' AND (planningOptionsE0.PLANNED_FOR_CODE != 'COST') THEN 
		(SELECT USER_CONVERSION_TYPE 
			FROM GL_DAILY_CONVERSION_TYPES
			WHERE CONVERSION_TYPE = planningOptionsE0.PFC_REV_RATE_TYPE )				
	END) RES_REVENUE_RATE_TYPE
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_COST = 'Y' AND checkSameConvsE0.USE_SAME_CONV_REV = 'Y' AND (planningOptionsE0.PLANNED_FOR_CODE != 'COST') THEN 
		(SELECT MEANING 
			FROM FND_LOOKUPS
			WHERE LOOKUP_CODE = planningOptionsE0.PFC_REV_RATE_DATE_TYPE
			AND LOOKUP_TYPE = 'PJO_RATE_DRV_DATE_TYPE')	
	END) RES_REVENUE_DATE_TYPE
,(CASE
	 WHEN checkSameConvsE0.USE_SAME_CONV_COST = 'Y' AND checkSameConvsE0.USE_SAME_CONV_REV = 'Y' AND (planningOptionsE0.PLANNED_FOR_CODE != 'COST') THEN 
		TO_CHAR(planningOptionsE0.PFC_REV_RATE_DATE,'DD-Mon-YYYY') 				
	END) RES_REVENUE_FIXED_DATE
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_REV = 'N' AND (planningOptionsE0.PLANNED_FOR_CODE != 'COST') THEN 
		(SELECT USER_CONVERSION_TYPE 
			FROM GL_DAILY_CONVERSION_TYPES
			WHERE CONVERSION_TYPE = planningOptionsE0.PC_REV_RATE_TYPE)
	END) RES_REVENUE_RATE_TYPE_FOR_PROJ
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_REV = 'N' AND (planningOptionsE0.PLANNED_FOR_CODE != 'COST') THEN 
		(SELECT MEANING 
			FROM FND_LOOKUPS
			WHERE LOOKUP_CODE = planningOptionsE0.PC_REV_RATE_DATE_TYPE
			AND LOOKUP_TYPE = 'PJO_RATE_DRV_DATE_TYPE') 					
	END) RES_REVENUE_DATE_TYPE_FOR_PROJ
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_REV = 'N' AND (planningOptionsE0.PLANNED_FOR_CODE != 'COST') THEN 
		TO_CHAR(planningOptionsE0.PC_REV_RATE_DATE,'DD-Mon-YYYY') 				
	END) RES_REVENUE_FIXED_DATE_FOR_PRO
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_REV = 'N' AND (planningOptionsE0.PLANNED_FOR_CODE != 'COST') THEN 
		(SELECT USER_CONVERSION_TYPE 
			FROM GL_DAILY_CONVERSION_TYPES
			WHERE CONVERSION_TYPE = planningOptionsE0.RATE_SCH_REV_RATE_TYPE) 
	END) RES_REVENUE_RATE_TYPE_FOR_PLAN
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_REV = 'N' AND (planningOptionsE0.PLANNED_FOR_CODE != 'COST') THEN 
		(SELECT MEANING 
			FROM FND_LOOKUPS
			WHERE LOOKUP_CODE = planningOptionsE0.RATE_SCH_REV_RATE_DATE_TYPE
			AND LOOKUP_TYPE = 'PJO_RATE_DRV_DATE_TYPE') 					
	END) RES_REVENUE_DATE_TYPE_FOR_PLAN
,(CASE
	WHEN checkSameConvsE0.USE_SAME_CONV_REV = 'N' AND (planningOptionsE0.PLANNED_FOR_CODE != 'COST') THEN 
		TO_CHAR(planningOptionsE0.RATE_SCH_REV_RATE_DATE,'DD-Mon-YYYY') 				
	END) RES_REVENUE_FIXED_DATE_FOR_PLA
,budgetsE0.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,budgetsE0.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,budgetsE0.CREATED_BY  RSC_CREATED_BY
,budgetsE0.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,projTemplatesE0.ORG_ID RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJF_PROJECTS_ALL_VL projTemplatesE0
,FUN_ALL_BUSINESS_UNITS_V buE0
,PJO_PLAN_VERSIONS_VL budgetsE0
,PJO_PLANNING_OPTIONS planningOptionsE0
,CHECK_SAME_CONV checkSameConvsE0
,PJO_PLAN_TYPES_B planTypesE0
WHERE projTemplatesE0.TEMPLATE_FLAG = 'Y'
AND projTemplatesE0.ORG_ID = buE0.BU_ID
AND projTemplatesE0.PROJECT_ID = budgetsE0.PROJECT_ID
AND budgetsE0.PLAN_CLASS_CODE = 'FORECAST'
AND budgetsE0.STRUCTURE_VERSION_ID IS NULL
AND budgetsE0.PLAN_VERSION_ID = planningOptionsE0.PLAN_VERSION_ID
--AND planningOptionsE0.PLAN_TYPE_CODE = 'FINANCIAL_PLAN'
AND planningOptionsE0.PLANNING_OPTION_ID = checkSameConvsE0.PLANNING_OPTION_ID
AND planningOptionsE0.PLAN_TYPE_ID = planTypesE0.PLAN_TYPE_ID
--AND planTypesE0.PLAN_IN_MULTI_CURR_FLAG = 'Y'
ORDER BY projTemplatesE0.NAME
,budgetsE0.VERSION_NAME