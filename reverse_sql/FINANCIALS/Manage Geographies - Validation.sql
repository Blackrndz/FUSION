SELECT hzGEOE0.GEOGRAPHY_NAME RES_COUNTRY_NAME
,hzGEOSMAPEO.COUNTRY_CODE RES_COUNTRY_CODE
,NVL((SELECT STYLE_FORMAT_NAME FROM HZ_STYLE_FORMATS_VL WHERE STYLE_FORMAT_CODE = hzGEOSMAPEO.ADDRESS_STYLE
	),'No Styles Format')  RES_ADDRESS_STYLE_FORMAT
,(SELECT MEANING FROM FND_LOOKUPS WHERE LOOKUP_CODE = hzGEOSLEVELEO.ADDR_VAL_LEVEL AND 
	LOOKUP_TYPE = 'HZ_ADDRESS_VALIDATION_LEVEL' ) RES_GEOGRAPHY_VALIDATION_LEVEL
	
,hzGEOSLEVELEO.LAST_UPDATED_BY  RSC_LAST_UPDATED_BY
,hzGEOSLEVELEO.LAST_UPDATE_DATE  RSC_LAST_UPDATE_DATE
,hzGEOSLEVELEO.CREATED_BY  RSC_CREATED_BY
,hzGEOSLEVELEO.CREATION_DATE  RSC_CREATION_DATE
,null RSC_LEDGER_ID
,null RSC_CHART_OF_ACCOUNTS_ID
,null RSC_BUSINESS_UNIT_ID
,null RSC_LEGAL_ENTITY_ID
,null RSC_ORGANIZATION_ID
,null RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID


FROM HZ_GEOGRAPHIES hzGEOE0
,HZ_GEO_STRUCT_MAP hzGEOSMAPEO
,HZ_GEO_STRUCTURE_LEVELS hzGEOSLEVELEO
WHERE hzGEOE0.COUNTRY_CODE = hzGEOSMAPEO.COUNTRY_CODE
AND hzGEOSMAPEO.LOC_TBL_NAME = 'PER_ADDRESSES_F'
AND hzGEOSMAPEO.COUNTRY_CODE = hzGEOSLEVELEO.COUNTRY_CODE
AND hzGEOSLEVELEO.PARENT_GEOGRAPHY_TYPE = 'COUNTRY'
ORDER BY 1


