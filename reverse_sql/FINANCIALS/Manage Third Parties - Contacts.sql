--RSC_PREREQUISITE_OBJECTS=HZ_CONTACT_POINTS
 
SELECT hzPATIESE0.PARTY_NAME RES_PARTY_NAME
,DECODE(hzCONTACTPOINTE0.PRIMARY_FLAG,'Y','Yes','N','No') RES_PRIMARY
,(SELECT MEANING FROM FND_LOOKUP_VALUES WHERE LOOKUP_CODE = hzCONTACTPOINTE0.PHONE_TYPE AND LANGUAGE = USERENV('LANG')
	AND LOOKUP_TYPE = 'ORA_HZ_PHONE_TYPE') RES_TYPE
,DECODE(hzCONTACTPOINTE0.PRIMARY_BY_PURPOSE,'Y','Yes','No') RES_PRIMARY_BY_PURPOSE
,hzCONTACTPOINTE0.CONTACT_POINT_PURPOSE RES_PURPOSE
,hzCONTACTPOINTE0.PHONE_COUNTRY_CODE RES_PHONE_COUNTRY_CODE
,NULL RES_PHONE_COUNTRY_CODE_COUNTRY
,hzCONTACTPOINTE0.PHONE_AREA_CODE RES_AREA_CODE
,hzCONTACTPOINTE0.PHONE_NUMBER RES_PHONE
,hzCONTACTPOINTE0.PHONE_EXTENSION RES_EXTENSION
,NULL RES_CONTACT_PREFERENCES
,(SELECT NAME FROM FND_TIMEZONES_VL WHERE TIMEZONE_CODE = hzCONTACTPOINTE0.TIMEZONE_CODE) RES_TIME_ZONE
,NULL RES_ROLE

,hzCONTACTPOINTE0.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,hzCONTACTPOINTE0.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,hzCONTACTPOINTE0.CREATED_BY RSC_CREATED_BY
,hzCONTACTPOINTE0.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID


FROM HZ_PARTIES hzPATIESE0
,HZ_PARTY_USG_ASSIGNMENTS hzPARTYUSEASSE0
,HZ_CONTACT_POINTS hzCONTACTPOINTE0
--,HZ_PHONE_COUNTRY_CODES hzPHONE

WHERE hzPATIESE0.PARTY_ID = hzPARTYUSEASSE0.PARTY_ID 
AND hzPARTYUSEASSE0.PARTY_ID = hzCONTACTPOINTE0.OWNER_TABLE_ID
AND hzCONTACTPOINTE0.OWNER_TABLE_NAME = 'HZ_PARTIES'
AND hzPARTYUSEASSE0.PARTY_USAGE_CODE IN ('PAYMENT_ISSUING_AUTHORITY','PENSION_PROVIDER','PROFESSIONAL_BODY','BARGAINING_ASSOCIATION','DISABILITY_ORGANIZATION','EXTERNAL_PAYEE')
ORDER BY hzPATIESE0.PARTY_NAME
