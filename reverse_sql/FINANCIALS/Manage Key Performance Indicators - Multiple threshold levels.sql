SELECT pjsPERFRULEEO.RULE_NAME RES_KPI_NAME
,pjsPERFTHRESEO.FROM_VALUE RES_THRESHOLD_START
,pjsPERFTHRESEO.TO_VALUE RES_THRESHOLD_END
,(SELECT MEANING FROM FND_LOOKUPS WHERE LOOKUP_CODE = pjsPERFTHRESEO.INDICATOR_CODE 
	AND LOOKUP_TYPE = 'PJS_PERF_INDICATORS') RES_STATUS_INDICATOR

,pjsPERFTHRESEO.LAST_UPDATED_BY RSC_LAST_UPDATED_BY
,pjsPERFTHRESEO.LAST_UPDATE_DATE RSC_LAST_UPDATE_DATE
,pjsPERFTHRESEO.CREATED_BY RSC_CREATED_BY
,pjsPERFTHRESEO.CREATION_DATE RSC_CREATION_DATE
,NULL RSC_LEDGER_ID
,NULL RSC_CHART_OF_ACCOUNTS_ID
,NULL RSC_BUSINESS_UNIT_ID
,NULL RSC_LEGAL_ENTITY_ID
,NULL RSC_ORGANIZATION_ID
,NULL RSC_BUSINESS_GROUP_ID
,NULL RSC_ENTERPRISE_ID
,NULL RSC_COUNTRY_ID

FROM PJS_PERF_RULES_VL pjsPERFRULEEO
,PJS_PERF_THRESHOLDS pjsPERFTHRESEO
WHERE pjsPERFRULEEO.RULE_ID = pjsPERFTHRESEO.THRES_OBJ_ID
AND pjsPERFRULEEO.MEASURE_FORMAT = 'HOURS'
AND pjsPERFRULEEO.THRESHOLD_LEVEL = 'M'
AND SYSDATE BETWEEN pjsPERFRULEEO.START_DATE_ACTIVE AND NVL(pjsPERFRULEEO.END_DATE_ACTIVE,TO_DATE('4712-12-31','YYYY-MM-DD'))
ORDER BY pjsPERFTHRESEO.THRESHOLD_ID
